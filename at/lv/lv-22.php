        <a href="lv.php"><h3>Levítico 22</h3></a>
        <p><span class="numeros">1)</span> E o Senhor falou a Moisés, dizendo:</p>
        <p><span class="numeros">2)</span> Dize a Arão e a seus filhos que eles se separem das coisas santas dos filhos de Israel, e que eles não profanem o meu santo nome nestas coisas que eles consagram a mim. Eu sou o Senhor.</p>
        <p><span class="numeros">3)</span> Dize-lhes: Qualquer pessoa de toda a vossa semente que entre as vossas gerações se chegar às coisas santas que os filhos de Israel santificam ao Senhor, tendo sobre si a sua impureza, aquela alma será cortada da minha presença. Eu sou o Senhor.</p>
        <p><span class="numeros">4)</span> Qualquer homem da semente de Arão que for leproso ou tiver um corrimento não comerá das coisas santas, até que esteja limpo; e quem tocar alguma coisa que é impura de cadáver, ou um homem cuja semente sai dele;</p>
        <p><span class="numeros">5)</span> ou qualquer que tocar alguma coisa rastejante, pela qual se fez impuro, ou a algum homem, de quem se pegou impureza, seja qual for a sua impureza.</p>
        <p><span class="numeros">6)</span> A alma que nele tocar será impura até à tarde, e não comerá das coisas santas, a não ser que ele lave a sua carne com água.</p>
        <p><span class="numeros">7)</span> E quando o sol se pôr, ele estará limpo, e depois comerá das coisas santas; porque este é o seu alimento.</p>
        <p><span class="numeros">8)</span> Aquilo que morreu por si, ou foi dilacerado por animais, ele não comerá, para que não se contamine. Eu sou o Senhor.</p>
        <p><span class="numeros">9)</span> Portanto, eles guardarão a minha ordenança, para que por isso não levem pecado e, portanto, morram nele, havendo-o profanado. Eu, o Senhor, os santifico.</p>
        <p><span class="numeros">10)</span> Nenhum estranho comerá de coisa santa; nem o hóspede do sacerdote, nem o servo contratado; não comerão da coisa santa.</p>
        <p><span class="numeros">11)</span> Mas se o sacerdote comprar alguma alma com o seu dinheiro, ele comerá disto, e aquele que é nascido na sua casa; estes comerão de seu alimento.</p>
        <p><span class="numeros">12)</span> Se a filha do sacerdote também se casar com um estrangeiro, ela não comerá da oferta das coisas santas.</p>
        <p><span class="numeros">13)</span> Mas se a filha do sacerdote for viúva, ou divorciada, e não tiver filho, e se houver tornado à casa de seu pai, como na sua juventude, ela poderá comer da carne do seu pai; mas nenhum estrangeiro comerá dele.</p>
        <p><span class="numeros">14)</span> E se algum homem comer da coisa santa involuntariamente, então sobre ele acrescentará a sua quinta parte, e a dará ao sacerdote com a coisa santa.</p>
        <p><span class="numeros">15)</span> E eles não profanarão as coisas santas dos filhos de Israel, que eles ofereceram ao Senhor,</p>
        <p><span class="numeros">16)</span> fazendo com que eles carreguem a iniquidade da transgressão, quando eles comem as suas coisas santas; porque eu, o Senhor, os santifico.</p>
        <p><span class="numeros">17)</span> E o Senhor falou a Moisés, dizendo:</p>
        <p><span class="numeros">18)</span> Fala a Arão, e a seus filhos, e a todos os filhos de Israel, e dize-lhes: Qualquer que seja da casa de Israel ou dos estrangeiros em Israel, que oferecer a sua oblação por todos os seus votos, e por todas as suas ofertas voluntárias, que eles oferecerão ao Senhor como uma oferta queimada,</p>
        <p><span class="numeros">19)</span> oferecerá segundo a sua própria vontade, um macho sem defeito, dos bois, das ovelhas, ou das cabras.</p>
        <p><span class="numeros">20)</span> Mas nenhuma coisa em que haja defeito oferecereis, porque não seria aceitável de vós.</p>
        <p><span class="numeros">21)</span> E quando alguém oferecer um sacrifício das ofertas de paz ao Senhor, para realizar seu voto, ou oferta voluntária, com bois ou ovelhas, este será perfeito para que seja aceito; não haverá defeito nele.</p>
        <p><span class="numeros">22)</span> Cego, ou quebrado, ou mutilado, ou que tenha sarna, ou escorbuto, ou impigens, estes não oferecereis ao Senhor, nem deles fareis oferta por fogo sobre o altar ao Senhor.</p>
        <p><span class="numeros">23)</span> Qualquer novilho ou cordeiro, com alguma coisa em excesso, ou faltando em suas partes, poderás oferecer por oferta voluntária, mas por um voto não será aceito.</p>
        <p><span class="numeros">24)</span> Não oferecereis ao Senhor o que está machucado, ou moído, ou quebrado, ou cortado; não fareis qualquer oferta assim na vossa terra.</p>
        <p><span class="numeros">25)</span> Nem da mão do estrangeiro oferecereis pão ao vosso Deus, de todas estas coisas, porque a sua corrupção está nelas; e há nelas defeito; não serão aceitas de vós.</p>
        <p><span class="numeros">26)</span> E o Senhor falou a Moisés, dizendo:</p>
        <p><span class="numeros">27)</span> Quando o novilho, ou ovelha, ou cabra, nasce, sete dias estará debaixo de sua mãe; do oitavo dia em diante será aceito por oferta feita por fogo ao Senhor.</p>
        <p><span class="numeros">28)</span> Ou seja vaca ou ovelha, não a matarás a elas e a sua cria ambos no mesmo dia.</p>
        <p><span class="numeros">29)</span> E quando oferecerdes sacrifício de ações de graça ao Senhor, vós o oferecereis de vossa própria vontade.</p>
        <p><span class="numeros">30)</span> No mesmo dia se comerá; nada deixareis ficar até à manhã. Eu sou o Senhor.</p>
        <p><span class="numeros">31)</span> Portanto, guardareis os meus mandamentos e os cumprireis. Eu sou o Senhor.</p>
        <p><span class="numeros">32)</span> Nem profanareis o meu santo nome, mas serei santificado entre os filhos de Israel. Eu sou o Senhor que vos santifico,</p>
        <p><span class="numeros">33)</span> que vos tirei da terra do Egito, para ser vosso Deus. Eu sou o Senhor.&nbsp;<a href="lv-23.php"><span class="proximo">Próximo capítulo</span></a></p>
