        <a href="lv.php"><h3>Levítico 8</h3></a>
        <p><span class="numeros">1)</span> E o Senhor falou a Moisés, dizendo:</p>
        <p><span class="numeros">2)</span> Toma a Arão e seus filhos com ele, e as vestes, e o óleo da unção, e um novilho por oferta pelo pecado, e os dois carneiros, e um cesto de pães sem fermento;</p>
        <p><span class="numeros">3)</span> e reúne toda a congregação à porta do tabernáculo da congregação.</p>
        <p><span class="numeros">4)</span> E Moisés fez como o Senhor lhe ordenou, e a assembleia foi reunida à porta do tabernáculo da congregação.</p>
        <p><span class="numeros">5)</span> E Moisés disse à congregação: Isto é o que o Senhor ordenou que se fizesse.</p>
        <p><span class="numeros">6)</span> E Moisés trouxe Arão e seus filhos, e os lavou com água,</p>
        <p><span class="numeros">7)</span> e lhe vestiu a túnica, e cingiu-o com o cinto, e vestiu-lhe o manto; e colocou o éfode sobre ele, e cingiu-o com o cinto tecido do éfode, e o ajustou nele.</p>
        <p><span class="numeros">8)</span> E ele pôs-lhe o peitoral, ele também colocou no peitoral o Urim e o Tumim;</p>
        <p><span class="numeros">9)</span> e pôs a mitra sobre a sua cabeça e também sobre a mitra, na sua parte frontal, ele pôs a lâmina de ouro, a coroa santa, como o Senhor ordenou a Moisés.</p>
        <p><span class="numeros">10)</span> E Moisés tomou o óleo da unção, e ungiu o tabernáculo e tudo o que havia nele, e o santificou;</p>
        <p><span class="numeros">11)</span> e ele aspergiu sete vezes o altar, e ungiu o altar e todos os seus vasos, como também a pia e o seu pé, para santificá-los.</p>
        <p><span class="numeros">12)</span> E ele derramou do óleo da unção sobre a cabeça de Arão e ungiu-o, para santificá-lo.</p>
        <p><span class="numeros">13)</span> E Moisés trouxe os filhos de Arão, e colocou as túnicas sobre eles, e cingiu-os com o cinto, e colocou neles gorros, como o Senhor ordenou a Moisés.</p>
        <p><span class="numeros">14)</span> E ele trouxe o novilho por oferta pelo pecado; e Arão e seus filhos puseram as mãos sobre a cabeça do novilho por oferta pelo pecado;</p>
        <p><span class="numeros">15)</span> e o matou; e Moisés tomou o sangue, e o pôs sobre os chifres do altar e em redor com o seu dedo, e purificou o altar; depois derramou o sangue na base do altar e o santificou, para fazer reconciliação sobre ele.</p>
        <p><span class="numeros">16)</span> E ele tomou toda a gordura que estava na entranha, e o redanho sobre o fígado, e os dois rins, e sua gordura; e Moisés os queimou sobre o altar.</p>
        <p><span class="numeros">17)</span> Mas o novilho, e o seu couro, e a sua carne, e o seu esterco ele queimou com fogo fora do acampamento, como o Senhor ordenou a Moisés.</p>
        <p><span class="numeros">18)</span> E ele trouxe o carneiro da oferta queimada; e Arão e seus filhos puseram as mãos sobre a cabeça do carneiro.</p>
        <p><span class="numeros">19)</span> E ele o matou; e Moisés espargiu o sangue sobre o altar e ao redor.</p>
        <p><span class="numeros">20)</span> E ele cortou o carneiro em pedaços; e Moisés queimou a cabeça, e os pedaços, e a gordura.</p>
        <p><span class="numeros">21)</span> E ele lavou a entranha e as pernas com água; e Moisés queimou todo o carneiro sobre o altar; era um sacrifício queimado de cheiro suave, e uma oferta feita por fogo ao Senhor, como o Senhor ordenou a Moisés.</p>
        <p><span class="numeros">22)</span> E ele trouxe o outro carneiro, o carneiro da consagração; e Arão e seus filhos puseram as suas mãos sobre a cabeça do carneiro;</p>
        <p><span class="numeros">23)</span> e ele o matou; e Moisés tomou do seu sangue, e o pôs sobre a ponta da orelha direita de Arão, e sobre o polegar da sua mão direita, e sobre o dedo grande do seu pé direito.</p>
        <p><span class="numeros">24)</span> E ele trouxe os filhos de Arão; e Moisés pôs o sangue sobre a ponta da orelha direita deles, e sobre o polegar da mão direita, e sobre o dedo grande do pé direito; e Moisés espargiu o sangue sobre o altar e ao redor.</p>
        <p><span class="numeros">25)</span> E ele tomou a gordura, e a cauda, e toda a gordura que estava na entranha, e o redanho sobre o fígado, e os dois rins, e a sua gordura e a espádua direita.</p>
        <p><span class="numeros">26)</span> E do cesto dos pães sem fermentos, que estava diante do Senhor, ele tomou um bolo sem fermento, e um bolo de pão azeitado, e uma obreia, e os pôs sobre a gordura e sobre a espádua direita.</p>
        <p><span class="numeros">27)</span> E tudo isso ele pôs nas mãos de Arão e sobre as mãos de seus filhos, e os moveu por oferta movida perante o Senhor.</p>
        <p><span class="numeros">28)</span> E Moisés tomou-os das suas mãos, e os queimou no altar sobre a oferta queimada; estas foram consagrações por cheiro suave, isto é uma oferta feita por fogo ao Senhor.</p>
        <p><span class="numeros">29)</span> E Moisés tomou o peito e moveu-o por oferta movida perante o Senhor; aquela foi, pois, a parte de Moisés, do carneiro da consagração, como o Senhor ordenou a Moisés.</p>
        <p><span class="numeros">30)</span> E Moisés tomou do óleo da unção, e do sangue que estava sobre o altar, e o espargiu sobre Arão, e sobre as suas vestes, e sobre os seus filhos, e sobre as vestes de seus filhos com ele; e santificou a Arão, e as suas vestes, e seus filhos, e as vestes de seus filhos com ele.</p>
        <p><span class="numeros">31)</span> E Moisés disse a Arão e a seus filhos: Cozei a carne na porta do tabernáculo da congregação, e ali a comei com o pão que está no cesto das consagrações, como eu ordenei, dizendo: Arão e seus filhos a comerão.</p>
        <p><span class="numeros">32)</span> E o que restar da carne e do pão queimareis com fogo.</p>
        <p><span class="numeros">33)</span> E não saireis da porta do tabernáculo da congregação por sete dias, até se cumprirem os dias da vossa consagração; por sete dias ele vos consagrará.</p>
        <p><span class="numeros">34)</span> Como se fez neste dia, assim o Senhor ordenou que se fizesse, para fazer expiação por vós.</p>
        <p><span class="numeros">35)</span> Portanto, ficareis à porta do tabernáculo da congregação dia e noite por sete dias, e fareis a guarda do Senhor, para que não morrais; porque assim me foi ordenado.</p>
        <p><span class="numeros">36)</span> Então Arão e seus filhos fizeram todas as coisas que o Senhor ordenou pela mão de Moisés.&nbsp;<a href="lv-9.php"><span class="proximo">Próximo capítulo</span></a></p>
