        <a href="lv.php"><h3>Levítico 27</h3></a>
        <p><span class="numeros">2)</span> Fala aos filhos de Israel, e dize-lhes: Quando um homem fizer um voto especial, as pessoas serão ao Senhor por tua avaliação.</p>
        <p><span class="numeros">3)</span> E a tua avaliação será de um homem, da idade de vinte anos até os sessenta anos, será a tua avaliação de cinquenta siclos de prata, segundo o siclo do santuário.</p>
        <p><span class="numeros">4)</span> E se for mulher, a tua avaliação será de trinta siclos.</p>
        <p><span class="numeros">5)</span> E se for da idade de cinco anos até vinte anos, a tua avaliação do homem será de vinte siclos, e a da mulher, de dez siclos.</p>
        <p><span class="numeros">6)</span> E se for da idade de um mês até cinco anos, então a tua avaliação do homem será de cinco siclos de prata, e a tua avaliação da mulher será de três siclos de prata.</p>
        <p><span class="numeros">7)</span> E se for da idade de sessenta anos e acima, se for homem, a tua avaliação será de quinze siclos, e a da mulher, de dez siclos.</p>
        <p><span class="numeros">8)</span> Mas se ele for mais pobre do que a tua avaliação, então ele se apresentará diante do sacerdote, para que o sacerdote o avalie; conforme a habilidade que prometeu o sacerdote o avaliará.</p>
        <p><span class="numeros">9)</span> E se for um animal, do qual o homem traz uma oferta ao Senhor, tudo que algum homem der ao Senhor será santo.</p>
        <p><span class="numeros">10)</span> Ele não a alterará, nem a trocará bom por mau, ou mau por bom; mas se ele trocar animal por animal, tanto o que for trocado como aquele por que se trocou, serão santas.</p>
        <p><span class="numeros">11)</span> E, se for algum animal impuro, dos que eles não oferecem em sacrifício ao Senhor, então ele apresentará o animal diante do sacerdote.</p>
        <p><span class="numeros">12)</span> E o sacerdote o avaliará, seja bom ou seja mau; conforme avaliação do sacerdote, assim será.</p>
        <p><span class="numeros">13)</span> Porém, se em alguma maneira o resgatar, então, ele acrescentará a quinta parte, além da tua avaliação.</p>
        <p><span class="numeros">14)</span> E quando um homem santificar a sua casa para ser santa ao Senhor, então o sacerdote a avaliará, seja boa, seja má; como o sacerdote a avaliar, assim será.</p>
        <p><span class="numeros">15)</span> E se o que santificou resgatar a sua casa, então, ele acrescentará a quinta parte do dinheiro da tua avaliação, e será dele.</p>
        <p><span class="numeros">16)</span> E se um homem santificar ao Senhor uma parte do campo da sua possessão, então a tua avaliação será segundo a sua semente: um ômer de semente de cevada será avaliado por cinquenta siclos de prata.</p>
        <p><span class="numeros">17)</span> Se ele santificar o seu campo desde o ano do jubileu, conforme a tua avaliação ficará.</p>
        <p><span class="numeros">18)</span> Mas se ele santificar o seu campo depois do jubileu, então o sacerdote lhe contará o dinheiro conforme os anos restantes até o ano do jubileu, e isto se abaterá da tua estimativa.</p>
        <p><span class="numeros">19)</span> E se aquele que santificou o campo de alguma maneira o resgatar, então ele acrescentará a quinta parte do dinheiro da tua estimativa, e lhe será assegurado.</p>
        <p><span class="numeros">20)</span> E se ele não resgatar o campo ou se ele vender o campo a outro homem, não será mais resgatado.</p>
        <p><span class="numeros">21)</span> Mas o campo, quando sair no ano do jubileu, será santo ao Senhor, como um campo consagrado; a possessão dele será do sacerdote.</p>
        <p><span class="numeros">22)</span> E se um homem santificar ao Senhor um campo que ele comprou, o qual não é dos campos da sua possessão,</p>
        <p><span class="numeros">23)</span> então o sacerdote contará o valor da tua avaliação até o ano do jubileu; e ele dará a tua avaliação naquele dia, como coisa santa ao Senhor.</p>
        <p><span class="numeros">24)</span> No ano do jubileu, o campo retornará àquele de quem foi comprado, àquele a quem a terra pertencia.</p>
        <p><span class="numeros">25)</span> E toda a tua avaliação será conforme o siclo do santuário; de vinte geras será o siclo.</p>
        <p><span class="numeros">26)</span> Somente o primogênito dos animais, por já ser do Senhor, nenhum homem o santificará; seja boi ou ovelha, ele é do Senhor.</p>
        <p><span class="numeros">27)</span> E se for um animal impuro, então ele o resgatará, de acordo com a tua avaliação, e acrescentará a quinta parte; ou se não for resgatado, então deverá ser vendido de acordo com a tua avaliação.</p>
        <p><span class="numeros">28)</span> Todavia, nenhuma coisa consagrada, que um homem consagrar ao Senhor de tudo o que ele tem, tanto de homem como de animal, ou do campo de sua possessão, se venderá ou se resgatará; toda coisa consagrada será uma coisa santíssima ao Senhor.</p>
        <p><span class="numeros">29)</span> Nenhuma coisa consagrada, que for consagrada do homem, será resgatada; mas certamente será colocada à morte.</p>
        <p><span class="numeros">30)</span> E todos os dízimos da terra, quer da semente da terra, ou do fruto da árvore são do Senhor; santas são ao Senhor.</p>
        <p><span class="numeros">31)</span> E se um homem quiser resgatar de seus dízimos, acrescentará a sua quinta parte sobre ela.</p>
        <p><span class="numeros">32)</span> E acerca dos dízimos do gado e do rebanho, de tudo o que passar debaixo da vara, a décima parte será santa ao Senhor.</p>
        <p><span class="numeros">33)</span> Ele não procurará entre o bom e o mau, nem o trocará; e se ele o trocar, então tanto ele como o trocado serão santos; não serão resgatados.</p>
