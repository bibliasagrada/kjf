        <p></p>
        <p>Terceiro livro da Bíblia, a palavra “levítico” é uma referência aos levitas, os primeiros sacerdotes judaicos, responsáveis por exercer a função sacerdotal para o seu povo. Nesse livro, que é direcionado ao povo de Israel, encontramos passagens voltadas aos sacerdotes. Acompanhamos também os acontecimentos desde o fim da construção do Tabernáculo até a saída do povo do Monte Sinai.</p>

        <p>Ao longo dos capítulos, vemos os discursos de Deus a Moisés, que deveria reproduzi-los aos israelitas durante o êxodo e os modos como as ofertas deveriam ser realizadas no Tabernáculo. Além disso, são mostradas informações sobre a ordenação dos sacerdotes de Israel, tendo Arão e seus filhos como os primeiros sacerdotes.</p>

        <p>Em Levítico, podemos entender que o Senhor desejava que os israelitas vivessem em santidade. Assim, o povo deveria oferecer sacrifícios e ofertas que agradassem a Deus, como a expiação (purificação ou perdão).</p>

        <p>Com o estabelecimento dos sacerdotes e das ofertas, nos é mostrado as leis de pureza e impureza. A pureza era considerada um meio para que as pessoas pudessem se aproximar de Deus e permanecer em bom convívio com a comunidade.</p>

        <p>Os capítulos finais são compostos pelo Código de Santidade, onde práticas são proibidas, como por exemplo, a blasfêmia e as penalidades voltadas para esses atos.</p>

        <p>Portanto, o livro de Levítico é composto de diversas informações sobre o início dos sacerdotes, as ofertas e sacrifícios que o povo deveria realizar, as leis de pureza e impureza e o Código de Santidade. Além dessas orientações, o livro também traz incentivos de uma boa postura e convívio entre as pessoas.</p>
