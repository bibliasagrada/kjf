        <p><span class="numero">1)</span> E o Senhor chamou Moisés, e falou com ele do tabernáculo da congregação, dizendo:</p>
        <p><span class="numero">2)</span> Fala aos filhos de Israel, e dize-lhes: se algum de vós oferecer uma oferta ao Senhor, oferecereis as vossas ofertas de gado, de manada e de rebanho.</p>
        <p><span class="numero">3)</span> Se a sua oferta for um sacrifício queimado do rebanho, que ele ofereça um macho sem defeito; ele a oferecerá de sua própria vontade à porta do tabernáculo da congregação, perante o Senhor.</p>
        <p><span class="numero">4)</span> E ele colocará a sua mão sobre a cabeça da oferta queimada, para que este seja aceito por ele, para fazer expiação por ele.</p>
        <p><span class="numero">5)</span> E ele matará o novilho perante o Senhor; e os sacerdotes, filhos de Arão, trarão o sangue e espargirão o sangue ao redor e sobre o altar que está à porta do tabernáculo da congregação.</p>
        <p><span class="numero">6)</span> E ele esfolará a oferta queimada, e a partirá nos seus pedaços.</p>
        <p><span class="numero">7)</span> E os filhos de Arão, o sacerdote, colocarão fogo sobre o altar, e colocarão a lenha em ordem sobre o fogo.</p>
        <p><span class="numero">8)</span> E os sacerdotes, os filhos de Arão, colocarão as partes, a cabeça e a gordura em ordem sobre a lenha que está no fogo, que está sobre o altar;</p>
        <p><span class="numero">9)</span> mas a sua entranha e as suas pernas serão lavadas na água; e o sacerdote queimará tudo no altar; para ser um sacrifício queimado, uma oferta feita por fogo, de cheiro suave ao Senhor.</p>
        <p><span class="numero">10)</span> E se a sua oferta for de rebanhos, a saber, de ovelhas ou de cabras, para sacrifício queimado, ele trará um macho sem defeito.</p>
        <p><span class="numero">11)</span> E ele o matará ao lado do altar, em direção ao norte, perante o Senhor; e os sacerdotes, os filhos de Arão, espargirão o seu sangue ao redor e sobre o altar.</p>
        <p><span class="numero">12)</span> E ele cortará os seus pedaços, com sua cabeça e sua gordura; e o sacerdote os colocará em ordem sobre a lenha que está no fogo, que está sobre o altar;</p>
        <p><span class="numero">13)</span> mas serão lavadas a entranha e as pernas com água; e o sacerdote trará tudo isto e o queimará sobre o altar; isto é um sacrifício queimado, uma oferta feita por fogo, de cheiro suave ao Senhor.</p>
        <p><span class="numero">14)</span> E se a sua oferta ao Senhor for sacrifício queimado de aves, então ele trará a sua oferta de rolas, ou de pombinhos.</p>
        <p><span class="numero">15)</span> E o sacerdote a trará sobre o altar, e lhe torcerá a cabeça, e a queimará sobre o altar; e o seu sangue será espremido ao lado do altar;</p>
        <p><span class="numero">16)</span> e ele arrancará o seu papo com as suas penas, e o lançará ao lado do altar, na parte do leste, perto do lugar das cinzas.</p>
