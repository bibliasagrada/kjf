        <a href="lv.php"><h3>Levítico 21</h3></a>
        <p><span class="numeros">1)</span> E o Senhor disse a Moisés: Fala aos sacerdotes, filhos de Arão, e dize-lhes: Ninguém se contaminará por causa do morto entre o seu povo,</p>
        <p><span class="numeros">2)</span> salvo por seu parente mais próximo, ou seja, por sua mãe, e por seu pai, e por seu filho, e por sua filha, e por seu irmão,</p>
        <p><span class="numeros">3)</span> e por sua irmã virgem, que é próxima a ele, que ainda não teve marido; por ela poderá ele ser contaminado.</p>
        <p><span class="numeros">4)</span> Mas ele não se contaminará a si mesmo, sendo o principal homem entre o seu povo, para se profanar.</p>
        <p><span class="numeros">5)</span> Eles não farão calvície sobre a sua cabeça, nem rasparão os cantos da sua barba, nem farão cortes na sua carne.</p>
        <p><span class="numeros">6)</span> Eles serão santos para o seu Deus, e não profanarão o nome do seu Deus, porque as ofertas do Senhor feitas por fogo, e o pão do seu Deus eles oferecem; portanto, eles serão santos.</p>
        <p><span class="numeros">7)</span> Eles não tomarão mulher que é prostituta ou profana, nem tomarão mulher repudiada de seu marido, porque ele é santo para o seu Deus.</p>
        <p><span class="numeros">8)</span> Portanto, tu o santificarás, pois ele oferece o pão do teu Deus; ele será santo para ti, porque eu, o Senhor que vos santifica, sou santo.</p>
        <p><span class="numeros">9)</span> E a filha de qualquer sacerdote, se ela profanar-se, prostituindo-se, profana a seu pai; com fogo ela será queimada.</p>
        <p><span class="numeros">10)</span> E aquele que é o sumo sacerdote entre seus irmãos, sobre cuja cabeça foi derramado o óleo da unção, e que é consagrado para colocar as vestes, não descobrirá a sua cabeça, nem rasgará as suas vestes;</p>
        <p><span class="numeros">11)</span> e não se chegará a nenhum corpo morto, nem se contaminará por causa de seu pai, ou por sua mãe;</p>
        <p><span class="numeros">12)</span> nem sairá do santuário, para que não profane o santuário do seu Deus, porque a coroa do óleo da unção do seu Deus está sobre ele. Eu sou o Senhor.</p>
        <p><span class="numeros">13)</span> E ele tomará uma mulher na sua virgindade.</p>
        <p><span class="numeros">14)</span> Uma viúva, ou uma mulher divorciada, ou profana, ou uma prostituta, estas ele não tomará, mas ele tomará uma virgem de seu próprio povo por mulher.</p>
        <p><span class="numeros">15)</span> Ele não profanará a sua semente entre os seus povos; porque eu, o Senhor, o santifico.</p>
        <p><span class="numeros">16)</span> E o Senhor falou a Moisés, dizendo:</p>
        <p><span class="numeros">17)</span> Fala a Arão, dizendo: Quem quer que seja da tua semente, nas suas gerações, que tiver algum defeito, não se aproximará para oferecer o pão do seu Deus.</p>
        <p><span class="numeros">18)</span> Porque qualquer que seja o homem que tiver defeito não se aproximará: um homem cego, ou coxo, ou de nariz chato, ou com alguma coisa em excesso;</p>
        <p><span class="numeros">19)</span> ou homem que tiver o pé quebrado, ou a mão quebrada,</p>
        <p><span class="numeros">20)</span> ou corcunda, ou anão, ou que tiver defeito no olho, ou sarna, ou impigens, ou que tenha suas pedras quebradas.</p>
        <p><span class="numeros">21)</span> Nenhum homem da semente de Arão, o sacerdote, que tiver defeito, se chegará para oferecer as ofertas do Senhor feitas por fogo; ele tem um defeito; não se chegará para oferecer o pão do seu Deus.</p>
        <p><span class="numeros">22)</span> Ele comerá o pão do seu Deus, tanto do santíssimo como do santo.</p>
        <p><span class="numeros">23)</span> Ele apenas não entrará até ao véu, nem se chegará ao altar, porque ele tem um defeito; para que não profane os meus santuários; porque eu, o Senhor, os santifico.</p>
