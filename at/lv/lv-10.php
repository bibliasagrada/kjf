        <a href="lv.php"><h3>Levítico 10</h3></a>
        <p><span class="numeros">1)</span> E Nabade e Abiú, filhos de Arão, tomaram cada um o seu incensário, e puseram neles fogo, e puseram incenso sobre ele, e ofereceram fogo estranho perante o Senhor, o qual não lhes fora ordenado.</p>
        <p><span class="numeros">2)</span> E saiu fogo de diante do Senhor e os devorou; e eles morreram perante o Senhor.</p>
        <p><span class="numeros">3)</span> Então disse Moisés a Arão: isto é o que o Senhor falou, dizendo: Serei santificado naqueles que se chegarem a mim e diante de todo o povo eu serei glorificado. E Arão calou-se.</p>
        <p><span class="numeros">4)</span> E Moisés chamou Misael e Elzafã, filhos de Uziel, tio de Arão, e disse-lhes: chegai, tirai vossos irmãos de diante do santuário para fora do acampamento.</p>
        <p><span class="numeros">5)</span> Então eles chegaram e levaram-nos nas suas túnicas para fora do acampamento, como Moisés havia dito.</p>
        <p><span class="numeros">6)</span> E Moisés disse a Arão, e a Eleazar e Itamar, seus filhos: Não descobrireis as vossas cabeças, nem rasgareis vossas vestes, para que não morrais, nem venha a ira sobre todo o povo; mas que os vossos irmãos, toda a casa de Israel, lamentem o fogo que o Senhor acendeu.</p>
        <p><span class="numeros">7)</span> E não saireis da porta do tabernáculo da congregação, para que não morrais; porque o óleo da unção do Senhor está sobre vós. E eles fizeram conforme a palavra de Moisés.</p>
        <p><span class="numeros">8)</span> E o Senhor falou a Arão, dizendo:</p>
        <p><span class="numeros">9)</span> Não bebereis vinho ou bebida forte, tu, e nem teus filhos contigo, quando entrardes no tabernáculo da congregação, para que não morrais; isso será um estatuto eterno por vossas gerações,</p>
        <p><span class="numeros">10)</span> para fazer diferença entre o santo e o profano, e entre o impuro e o limpo,</p>
        <p><span class="numeros">11)</span> e para ensinar aos filhos de Israel todos os estatutos que o Senhor lhes tem falado pela mão de Moisés.</p>
        <p><span class="numeros">12)</span> E disse Moisés a Arão, e a Eleazar e a Itamar, seus filhos que ficaram: Tomai a oferta de alimento, que restou das ofertas ao Senhor feitas por fogo, e comei-a sem levedura junto ao altar, porquanto é uma coisa santíssima.</p>
        <p><span class="numeros">13)</span> E o comereis no lugar santo; porque esta é a tua porção, e a porção de teus filhos, dos sacrifícios ao Senhor feitos por fogo; porque assim me foi ordenado.</p>
        <p><span class="numeros">14)</span> Também o peito da oferta movida e a espádua da oferta alçada comereis em lugar limpo, tu, e teus filhos, e tuas filhas contigo; porque foram dados por tua porção e por porção de teus filhos, dos sacrifícios das ofertas de paz dos filhos de Israel.</p>
        <p><span class="numeros">15)</span> A espádua da oferta alçada e o peito da oferta movida trarão com as ofertas feitas por fogo de gordura, para mover por uma oferta movida perante o Senhor; e para ti e teus filhos contigo por um estatuto eterno, como o Senhor tem ordenado.</p>
        <p><span class="numeros">16)</span> E Moisés diligentemente buscou o bode da oferta pelo pecado, e eis que estava queimado; e ele irou-se com Eleazar e Itamar, os filhos de Arão que ficaram vivos, dizendo:</p>
        <p><span class="numeros">17)</span> Por que não comestes a oferta pelo pecado no lugar santo, sabendo que é coisa santíssima, e que Deus a deu a vós para que levásseis a iniquidade da congregação, para fazer expiação por eles diante do Senhor?</p>
        <p><span class="numeros">18)</span> Eis que não se trouxe o seu sangue para dentro do lugar santo; certamente deveríeis tê-la comido no lugar santo, como eu ordenei.</p>
        <p><span class="numeros">19)</span> E disse Arão a Moisés: Eis que hoje eles ofereceram a sua oferta pelo pecado e a sua oferta queimada perante o Senhor, e tais coisas me sucederam; se eu tivesse comido hoje a oferta pelo pecado, deveria ter sido aceita aos olhos do Senhor?</p>
