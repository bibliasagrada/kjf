        <p><span class="numeros">1)</span> E o Senhor falou a Moisés, dizendo:</p>
        <p><span class="numeros">2)</span> Ordena aos filhos de Israel que eles te tragam óleo puro de oliveira, batido, para a luminária, para fazer com que as lâmpadas queimem continuamente.</p>
        <p><span class="numeros">3)</span> Fora do véu do testemunho, no tabernáculo da congregação, Arão as porá em ordem perante o Senhor continuamente, desde a tarde até à manhã; isto será um estatuto eterno pelas vossas gerações.</p>
        <p><span class="numeros">4)</span> Ele colocará as lâmpadas em ordem sobre o castiçal puro, perante o Senhor continuamente.</p>
        <p><span class="numeros">5)</span> E tomarás da farinha fina e dela assarás doze bolos; cada bolo será de dois décimos.</p>
        <p><span class="numeros">6)</span> E os colocarás em duas fileiras, seis em cada fileira, sobre a mesa pura, perante o Senhor.</p>
        <p><span class="numeros">7)</span> E colocarás incenso puro sobre cada fileira, que será sobre o pão por memorial; uma oferta feita por fogo ao Senhor.</p>
        <p><span class="numeros">8)</span> Em cada dia do shabat, ele colocará em ordem perante o Senhor continuamente, sendo tirado dos filhos de Israel, por um pacto eterno.</p>
        <p><span class="numeros">9)</span> E será de Arão e de seus filhos, e eles comerão no lugar santo, porque coisa santíssima é para ele, das ofertas ao Senhor feitas por fogo, por estatuto eterno.</p>
        <p><span class="numeros">10)</span> E o filho de uma mulher israelita, cujo pai era um egípcio, apareceu no meio dos filhos de Israel; e este filho da mulher israelita e um homem de Israel brigavam entre si no acampamento.</p>
        <p><span class="numeros">11)</span> E o filho da mulher israelita blasfemou o nome do Senhor e o amaldiçoou. E eles trouxeram-no a Moisés (e o nome de sua mãe era Selomite, filha de Dibri, da tribo de Dã).</p>
        <p><span class="numeros">12)</span> E eles o colocaram em custódia, até que a mente do Senhor se revelasse a eles.</p>
        <p><span class="numeros">13)</span> E o Senhor falou a Moisés, dizendo:</p>
        <p><span class="numeros">14)</span> Trazei para fora do acampamento aquele que amaldiçoou, e que todos os que o ouviram ponham as suas mãos sobre a sua cabeça e que toda a congregação o apedreje.</p>
        <p><span class="numeros">15)</span> E tu falarás aos filhos de Israel, dizendo: Todo aquele que amaldiçoar o seu Deus carregará o seu pecado.</p>
        <p><span class="numeros">16)</span> E aquele que blasfemar o nome do Senhor certamente morrerá; e toda a congregação certamente o apedrejará; assim como o estrangeiro e o que é nascido na terra, quando blasfemar o nome do Senhor, será morto.</p>
        <p><span class="numeros">17)</span> E aquele que matar a algum homem, certamente morrerá.</p>
        <p><span class="numeros">18)</span> E aquele que matar um animal, o restituirá; animal por animal.</p>
        <p><span class="numeros">19)</span> E se um homem causar uma lesão em seu próximo, como ele fez, assim lhe será feito:</p>
        <p><span class="numeros">20)</span> quebradura por quebradura, olho por olho, dente por dente; assim como ele causou uma lesão em um homem, assim se lhe fará também.</p>
        <p><span class="numeros">21)</span> E o que matar um animal, o restituirá; mas quem matar um homem será morto.</p>
        <p><span class="numeros">22)</span> Tereis uma mesma lei, assim será para o estrangeiro como para um do seu próprio país; porque eu sou o Senhor vosso Deus.</p>
        <p><span class="numeros">23)</span> E Moisés falou aos filhos de Israel, que levassem o que havia amaldiçoado para fora do acampamento, e o apedrejassem com pedras; e os filhos de Israel fizeram como o Senhor ordenara a Moisés.&nbsp;<a href="lv-25.php"><span class="proximo">Próximo capítulo</span></a></p>
