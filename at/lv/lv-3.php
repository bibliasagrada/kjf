        <a href="lv.php"><h3>Levítico 3</h3></a>
        <p><span class="numero">1)</span> E se a sua oblação for um sacrifício de oferta de paz, se ele a oferecer do rebanho, seja macho ou fêmea, ele a oferecerá sem defeito diante do Senhor.</p>
        <p><span class="numero">2)</span> E ele colocará a sua mão sobre a cabeça da sua oferta, e a matará na porta do tabernáculo da congregação; e os filhos de Arão, os sacerdotes, espargirão o sangue sobre e ao redor do altar.</p>
        <p><span class="numero">3)</span> E ele oferecerá do sacrifício da oferta de paz uma oferta feita por fogo ao Senhor; a gordura que cobre a entranha e toda a gordura que está sobre a entranha;</p>
        <p><span class="numero">4)</span> e os dois rins, e a gordura que está sobre eles, e sobre os lados, e o redanho que está sobre o fígado, com os rins, ele tirará.</p>
        <p><span class="numero">5)</span> E os filhos de Arão queimarão isso sobre o altar, em cima do sacrifício queimado, que está sobre a lenha que está no fogo; isto é uma oferta feita por fogo, de cheiro suave ao Senhor.</p>
        <p><span class="numero">6)</span> E se a sua oferta por um sacrifício de oferta de paz ao Senhor for de rebanho, macho ou fêmea, ele a oferecerá sem defeito.</p>
        <p><span class="numero">7)</span> Se ele oferecer um cordeiro por sua oferta, oferecê-lo-á perante o Senhor.</p>
        <p><span class="numero">8)</span> E ele colocará a sua mão sobre a cabeça da sua oferta, e a matará diante do tabernáculo da congregação; e os filhos de Arão espargirão o seu sangue sobre o altar, e ao redor.</p>
        <p><span class="numero">9)</span> E ele oferecerá do sacrifício da oferta de paz uma oferta feita por fogo ao Senhor, a sua gordura, e todo traseiro, que ele deve tirar com força pela espinha dorsal, e a gordura que cobre a entranha, e toda a gordura que está sobre a entranha,</p>
        <p><span class="numero">10)</span> como também tirará ambos os rins, e a gordura que está sobre eles, e sobre os lados, e o redanho que está sobre o fígado com os rins.</p>
        <p><span class="numero">11)</span> E o sacerdote queimará isso sobre o altar; isto é o alimento da oferta feita por fogo ao Senhor.</p>
        <p><span class="numero">12)</span> E se a sua oferta for uma cabra, então ele a oferecerá perante o Senhor.</p>
        <p><span class="numero">13)</span> E ele colocará a sua mão sobre a sua cabeça, e a matará diante do tabernáculo da congregação; e os filhos de Arão espargirão o seu sangue sobre o altar e em redor.</p>
        <p><span class="numero">14)</span> E ele oferecerá a sua oferta, uma oferta feita por fogo ao Senhor; a gordura que cobre a entranha e toda a gordura que está sobre a entranha,</p>
        <p><span class="numero">15)</span> e os dois rins, e a gordura que está sobre eles, e que está sobre as tripas e o redanho que está sobre o fígado com os rins, ele removerá.</p>
        <p><span class="numero">16)</span> E o sacerdote queimará isso sobre o altar; isto é o alimento da oferta feita por fogo, de cheiro suave; toda a gordura será do Senhor.</p>
