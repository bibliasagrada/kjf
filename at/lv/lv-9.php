        <p><span class="numeros">1)</span> E aconteceu, ao dia oitavo, que Moisés chamou Arão, e seus filhos, e os anciãos de Israel,</p>
        <p><span class="numeros">2)</span> e ele disse a Arão: toma um novilho por oferta pelo pecado, e um carneiro por oferta queimada, sem defeito, e oferece-os perante o Senhor.</p>
        <p><span class="numeros">3)</span> E aos filhos de Israel falará, dizendo: Tomai um cabrito de entre as cabras por uma oferta pelo pecado, e um novilho e um cordeiro, ambos de um ano, sem defeito, por oferta queimada;</p>
        <p><span class="numeros">4)</span> também um novilho e um carneiro, por ofertas de paz, para sacrificar perante o Senhor, e uma oferta de alimentos, misturado com óleo; porque hoje o Senhor vos aparecerá.</p>
        <p><span class="numeros">5)</span> E eles trouxeram o que ordenara Moisés, diante do tabernáculo da congregação, e chegou-se toda a congregação, e ficou de pé perante o Senhor.</p>
        <p><span class="numeros">6)</span> E disse Moisés: Esta é a coisa que o Senhor ordenou que fizésseis; e a glória do Senhor vos aparecerá.</p>
        <p><span class="numeros">7)</span> E disse Moisés a Arão: Vai até o altar, e oferece a tua oferta pelo pecado e a tua oferta queimada; e faze expiação por ti mesmo, e pelo povo; e oferece a oferta do povo, e faze expiação por eles, como ordenou o Senhor.</p>
        <p><span class="numeros">8)</span> Por isso Arão foi ao altar e matou o novilho da oferta pelo pecado, que era para si mesmo.</p>
        <p><span class="numeros">9)</span> E os filhos de Arão trouxeram-lhe o sangue; e ele molhou o dedo no sangue e o pôs sobre os chifres do altar; e derramará o sangue na base do altar;</p>
        <p><span class="numeros">10)</span> mas a gordura, e os rins, e o redanho sobre o fígado da oferta pelo pecado ele queimou sobre o altar, como Senhor ordenou a Moisés.</p>
        <p><span class="numeros">11)</span> E a carne e o couro ele queimou com fogo fora do acampamento.</p>
        <p><span class="numeros">12)</span> E ele matou a oferta queimada, e os filhos de Arão lhe apresentaram o sangue, que ele espargiu sobre o altar ao redor.</p>
        <p><span class="numeros">13)</span> E eles lhe apresentaram a oferta queimada, com seus pedaços, e a cabeça; e ele queimou-os sobre o altar.</p>
        <p><span class="numeros">14)</span> E ele lavou a entranha e as pernas, e as queimou sobre a oferta queimada no altar.</p>
        <p><span class="numeros">15)</span> E ele trouxe a oferta do povo, e tomou o bode, que era a oferta do pecado pelo povo, e o matou, e o ofereceu pelo pecado, como o primeiro.</p>
        <p><span class="numeros">16)</span> E ele trouxe a oferta queimada, e a ofereceu de acordo com o costume.</p>
        <p><span class="numeros">17)</span> E trouxe a oferta de alimentos, e dela tomou um punhado, e a queimou sobre o altar, ao lado do sacrifício queimado da manhã.</p>
        <p><span class="numeros">18)</span> Ele também matou o novilho e o carneiro por sacrifício das ofertas de paz, que era pelo povo; e os filhos de Arão apresentaram- lhe o sangue, que ele espargiu sobre o altar, e ao redor.</p>
        <p><span class="numeros">19)</span> E a gordura do novilho e do carneiro, e a cauda, e o que cobre a entranha, e os rins, e o redanho sobre o fígado.</p>
        <p><span class="numeros">20)</span> E eles colocaram a gordura sobre os peitos, e ele queimou a gordura sobre o altar;</p>
        <p><span class="numeros">21)</span> e os peitos e a espádua direita Arão moveu por oferta movida perante o Senhor, como Moisés ordenou.</p>
        <p><span class="numeros">22)</span> E Arão levantou as suas mãos em direção ao povo e os abençoou; e desceu depois de ter oferecido a oferta pelo pecado, e a oferta queimada, e a oferta de paz.</p>
        <p><span class="numeros">23)</span> E Moisés e Arão entraram no tabernáculo da congregação; e saíram, e abençoaram o povo; e a glória do Senhor apareceu a todo o povo.</p>
        <p><span class="numeros">24)</span> E saiu um fogo de diante do Senhor, e consumiu sobre o altar a oferta queimada e a gordura; vendo isso, todo o povo gritou, e caiu sobre as suas faces.&nbsp;<a href="lv-10.php"><span class="proximo">Próximo capítulo</span></a></p>
