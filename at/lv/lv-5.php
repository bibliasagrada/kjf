        <a href="lv.php"><h3>Levítico 5</h3></a>
        <p><span class="numero">1)</span> E se uma alma pecar, e ouvir uma voz de blasfêmia, e for testemunha, de algo que viu ou soube, se ele não o disser, então ele levará a sua iniquidade;</p>
        <p><span class="numero">2)</span> ou se uma alma tocar em alguma coisa impura, seja carcaça de um animal impuro, ou a carcaça de gado impuro, seja carcaça de coisas impuras que se arrastam, ainda que lhe seja oculto, contudo, ele será impuro e culpado;</p>
        <p><span class="numero">3)</span> ou se ele tocar a impureza de um homem, seja qual for a impureza com que o homem se tornou impuro, e lhe for oculto, quando o souber depois, então ele será culpado;</p>
        <p><span class="numero">4)</span> ou se uma alma jurar, pronunciando com os seus lábios para fazer o mal ou para fazer o bem, seja o que for que o homem pronuncie com juramento, e lhe for oculto, e o souber depois, então culpado será numa destas coisas.</p>
        <p><span class="numero">5)</span> E será que, sendo ele culpado em uma destas coisas, então ele confessará que pecou naquela coisa.</p>
        <p><span class="numero">6)</span> E ele trará sua oferta pela transgressão ao Senhor, pelo pecado que ele pecou, uma fêmea do rebanho, um cordeiro ou cabrito das cabras, como oferta pelo pecado; e o sacerdote fará expiação por ele acerca do seu pecado.</p>
        <p><span class="numero">7)</span> Mas se ele não for capaz de trazer um cordeiro, então ele trará por sua transgressão que cometeu, duas rolas ou dois pombinhos ao Senhor; um como oferta pelo pecado, e o outro para uma oferta queimada.</p>
        <p><span class="numero">8)</span> E ele os trará ao sacerdote, o qual oferecerá aquele que é para a oferta pelo pecado primeiro; e lhe torcerá a cabeça junto ao pescoço, mas não o dividirá.</p>
        <p><span class="numero">9)</span> E espargirá do sangue da oferta pelo pecado sobre a parte lateral do altar, porém o resto do sangue será espremido na base do altar; isto é uma oferta pelo pecado.</p>
        <p><span class="numero">10)</span> E ele oferecerá o segundo como oferta queimada conforme o costume; e o sacerdote fará expiação por ele pelo seu pecado, que ele pecou, e ele será perdoado.</p>
        <p><span class="numero">11)</span> Mas se ele não for capaz de trazer duas rolas ou dois pombinhos, então, aquele que pecou trará pela sua oferta a décima parte de um efa de farinha fina por uma oferta pelo pecado; ele não colocará óleo sobre ela, nem colocará em cima qualquer incenso, porque isto é uma oferta pelo pecado.</p>
        <p><span class="numero">12)</span> Então ele a trará ao sacerdote, e o sacerdote dela tomará um punhado como o memorial, e a queimará sobre o altar, conforme as ofertas feitas por fogo ao Senhor; isto é uma oferta pelo pecado.</p>
        <p><span class="numero">13)</span> E o sacerdote fará expiação por ele acerca do pecado que ele pecou em alguma destas coisas, e ele será perdoado; e o resto será do sacerdote, como uma oferta de alimentos.</p>
        <p><span class="numero">14)</span> E falou o Senhor a Moisés, dizendo:</p>
        <p><span class="numero">15)</span> Se uma alma cometer uma transgressão, e pecar por ignorância nas coisas sagradas do Senhor, então ele trará ao Senhor por sua transgressão um carneiro sem defeito do rebanho, com a tua estimação em siclos de prata, segundo o siclo do santuário, por uma oferta pela transgressão.</p>
        <p><span class="numero">16)</span> E ele fará restituição pelo dano que ele fez à coisa sagrada, e acrescentará a quinta parte, e a dará ao sacerdote; e o sacerdote fará expiação para ele com o carneiro da oferta pela transgressão, e ele será perdoado.</p>
        <p><span class="numero">17)</span> E se uma alma pecar, e cometer alguma destas coisas que são proibidas de serem feitas, conforme os mandamentos do Senhor, e ele não sabia, ainda assim será culpado, e levará a sua iniquidade.</p>
        <p><span class="numero">18)</span> E ele trará ao sacerdote um carneiro sem defeito do rebanho, conforme a tua estimativa, por oferta pela transgressão, e o sacerdote fará expiação para ele acerca da sua ignorância, naquilo que errou sem saber, e ele será perdoado.</p>
