        <p><span class="numeros">1)</span> E o Senhor falou a Moisés, dizendo:</p>
        <p><span class="numeros">2)</span> Fala a Arão, e aos seus filhos, e a todos os filhos de Israel, e dize-lhes: Esta é acoisa que o Senhor ordenou, dizendo:</p>
        <p><span class="numeros">3)</span> Qualquer homem da casa de Israel que matar boi, ou cordeiro, ou cabra no acampamento, ou quem os matar fora do acampamento,</p>
        <p><span class="numeros">4)</span> e os não trouxer à porta do tabernáculo da congregação, para oferecer uma oferta ao Senhor diante do tabernáculo do Senhor, a tal homem será imputado o sangue; ele derramou sangue; e esse homem será cortado do seu povo;</p>
        <p><span class="numeros">5)</span> a fim de que os filhos de Israel possam trazer seus sacrifícios, que eles ofereceram sobre campo aberto, e que eles possam trazê- los ao Senhor, à porta do tabernáculo da congregação, ao sacerdote, e os ofereçam por ofertas de paz ao Senhor.</p>
        <p><span class="numeros">6)</span> E o sacerdote espargirá o sangue sobre o altar do Senhor à porta do
        tabernáculo da congregação, e queimará a gordura por cheiro suave ao Senhor.</p>
        <p><span class="numeros">7)</span> E nunca mais eles oferecerão os seus sacrifícios aos demônios, com os quais eles se prostituíram; isto lhes será por estatuto eterno nas suas gerações.</p>
        <p><span class="numeros">8)</span> E tu lhes dirás: Qualquer homem da casa de Israel, ou dos estrangeiros que peregrinam entre vós, que oferecer uma oferta queimada ou sacrifício,</p>
        <p><span class="numeros">9)</span> e não o trouxer à porta do tabernáculo da congregação, para oferecê-lo ao Senhor, até o tal homem será cortado entre o seu povo.</p>
        <p><span class="numeros">10)</span> E qualquer homem da casa de Israel, ou dos estrangeiros que peregrinam entre vós que comer qualquer tipo de sangue, eu colocarei a minha face contra a
        alma que comer o sangue, e a cortarei entre o seu povo.</p>
        <p><span class="numeros">11)</span> Porque a vida da carne está no sangue, e eu o tenho dado a vós sobre o altar, para fazer expiação pelas vossas almas, pois este é o sangue que faz expiação
        pela alma.</p>
        <p><span class="numeros">12)</span> Portanto, eu disse aos filhos de Israel: Nenhuma alma dentre vós comerá sangue, nem o estrangeiro que peregrina entre vós comerá sangue.</p>
        <p><span class="numeros">13)</span> E qualquer homem dos filhos de Israel, ou dos estrangeiros que peregrinam entre vós que caçar e capturar qualquer animal ou ave que possa ser comida; derramará o seu sangue e o cobrirá com pó.</p>
        <p><span class="numeros">14)</span> Porquanto é a vida de toda a carne; o seu sangue é pela sua vida; portanto eu disse aos filhos de Israel: Não comereis o sangue de nenhum tipo de carne, porque a vida de toda a carne é o seu sangue; qualquer que o comer será cortado.</p>
        <p><span class="numeros">15)</span> E toda alma que comer do que morreu por si, ou que foi dilacerado por
        animais, seja alguém do seu próprio país, ou um estrangeiro, lavará as suas vestes, e se banhará com água, e será impura até à tarde; depois estará limpa.</p>
