		<a href="ex.php"><h3>Êxodo 18</h3></a>
		<p><span class="numero">1)</span> Quando Jetro, o sacerdote de Midiã, sogro de Moisés, ouviu tudo que Deus havia feito a Moisés e a Israel, seu povo, e que o Senhor havia tirado Israel do Egito,</p>
		<p><span class="numero">2)</span> então Jetro, sogro de Moisés, tomou Zípora, mulher de Moisés, depois que ele a enviara de volta,</p>
		<p><span class="numero">3)</span> e seus dois filhos, dos quais o nome de um era Gérson, porque ele disse: Fui estrangeiro em uma terra estranha,</p>
		<p><span class="numero">4)</span> o nome do outro era Eliézer, porque o Deus de meu pai, disse ele, foi minha ajuda, e me libertou da espada de Faraó,</p>
		<p><span class="numero">5)</span> e Jetro, sogro de Moisés, veio com seus filhos e sua mulher a Moisés no deserto, onde ele acampava junto ao monte de Deus;</p>
		<p><span class="numero">6)</span> e disse a Moisés: Eu, teu sogro Jetro, venho a ti, com tua mulher e os seus dois filhos com ela.</p>
		<p><span class="numero">7)</span> E Moisés saiu ao encontro de seu sogro, e inclinou-se e o beijou. E perguntaram-se pelo seu bem-estar, e entraram na tenda.</p>
		<p><span class="numero">8)</span> E Moisés contou a seu sogro tudo que o Senhor havia feito a Faraó e aos egípcios por causa de Israel, e toda a aflição que os acometera no caminho, e como o Senhor os livrara.</p>
		<p><span class="numero">9)</span> E Jetro se alegrou por toda a bondade que o Senhor havia feito a Israel, a quem havia libertado da mão dos egípcios.</p>
		<p><span class="numero">10)</span> E Jetro disse: Bendito seja o Senhor, que vos livrou da mão dos egípcios, e da mão de Faraó; que livrou o povo de debaixo da mão dos egípcios.</p>
		<p><span class="numero">11)</span> Agora eu sei que o Senhor é maior que todos os deuses, naquilo em que se ensoberbeceram, ele prevaleceu contra eles.</p>
		<p><span class="numero">12)</span> E Jetro, sogro de Moisés, tomou uma oferta queimada e sacrifícios para Deus. E veio Arão, e todos os anciãos de Israel, para comer pão com o sogro de Moisés diante de Deus.</p>
		<p><span class="numero">13)</span> E aconteceu que ao amanhecer, Moisés sentou-se para julgar o povo. E o povo estava em pé diante de Moisés desde a manhã até a tarde.</p>
		<p><span class="numero">14)</span> E quando o sogro de Moisés viu tudo que ele fazia ao povo, disse: O que é isto que fazes ao povo? Por que te assentas sozinho, e todo o povo fica em pé diante de ti desde a manhã até a tarde?</p>
		<p><span class="numero">15)</span> E disse Moisés a seu sogro: Porque o povo vem a mim para perguntar a Deus.</p>
		<p><span class="numero">16)</span> Quando eles têm uma questão, vêm a mim, e eu julgo entre um e outro, e os faço saber os estatutos de Deus, e suas leis.</p>
		<p><span class="numero">17)</span> E o sogro de Moisés lhe disse: O que estás fazendo não é bom.</p>
		<p><span class="numero">18)</span> Certamente desfalecerás, tu e este povo que está contigo, porque isto é muito pesado para ti; não és capaz de realizar isto sozinho.</p>
		<p><span class="numero">19)</span> Ouve a minha voz: Dar-te-ei conselho, e Deus será contigo; sê tu pelo povo diante de Deus, para que leves as causas a Deus;</p>
		<p><span class="numero">20)</span> e tu lhes ensinarás as ordens e leis, e lhes mostrarás o caminho em que devem andar, e a obra que devem realizar.</p>
		<p><span class="numero">21)</span> Além disso, tu proverás de todo o povo homens capazes, tementes a Deus, homens da verdade, que aborreçam a avareza, e os porás sobre eles, para serem governantes de mil, e governantes de cem, governantes de cinquenta e governantes de dez.</p>
		<p><span class="numero">22)</span> Para que julguem o povo em todo o tempo. E será que, toda causa grave eles trarão a ti, mas toda causa pequena eles julgarão; assim será mais fácil para ti, e eles carregarão a carga contigo.</p>
		<p><span class="numero">23)</span> Se fizeres isto, e Deus te ordenar assim, então serás capaz de suportar, e todo este povo também voltará a seu lugar em paz.</p>
		<p><span class="numero">24)</span> Então Moisés ouviu a voz do seu sogro, e fez tudo que ele dissera.</p>
		<p><span class="numero">25)</span> E Moisés escolheu homens capazes de todo o Israel, e os fez por cabeças sobre o povo; governantes de mil, governantes de cem, governantes de cinquenta e governantes de dez.</p>
		<p><span class="numero">26)</span> E eles julgaram o povo em todo o tempo; as causas graves traziam a Moisés, mas toda causa pequena julgavam eles mesmos.</p>
		<p><span class="numero">27)</span> E Moisés deixou seu sogro partir. E ele seguiu o caminho para a sua própria terra.&nbsp;<a href="ex-19.php"><span class="proximo">Próximo capítulo</span></a></p>
