		<a href="ex.php"><h3>Êxodo 34</h3></a>
		<p><span class="numero">1)</span> E disse o Senhor a Moisés: Lavra-te duas tábuas de pedra como as primeiras, e escreverei sobre estas tábuas as palavras que estavam nas primeiras tábuas, que quebraste.</p>
		<p><span class="numero">2)</span> E prepara-te para amanhã, e sobe de manhã ao monte Sinai, e apresenta-te ali a mim no cume do monte.</p>
		<p><span class="numero">3)</span> E nenhum homem virá contigo, nem permitas que homem algum seja visto em todo o monte; nem ovelhas nem bois se apascentarão diante do monte.</p>
		<p><span class="numero">4)</span> E ele lavrou as duas tábuas de pedra como as primeiras. E Moisés levantou-se de manhã cedo, e subiu ao monte Sinai, conforme o Senhor lhe ordenara, e tomou em suas mãos as duas tábuas de pedra.</p>
		<p><span class="numero">5)</span> E o Senhor desceu na nuvem, e se pôs ali com ele, e proclamou o nome do Senhor.</p>
		<p><span class="numero">6)</span> E o Senhor passou diante dele e proclamou: O Senhor, O Senhor Deus, misericordioso e gracioso, longânimo e grande em bondade e verdade,</p>
		<p><span class="numero">7)</span> que guarda a misericórdia em milhares, perdoando a iniquidade e a transgressão e o pecado, e que de forma alguma inocenta o culpado, e que visita a iniquidade dos pais sobre os filhos, e sobre os filhos dos filhos, até a terceira e quarta geração.</p>
		<p><span class="numero">8)</span> E Moisés se apressou, e curvou a sua cabeça na terra, e adorou.</p>
		<p><span class="numero">9)</span> E ele disse: Se agora encontrei graça aos teus olhos, ó Senhor, que o meu Senhor, rogo-te, vá entre nós, pois é um povo obstinado; e perdoa a nossa iniquidade e o nosso pecado, e toma-nos pela tua herança.</p>
		<p><span class="numero">10)</span> E ele disse: Eis que faço um pacto; diante de todo o teu povo farei maravilhas, tais que ainda não foram feitas em toda a terra, nem em qualquer nação. E todo o povo entre o qual tu estás verá a obra do Senhor; porque coisa terrível é o que faço contigo.</p>
		<p><span class="numero">11)</span> Observa o que te ordeno hoje; eis que expulso de diante de ti os amorreus, e os cananeus, e os heteus, e os ferezeus, e os heveus, e os jebuseus.</p>
		<p><span class="numero">12)</span> Toma cuidado para que não faças pacto com os habitantes da terra para a qual estás indo, para que não seja por laço no meio de ti.</p>
		<p><span class="numero">13)</span> Mas destruireis os seus altares, quebrareis as suas imagens e cortareis os seus bosques.</p>
		<p><span class="numero">14)</span> Porque não adorareis a outro deus, porque o Senhor, cujo nome é Ciumento, é um Deus ciumento,</p>
		<p><span class="numero">15)</span> para que não faças pacto com os habitantes da terra, e eles não se prostituam após os seus deuses e façam sacrifícios aos seus deuses, e te convidem, e tu comas do seu sacrifício,</p>
		<p><span class="numero">16)</span> e tomes das suas filhas para os teus filhos, e as suas filhas se prostituam após os seus deuses, e façam teus filhos se prostituírem após os seus deuses.</p>
		<p><span class="numero">17)</span> Não farás para ti deuses de fundição.</p>
		<p><span class="numero">18)</span> A festa dos pães sem fermento guardarás. Sete dias comerás pão sem fermento, como te ordenei, no tempo do mês de abibe, porque no mês de abibe saíste do Egito.</p>
		<p><span class="numero">19)</span> Tudo que abrir a madre é meu, e todo primogênito entre o teu gado, seja boi ou ovelha, que seja macho.</p>
		<p><span class="numero">20)</span> Mas o primogênito de um jumento resgatarás com um cordeiro; e se não o resgatares, então quebrarás o seu pescoço. Todo o primogênito de teus filhos resgatarás. E ninguém aparecerá diante de mim vazio.</p>
		<p><span class="numero">21)</span> Seis dias trabalharás, mas no sétimo dia descansarás; no tempo de arar e de ceifar descansarás.</p>
		<p><span class="numero">22)</span> E guardarás a festa das semanas, das primícias da ceifa do trigo, e a festa da colheita no fim do ano.</p>
		<p><span class="numero">23)</span> Três vezes por ano todos os homens aparecerão diante do Senhor Deus, o Deus de Israel.</p>
		<p><span class="numero">24)</span> Porque eu expulsarei as nações de diante de ti e alargarei os teus termos. Nenhum homem cobiçará a tua terra, quando subires para aparecer diante do Senhor teu Deus, três vezes por ano.</p>
		<p><span class="numero">25)</span> Não oferecerás o sangue do meu sacrifício com fermento; tampouco será o sacrifício da festa da páscoa deixado até a manhã seguinte.</p>
		<p><span class="numero">26)</span> O primeiro das primícias da tua terra trarás à casa do Senhor teu Deus. Não cozinharás o cabrito no leite de sua mãe.</p>
		<p><span class="numero">27)</span> E o Senhor disse a Moisés: Escreve estas palavras, porque conforme o teor destas palavras fiz um pacto contigo e com Israel.</p>
		<p><span class="numero">28)</span> E ele esteve ali com o Senhor quarenta dias e quarenta noites; não comeu pão, nem bebeu água. E ele escreveu sobre as tábuas as palavras do pacto, os dez mandamentos.</p>
		<p><span class="numero">29)</span> E ao descer Moisés do monte Sinai, as duas tábuas do testemunho estavam na mão de Moisés quando desceu do monte, não sabia Moisés que a pele da sua face resplandecia depois que o Senhor falara com ele.</p>
		<p><span class="numero">30)</span> E quando Arão e todos os filhos de Israel viram Moisés, eis que a pele da sua face resplandecia. E temeram aproximar-se dele.</p>
		<p><span class="numero">31)</span> E Moisés os chamou; e Arão e todos os governantes da congregação se voltaram a ele. E Moisés lhes falou.</p>
		<p><span class="numero">32)</span> E depois, todos os filhos de Israel se aproximaram, e ele lhes deu como ordem tudo que o Senhor havia falado com ele no monte Sinai.</p>
		<p><span class="numero">33)</span> Assim que Moisés terminou de falar com eles, colocou um véu sobre a sua face.</p>
		<p><span class="numero">34)</span> Mas quando Moisés entrava diante do Senhor para falar com ele, tirava o véu, até quando saía. E saía, e falava com os filhos de Israel aquilo que lhe fora ordenado.</p>
		<p><span class="numero">35)</span> E os filhos de Israel viam a face de Moisés, e que a pele da sua face resplandecia. E Moisés colocava novamente o véu sobre a sua face, até que entrava para falar com ele.&nbsp;<a href="ex-35.php"><span class="proximo">Próximo capítulo</span></a></p>
