		<p><span class="numero">1)</span> E o Senhor falou a Moisés, dizendo:</p>
		<p><span class="numero">2)</span> Santificai-me todos os primogênitos, todo o que abriu a madre entre os filhos de Israel, tanto do homem quanto do animal; este é meu.</p>
		<p><span class="numero">3)</span> E Moisés disse ao povo: Lembrai-vos deste dia, no qual saístes do Egito, da casa da servidão, pois com mão poderosa o Senhor os tirou desse lugar. Não se comerá pão levedado.</p>
		<p><span class="numero">4)</span> Neste dia, no mês de abibe, vós saístes.</p>
		<p><span class="numero">5)</span> E será que, quando o Senhor te houver introduzido na terra dos cananeus, e dos heteus, e dos amorreus, e dos heveus, e dos jebuseus, que ele jurou a teus pais que te daria, uma terra em que mana leite e mel, que guardarás este culto neste mês.</p>
		<p><span class="numero">6)</span> Sete dias comerás pão sem fermento, e no sétimo dia será uma festa ao Senhor.</p>
		<p><span class="numero">7)</span> Comer-se-á pão sem fermento durante sete dias, e não se verá pão levedado contigo, nem ainda se verá fermento contigo em todos os teus alojamentos.</p>
		<p><span class="numero">8)</span> E naquele dia mostrarás a teu filho, dizendo: Isso se faz por causa do que o Senhor me fez, quando eu saí do Egito.</p>
		<p><span class="numero">9)</span> E te será por sinal sobre tua mão, e por memorial entre teus olhos, para que a lei do Senhor esteja na tua boca, porque com uma mão forte o Senhor te tirou do Egito.</p>
		<p><span class="numero">10)</span> Por isso guardarás esta ordenança a seu tempo, ano após ano.</p>
		<p><span class="numero">11)</span> E acontecerá que, quando o Senhor te houver introduzido na terra dos cananeus, como jurou a ti e a teus pais, e te der,</p>
		<p><span class="numero">12)</span> que tu separarás para o Senhor tudo o que abrir a madre, e todo primogênito que vier do animal que tiveres, o macho será do Senhor.</p>
		<p><span class="numero">13)</span> E todo o primogênito da jumenta resgatarás com um cordeiro; e se não o resgatares, quebrarás o seu pescoço. E todo o primogênito do homem entre teus filhos resgatarás.</p>
		<p><span class="numero">14)</span> E acontecerá que, quando teu filho te perguntar no futuro, dizendo: O que é isto? Tu lhe dirás: Por força da mão o Senhor nos tirou do Egito, da casa de servidão.</p>
		<p><span class="numero">15)</span> E aconteceu que, quando Faraó não queria nos deixar sair, o Senhor matou todos os primogênitos da terra do Egito, tanto o primogênito do homem quanto o primogênito do animal. Por isso, eu sacrifico ao Senhor tudo que abre a madre, sendo macho, mas resgato todos os primogênitos de meus filhos.</p>
		<p><span class="numero">16)</span> E será por sinal sobre tua mão, e por frontais entre teus olhos, porque, com mão forte, o Senhor nos tirou do Egito.</p>
		<p><span class="numero">17)</span> E aconteceu que, quando Faraó havia deixado o povo ir, Deus não o conduziu pelo caminho da terra dos filisteus, embora esse fosse perto; porque Deus disse: Para que porventura o povo não se arrependa, vendo a guerra, e volte para o Egito.</p>
		<p><span class="numero">18)</span> Mas Deus fez o povo rodear, pelo caminho do deserto do mar Vermelho, e os filhos de Israel subiram armados da terra do Egito.</p>
		<p><span class="numero">19)</span> E Moisés tomou consigo os ossos de José, porque havia jurado solenemente aos filhos de Israel, dizendo: Deus certamente vos visitará, e daqui levareis meus ossos convosco.</p>
		<p><span class="numero">20)</span> E começaram sua jornada de Sucote, e acamparam em Etã, na borda do deserto.</p>
		<p><span class="numero">21)</span> E o Senhor ia adiante deles de dia numa coluna de nuvem, para conduzi-los pelo caminho, e à noite numa coluna de fogo, para lhes dar luz, para que fossem de dia e de noite.</p>
