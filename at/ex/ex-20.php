		<p><span class="numero">5)</span> Não te curvarás diante delas, nem as servirás; porque eu o Senhor teu Deus, sou um Deus ciumento, que visito a iniquidade dos pais sobre os filhos até a terceira e quarta geração daqueles que me aborrecem,</p>
		<p><span class="numero">6)</span> e mostro misericórdia a milhares dos que me amam, e guardam os meus mandamentos.</p>
		<p><span class="numero">7)</span> Não tomarás o nome do Senhor teu Deus em vão; porque o Senhor não terá por inocente aquele que tomar o seu nome em vão.</p>
		<p><span class="numero">8)</span> Lembra-te do dia do shabat, para santificá-lo.</p>
		<p><span class="numero">9)</span> Seis dias trabalharás, e farás toda a tua obra,</p>
		<p><span class="numero">10)</span> mas o sétimo dia é o shabat do Senhor teu Deus, nele não farás obra alguma, nem tu, nem teu filho, nem tua filha, nem teu servo, nem tua serva, nem teu gado, nem teu estrangeiro que está em tuas portas,</p>
		<p><span class="numero">11)</span> pois em seis dias o Senhor fez os céus e a terra, o mar e tudo que neles há, e descansou no sétimo dia. Portanto, o Senhor abençoou o dia do shabat e o santificou.</p>
		<p><span class="numero">12)</span> Honra a teu pai e a tua mãe, para que se prolonguem os teus dias na terra que o Senhor teu Deus te dá.</p>
		<p><span class="numero">13)</span> Não assassinará.</p>
		<p><span class="numero">14)</span> Não cometerás adultério.</p>
		<p><span class="numero">15)</span> Não furtarás.</p>
		<p><span class="numero">16)</span> Não darás falso testemunho contra teu próximo.</p>
		<p><span class="numero">17)</span> Não cobiçarás a casa de teu próximo, não cobiçarás a mulher de teu próximo, nem o seu servo, nem a sua serva, nem o seu boi, nem o seu jumento, nem alguma coisa que é de teu próximo.</p>
		<p><span class="numero">18)</span> E todo o povo viu os trovões, e os relâmpagos, e o barulho da trombeta, e o monte fumegando. E quando o povo viu isso, apartou- se e ficou de longe.</p>
		<p><span class="numero">19)</span> E disseram a Moisés: Fala tu conosco, e ouviremos, mas não permitas que Deus fale conosco para que não morramos.</p>
		<p><span class="numero">20)</span> E disse Moisés ao povo: Não temais, pois Deus veio para vos provar, e para que o seu temor esteja diante de vós, para que não pequeis.</p>
		<p><span class="numero">21)</span> E o povo ficou de longe, e Moisés se aproximou da densa escuridão em que Deus estava.</p>
		<p><span class="numero">22)</span> E disse o Senhor a Moisés: Assim dirás aos filhos de Israel: Vistes que falei a vós do céu.</p>
		<p><span class="numero">23)</span> Não fareis comigo deuses de prata, nem fareis para vós deuses de ouro.</p>
		<p><span class="numero">24)</span> Um altar de terra me farás e nele sacrificarás as tuas ofertas queimadas, e as tuas ofertas pacíficas, as tuas ovelhas e os teus bois. Em todos os lugares em que eu registrar o meu nome, virei a ti e te abençoarei.</p>
		<p><span class="numero">25)</span> E se me fizeres um altar de pedra, não o edificarás de pedra esculpida, porque se levantares tua ferramenta sobre ele, já o profanaste.</p>
