		<p><span class="numero">1)</span> Ora, Moisés estava apascentando o rebanho de Jetro, seu sogro, o sacerdote de Midiã. E ele conduziu o rebanho para trás do deserto, e chegou ao monte de Deus, até o Horebe.</p>
		<p><span class="numero">2)</span> E o anjo do Senhor lhe apareceu em uma chama de fogo do meio de uma sarça. E ele olhou, e eis que, a sarça queimava com fogo, e a sarça não era consumida.</p>
		<p><span class="numero">3)</span> E Moisés disse: Eu vou virar agora de lado, e verei essa grande visão, porque a sarça não é queimada.</p>
		<p><span class="numero">4)</span> E quando o Senhor viu que ele se virara para ver, Deus o chamou do meio da sarça e disse: Moisés, Moisés. E ele disse: Aqui estou.</p>
		<p><span class="numero">5)</span> E ele disse: Não te aproximes até aqui. Tira tuas sandálias dos teus pés, porque o lugar em que estás é terra santa.</p>
		<p><span class="numero">6)</span> Além disso, ele disse: Eu sou o Deus de teu pai, o Deus de Abraão, o Deus de Isaque, e o Deus de Jacó. E Moisés escondeu a sua face, pois estava com medo de olhar para Deus.</p>
		<p><span class="numero">7)</span> E o Senhor disse: Certamente vi a aflição de meu povo que está no Egito, e ouvi o seu clamor por causa de seus capatazes, pois eu conheço os seus sofrimentos;</p>
		<p><span class="numero">8)</span> e eu desci para libertá-los da mão dos egípcios, e para fazê-los sair daquela terra para uma terra boa e grande, para uma terra que mana leite e mel, para o lugar dos cananeus, e dos heteus, e dos amorreus, e dos ferezeus, e dos heveus, e dos jebuseus.</p>
		<p><span class="numero">9)</span> Agora, portanto, eis que o clamor dos filhos de Israel é vindo até mim. E eu também vi a opressão com que os egípcios os oprimem.</p>
		<p><span class="numero">10)</span> Vem agora, pois eu te enviarei a Faraó, para que tires o meu povo, os filhos de Israel, do Egito.</p>
		<p><span class="numero">11)</span> E Moisés disse a Deus: Quem sou eu, para que vá a Faraó, e para que tire os filhos de Israel do Egito?</p>
		<p><span class="numero">12)</span> E ele disse: Certamente estarei contigo, e este será um sinal para ti, de que te enviei: Quando tiveres tirado o povo do Egito, vós servireis a Deus sobre este monte.</p>
		<p><span class="numero">13)</span> E disse Moisés a Deus: Eis que, quando eu vier aos filhos de Israel e lhes disser: O Deus de vossos pais me enviou a vós, e eles me perguntarem: Qual é o seu nome? Que direi a eles?</p>
		<p><span class="numero">14)</span> E disse Deus a Moisés: Eu sou o que sou, e ele disse: Assim dirás aos filhos de Israel: Eu sou me enviou a vós.</p>
		<p><span class="numero">15)</span> E Deus disse mais a Moisés: Assim dirás aos filhos de Israel: O Senhor Deus de vossos pais, o Deus de Abraão, o Deus de Isaque e o Deus de Jacó, me enviou a vós. Este é o meu nome para sempre, e este é meu memorial para todas as gerações.</p>
		<p><span class="numero">16)</span> Vai, e ajunta os anciãos de Israel, e dize- lhes: O Senhor Deus de vossos pais, o Deus de Abraão, de Isaque, e de Jacó, me apareceu, dizendo: Eu certamente vos tenho visitado e visto o que vos tem sido feito no Egito,</p>
		<p><span class="numero">17)</span> e tenho dito: Eu vos farei subir da aflição do Egito para a terra dos cananeus, e dos heteus, e dos amorreus, e dos ferezeus, e dos heveus, e dos jebuseus, para uma terra que mana leite e mel.</p>
		<p><span class="numero">18)</span> E eles ouvirão a tua voz; e irás, tu e os anciãos de Israel, ao rei do Egito, e lhe direis: O Senhor Deus dos hebreus se encontrou conosco, e agora deixa-nos ir, te suplicamos, três dias de jornada para o deserto, para que sacrifiquemos ao Senhor nosso Deus.</p>
		<p><span class="numero">19)</span> E eu tenho certeza de que o rei do Egito não vos deixará ir, a não ser por uma mão poderosa.</p>
		<p><span class="numero">20)</span> E eu estenderei a minha mão, e ferirei o Egito com todas as maravilhas que farei no meio deles; e depois disso ele vos deixará ir.</p>
		<p><span class="numero">21)</span> E eu darei favor a esse povo aos olhos dos egípcios, e acontecerá que, quando fordes, não ireis de mãos vazias,</p>
		<p><span class="numero">22)</span> mas cada mulher pedirá de sua vizinha, e da que estiver hospedada em sua casa, joias de prata, e joias de ouro e vestes, e as poreis sobre vossos filhos, e sobre vossas filhas, e despojareis os egípcios.&nbsp;<a href="ex-4.php"><span class="proximo">Próximo capítulo</span></a></p>
