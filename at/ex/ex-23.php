		<a href="ex.php"><h3>Êxodo 23</h3></a>
		<p><span class="numero">1)</span> Não levantarás falso rumor; não porás a tua mão com o ímpio para ser testemunha falsa.</p>
		<p><span class="numero">2)</span> Não seguirás uma multidão para fazeres o mal; nem falarás em uma causa modificando a ação após muitos, distorcendo o julgamento.</p>
		<p><span class="numero">3)</span> Nem ao homem pobre favorecerás na sua causa.</p>
		<p><span class="numero">4)</span> Se encontrares o boi ou o jumento de teu inimigo desgarrado, certamente o levarás de volta a ele novamente.</p>
		<p><span class="numero">5)</span> Se vires o jumento de alguém que te aborrece deitado debaixo de sua carga, deixarás de ajudá-lo? Certamente o ajudarás.</p>
		<p><span class="numero">6)</span> Não perverterás o direito de teu pobre na sua causa.</p>
		<p><span class="numero">7)</span> Guarda-te de questões falsas; e o inocente e o justo não matarás, porque não justificarei o ímpio.</p>
		<p><span class="numero">8)</span> E tu não aceitarás presente; porque o presente cega o sábio, e perverte as palavras do justo.</p>
		<p><span class="numero">9)</span> Não oprimirás o estrangeiro, porque conheceis o coração de um estrangeiro, visto que fostes estrangeiros na terra do Egito.</p>
		<p><span class="numero">10)</span> E seis anos semearás a tua terra, e colherás o seu fruto,</p>
		<p><span class="numero">11)</span> mas no sétimo ano a deixarás descansar e ficar sossegada, para que os pobres do teu povo possam comer. E o que eles deixarem, os animais do campo comerão. Da mesma maneira agirás com tua vinha, e com teu olival.</p>
		<p><span class="numero">12)</span> Seis dias farás tua obra, e no sétimo dia descansarás, para que o teu boi e o teu jumento possam descansar, e o filho de tua serva, e o estrangeiro, possam ser revigorados.</p>
		<p><span class="numero">13)</span> E em todas as coisas que vos tenho dito, guardai-vos; e do nome de outros deuses nem fazei menção, nem sejam ouvidos da vossa boca.</p>
		<p><span class="numero">14)</span> Três vezes por ano celebrarás uma festa para mim.</p>
		<p><span class="numero">15)</span> Guardarás a festa dos pães sem fermento (Comereis pão sem fermento sete dias, como te ordenei, no tempo determinado do mês de abibe; porque nele saíste do Egito; e ninguém aparecerá vazio perante mim).</p>
		<p><span class="numero">16)</span> E a festa da sega, as primícias do teu trabalho, que semeaste no campo; e a festa da colheita, que é no fim do ano, quando tiveres colhido o teu trabalho do campo.</p>
		<p><span class="numero">17)</span> Três vezes por ano todos os teus homens aparecerão diante do Senhor Deus.</p>
		<p><span class="numero">18)</span> Não oferecerás o sangue do meu sacrifício com pão levedado; nem ficará a gordura do meu sacrifício até de manhã.</p>
		<p><span class="numero">19)</span> O primeiro das primícias da terra trarás para a casa do Senhor teu Deus. Não cozinharás um cabrito no leite de sua mãe.</p>
		<p><span class="numero">20)</span> Eis que envio um Anjo adiante de ti, para que te guarde no caminho e te conduza ao lugar que te tenho preparado.</p>
		<p><span class="numero">21)</span> Guarda-te diante dele, e obedece à sua voz; não o provoques, pois ele não perdoará as vossas transgressões, porque o meu nome está nele.</p>
		<p><span class="numero">22)</span> Mas se realmente obedeceres à sua voz, e fizeres tudo que eu disser, então serei um inimigo dos teus inimigos, e um adversário dos teus adversários.</p>
		<p><span class="numero">23)</span> Pois, o meu Anjo irá adiante de ti, e te conduzirá aos amorreus, aos heteus, aos ferezeus, aos cananeus, aos heveus, aos jebuseus. E eu os eliminarei.</p>
		<p><span class="numero">24)</span> Não te curvarás diante dos seus deuses, nem os servirás, nem farás conforme as suas obras; mas os destruirás totalmente e quebrarás completamente as suas imagens.</p>
		<p><span class="numero">25)</span> E servireis ao Senhor vosso Deus, e ele abençoará o vosso pão e a vossa água; e eu tirarei do meio de vós a enfermidade.</p>
		<p><span class="numero">26)</span> Na tua terra não haverá ninguém que aborte, nem estéril; cumprirei o número dos teus dias.</p>
		<p><span class="numero">27)</span> Enviarei o meu temor diante de ti, e destruirei todo o povo a quem fores, e farei com que todos os teus inimigos te virem as costas.</p>
		<p><span class="numero">28)</span> E enviarei vespões diante de ti, que expulsarão os heveus, os cananeus, e os heteus, de diante de ti.</p>
		<p><span class="numero">29)</span> Não os expulsarei de diante de ti em um ano, para que a terra não se torne desolada e os animais do campo se multipliquem contra ti.</p>
		<p><span class="numero">30)</span> Aos poucos os expulsarei de diante de ti, até que tu aumentes, e herdes a terra.</p>
		<p><span class="numero">31)</span> E colocarei os teus limites desde o mar Vermelho até o mar dos filisteus, e do deserto até o rio; porque entregarei os habitantes da terra em tua mão, e tu os expulsarás de diante de ti.</p>
		<p><span class="numero">32)</span> Não farás pacto com eles, nem com os seus deuses.</p>
		<p><span class="numero">33)</span> Eles não habitarão na tua terra, para que não te façam pecar contra mim, pois se servires aos seus deuses, isso certamente te será por armadilha.&nbsp;<a href="ex-24.php"><span class="proximo">Próximo capítulo</span></a></p>
