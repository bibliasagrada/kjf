		<p><span class="numero">6)</span> E José morreu, e todos os seus irmãos, e toda aquela geração.</p>
		<p><span class="numero">7)</span> E os filhos de Israel frutificaram, e aumentaram muito, e multiplicaram-se, e tornaram- se grandemente fortes; e a terra se encheu deles.</p>
		<p><span class="numero">8)</span> Depois, levantou-se um novo rei sobre o Egito, que não conhecera José.</p>
		<p><span class="numero">9)</span> E ele disse a seu povo: Eis que o povo dos filhos de Israel é maior e mais poderoso do que nós.</p>
		<p><span class="numero">10)</span> Vinde, atuemos sabiamente com eles, para que não se multipliquem e aconteça que, vindo uma guerra, eles se ajuntem com os nossos inimigos e lutem contra nós, e assim se retirem da terra.</p>
		<p><span class="numero">11)</span> Por isso, designaram sobre eles capatazes para afligi-los com suas cargas. E construíram para Faraó cidades de tesouros, Pitom e Ramessés.</p>
		<p><span class="numero">12)</span> Mas quanto mais os afligiam, mais eles se multiplicavam e cresciam. E por isso se afligiram por causa dos filhos de Israel.</p>
		<p><span class="numero">13)</span> E os egípcios fizeram os filhos de Israel servir com rigor,</p>
		<p><span class="numero">14)</span> e tornaram a sua vida amarga com dura escravidão, com argamassa e tijolos, e em todos os tipos de serviço no campo, em todo seu serviço, em que os faziam servir, era com rigor.</p>
		<p><span class="numero">15)</span> E o rei do Egito falou às parteiras hebreias, do qual o nome de uma era Sifrá, e o nome da outra Puá,</p>
		<p><span class="numero">16)</span> e ele disse: Quando fizerdes o trabalho de parteira às mulheres hebreias, e as virdes sobre os assentos, se for filho, havereis de matá-lo, mas se for filha, ela deverá viver.</p>
		<p><span class="numero">17)</span> Mas as parteiras temiam a Deus e não fizeram conforme o rei do Egito lhes ordenara, mas salvaram os meninos, deixando-os viver.</p>
		<p><span class="numero">18)</span> E o rei do Egito chamou as parteiras e lhes disse: Por que fizestes esta coisa, e salvastes os meninos, deixando-os viver?</p>
		<p><span class="numero">19)</span> E as parteiras disseram a Faraó: Porque as mulheres hebreias não são como as mulheres egípcias, pois elas são vivazes e já deram à luz antes que as parteiras cheguem a elas.</p>
		<p><span class="numero">20)</span> Por isso Deus agiu bem com as parteiras; e o povo se multiplicou, e tornou-se muito forte.</p>
		<p><span class="numero">21)</span> E aconteceu que, porque as parteiras temeram a Deus, ele lhes estabeleceu casas.</p>
