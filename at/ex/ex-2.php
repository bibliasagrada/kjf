		<a href="ex.php"><h3>Êxodo 2</h3></a>
		<p><span class="numero">1)</span> E foi um homem da casa de Levi, e tomou por mulher uma filha de Levi.</p>
		<p><span class="numero">2)</span> E a mulher concebeu, e deu à luz um filho. E quando ela viu que ele era um menino formoso, ela o escondeu por três meses.</p>
		<p><span class="numero">3)</span> E quando já não podia escondê-lo, ela tomou para ele uma arca de juncos, e a revestiu com barro e piche, e pôs o menino nela. E ela a colocou nos juncos à beira do rio.</p>
		<p><span class="numero">4)</span> E sua irmã ficou à distância, para saber o que lhe seria feito.</p>
		<p><span class="numero">5)</span> E a filha de Faraó desceu para se lavar no rio. E suas servas caminhavam ao lado do rio. E quando ela viu a arca entre os juncos, enviou sua serva para buscá-la.</p>
		<p><span class="numero">6)</span> E tendo-a aberto, ela viu o menino. E eis que o bebê chorava. E ela teve compaixão dele, e disse: Esta é uma das crianças dos hebreus.</p>
		<p><span class="numero">7)</span> Então disse sua irmã à filha de Faraó: Devo ir e chamar uma ama das mulheres dos hebreus, para que amamente o menino para ti?</p>
		<p><span class="numero">8)</span> E a filha de Faraó lhe disse: Vai. E a serva foi e chamou a mãe do menino.</p>
		<p><span class="numero">9)</span> E a filha de Faraó lhe disse: Toma este menino, e amamenta-o para mim, e eu te darei o teu salário. E a mulher tomou o menino, e o amamentou.</p>
		<p><span class="numero">10)</span> E o menino cresceu, e ela o levou à filha de Faraó, e ele se tornou seu filho. E ela chamou seu nome Moisés, e disse: Porque o tirei da água.</p>
		<p><span class="numero">11)</span> E aconteceu naqueles dias, quando Moisés havia crescido, que ele saiu a seus irmãos e viu as suas cargas. E ele viu um egípcio ferir um hebreu, um de seus irmãos.</p>
		<p><span class="numero">12)</span> E ele olhou para um lado e para o outro, e quando viu que não havia nenhum homem, matou o egípcio e o escondeu na areia.</p>
		<p><span class="numero">13)</span> E quando saiu no segundo dia, eis que dois homens dos hebreus estavam contendendo. E ele disse ao que fazia a injustiça: Por que feres a teu companheiro?</p>
		<p><span class="numero">14)</span> E ele disse: Quem te fez por príncipe e juiz sobre nós? Intentas matar-me como mataste o egípcio? E Moisés temeu e disse: Certamente este negócio já é conhecido.</p>
		<p><span class="numero">15)</span> Então, quando Faraó soube disso, tentou matar Moisés. Mas Moisés fugiu da face de Faraó; e habitou na terra de Midiã. E se assentou junto a um poço.</p>
		<p><span class="numero">16)</span> E o sacerdote de Midiã tinha sete filhas. E elas vieram e tiraram água, e encheram seus bebedouros para dar de beber ao rebanho de seu pai.</p>
		<p><span class="numero">17)</span> Então vieram os pastores e as expulsaram, mas Moisés se levantou e as ajudou, e deu de beber ao seu rebanho.</p>
		<p><span class="numero">18)</span> E quando elas vieram a Reuel, seu pai, ele disse: Por que vocês voltaram tão cedo hoje?</p>
		<p><span class="numero">19)</span> E elas disseram: Um egípcio nos libertou da mão dos pastores, e também tirou água suficiente para nós, e deu de beber ao rebanho.</p>
		<p><span class="numero">20)</span> E ele disse a suas filhas: E onde ele está? Por que vós deixastes o homem partir? Chamai-o, para que ele coma pão.</p>
		<p><span class="numero">21)</span> E Moisés ficou contente em habitar com aquele homem. E ele deu a Moisés sua filha Zípora.</p>
		<p><span class="numero">22)</span> E ela lhe deu um filho, e ele chamou seu nome Gérson, pois ele disse: Fui estrangeiro em uma terra estranha.</p>
		<p><span class="numero">23)</span> E aconteceu, ao passar do tempo, que o rei do Egito morreu. E os filhos de Israel suspiraram por causa da servidão, e eles clamaram, e seu grito subiu a Deus por causa da servidão.</p>
		<p><span class="numero">24)</span> E Deus ouviu os seus gemidos, e Deus lembrou-se do seu pacto com Abraão, com Isaque e com Jacó.</p>
