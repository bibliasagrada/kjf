		<a href="ex.php"><h3>Êxodo 17</h3></a>
		<p><span class="numero">1)</span> E toda a congregação dos filhos de Israel partiu do deserto de Sim, segundo as suas jornadas, conforme a ordem do Senhor, e acampou em Refidim. E não havia água para o povo beber.</p>
		<p><span class="numero">2)</span> Por isso o povo contendeu com Moisés, e disse: Dá-nos água para bebermos. E Moisés lhes disse: Por que contendeis comigo? Por que tentais o Senhor?</p>
		<p><span class="numero">3)</span> E o povo estava sedento por água; e o povo murmurou contra Moisés, e disse: Por que nos trouxeste do Egito, para nos matares de sede, a nós e aos nossos filhos, e ao nosso gado?</p>
		<p><span class="numero">4)</span> E Moisés clamou ao Senhor, dizendo: O que farei com este povo? Estão quase prontos para me apedrejar.</p>
		<p><span class="numero">5)</span> E disse o Senhor a Moisés: Vai adiante do povo, e toma contigo os anciãos de Israel; e o teu cajado, com a qual feriste o rio, toma na tua mão e vai.</p>
		<p><span class="numero">6)</span> Eis que estarei em pé diante de ti sobre a rocha em Horebe, e tu ferirás a rocha, e dela sairá água para que o povo possa beber. E Moisés fez assim à vista dos anciãos de Israel.</p>
		<p><span class="numero">7)</span> E chamou o nome do lugar Massá e Meribá, porque contenderam os filhos de Israel, e porque tentaram ao Senhor, dizendo: Está o Senhor entre nós ou não?</p>
		<p><span class="numero">8)</span> E veio Amaleque e lutou com Israel em Refidim.</p>
		<p><span class="numero">9)</span> E Moisés disse a Josué: Escolhe para nós homens, e vai, luta contra Amaleque. Amanhã estarei no cume do outeiro com o cajado de Deus na minha mão.</p>
		<p><span class="numero">10)</span> Então Josué fez como Moisés lhe dissera, e lutou contra Amaleque; e Moisés, Arão e Hur subiram ao cume do outeiro.</p>
		<p><span class="numero">11)</span> E aconteceu que, quando Moisés levantava sua mão, Israel prevalecia; e quando ele baixava sua mão, Amaleque prevalecia.</p>
		<p><span class="numero">12)</span> Mas as mãos de Moisés estavam pesadas; e tomaram uma pedra e a colocaram debaixo dele, e ele assentou-se nela. E Arão e Hur apoiaram suas mãos, um de um lado, e o outro, de outro lado; e suas mãos ficaram firmes até que o sol se pôs.</p>
		<p><span class="numero">13)</span> E Josué derrotou Amaleque e seu povo ao fio da espada.</p>
		<p><span class="numero">14)</span> E o Senhor disse a Moisés: Escreve isto por memorial em um livro, e repita-o nos ouvidos de Josué, porquanto eu eliminarei totalmente a memória de Amaleque de debaixo do céu.</p>
		<p><span class="numero">15)</span> E Moisés construiu um altar, e chamou seu nome Jeová-Níssi,</p>
