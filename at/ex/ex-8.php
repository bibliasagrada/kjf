		<a href="ex.php"><h3>Êxodo 8</h3></a>
		<p><span class="numero">1)</span> E falou o Senhor a Moisés: Vai a Faraó, e dize- lhe: Assim diz o Senhor: Deixa o meu povo ir, para que me sirva.</p>
		<p><span class="numero">2)</span> E se te recusares a deixá-los ir, eis que ferirei todos os teus termos com rãs;</p>
		<p><span class="numero">3)</span> e o rio produzirá rãs em abundância, que subirão e entrarão em tua casa, e em teu quarto, e sobre a tua cama, e na casa dos teus servos, e sobre o teu povo, e nos teus fornos, e nas amassadeiras.</p>
		<p><span class="numero">4)</span> E as rãs subirão sobre ti, e sobre o teu povo, e sobre todos os teus servos.</p>
		<p><span class="numero">5)</span> E o Senhor falou a Moisés: Dize a Arão: Estende a tua mão com o cajado sobre as correntes, sobre os rios, e sobre os tanques e faze subir rãs sobre a terra do Egito.</p>
		<p><span class="numero">6)</span> E Arão estendeu a sua mão sobre as águas do Egito; e as rãs subiram, e cobriram a terra do Egito.</p>
		<p><span class="numero">7)</span> E os magos fizeram o mesmo com seus encantamentos, e fizeram subir rãs sobre a terra do Egito.</p>
		<p><span class="numero">8)</span> Então chamou Faraó a Moisés e a Arão, e disse: Rogai ao Senhor, para que retire as rãs de mim e do meu povo, e deixarei o povo ir, para que possam fazer sacrifício ao Senhor.</p>
		<p><span class="numero">9)</span> E disse Moisés a Faraó: Glória sobre mim; quando devo rogar por ti, e por teus servos, e por teu povo, para destruir as rãs de ti e de tuas casas, para que permaneçam somente no rio?</p>
		<p><span class="numero">10)</span> E ele disse: Amanhã. E disse: Seja conforme a tua palavra, para que saibas que não há ninguém como o Senhor nosso Deus.</p>
		<p><span class="numero">11)</span> E as rãs se apartarão de ti, e de tuas casas, e de teus servos, e de teu povo; elas permanecerão somente no rio.</p>
		<p><span class="numero">12)</span> E Moisés e Arão saíram da presença de Faraó; e Moisés clamou ao Senhor por causa das rãs que ele havia trazido sobre Faraó.</p>
		<p><span class="numero">13)</span> E o Senhor fez conforme a palavra de Moisés, e as rãs morreram nas casas, nas aldeias e nos campos.</p>
		<p><span class="numero">14)</span> E as ajuntaram em montes, e a terra cheirou mal.</p>
		<p><span class="numero">15)</span> Mas quando Faraó viu que houve alívio, endureceu o seu coração, e não os ouviu, como o Senhor havia dito.</p>
		<p><span class="numero">16)</span> E disse o Senhor a Moisés: Dize a Arão: Estende o teu cajado, e fere o pó da terra, para que se torne em piolhos por toda a terra do Egito.</p>
		<p><span class="numero">17)</span> E eles assim fizeram, porque Arão estendeu a sua mão com seu cajado, e feriu o pó da terra, e ele se tornou piolhos nos homens, e nos animais; todo o pó da terra se tornou em piolhos em toda a terra do Egito.</p>
		<p><span class="numero">18)</span> E os magos fizeram o mesmo com seus encantamentos para produzir piolhos, mas não conseguiram. Assim havia piolhos sobre os homens e sobre os animais.</p>
		<p><span class="numero">19)</span> Então disseram os magos a Faraó: Este é o dedo de Deus. E o coração de Faraó foi endurecido, e ele não os ouviu, conforme o Senhor havia dito.</p>
		<p><span class="numero">20)</span> E o Senhor disse a Moisés: Levanta-te de manhã cedo, e põe-te diante de Faraó. Eis que ele sairá às águas, e dize-lhe: Assim diz o Senhor: Deixa o meu povo ir, para que me sirva.</p>
		<p><span class="numero">21)</span> De outro modo, se não deixares meu povo ir, eis que enviarei enxames de moscas sobre ti, e sobre teus servos, e sobre teu povo, e nas tuas casas. E as casas dos egípcios estarão cheias de enxames de moscas, e também o chão em que eles estão.</p>
		<p><span class="numero">22)</span> E eu separarei naquele dia a terra de Gósen, em que habita o meu povo, para que lá não haja enxames de moscas, para que ao final saibas que eu sou o Senhor no meio da terra.</p>
		<p><span class="numero">23)</span> E porei uma divisão entre o meu povo e o teu povo. Amanhã será este sinal.</p>
		<p><span class="numero">24)</span> E o Senhor assim fez, e vieram grandes enxames de moscas para dentro da casa de Faraó, e para dentro das casas de seus servos, e para toda a terra do Egito. A terra estava corrompida por causa desses enxames de moscas.</p>
		<p><span class="numero">25)</span> E chamou Faraó a Moisés e a Arão e disse: Ide e sacrificai a vosso Deus nesta terra.</p>
		<p><span class="numero">26)</span> E Moisés disse: Não convém fazer assim, pois sacrificaríamos a abominação dos egípcios ao Senhor nosso Deus. Eis que se sacrificássemos a abominação dos egípcios perante os seus olhos, eles não nos apedrejariam?</p>
		<p><span class="numero">27)</span> Nós vamos três dias de jornada no deserto, e sacrificaremos ao Senhor nosso Deus, conforme ele nos ordenou.</p>
		<p><span class="numero">28)</span> E Faraó disse: Eu vos deixarei ir, para que ofereçais sacrifícios ao Senhor vosso Deus no deserto. Somente não devereis ir muito longe. Rogai por mim.</p>
		<p><span class="numero">29)</span> E Moisés disse: Eis que sairei da tua presença, e rogarei ao Senhor, para que os enxames de moscas se apartem amanhã de Faraó, dos seus servos, e do seu povo; mas que Faraó não aja mais de forma enganosa ao não deixar o povo ir e sacrificar ao Senhor.</p>
		<p><span class="numero">30)</span> E Moisés saiu da presença de Faraó, e rogou ao Senhor.</p>
		<p><span class="numero">31)</span> E o Senhor fez segundo a palavra de Moisés, e ele removeu os enxames de moscas de Faraó, de seus servos e de seu povo. Não permaneceu nem mesmo uma.</p>
