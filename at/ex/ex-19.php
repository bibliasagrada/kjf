		<a href="ex.php"><h3>Êxodo 19</h3></a>
		<p><span class="numero">1)</span> No terceiro mês, quando os filhos de Israel haviam saído da terra do Egito, no mesmo dia entraram no deserto do Sinai.</p>
		<p><span class="numero">2)</span> Porque haviam partido de Refidim, e chegaram ao deserto do Sinai, e tinham acampado no deserto; e ali Israel acampou diante do monte.</p>
		<p><span class="numero">3)</span> E Moisés subiu a Deus, e o Senhor o chamou do monte, dizendo: Assim dirás à casa de Jacó, e falarás aos filhos de Israel:</p>
		<p><span class="numero">4)</span> Vistes o que fiz aos egípcios, e como vos carreguei sobre asas de águia, e vos trouxe a mim.</p>
		<p><span class="numero">5)</span> Agora, portanto, se realmente obedeceres a minha voz, e guardares o meu pacto, então sereis o meu tesouro peculiar acima de todos os povos, porque toda a terra é minha.</p>
		<p><span class="numero">6)</span> E sereis para mim um reino de sacerdotes e uma nação santa. Estas são as palavras que falarás aos filhos de Israel.</p>
		<p><span class="numero">7)</span> E Moisés veio, e chamou os anciãos do povo, e colocou diante de suas faces todas essas palavras que o Senhor lhe ordenara.</p>
		<p><span class="numero">8)</span> E todo o povo respondeu unido, e disse: Tudo que o Senhor falou faremos. E Moisés retornou ao Senhor com as palavras do povo.</p>
		<p><span class="numero">9)</span> E o Senhor disse a Moisés: Eis que eu virei a ti em uma densa nuvem, para que o povo ouça quando eu falar contigo e creia em ti para sempre. E Moisés disse as palavras do povo ao Senhor.</p>
		<p><span class="numero">10)</span> E o Senhor disse a Moisés: Vai ao povo e santifica-os hoje e amanhã, e faze com que lavem suas vestes,</p>
		<p><span class="numero">11)</span> e estejam prontos para o terceiro dia; porque no terceiro dia o Senhor descerá à vista de todo o povo sobre o monte Sinai.</p>
		<p><span class="numero">12)</span> E tu estabelecerás limites ao povo em redor, dizendo: Fiquem atentos para que não subais ao monte ou toqueis os seus limites; todo aquele que tocar no monte certamente morrerá.</p>
		<p><span class="numero">13)</span> Nem mesmo mão alguma o tocará, porque certamente será apedrejado ou asseteado; seja animal ou homem, não viverá. Quando a trombeta soar o toque longo, subirão ao monte.</p>
		<p><span class="numero">14)</span> E Moisés desceu do monte ao povo e santificou o povo; e eles lavaram as suas vestes.</p>
		<p><span class="numero">15)</span> E ele disse ao povo: Estai prontos no terceiro dia, e não chegueis a suas esposas.</p>
		<p><span class="numero">16)</span> E aconteceu que, no terceiro dia de manhã, houve trovões e relâmpagos, e uma densa nuvem sobre o monte, e a voz da trombeta soou o toque longo, assim tremeu todo o povo que estava no acampamento.</p>
		<p><span class="numero">17)</span> E Moisés levou o povo para fora do acampamento ao encontro de Deus; e ficaram ao pé do monte.</p>
		<p><span class="numero">18)</span> E o monte Sinai estava todo ele envolto em fumaça, porque o Senhor desceu sobre ele em fogo. E a fumaça dele subia como a fumaça de um forno, e todo o monte tremia grandemente.</p>
		<p><span class="numero">19)</span> E quando a voz da trombeta soou o toque longo, e se tornou cada vez mais alto, Moisés falou, e Deus lhe respondeu por uma voz.</p>
		<p><span class="numero">20)</span> E o Senhor desceu sobre o monte Sinai, no cume do monte. E o Senhor chamou Moisés para o cume do monte; e Moisés subiu.</p>
		<p><span class="numero">21)</span> E disse o Senhor a Moisés: Desce, adverte o povo, para que não ultrapasse para ver o Senhor e muitos deles morram.</p>
		<p><span class="numero">22)</span> E que também os sacerdotes, que se chegam ao Senhor, se santifiquem, para que o Senhor não se lance sobre eles.</p>
		<p><span class="numero">23)</span> E disse Moisés ao Senhor: O povo não pode subir ao monte Sinai, pois nos ordenaste, dizendo: Estabelece limites ao redor do monte, e santifica-o.</p>
		<p><span class="numero">24)</span> E o Senhor lhe disse: Vai, desce, e subirás, tu e Arão contigo; mas não deixai os sacerdotes e o povo ultrapassar para subir ao Senhor, para que ele não se lance sobre eles.</p>
