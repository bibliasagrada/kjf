		<p><span class="numero">1)</span> E ele disse a Moisés: Sobe ao Senhor, tu, e Arão, Nadabe, e Abiú, e setenta dos anciãos de Israel, e adorai de longe.</p>
		<p><span class="numero">2)</span> E somente Moisés se aproximará do Senhor, mas eles não se aproximarão, nem subirá o povo com ele.</p>
		<p><span class="numero">3)</span> E Moisés veio e disse ao povo todas as palavras do Senhor, e todos os juízos. E todo o povo respondeu a uma voz e disse: Todas as palavras que o Senhor disse nós faremos.</p>
		<p><span class="numero">4)</span> E Moisés escreveu todas as palavras do Senhor, e levantou-se de manhã cedo e construiu um altar sob o monte, e doze pilares, segundo as doze tribos de Israel.</p>
		<p><span class="numero">5)</span> E ele enviou jovens dos filhos de Israel, que ofereceram ofertas queimadas, e sacrificaram ofertas pacíficas de bois ao Senhor.</p>
		<p><span class="numero">6)</span> E Moisés tomou metade do sangue, e o colocou em bacias; e metade do sangue aspergiu sobre o altar.</p>
		<p><span class="numero">7)</span> E ele tomou o livro do pacto e o leu aos ouvidos do povo, e eles disseram: Tudo que o Senhor tem dito faremos, e seremos obedientes.</p>
		<p><span class="numero">8)</span> E Moisés tomou o sangue e o aspergiu sobre o povo, e disse: Eis aqui o sangue do pacto que o Senhor fez convosco a respeito de todas estas palavras.</p>
		<p><span class="numero">9)</span> Então subiram Moisés, e Arão, Nadabe, e Abiú, e setenta dos anciãos de Israel,</p>
		<p><span class="numero">10)</span> e viram o Deus de Israel, e havia debaixo de seus pés como trabalho pavimentado de pedra de safira, e como o corpo do céu na sua clareza.</p>
		<p><span class="numero">11)</span> E sobre os nobres dos filhos de Israel ele não colocou a sua mão; eles também viram a Deus, e comeram e beberam.</p>
		<p><span class="numero">12)</span> E disse o Senhor a Moisés: Sobe a mim no monte, e fique ali; e dar-te-ei tábuas de pedra, e a lei, e os mandamentos que escrevi, para que os ensines.</p>
		<p><span class="numero">13)</span> E Moisés subiu, e seu ajudante Josué. E Moisés subiu o monte de Deus.</p>
		<p><span class="numero">14)</span> E disse aos anciãos: Esperai por nós aqui, até que voltemos a vós; e eis que Arão e Hur estão convosco. Se algum homem tiver qualquer negócio a fazer, achegue-se a eles.</p>
		<p><span class="numero">15)</span> E Moisés subiu o monte, e uma nuvem cobriu o monte.</p>
		<p><span class="numero">16)</span> E a glória do Senhor habitou sobre o monte Sinai, e a nuvem o cobriu seis dias; e ao sétimo dia ele chamou Moisés do meio da nuvem.</p>
		<p><span class="numero">17)</span> E a visão da glória do Senhor era como fogo consumidor no cume do monte aos olhos dos filhos de Israel.</p>
