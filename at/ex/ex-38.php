		<a href="ex.php"><h3>Êxodo 38</h3></a>
		<p><span class="numero">1)</span> E fez o altar da oferta queimada de madeira de acácia, com cinco côvados de comprimento, e cinco côvados de largura; ele era quadrado; e a sua altura era de três côvados.</p>
		<p><span class="numero">2)</span> E fez os seus chifres sobre os seus quatro cantos; os seus chifres eram do mesmo, e o revestiu de bronze.</p>
		<p><span class="numero">3)</span> E fez todos os utensílios do altar: os cinzeiros, e as pás, e as bacias, e os ganchos de carne, e os braseiros. Todos os seus utensílios feitos de bronze.</p>
		<p><span class="numero">4)</span> E fez para o altar uma grade de bronze em forma de rede, que pôs debaixo da sua borda até a metade.</p>
		<p><span class="numero">5)</span> E fundiu quatro argolas para as extremidades da grade de bronze, para lugares para as varas.</p>
		<p><span class="numero">6)</span> E fez as varas de madeira de acácia, e as revestiu de bronze.</p>
		<p><span class="numero">7)</span> E pôs as varas nas argolas nos dois lados do altar, para carregá-lo. Fez o altar oco, de tábuas.</p>
		<p><span class="numero">8)</span> E fez uma pia de bronze, e sua base de bronze, dos espelhos das mulheres reunidas, que se reuniam na porta do tabernáculo da congregação.</p>
		<p><span class="numero">9)</span> E fez o pátio; do lado sul em direção ao sul as cortinas para o pátio eram de linho fino torcido de cem côvados de comprimento,</p>
		<p><span class="numero">10)</span> as suas colunas eram vinte, e as suas bases vinte, de bronze. Os colchetes das colunas e suas faixas eram de prata.</p>
		<p><span class="numero">11)</span> E para o lado norte as cortinas eram de cem côvados, suas colunas eram vinte e vinte as suas bases, de bronze; os colchetes das colunas e suas faixas de prata.</p>
		<p><span class="numero">12)</span> E para o lado oeste havia cortinas de cinquenta côvados; e eram dez as suas colunas, e dez as suas bases; os colchetes das colunas e suas faixas de prata.</p>
		<p><span class="numero">13)</span> E para o lado leste em direção ao leste, cinquenta côvados.</p>
		<p><span class="numero">14)</span> As cortinas de um lado da porta eram de quinze côvados; suas colunas, três, e suas bases, três.</p>
		<p><span class="numero">15)</span> E do outro lado da porta do pátio, de ambos os lados, havia cortinas de quinze côvados; suas colunas, três, e suas bases, três.</p>
		<p><span class="numero">16)</span> Todas as cortinas do pátio ao redor eram de linho fino torcido.</p>
		<p><span class="numero">17)</span> E as bases para as colunas eram de bronze; os colchetes das colunas e suas faixas de prata; e o revestimento dos seus capitéis de prata; e todas as colunas do pátio eram cingidas de prata.</p>
		<p><span class="numero">18)</span> E a cortina para a porta do pátio era bordado, de azul, de púrpura, de carmesim, e linho fino torcido. E vinte côvados era o comprimento, e a altura, na largura, era de cinco côvados correspondente às cortinas do pátio.</p>
		<p><span class="numero">19)</span> E suas colunas eram quatro, e quatro as suas bases de bronze; seus colchetes de prata, e o revestimento dos capitéis e as suas faixas de prata.</p>
		<p><span class="numero">20)</span> E todas as estacas do tabernáculo, e do pátio ao redor, eram de bronze.</p>
		<p><span class="numero">21)</span> Esta é a enumeração do tabernáculo, do tabernáculo do testemunho, como foi contado, conforme a ordem de Moisés, para o serviço dos levitas, pela mão de Itamar, filho do sacerdote Arão.</p>
		<p><span class="numero">22)</span> E Bezalel, o filho de Uri, o filho de Hur, da tribo de Judá, fez tudo que o Senhor ordenara a Moisés.</p>
		<p><span class="numero">23)</span> E com ele estava Aoliabe, filho de Aisamaque, da tribo de Dã, um gravador, e trabalhador esmerado, e bordador em azul, e em púrpura, e em carmesim, e linho fino.</p>
		<p><span class="numero">24)</span> Todo o ouro que foi usado na obra, em toda a obra do lugar santo, o ouro das ofertas, foi vinte e nove talentos, e setecentos e trinta siclos, segundo o siclo do santuário.</p>
		<p><span class="numero">25)</span> E a sua prata, que foi enumerada da congregação foi cem talentos, e mil e setecentos e setenta e cinco siclos, segundo o siclo do santuário;</p>
		<p><span class="numero">26)</span> um beca para cada homem, isto é, metade de um siclo, segundo o siclo do santuário, para cada um que foi enumerado, de vinte anos para cima, para seiscentos e três mil e quinhentos e cinquenta homens.</p>
		<p><span class="numero">27)</span> E dos cem talentos de prata foram fundidas as bases do santuário, e as bases do véu; cem bases dos cem talentos, um talento por base.</p>
		<p><span class="numero">28)</span> E dos mil e setecentos e setenta e cinco siclos ele fez colchetes para as colunas, e revestiu os seus capitéis, e os cingiu.</p>
		<p><span class="numero">29)</span> E o bronze da oferta foi setenta talentos, e dois mil e quatrocentos siclos.</p>
		<p><span class="numero">30)</span> E com isso ele fez as bases para a porta do tabernáculo da congregação, e o altar de bronze, e a grade de bronze para ele, e todos os utensílios do altar,</p>
		<p><span class="numero">31)</span> e as bases do pátio ao redor, e as bases da porta do pátio, e todas as estacas do tabernáculo, e todas as estacas do pátio ao redor.&nbsp;<a href="ex-39.php"><span class="proximo">Próximo capítulo</span></a></p>
