		<a href="ex.php"><h3>Êxodo 37</h3></a>
		<p><span class="numero">1)</span> E Bezalel fez a arca de madeira de acácia; dois côvados e meio era o comprimento dela, e um côvado e meio a largura dela, e um côvado e meio a altura dela.</p>
		<p><span class="numero">2)</span> E a revestiu de ouro puro, por dentro e por fora, e fez sobre ela uma coroa de ouro ao redor.</p>
		<p><span class="numero">3)</span> E fundiu-lhe quatro argolas de ouro, para serem colocadas nos seus quatro cantos; duas argolas sobre um lado, e duas argolas sobre o seu outro lado.</p>
		<p><span class="numero">4)</span> E fez varas de madeira de acácia, e as revestiu com ouro.</p>
		<p><span class="numero">5)</span> E colocou as varas nas argolas nos lados da arca, para carregar a arca.</p>
		<p><span class="numero">6)</span> E fez o propiciatório de ouro puro; dois côvados e meio era o seu comprimento, e um côvado e meio a sua largura.</p>
		<p><span class="numero">7)</span> E fez dois querubins de ouro, batido de uma peça ele os fez, nas duas extremidades do propiciatório;</p>
		<p><span class="numero">8)</span> um querubim em uma extremidade deste lado, e outro querubim na outra extremidade desse lado. Do propiciatório fez ele os querubins nas suas duas extremidades.</p>
		<p><span class="numero">9)</span> E os querubins estendiam suas asas ao alto, cobrindo o propiciatório com suas asas, e suas faces uma para a outra. Para o propiciatório estavam voltadas as faces dos querubins.</p>
		<p><span class="numero">10)</span> E fez a mesa de madeira de acácia; dois côvados era o seu comprimento, e um côvado a sua largura, e um côvado e meio a sua altura,</p>
		<p><span class="numero">11)</span> e a revestiu com ouro puro, e fez para ela uma coroa de ouro ao redor.</p>
		<p><span class="numero">12)</span> E fez também para ela uma moldura ao redor, da largura de uma mão, e fez uma coroa de ouro ao redor da moldura.</p>
		<p><span class="numero">13)</span> E fundiu para ela quatro argolas, e colocou as argolas sobre os quatro cantos que estavam nos seus pés.</p>
		<p><span class="numero">14)</span> Defronte das molduras estavam as argolas, os lugares para as varas, para carregar a mesa.</p>
		<p><span class="numero">15)</span> E fez as varas de madeira de acácia, e as revestiu com ouro, para carregar a mesa.</p>
		<p><span class="numero">16)</span> E de ouro puro fez os utensílios que estavam sobre a mesa, os seus pratos, e as suas colheres, e as suas tigelas e as suas taças de ouro puro.</p>
		<p><span class="numero">17)</span> E fez o candelabro de ouro puro; de obra batida fez o candelabro. O seu eixo, e as suas hastes, as suas tigelas, os seus botões e as suas flores eram do mesmo;</p>
		<p><span class="numero">18)</span> e seis hastes saíam de seus lados; três hastes do candelabro de um lado, e três hastes do candelabro do outro lado;</p>
		<p><span class="numero">19)</span> três tigelas a modo de amêndoas em uma haste, um botão e uma flor; e três tigelas feitas como amêndoas em outra haste, um botão e uma flor; assim nas suas seis hastes que saem do candelabro.</p>
		<p><span class="numero">20)</span> E no candelabro havia quatro tigelas feitas como amêndoas, seus botões e suas flores;</p>
		<p><span class="numero">21)</span> e um botão debaixo de duas hastes do mesmo, e um botão debaixo de duas hastes do mesmo, e um botão debaixo de duas hastes do mesmo, conforme as seis hastes que saíam dele.</p>
		<p><span class="numero">22)</span> Seus botões e suas hastes eram do mesmo; tudo era uma obra batida de ouro puro.</p>
		<p><span class="numero">23)</span> E fez as sete lâmpadas dele; seus espivetadores e os seus apagadores de ouro puro.</p>
		<p><span class="numero">24)</span> De um talento de ouro puro ele o fez, com todos os seus utensílios.</p>
		<p><span class="numero">25)</span> E fez o altar de incenso de madeira de acácia. Um côvado era o seu comprimento, e um côvado a sua largura. Era quadrado; e dois côvados era a sua altura. Os seus chifres eram do mesmo.</p>
		<p><span class="numero">26)</span> E o revestiu com ouro puro, o seu topo, e seus lados ao redor, e os seus chifres. E fez para ele uma coroa de ouro em redor.</p>
		<p><span class="numero">27)</span> E fez duas argolas de ouro debaixo da sua coroa, nos seus dois cantos, nos seus dois lados, para serem os lugares das varas, para carregá-lo.</p>
		<p><span class="numero">28)</span> E fez as varas de madeira de acácia, e os revestiu com ouro.</p>
