		<a href="ex.php"><h3>Êxodo 26</h3></a>
		<p><span class="numero">1)</span> Além disso, farás o tabernáculo com dez cortinas de linho torcido, e azul, e púrpura, e carmesim; com querubins de trabalho esmerado os farás.</p>
		<p><span class="numero">2)</span> O comprimento de uma cortina será vinte e oito côvados, e a largura de uma cortina quatro côvados; e todas as cortinas terão uma medida.</p>
		<p><span class="numero">3)</span> As cinco cortinas se enlaçarão umas às outras; e as outras cinco cortinas se enlaçarão umas às outras.</p>
		<p><span class="numero">4)</span> E farás laçadas de azul na borda de uma cortina, na extremidade e na juntura; e da mesma forma farás a extremidade de outra cortina, na juntura da segunda.</p>
		<p><span class="numero">5)</span> Cinquenta laçadas farás em uma cortina, e cinquenta laçadas farás na borda da outra cortina que está na juntura da segunda, para que as laçadas se prendam uma à outra.</p>
		<p><span class="numero">6)</span> E farás cinquenta colchetes de ouro, e ajuntarás as cortinas com esses colchetes, e será um tabernáculo.</p>
		<p><span class="numero">7)</span> E farás cortinas de pelo de cabra para servir de tenda sobre o tabernáculo: onze cortinas farás.</p>
		<p><span class="numero">8)</span> O comprimento de uma cortina será de trinta côvados, e a largura de uma cortina será de quatro côvados. E as onze cortinas serão de uma medida.</p>
		<p><span class="numero">9)</span> E ajuntarás cinco cortinas entre si, e seis cortinas entre si, e dobrarás a sexta cortina em frente ao tabernáculo.</p>
		<p><span class="numero">10)</span> E farás cinquenta laçadas na borda da cortina que está na extremidade da juntura, e cinquenta laçadas na borda da cortina que ajunta com a segunda.</p>
		<p><span class="numero">11)</span> E farás cinquenta colchetes de bronze, e colocarás os colchetes nas laçadas, e ajuntarás a tenda para que seja uma.</p>
		<p><span class="numero">12)</span> E o restante que permanecer das cortinas da tenda, a metade da cortina que permanecer, pendurará sobre a parte traseira do tabernáculo.</p>
		<p><span class="numero">13)</span> E um côvado de um lado, e um côvado no outro lado, do que restar no comprimento das cortinas da tenda, penderão sobre os lados do tabernáculo, deste lado e do outro, para cobri-lo.</p>
		<p><span class="numero">14)</span> E farás para a tenda uma coberta de pele de carneiro tingida de vermelho, e sobre esta, uma coberta de peles de texugo.</p>
		<p><span class="numero">15)</span> E farás as tábuas para o tabernáculo de madeira de acácia que ficarão em pé.</p>
		<p><span class="numero">16)</span> Dez côvados será o comprimento de uma tábua, e um côvado e meio será a largura de uma tábua.</p>
		<p><span class="numero">17)</span> Dois encaixes haverá em uma tábua, dispostos em ordem um contra o outro. Assim farás para todas as tábuas do tabernáculo.</p>
		<p><span class="numero">18)</span> E farás as tábuas para o tabernáculo, vinte tábuas no lado sul para o sul.</p>
		<p><span class="numero">19)</span> E farás quarenta bases de prata debaixo das vinte tábuas; duas bases debaixo de uma tábua para os seus dois encaixes, e duas bases debaixo de outra tábua para os seus dois encaixes.</p>
		<p><span class="numero">20)</span> E para o segundo lado do tabernáculo, no lado norte haverá vinte tábuas,</p>
		<p><span class="numero">21)</span> e suas quarenta bases de prata; duas bases debaixo de uma tábua, e duas bases debaixo de outra tábua.</p>
		<p><span class="numero">22)</span> E do lado do tabernáculo para o oeste farás seis tábuas.</p>
		<p><span class="numero">23)</span> E farás duas tábuas para os cantos do tabernáculo, nos dois lados.</p>
		<p><span class="numero">24)</span> E por baixo se ajuntarão, e serão ajuntados acima da sua cabeça em uma argola. Assim será para as duas; serão para os dois cantos.</p>
		<p><span class="numero">25)</span> E serão oito tábuas, e suas bases de prata, dezesseis bases; duas bases debaixo de uma tábua, e duas bases debaixo de outra tábua.</p>
		<p><span class="numero">26)</span> E farás barras de madeira de acácia; cinco para as tábuas de um lado do tabernáculo,</p>
		<p><span class="numero">27)</span> e cinco barras para as tábuas do outro lado do tabernáculo, e cinco barras para as tábuas do lado do tabernáculo, para os dois lados, para o oeste.</p>
		<p><span class="numero">28)</span> E a barra do meio das tábuas passará de extremidade a extremidade.</p>
		<p><span class="numero">29)</span> E revestirás as tábuas com ouro, e farás suas argolas de ouro, como lugares para as barras; e revestirás as barras com ouro.</p>
		<p><span class="numero">30)</span> E levantarás o tabernáculo de acordo com o modelo do que te foi mostrado no monte.</p>
		<p><span class="numero">31)</span> E farás um véu de azul, e púrpura, e carmesim, e linho fino torcido de trabalho esmerado; com querubins deverá ser feito.</p>
		<p><span class="numero">32)</span> E o pendurarás sobre quatro pilares de madeira de acácia, revestidas de ouro; seus colchetes serão de ouro, sobre as quatro bases de prata.</p>
		<p><span class="numero">33)</span> E pendurarás o véu debaixo dos colchetes, para que coloques ali dentro o véu da arca do testemunho; e o véu vos fará separação entre o lugar santo e o santíssimo.</p>
		<p><span class="numero">34)</span> E colocarás o propiciatório sobre a arca do testemunho no lugar santíssimo.</p>
		<p><span class="numero">35)</span> E colocarás a mesa sem o véu, e o candelabro diante da mesa, no lado do tabernáculo em direção ao sul; e colocarás a mesa no lado norte.</p>
		<p><span class="numero">36)</span> E farás para a tenda uma cortina de azul, e púrpura, e carmesim, e linho fino torcido, obra de bordador.</p>
		<p><span class="numero">37)</span> E farás para a cortina cinco colunas de madeira de acácia, e as revestirás de ouro, e seus colchetes serão de ouro. E fundirás cinco colchetes de bronze para elas.&nbsp;<a href="ex-27.php"><span class="proximo">Próximo capítulo</span></a></p>
