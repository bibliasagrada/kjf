		<a href="ex.php"><h3>Êxodo 4</h3></a>
		<p><span class="numero">1)</span> E respondeu Moisés e disse: Mas eis que eles não crerão em mim, nem ouvirão a minha voz, pois dirão: O Senhor não te apareceu.</p>
		<p><span class="numero">2)</span> E disse-lhe o Senhor: O que há em tua mão? E ele disse: Um cajado.</p>
		<p><span class="numero">3)</span> E ele disse: Lança-a na terra. E ele a lançou na terra, e ela se tornou uma serpente. E Moisés fugiu dela.</p>
		<p><span class="numero">4)</span> E o Senhor disse a Moisés: Estende a tua mão; e toma-a pela cauda. E ele estendeu a sua mão, e a pegou, e se tornou um cajado em sua mão,</p>
		<p><span class="numero">5)</span> para que eles creiam que te apareceu o Senhor, Deus de seus pais, o Deus de Abraão, o Deus de Isaque e o Deus de Jacó.</p>
		<p><span class="numero">6)</span> E disse mais o Senhor: Põe agora a tua mão no peito. E ele pôs a mão no seu peito, e quando a tirou, eis que a sua mão estava leprosa como neve.</p>
		<p><span class="numero">7)</span> E disse: Põe tua mão no peito de novo. E ele novamente pôs a mão no peito, e quando a tirou do peito, eis que se tornara como sua outra carne.</p>
		<p><span class="numero">8)</span> E acontecerá que, se eles não crerem em ti, nem derem ouvidos à voz do primeiro sinal, então eles crerão na voz do segundo sinal.</p>
		<p><span class="numero">9)</span> E acontecerá que, se eles não crerem também nesses dois sinais, nem derem ouvidos à tua voz, então tomarás da água do rio e a derramarás sobre a terra seca. E a água que tirares do rio se tornará em sangue sobre a terra seca.</p>
		<p><span class="numero">10)</span> E disse Moisés ao Senhor: Ó meu Senhor, eu não sou eloquente, nem até agora, nem desde que falaste ao teu servo. Mas eu sou lento de fala, e de uma língua lenta.</p>
		<p><span class="numero">11)</span> E o Senhor lhe disse: Quem faz a boca do homem? Quem faz o mudo, o surdo, o que vê ou o cego? Não fiz eu o Senhor?</p>
		<p><span class="numero">12)</span> Portanto, vai, e eu serei com a tua boca, e te ensinarei o que dirás.</p>
		<p><span class="numero">13)</span> E ele disse: Ó meu Senhor, envia, rogo-te, pela tua mão aquele a quem enviarás.</p>
		<p><span class="numero">14)</span> E a ira do Senhor se acendeu contra Moisés, e ele disse: Não é Arão, o levita, teu irmão? Sei que ele pode falar bem. E eis que ele está vindo para encontrar-se contigo, e vendo-te, se alegrará em seu coração.</p>
		<p><span class="numero">15)</span> E tu falarás a ele; e colocarás palavras na sua boca; e eu serei com a tua boca, e com a dele, e vos ensinarei o que deveis fazer.</p>
		<p><span class="numero">16)</span> E ele será teu porta-voz ao povo. E assim ele te será por boca, e tu lhe serás por Deus.</p>
		<p><span class="numero">17)</span> E tomarás este cajado em tuas mãos, com o qual farás sinais.</p>
		<p><span class="numero">18)</span> E Moisés partiu e voltou a Jetro, seu sogro, e lhe disse: Deixa-me ir, rogo-te, e voltar a meus irmãos que estão no Egito, e ver se ainda vivem. E Jetro disse a Moisés: Vai em paz.</p>
		<p><span class="numero">19)</span> E disse o Senhor a Moisés em Midiã: Vai, volta ao Egito, pois estão mortos todos os homens que procuravam tirar-te a vida.</p>
		<p><span class="numero">20)</span> E Moisés tomou sua mulher e seus filhos, e os colocou sobre um jumento, e voltou à terra do Egito. E Moisés tomou o cajado de Deus em sua mão.</p>
		<p><span class="numero">21)</span> E disse o Senhor a Moisés: Quando tu voltares ao Egito, procure fazer diante do Faraó, todas as maravilhas que tenho posto na tua mão. Mas eu vou endurecer o seu coração, para que ele não deixe o povo ir.</p>
		<p><span class="numero">22)</span> E tu dirás a Faraó: Assim diz o Senhor: Israel é meu filho, o meu primogênito,</p>
		<p><span class="numero">23)</span> e eu te digo: Deixa meu filho ir, para que me sirva. E se te recusares a deixá-lo ir, eis que matarei o teu filho, o teu primogênito.</p>
		<p><span class="numero">24)</span> E aconteceu que a caminho, em uma estalagem, o Senhor se encontrou com ele; e tentou matá-lo.</p>
		<p><span class="numero">25)</span> Então Zípora tomou uma pedra afiada, e cortou o prepúcio de seu filho, e o lançou aos pés dele, e disse: Certamente és esposo sanguinário para mim.</p>
		<p><span class="numero">26)</span> Assim ele o deixou ir. Então ela disse: Um esposo sanguinário és, por causa da circuncisão.</p>
		<p><span class="numero">27)</span> E o Senhor disse a Arão: Vai ao deserto para te encontrares com Moisés. E ele foi, e o encontrou no monte de Deus, e o beijou.</p>
		<p><span class="numero">28)</span> E disse Moisés a Arão todas as palavras do Senhor, que o enviara, e todos os sinais que lhe ordenara.</p>
		<p><span class="numero">29)</span> E Moisés e Arão foram e reuniram todos os anciãos dos filhos de Israel,</p>
		<p><span class="numero">30)</span> e Arão falou todas as palavras que o Senhor havia falado a Moisés, e fez os sinais à vista do povo.</p>
		<p><span class="numero">31)</span> E o povo creu. E quando ouviram que o Senhor havia visitado os filhos de Israel, e que ele havia visto a sua aflição, então curvaram a sua cabeça e adoraram.&nbsp;<a href="ex-5.php"><span class="proximo">Próximo capítulo</span></a></p>
