		<a href="ex.php"><h3>Êxodo 32</h3></a>
		<p><span class="numero">1)</span> E quando o povo viu que Moisés demorava para descer do monte, o povo se reuniu com Arão, e lhe disse: Levanta- te, faze-nos deuses que vão adiante de nós, pois quanto a este Moisés, o homem que nos tirou da terra do Egito, não sabemos o que lhe sucedeu.</p>
		<p><span class="numero">2)</span> E Arão lhes disse: Arrancai os pendentes de ouro, que estão nas orelhas de vossas mulheres, de vossos filhos e de vossas filhas, e trazei-os a mim.</p>
		<p><span class="numero">3)</span> E todo o povo arrancou os pendentes de ouro que estavam nas suas orelhas, e os trouxeram a Arão.</p>
		<p><span class="numero">4)</span> E ele o recebeu das suas mãos, e o formou com um buril, depois de o ter formado em bezerro de fundição, e eles disseram: Estes são teus deuses, ó Israel, que te tiraram da terra do Egito.</p>
		<p><span class="numero">5)</span> E quando Arão o viu, construiu um altar diante dele. E Arão fez uma proclamação e disse: Amanhã é a festa ao Senhor.</p>
		<p><span class="numero">6)</span> E eles se levantaram cedo no dia seguinte, e ofereceram ofertas queimadas, e trouxeram ofertas pacíficas; e o povo assentou- se para comer e beber, e se levantou para festejar.</p>
		<p><span class="numero">7)</span> E disse o Senhor a Moisés: Vai e desce, porque o teu povo, que tiraste da terra do Egito, se corrompeu.</p>
		<p><span class="numero">8)</span> Depressa se desviaram do caminho que eu lhes ordenei. Fizeram para si um bezerro de fundição, e o adoraram, e lhe fizeram sacrifícios, e disseram: Estes são os teus deuses, ó Israel, que te tiraram da terra do Egito.</p>
		<p><span class="numero">9)</span> E o Senhor disse a Moisés: Tenho visto esse povo e, eis que é um povo obstinado.</p>
		<p><span class="numero">10)</span> Por isso, agora deixa-me só, para que minha ira se acenda contra eles, e para que eu os consuma; e farei de ti uma grande nação.</p>
		<p><span class="numero">11)</span> E suplicou Moisés ao Senhor seu Deus, e disse: Senhor, por que a tua ira se acende contra o teu povo, que tiraste da terra do Egito com grande poder, e com mão forte?</p>
		<p><span class="numero">12)</span> Por que falariam os egípcios e diriam: Para mal os tirou, para matá-los nos montes, e para destruí-los da face da terra? Desvia-te da tua ardente ira, e arrepende-te deste mal contra o teu povo.</p>
		<p><span class="numero">13)</span> Lembra-te de Abraão, Isaque e Israel, teus servos, aos quais juraste por ti mesmo e lhes disseste: Multiplicarei a vossa semente como as estrelas do céu, e toda esta terra de que falei darei à vossa semente, e eles a herdarão para sempre.</p>
		<p><span class="numero">14)</span> E o Senhor desistiu do mal que tinha pensado fazer ao seu povo.</p>
		<p><span class="numero">15)</span> E Moisés se virou e desceu do monte, e as duas tábuas do testemunho estavam em sua mão. As tábuas estavam escritas de ambos os lados; de um e de outro lado estavam escritas.</p>
		<p><span class="numero">16)</span> E as tábuas eram a obra de Deus, e a escrita era a escrita de Deus, gravada nas tábuas.</p>
		<p><span class="numero">17)</span> E quando Josué ouviu o som do povo que jubilava, disse a Moisés: Há alarido de guerra no acampamento.</p>
		<p><span class="numero">18)</span> E ele disse: Não é voz dos que gritam por domínio, nem é a voz dos que entoam derrota, mas ouço um alarido dos que cantam.</p>
		<p><span class="numero">19)</span> E aconteceu, assim que ele se aproximou do acampamento, que viu o bezerro, e a dança, a ira de Moisés se acendeu, e ele lançou as tábuas da sua mão, e as quebrou ao pé do monte.</p>
		<p><span class="numero">20)</span> E ele tomou o bezerro que eles tinham feito, e queimou-o no fogo; e, moendo-o até se tornar em pó, e o espalhou sobre a água, e fez os filhos de Israel beberem dela.</p>
		<p><span class="numero">21)</span> E disse Moisés a Arão: O que este povo fez a ti para que trouxesses tão grande pecado sobre eles?</p>
		<p><span class="numero">22)</span> E disse Arão: Que a ira de meu senhor não se acenda; conheces o povo, que ele é inclinado para o mal.</p>
		<p><span class="numero">23)</span> Porque me disseram: Faze-nos deuses, que irão adiante de nós, pois quanto a este Moisés, o homem que nos tirou da terra do Egito, não sabemos o que lhe sucedeu.</p>
		<p><span class="numero">24)</span> E eu lhes disse: Todo aquele que tiver algum ouro, que o arranque. Assim eles me deram. Então eu o lancei no fogo, e dele saiu este bezerro.</p>
		<p><span class="numero">25)</span> E quando Moisés viu que o povo estava nu (porque Arão os havia despido para vergonha entre os seus inimigos),</p>
		<p><span class="numero">26)</span> então Moisés se colocou na porta do acampamento e disse: Quem está do lado do Senhor? Que ele venha a mim. E todos os filhos de Levi se achegaram a ele.</p>
		<p><span class="numero">27)</span> E ele lhes disse: Assim diz o Senhor Deus de Israel: Ponha cada homem sua espada sobre o seu lado, e entrai e saí de porta em porta em todo o acampamento, e mate cada homem o seu irmão, e cada homem o seu amigo, e cada homem o seu próximo.</p>
		<p><span class="numero">28)</span> E os filhos de Levi fizeram conforme a palavra de Moisés, e caíram do povo naquele dia em torno de três mil homens.</p>
		<p><span class="numero">29)</span> Porque Moisés havia dito: Consagrai-vos hoje ao Senhor, cada homem contra o seu filho, e sobre o seu irmão, para que ele vos possa conceder bênção hoje.</p>
		<p><span class="numero">30)</span> E aconteceu que, no dia seguinte, Moisés disse ao povo: Vós pecastes com grande pecado, e agora subirei ao Senhor; talvez possa fazer propiciação pelo vosso pecado.</p>
		<p><span class="numero">31)</span> E Moisés retornou ao Senhor, e disse: Ó, este povo pecou com grande pecado, e fizeram deuses de ouro para si.</p>
		<p><span class="numero">32)</span> Agora, pois, perdoa o seu pecado; e se não, apaga-me, rogo-te, do teu livro que escreveste.</p>
		<p><span class="numero">33)</span> E disse o Senhor a Moisés: Aquele que pecou contra mim, este apagarei do meu livro.</p>
		<p><span class="numero">34)</span> Por isso, agora vai, leva o povo até o lugar de que te falei. Eis que meu Anjo irá adiante de ti; porém no dia da minha visitação, sobre eles visitarei o seu pecado.</p>
