		<a href="ex.php"><h3>Êxodo 21</h3></a>
		<p><span class="numero">1)</span> Agora estes são os juízos que colocarás diante deles.</p>
		<p><span class="numero">2)</span> Se comprares um servo hebreu, seis anos ele te servirá, e no sétimo ele sairá livre, gratuitamente.</p>
		<p><span class="numero">3)</span> Se ele entrou só, deverá sair só. Se era casado, sua mulher sairá com ele.</p>
		<p><span class="numero">4)</span> Se seu senhor lhe deu uma mulher, e ela lhe gerou filhos ou filhas, a mulher e seus filhos serão do seu senhor, e ele sairá só.</p>
		<p><span class="numero">5)</span> E se o servo disser claramente: Amo o meu senhor, a minha mulher e meus filhos, eu não sairei livre,</p>
		<p><span class="numero">6)</span> então seu senhor o levará aos juízes, e também o levará à porta, ou ao umbral da porta, e o seu senhor lhe furará a orelha com uma sovela; e ele o servirá para sempre.</p>
		<p><span class="numero">7)</span> E se um homem vender sua filha para ser serva, ela não sairá como saem os servos.</p>
		<p><span class="numero">8)</span> Se ela não agradar ao seu senhor, que se comprometeu a desposá-la então ele permitirá que seja resgatada; ele não poderá vendê-la a uma nação estrangeira, visto tê- la enganado.</p>
		<p><span class="numero">9)</span> Mas se a desposar com seu filho, agirá com ela conforme a maneira das filhas.</p>
		<p><span class="numero">10)</span> Se tomar para ele outra mulher, não diminuirá seu alimento, sua vestimenta e nem sua obrigação matrimonial.</p>
		<p><span class="numero">11)</span> E se ele não cumprir estes três para com ela, então ela sairá livre destituída de dinheiro.</p>
		<p><span class="numero">12)</span> Aquele que ferir a um homem, e ele morrer, certamente será morto.</p>
		<p><span class="numero">13)</span> Se um homem não estiver de emboscada, mas Deus o entregar em sua mão, então eu te designarei um lugar para o qual ele fugirá.</p>
		<p><span class="numero">14)</span> Mas se um homem vier premeditadamente contra o seu próximo, para matá-lo à traição, afasta-lo-ás do meu altar para que morra.</p>
		<p><span class="numero">15)</span> E aquele que ferir seu pai ou sua mãe, certamente será morto.</p>
		<p><span class="numero">16)</span> E aquele que furtar um homem e o vender, ou se for encontrado em sua mão, certamente será morto.</p>
		<p><span class="numero">17)</span> E aquele que amaldiçoa o seu pai ou a sua mãe, certamente será morto.</p>
		<p><span class="numero">18)</span> E se homens lutarem juntos, e um ferir o outro com uma pedra, ou com seu punho, e ele não morrer, mas se mantiver na cama,</p>
		<p><span class="numero">19)</span> se novamente levantar-se, e caminhar sobre o seu cajado, então aquele que o feriu será absolvido; somente lhe pagará pela perda do seu tempo e o fará curar totalmente.</p>
		<p><span class="numero">20)</span> E se um homem ferir seu servo, ou sua serva, com um cajado, e ele morrer debaixo da sua mão, ele certamente será punido.</p>
		<p><span class="numero">21)</span> Todavia, se sobreviver por um dia ou dois, não será punido, porque é seu dinheiro.</p>
		<p><span class="numero">22)</span> Se homens lutarem e ferirem uma mulher grávida, assim que seu fruto se apartar dela, e não havendo outro dano, certamente será punido, conforme o que impuser o marido da mulher; e ele pagará segundo o que os juízes determinarem.</p>
		<p><span class="numero">23)</span> E se houver qualquer dano, então darás vida por vida,</p>
		<p><span class="numero">24)</span> olho por olho, dente por dente, mão por mão, pé por pé,</p>
		<p><span class="numero">25)</span> queimadura por queimadura, ferida por ferida, golpe por golpe.</p>
		<p><span class="numero">26)</span> E se um homem ferir o olho de seu servo, ou o olho de sua serva, assim que o danifique, deixa-lo-á sair livre por causa do seu olho.</p>
		<p><span class="numero">27)</span> E se ele atingir o dente de seu servo, ou o dente de sua serva, deixa-lo-á sair livre por causa do seu dente.</p>
		<p><span class="numero">28)</span> Se um boi escornear um homem ou uma mulher, de modo que morra, então o boi certamente será apedrejado, e sua carne não se comerá; mas o dono do boi será absolvido.</p>
		<p><span class="numero">29)</span> Mas se o boi era acostumado em tempos passados a empurrar com o seu chifre, tendo sido testemunhado ao seu dono, e não o guardou, mas ele matou um homem ou uma mulher, o boi será apedrejado, e seu dono também morrerá.</p>
		<p><span class="numero">30)</span> Se lhe for posto uma quantia de dinheiro, então ele dará pelo resgate de sua vida o que lhe for posto.</p>
		<p><span class="numero">31)</span> Se ele escorneou um filho, ou escorneou uma filha, de acordo com este juízo lhe será feito.</p>
		<p><span class="numero">32)</span> Se o boi escornear um servo ou uma serva, dará ao seu senhor trinta ciclos de prata, e o boi será apedrejado.</p>
		<p><span class="numero">33)</span> E se um homem abrir um buraco, ou se um homem cavar um buraco, e não o cobrir, e nele cair um boi ou um jumento,</p>
		<p><span class="numero">34)</span> o dono do buraco pagará por ele, e dará dinheiro ao dono deles. E o animal morto será seu.</p>
		<p><span class="numero">35)</span> E se o boi de um homem ferir o de outro, e ele morrer, então venderão o boi vivo e dividirão o seu dinheiro, e também dividirão o boi morto.</p>
		<p><span class="numero">36)</span> Ou se for conhecido que o boi escorneava no passado, e seu dono não o guardou, certamente pagará boi por boi, e o morto será seu.&nbsp;<a href="ex-22.php"><span class="proximo">Próximo capítulo</span></a></p>
