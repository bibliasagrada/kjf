		<a href="ex.php"><h3>Êxodo 16</h3></a>
		<p><span class="numero">1)</span> E tomaram a sua jornada de Elim, e toda a congregação dos filhos de Israel veio ao deserto de Sim, que está entre Elim e Sinai, no décimo quinto dia do segundo mês depois da sua partida da terra do Egito.</p>
		<p><span class="numero">2)</span> E toda a congregação dos filhos de Israel murmurou contra Moisés e Arão no deserto,</p>
		<p><span class="numero">3)</span> e os filhos de Israel disseram a eles: Quem dera tivéssemos morrido pela mão do Senhor na terra do Egito, quando estávamos assentados junto às panelas de carne, e quando comíamos pão à vontade; porque nos trouxestes para este deserto para matar toda esta assembleia de fome.</p>
		<p><span class="numero">4)</span> Então o Senhor disse a Moisés: Eis que vos farei chover pão do céu, e o povo sairá e colherá a porção de cada dia, para que eu o prove se anda na minha lei ou não.</p>
		<p><span class="numero">5)</span> E acontecerá que, no sexto dia, eles prepararão aquilo que trouxerem, e será duas vezes o que ajuntam diariamente.</p>
		<p><span class="numero">6)</span> Então disse Moisés e Arão aos filhos de Israel: À tarde sabereis que o Senhor é quem vos tirou da terra do Egito,</p>
		<p><span class="numero">7)</span> e pela manhã, então vereis a glória do Senhor; porque ele ouve as vossas murmurações contra o Senhor. E quem somos nós, para que murmureis contra nós?</p>
		<p><span class="numero">8)</span> E Moisés disse: Isso será quando o Senhor à tarde vos der carne para comer, e pela manhã pão à vontade, pois o Senhor ouve as vossas murmurações que murmurais contra ele. E quem somos nós? Vossas murmurações não são contra nós, mas contra o Senhor.</p>
		<p><span class="numero">9)</span> E falou Moisés a Arão: Dize a toda a congregação dos filhos de Israel: Aproximai-vos diante do Senhor, porque ele ouviu as vossas murmurações.</p>
		<p><span class="numero">10)</span> E aconteceu, quando Arão falou a toda a congregação dos filhos de Israel, que eles olharam para o deserto, e eis que a glória do Senhor apareceu na nuvem.
		<p><span class="numero">11)</span> E falou o Senhor a Moisés, dizendo:</p>
		<p><span class="numero">12)</span> Ouvi as murmurações dos filhos de Israel; fala-lhes, dizendo: À tarde comereis carne, e pela manhã vos fartareis com pão; e sabereis que eu sou o Senhor vosso Deus.</p>
		<p><span class="numero">13)</span> E aconteceu que, à tarde subiram codornizes e cobriram o acampamento, e pela manhã estava o orvalho ao redor do arraial.</p>
		<p><span class="numero">14)</span> E quando o orvalho subiu, eis que sobre a face do deserto estava uma coisa pequena e redonda, tão pequena quanto a geada sobre o chão.</p>
		<p><span class="numero">15)</span> E quando os filhos de Israel a viram, disseram uns aos outros: Isso é maná, porque não sabiam o que era. E Moisés lhes disse: Isto é o pão que o Senhor vos deu para comer.</p>
		<p><span class="numero">16)</span> Isto é o que o Senhor vos ordenou: Ajuntai dele cada homem segundo o seu comer, um ômer para cada homem, de acordo com o número de vossas pessoas; tomai cada homem para aqueles que estão nas suas tendas.</p>
		<p><span class="numero">17)</span> E os filhos de Israel assim fizeram, e colheram, alguns mais, outros menos.</p>
		<p><span class="numero">18)</span> E quando o mediram com um ômer, o que tinha ajuntado muito não tinha demais, e o que tinha ajuntado pouco não tinha falta; ajuntaram cada homem de acordo com o seu comer.</p>
		<p><span class="numero">19)</span> E disse Moisés: Que nenhum homem deixe dele até de manhã.</p>
		<p><span class="numero">20)</span> Mesmo assim, eles não ouviram a Moisés; mas alguns deles deixaram dele até a manhã seguinte, e criou vermes e cheirava mal. Moisés irou-se com eles.</p>
		<p><span class="numero">21)</span> E o colheram todas as manhãs, cada homem conforme o seu comer; e aquecendo o sol, derretia-se.</p>
		<p><span class="numero">22)</span> E aconteceu que, no sexto dia, eles colheram duas vezes mais pão, dois ômeres para um homem; e todos os governantes da congregação vieram e contaram a Moisés.</p>
		<p><span class="numero">23)</span> E disse-lhes: Isto é o que o Senhor disse: Amanhã é o descanso do shabat santo ao Senhor; o que quiserdes assar, assai-o, e o que quiserdes cozer, cozei-o; e o que restar guardai para vós até pela manhã.</p>
		<p><span class="numero">24)</span> E eles o guardaram até a manhã seguinte, como Moisés ordenara, e não cheirou mal, nem havia verme algum nele.</p>
		<p><span class="numero">25)</span> E disse Moisés: Comei isso hoje; pois hoje é o dia do Shabat do Senhor, pois hoje não o achareis no campo.</p>
		<p><span class="numero">26)</span> Seis dias o colhereis, mas no sétimo dia, que é o shabat, nele não haverá.</p>
		<p><span class="numero">27)</span> E aconteceu que saíram alguns do povo no sétimo dia para ajuntar, e não acharam nada.</p>
		<p><span class="numero">28)</span> E disse o Senhor a Moisés: Até quando recusareis obedecer aos meus mandamentos e às minhas leis?</p>
		<p><span class="numero">29)</span> Vede, pois o Senhor vos deu o shabat, por isso ele vos dá no sexto dia o pão para dois dias. Permanecei cada homem no seu lugar; que nenhum homem saia de seu lugar no sétimo dia.</p>
		<p><span class="numero">30)</span> Assim o povo descansou no sétimo dia.</p>
		<p><span class="numero">31)</span> E a casa de Israel chamou o nome disso maná. E era como semente de coentro, branco, e o seu gosto era como bolachas feitas com mel.</p>
		<p><span class="numero">32)</span> E Moisés disse: Isto é o que o Senhor ordenou: Enche um ômer e guarda-o para as vossas gerações, para que vejam o pão com o qual eu vos alimentei no deserto, quando vos tirei da terra do Egito.</p>
		<p><span class="numero">33)</span> E Moisés disse a Arão: Toma um vaso, e coloca um ômer de maná nele, e guarda-o diante do Senhor, para ser guardado para as vossas gerações.</p>
		<p><span class="numero">34)</span> Assim como o Senhor ordenara a Moisés, assim Arão o pôs diante do Testemunho, para ser guardado.</p>
		<p><span class="numero">35)</span> E os filhos de Israel comeram maná quarenta anos, até que chegaram a uma terra habitada; eles comeram o maná até que chegaram aos termos da terra de Canaã.</p>
