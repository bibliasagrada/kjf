		<a href="ex.php"><h3>Êxodo 35</h3></a>
		<p><span class="numero">1)</span> E Moisés reuniu toda a congregação dos filhos de Israel e lhes disse: Estas são as palavras que o Senhor ordenou, para que se cumprissem.</p>
		<p><span class="numero">2)</span> Seis dias se trabalhará, mas o sétimo dia será para vós dia santo, o shabat de descanso para o Senhor. Todo aquele que nele fizer trabalho morrerá.</p>
		<p><span class="numero">3)</span> Não acendereis fogo em nenhuma das vossas habitações no dia do shabat.</p>
		<p><span class="numero">4)</span> E Moisés falou a toda a congregação dos filhos de Israel, dizendo: Isto é o que o Senhor ordenou, dizendo:</p>
		<p><span class="numero">5)</span> Tomai dentre vós uma oferta ao Senhor. Todo aquele que tiver um coração disposto, que a traga, uma oferta ao Senhor: ouro, e prata, e bronze,</p>
		<p><span class="numero">6)</span> e azul, e púrpura, e carmesim, e linho fino, e pelo de cabra,</p>
		<p><span class="numero">7)</span> e peles de carneiro tingidas de vermelho, e peles de texugo, e madeira de acácia,</p>
		<p><span class="numero">8)</span> e óleo para a luz, e especiarias para o óleo da unção, e para o incenso aromático,</p>
		<p><span class="numero">9)</span> e pedras de ônix, e pedras de engaste para o éfode, e para o peitoral.</p>
		<p><span class="numero">10)</span> E todos os sábios de coração entre vós virão e farão tudo que o Senhor ordenou;</p>
		<p><span class="numero">11)</span> o tabernáculo, sua tenda, e sua coberta, os seus colchetes, e suas tábuas, suas barras, suas colunas, e suas bases;</p>
		<p><span class="numero">12)</span> a arca, e as suas varas, com o propiciatório e o véu de cobertura;</p>
		<p><span class="numero">13)</span> a mesa, e as suas varas, e todos os seus utensílios, e o pão da proposição;</p>
		<p><span class="numero">14)</span> o candelabro para a luz, e os seus utensílios, e suas lâmpadas, com o óleo para a luz;</p>
		<p><span class="numero">15)</span> e o altar do incenso, e suas varas, e o óleo da unção, e o incenso aromático, e a cortina da porta à entrada do tabernáculo;</p>
		<p><span class="numero">16)</span> o altar da oferta queimada, com sua grade de bronze, suas varas, e todos os seus utensílios, a pia e sua base;</p>
		<p><span class="numero">17)</span> as cortinas do pátio, suas colunas, e suas bases, e a cortina da porta do pátio;</p>
		<p><span class="numero">18)</span> as estacas do tabernáculo, e as estacas do pátio, e suas cordas;</p>
		<p><span class="numero">19)</span> as vestes do serviço, para realizar o serviço no lugar santo, as vestes santas para Arão, o sacerdote, e as vestes dos seus filhos, para ministrar no ofício sacerdotal.</p>
		<p><span class="numero">20)</span> E toda a congregação dos filhos de Israel saiu da presença de Moisés.</p>
		<p><span class="numero">21)</span> E veio, todo aquele cujo coração o moveu, e todo aquele cujo espírito o tornou disposto, e trouxeram a oferta do Senhor para a obra do tabernáculo da congregação, e para todo o seu serviço, e para as vestes santas.</p>
		<p><span class="numero">22)</span> E vieram, tanto homens quanto mulheres, com muitos que estavam dispostos de coração, e trouxeram fivelas, e pendentes, e anéis, e braceletes, todas as joias de ouro. E cada homem que oferecia, oferecia uma oferta de ouro ao Senhor.</p>
		<p><span class="numero">23)</span> E todo homem que possuía azul, e púrpura, e carmesim, e linho fino, e pelo de cabra, e peles vermelhas de carneiro, e peles de texugo, os trazia.</p>
		<p><span class="numero">24)</span> Todo aquele que oferecia uma oferta de prata e bronze trazia a oferta do Senhor; e todo homem que possuía madeira de acácia para qualquer obra de serviço, a trazia.</p>
		<p><span class="numero">25)</span> E todas as mulheres de coração sábio fiavam com as mãos, e traziam o que haviam fiado, tanto de azul, quanto de púrpura, e de carmesim, e de linho fino.</p>
		<p><span class="numero">26)</span> E todas as mulheres, cujo coração as moveu em sabedoria, fiaram pelo de cabra.</p>
		<p><span class="numero">27)</span> E os governantes trouxeram pedras de ônix, e pedras de engaste, para o éfode, e para o peitoral;</p>
		<p><span class="numero">28)</span> e especiarias, e óleo para a luz, e para o óleo da unção, e para o incenso aromático.</p>
		<p><span class="numero">29)</span> Os filhos de Israel trouxeram uma oferta voluntária ao Senhor, todo homem e toda mulher cujo coração voluntariamente se moveu a trazer para todo tipo de obra, que o Senhor ordenara que fosse feita pela mão de Moisés.</p>
		<p><span class="numero">30)</span> E Moisés disse aos filhos de Israel: Eis que o Senhor chamou pelo nome Bezalel, o filho de Uri, o filho de Hur, da tribo de Judá,</p>
		<p><span class="numero">31)</span> e ele o encheu com o espírito de Deus, em sabedoria, em entendimento, e em conhecimento, e em todo tipo de mão de obra,</p>
		<p><span class="numero">32)</span> para elaborar obras habilidosas, para trabalhar em ouro, e em prata, e em bronze,</p>
		<p><span class="numero">33)</span> e em lapidar pedras, para engastá-las, e em esculpir madeira, para fazer todo trabalho esmerado.</p>
		<p><span class="numero">34)</span> E ele havia colocado no seu coração que pudesse ensinar, tanto ele quanto Aoliabe, o filho de Aisamaque, da tribo de Dã.</p>
		<p><span class="numero">35)</span> A ele encheu com sabedoria de coração, para trabalhar em toda obra de mestre, de gravador, e a mais elaborada, e a do bordador, em azul, em púrpura, em carmesim, e em linho fino, e a do tecelão, também a daquele que faz qualquer trabalho, e a daqueles que elaboram trabalho esmerado.&nbsp;<a href="ex-36.php"><span class="proximo">Próximo capítulo</span></a></p>
