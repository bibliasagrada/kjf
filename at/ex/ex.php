		<p>Segundo livro da Bíblia, em Êxodo nos é apresentada a história de Moisés e o relato da saída dos israelitas do Egito em busca da Terra Prometida. Liderados por Moisés, o povo deixa para trás uma vida de escravidão, seguindo a crença em Deus e de uma vida melhor. A escrita desse livro é atribuída a Moisés.</p>

		<p>Ao longo dos 40 capítulos do livro, podemos acompanhar a tentativa de Moisés em convencer o Faraó a libertar os israelitas. Diante desse pedido, o faraó aumenta a pressão sobre o povo, fazendo com que Deus impusesse ao faraó e aos egípcios 10 pragas, as “Pragas do Egito”. Na décima praga, todos os filhos mais velhos das famílias egípcias morreriam. Porém, as famílias israelitas sacrificaram cordeiros e colocaram o sangue nas portas de suas casas, impedindo que o anjo da morte entrasse. Esse acontecimento foi considerado a primeira Páscoa.</p>

		<p>Com a morte do seu primogênito, o faraó deixa os israelitas partirem. A fim de salvar o povo, Moisés, acompanhado de seu irmão Arão, lidera pelo deserto a libertação. Entretanto, o faraó muda a sua decisão e decide perseguir o povo, levando os israelitas a ficarem encurralados entre o Mar Vermelho e o exército egípcio. Deus abre o Mar Vermelho e os israelitas atravessam em segurança.</p>

		<p>Após andarem pelo deserto, todos chegam ao Monte Sinai. Deus faz uma aliança com o povo, e assim, dita a Moisés os Dez Mandamentos. Moisés também lidera o povo na construção do Tabernáculo, tenda utilizada como um templo dos judeus.</p>	
