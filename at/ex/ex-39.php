		<a href="ex.php"><h3>Êxodo 39</h3></a>
		<p><span class="numero">1)</span> E do azul, e púrpura, e carmesim, fizeram vestes do serviço, para realizar o serviço no lugar santo, e fizeram as vestes santas para Arão, conforme o Senhor ordenara a Moisés.</p>
		<p><span class="numero">2)</span> E ele fez o éfode de ouro, azul, e púrpura, e carmesim, e linho fino torcido.</p>
		<p><span class="numero">3)</span> E bateram o ouro em lâminas finas, e o cortaram em fios, para tecê-lo em azul, e em púrpura, e em carmesim, e no linho fino, com trabalho esmerado.</p>
		<p><span class="numero">4)</span> Fizeram nele ombreiras que se juntassem; nas duas extremidades eram juntadas.</p>
		<p><span class="numero">5)</span> E o cinto trançado do seu éfode, que estava sobre ele, era do mesmo, conforme a sua obra; de ouro, azul, e púrpura, e carmesim, e linho fino torcido, conforme o Senhor ordenara a Moisés.</p>
		<p><span class="numero">6)</span> E prepararam as pedras de ônix engastadas em ouro, lavradas, como selos são lavrados, com os nomes dos filhos de Israel.</p>
		<p><span class="numero">7)</span> E ele os colocou sobre os ombros do éfode, para que fossem pedras por memorial para os filhos de Israel, conforme o Senhor ordenara a Moisés.</p>
		<p><span class="numero">8)</span> E ele fez o peitoral de trabalho esmerado, como a obra do éfode; de azul, e púrpura, e carmesim, e linho fino torcido.</p>
		<p><span class="numero">9)</span> Era quadrado, fizeram o peitoral dobrado; um palmo era o seu comprimento, e um palmo era a sua largura, sendo dobrado.</p>
		<p><span class="numero">10)</span> E engastaram nele quatro fileiras de pedras: a primeira fileira era um sárdio, um topázio e um carbúnculo; esta era a primeira fileira.</p>
		<p><span class="numero">11)</span> E a segunda fileira uma esmeralda, uma safira e um diamante.</p>
		<p><span class="numero">12)</span> E a terceira fileira era um jacinto, uma ágata e uma ametista.</p>
		<p><span class="numero">13)</span> E a quarta fileira era um berilo, um ônix e um jaspe. Eram engastadas em ouro nos seus engastes.</p>
		<p><span class="numero">14)</span> E as pedras eram de acordo com os nomes dos filhos de Israel, doze, segundo os seus nomes, de gravura como em selo, cada um com seu nome, de acordo com as doze tribos.</p>
		<p><span class="numero">15)</span> E fizeram sobre o peitoral correntes na extremidade, trançadas como cordão, obra de ouro puro.</p>
		<p><span class="numero">16)</span> E fizeram dois engastes de ouro, e dois anéis de ouro, e puseram os dois anéis nas extremidades do peitoral.</p>
		<p><span class="numero">17)</span> E colocaram as duas correntes de ouro nos dois anéis nas extremidades do peitoral.</p>
		<p><span class="numero">18)</span> E as duas extremidades das duas correntes prenderam nos dois engastes, e os colocaram nas ombreiras do éfode diante dele.</p>
		<p><span class="numero">19)</span> E fizeram dois anéis de ouro, e os colocaram sobre as duas extremidades do peitoral na sua borda, que estava no lado do éfode por dentro.</p>
		<p><span class="numero">20)</span> E fizeram os outros dois anéis de ouro, e os colocaram nos dois lados do éfode por baixo, diante dele, defronte da outra juntura dele, sobre o cinto trançado do éfode.</p>
		<p><span class="numero">21)</span> E ligaram o peitoral pelos seus anéis aos anéis do éfode com um laço de azul, para que estivesse acima do cinto trançado do éfode, e para que o peitoral não se separasse do éfode, conforme o Senhor ordenara a Moisés.</p>
		<p><span class="numero">22)</span> E fez o manto do éfode de obra tecida, todo de azul.</p>
		<p><span class="numero">23)</span> E havia uma abertura no meio do manto, como a abertura de cota de malha, com uma borda em redor da sua abertura, para que não se rompesse.</p>
		<p><span class="numero">24)</span> E nas bordas do manto fizeram romãs de azul, e de púrpura e de carmesim, e de linho torcido.</p>
		<p><span class="numero">25)</span> E fizeram sinos de ouro puro, e colocaram os sinos entre as romãs sobre a borda do manto, ao redor entre as romãs;</p>
		<p><span class="numero">26)</span> um sino e uma romã, outro sino e uma romã ao redor da borda do manto para ministrar, conforme o Senhor ordenara a Moisés.</p>
		<p><span class="numero">27)</span> E fizeram as túnicas de linho fino de obra tecida para Arão, e para os seus filhos,</p>
		<p><span class="numero">28)</span> e uma mitra de linho fino, e o adorno das tiaras de linho fino, e os calções de linho fino torcido,</p>
		<p><span class="numero">29)</span> e um cinto de linho fino torcido, e azul, e púrpura, e carmesim, obra de bordador, conforme o Senhor ordenara a Moisés.</p>
		<p><span class="numero">30)</span> E fizeram a lâmina da santa coroa de ouro puro, e escreveram sobre ela uma inscrição, semelhante a gravura de selos: Santidade para o Senhor.</p>
		<p><span class="numero">31)</span> E a amarraram com um cordão de azul, para prendê-la sobre a mitra, conforme o Senhor ordenou a Moisés.</p>
		<p><span class="numero">32)</span> Assim foi concluída toda a obra do tabernáculo do tabernáculo da congregação; e os filhos de Israel fizeram de acordo com tudo que o Senhor ordenara a Moisés, assim eles fizeram.</p>
		<p><span class="numero">33)</span> E eles trouxeram o tabernáculo a Moisés, a tenda, e todos os seus móveis, os seus colchetes, as suas tábuas, as suas barras, e as suas colunas, e as suas bases;</p>
		<p><span class="numero">34)</span> e a coberta de peles de carneiro tingidas de vermelho, e a coberta de peles de texugo, e o véu da cobertura;</p>
		<p><span class="numero">35)</span> a arca do testemunho, e as suas varas, e o propiciatório;</p>
		<p><span class="numero">36)</span> a mesa, e todos os seus utensílios, e o pão da proposição;</p>
		<p><span class="numero">37)</span> o candelabro puro, com as suas lâmpadas, com as lâmpadas a serem colocadas em ordem, e todos os seus utensílios e o óleo para a luz;</p>
		<p><span class="numero">38)</span> o altar de ouro, e o óleo da unção, e o incenso aromático, e a cortina da porta do tabernáculo;</p>
		<p><span class="numero">39)</span> o altar de bronze, e a sua grade de bronze, as suas varas, e todos os seus utensílios, a pia e a sua base;</p>
		<p><span class="numero">40)</span> as cortinas do pátio, as suas colunas, e as suas bases, e a cortina para a porta do pátio, as suas cordas, e as suas estacas, e todos os utensílios para o serviço do tabernáculo, para a tenda da congregação;</p>
		<p><span class="numero">41)</span> as vestes de serviço para fazer o serviço no lugar santo, e as vestes santas para Arão, o sacerdote, e as vestes dos seus filhos, para ministrar no ofício sacerdotal.</p>
		<p><span class="numero">42)</span> Conforme a tudo que o Senhor ordenara a Moisés, assim os filhos de Israel fizeram todo o trabalho.</p>
		<p><span class="numero">43)</span> E Moisés viu toda a obra, e eis que, eles a haviam realizado conforme o Senhor ordenara, assim eles haviam feito. E Moisés os abençoou.&nbsp;<a href="ex-40.php"><span class="proximo">Próximo capítulo</span></a></p>
