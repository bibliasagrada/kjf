		<a href="ex.php"><h3>Êxodo 22</h3></a>
		<p><span class="numero">1)</span> Se um homem furtar um boi, ou uma ovelha, e o matar ou vender, restituirá cinco bois por um boi, e quatro ovelhas por uma ovelha.</p>
		<p><span class="numero">2)</span> Se um ladrão for encontrado saqueando e for ferido para que morra, não se derramará sangue por ele.</p>
		<p><span class="numero">3)</span> Se o sol houver se levantado sobre ele, derramar-se-á sangue por ele, pois ele deve fazer restituição completa. Se ele não possuir nada, será vendido pelo seu furto.</p>
		<p><span class="numero">4)</span> Se o furto for achado vivo em sua mão, seja boi, jumento ou ovelha, ele restituirá em dobro.</p>
		<p><span class="numero">5)</span> Se um homem fizer um campo ou uma vinha a ser comida, e colocar nela seu animal, e for alimentar no campo de outro, então do melhor do seu próprio campo e do melhor da sua própria vinha fará restituição.</p>
		<p><span class="numero">6)</span> Se irromper um fogo, e pegar nos espinhos, de modo que sejam consumidos os feixes de trigo, ou a seara, ou o campo, o que iniciou o fogo certamente fará restituição.</p>
		<p><span class="numero">7)</span> Se um homem entregar a seu próximo dinheiro ou objetos para guardar, e isso for furtado da casa do homem, se o ladrão for encontrado, que ele pague em dobro.</p>
		<p><span class="numero">8)</span> Se o ladrão não for encontrado, então o dono da casa será levado aos juízes, para ver se ele colocou a mão nos bens de seu próximo.</p>
		<p><span class="numero">9)</span> Para todo tipo de transgressão, seja por boi, por jumento, por ovelhas, por vestes ou por qualquer coisa perdida, que outro protestar ser seu, a causa de ambas as partes virá diante dos juízes; e aquele a quem os juízes condenarem, este pagará em dobro ao seu próximo.</p>
		<p><span class="numero">10)</span> Se um homem entregar ao seu próximo um jumento, ou um boi, ou uma ovelha, ou algum animal para guardar, e este morrer, ou for ferido, ou levado embora, sem que nenhum homem veja,</p>
		<p><span class="numero">11)</span> então haverá um juramento do Senhor entre os dois, de que ele não pôs a mão nos bens de seu próximo. E o dono disso o aceitará, e o outro não o restituirá.</p>
		<p><span class="numero">12)</span> E se lhe for furtado, ele fará restituição ao seu dono.</p>
		<p><span class="numero">13)</span> Se for dilacerado, então que ele o traga em testemunho, e ele não fará restituição pelo que foi dilacerado.</p>
		<p><span class="numero">14)</span> E se um homem pedir emprestado alguma coisa de seu próximo, e for ferido, ou morrer, não estando com ele o dono, ele certamente fará restituição.</p>
		<p><span class="numero">15)</span> Mas se o dono disso estiver com ele, não fará restituição; se foi uma coisa alugada, será pelo seu aluguel.</p>
		<p><span class="numero">16)</span> E se um homem seduzir uma virgem que não é desposada, e se deitar com ela, ele certamente a dotará e tomará por sua mulher.</p>
		<p><span class="numero">17)</span> Se o seu pai completamente se recusar a dá-la, ele pagará em dinheiro conforme o dote das virgens.</p>
		<p><span class="numero">18)</span> Não deixarás viver uma feiticeira.</p>
		<p><span class="numero">19)</span> Todo aquele que se deitar com um animal certamente será morto.</p>
		<p><span class="numero">20)</span> Aquele que sacrificar a qualquer deus, e não somente ao Senhor, este será totalmente destruído.</p>
		<p><span class="numero">21)</span> Não afligirás um estrangeiro, nem o oprimirás, pois fostes estrangeiros na terra do Egito.</p>
		<p><span class="numero">22)</span> Não afligireis nenhuma viúva, nem o órfão.</p>
		<p><span class="numero">23)</span> Se os afligirdes de alguma maneira, e eles clamarem a mim, eu certamente ouvirei o seu clamor,</p>
		<p><span class="numero">24)</span> e a minha ira arderá, e eu vos matarei à espada, e vossas mulheres serão viúvas, e vossos filhos órfãos.</p>
		<p><span class="numero">25)</span> Se emprestares dinheiro a qualquer de meu povo, que é pobre junto a ti, não serás para ele agiota nem lhe imporás usura.</p>
		<p><span class="numero">26)</span> Se tomares a veste de teu próximo por penhor, tu lho restituirás antes do pôr do sol,</p>
		<p><span class="numero">27)</span> porque esta é sua única coberta, é a veste da sua pele, em que ele dormirá? E acontecerá que, quando ele clamar a mim, eu o ouvirei, pois sou benevolente.</p>
		<p><span class="numero">28)</span> Não injuriarás os juízes, nem maldirás o governante do teu povo.</p>
		<p><span class="numero">29)</span> Não tardarás a oferecer o primeiro de teus frutos maduros, nem de teus licores; o primogênito de teus filhos me darás.</p>
		<p><span class="numero">30)</span> Da mesma forma farás com os teus bois, e com as tuas ovelhas; sete dias estarão com sua mãe; no oitavo dia os darás a mim.</p>
		<p><span class="numero">31)</span> E sereis para mim homens santos; não comereis carne alguma que foi dilacerada por animal no campo; vós a lançareis aos cães.&nbsp;<a href="ex-23.php"><span class="proximo">Próximo capítulo</span></a></p>
