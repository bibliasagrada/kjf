		<a href="ex.php"><h3>Êxodo 10</h3></a>
		<p><span class="numero">1)</span> E disse o Senhor a Moisés: Vai à presença de Faraó, porque eu endureci o seu coração, e o coração de seus servos, para que eu mostre estes sinais diante dele,</p>
		<p><span class="numero">2)</span> e para que tu contes ao ouvido dos teus filhos, e dos filhos de teus filhos, as coisas e meus sinais que fiz entre eles no Egito, para que saibais que eu sou o Senhor.</p>
		<p><span class="numero">3)</span> E Moisés e Arão foram à presença de Faraó, e lhe disseram: Assim diz o Senhor Deus dos hebreus: Quanto tempo recusarás a humilhar-te diante de mim? Deixa meu povo ir, para que me sirva.</p>
		<p><span class="numero">4)</span> Se não, se te recusares a deixar meu povo ir, eis que amanhã trarei locustas para a tua costa,</p>
		<p><span class="numero">5)</span> e cobrirão a face da terra, para que não se consigas ver a terra. E eles comerão o resto do que escapou, o que restou do granizo, e comerão toda árvore que cresce no campo.</p>
		<p><span class="numero">6)</span> E encherão as tuas casas, e as casas de todos os teus servos, e as casas de todos os egípcios, que nem teus pais, nem os pais de teus pais viram, desde o dia em que eles estiveram sobre a terra até este dia. E virou-se e saiu da presença de Faraó.</p>
		<p><span class="numero">7)</span> E os servos de Faraó lhe disseram: Até quando este homem será uma armadilha para nós? Deixa ir os homens, para que sirvam ao Senhor seu Deus. Não sabes ainda que o Egito está destruído?</p>
		<p><span class="numero">8)</span> E Moisés e Arão foram levados novamente a Faraó, e ele lhes disse: Ide, servi ao Senhor vosso Deus; mas quem são os que irão?</p>
		<p><span class="numero">9)</span> E Moisés disse: Iremos com nossos jovens e com nossos velhos, com nossos filhos e nossas filhas, com nossos rebanhos e com nosso gado iremos, porque precisamos celebrar uma festa ao Senhor.</p>
		<p><span class="numero">10)</span> E ele lhes disse: Que o Senhor assim esteja convosco, assim como eu vos deixarei ir, e a vossos pequenos; olhai, pois há mal diante de vós.</p>
		<p><span class="numero">11)</span> Não será assim. Ide agora vós que sois homens, e servi ao Senhor, pois isso é o que desejastes. E eles foram expulsos da presença de Faraó.</p>
		<p><span class="numero">12)</span> E o Senhor disse a Moisés: Estende tua mão sobre a terra do Egito, para que as locustas venham sobre a terra do Egito, e comam toda erva da terra, tudo que o granizo deixou.</p>
		<p><span class="numero">13)</span> E Moisés estendeu seu cajado sobre a terra do Egito, e o Senhor trouxe um vento oriental sobre a terra durante todo aquele dia, e toda aquela noite; e ao amanhecer, o vento oriental trouxe as locustas.</p>
		<p><span class="numero">14)</span> E as locustas subiram por toda a terra do Egito, e descansaram em toda a costa do Egito, terríveis foram; antes destas nunca houve locustas como estas, nem depois destas haverá.</p>
		<p><span class="numero">15)</span> Pois elas cobriram a face de toda a terra, de modo que a terra se escureceu, e elas comeram toda a erva da terra, e todo o fruto das árvores que o granizo havia deixado. E não ficou verde algum nas árvores, nem nas ervas do campo, em toda a terra do Egito.</p>
		<p><span class="numero">16)</span> Então, se apressou Faraó em chamar a Moisés e a Arão e disse: Pequei contra o Senhor vosso Deus, e contra vós.</p>
		<p><span class="numero">17)</span> Agora, pois, peço-vos que perdoeis o meu pecado somente esta vez, e rogai ao Senhor, vosso Deus, que ele tire de mim somente esta morte.</p>
		<p><span class="numero">18)</span> E ele saiu da presença de Faraó, e suplicou ao Senhor.</p>
		<p><span class="numero">19)</span> E o Senhor trouxe um vento ocidental fortíssimo, que tirou as locustas e as lançou no mar Vermelho. Não ficou uma locusta em toda a costa do Egito.</p>
		<p><span class="numero">20)</span> Mas o Senhor endureceu o coração de Faraó, para que ele não deixasse os filhos de Israel ir.</p>
		<p><span class="numero">21)</span> E o Senhor disse a Moisés: Estende a tua mão para o céu, para que haja trevas sobre a terra do Egito, trevas tão escuras que possam ser sentidas.</p>
		<p><span class="numero">22)</span> E Moisés estendeu a mão para o céu, e houve densas trevas em toda a terra do Egito por três dias.</p>
		<p><span class="numero">23)</span> Eles não viam uns aos outros, e ninguém se levantou do seu lugar por três dias, mas todos os filhos de Israel tinham luz em suas habitações.</p>
		<p><span class="numero">24)</span> E Faraó chamou a Moisés e disse: Ide, servi ao Senhor; somente fiquem os vossos rebanhos e o vosso gado; deixai também os vossos pequenos ir convosco.</p>
		<p><span class="numero">25)</span> E disse Moisés: Deves dar-nos também sacrifícios e ofertas queimadas, para que possamos oferecer sacrifícios ao Senhor nosso Deus.</p>
		<p><span class="numero">26)</span> E também o nosso gado irá conosco; nenhum casco ficará, porque disso tomaremos para servir ao Senhor nosso Deus. E não sabemos com que serviremos ao Senhor, até chegarmos lá.</p>
		<p><span class="numero">27)</span> Mas o Senhor endureceu o coração de Faraó, e ele não os deixou ir.</p>
		<p><span class="numero">28)</span> E Faraó lhe disse: Sai da minha presença, toma cuidado para que não veja mais a minha face, porque no dia em que vires a minha face, morrerás.</p>
