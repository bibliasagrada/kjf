		<a href="ex.php"><h3>Êxodo 40</h3></a>
		<p><span class="numero">1)</span> E falou o Senhor a Moisés, dizendo:</p>
		<p><span class="numero">2)</span> No primeiro dia do primeiro mês levantarás o tabernáculo da tenda da congregação.</p>
		<p><span class="numero">3)</span> E nele colocarás a arca do testemunho, e cobrirás a arca com o véu.</p>
		<p><span class="numero">4)</span> E colocarás nele a mesa, e porás em ordem as coisas que devem ser postas em ordem sobre ela; e colocarás o candelabro e acenderás as suas lâmpadas.</p>
		<p><span class="numero">5)</span> E colocarás o altar de ouro para o incenso diante da arca do testemunho, e colocarás a cortina da porta do tabernáculo.</p>
		<p><span class="numero">6)</span> E colocarás o altar da oferta queimada diante da porta do tabernáculo da tenda da congregação.</p>
		<p><span class="numero">7)</span> E colocarás a pia entre a tenda da congregação e o altar, e nela colocarás água.</p>
		<p><span class="numero">8)</span> E levantarás o pátio ao redor, e pendurarás as cortinas da porta do pátio.</p>
		<p><span class="numero">9)</span> E tomarás o óleo da unção, e ungirás o tabernáculo e tudo que está nele, e o santificarás, e todos os seus utensílios; e será santo.</p>
		<p><span class="numero">10)</span> E ungirás o altar da oferta queimada, e todos os seus utensílios, e santificarás o altar; e será um altar santíssimo.</p>
		<p><span class="numero">11)</span> E ungirás a pia e a sua base, e a santificarás.</p>
		<p><span class="numero">12)</span> Trarás Arão e os seus filhos para a porta do tabernáculo da congregação e os lavarás com água.</p>
		<p><span class="numero">13)</span> E porás sobre Arão as vestes santas, e o ungirás, e o santificarás, para que ele ministre a mim no ofício sacerdotal.</p>
		<p><span class="numero">14)</span> E trarás os seus filhos, e os vestirás com as túnicas,</p>
		<p><span class="numero">15)</span> e os ungirás, assim como ungiste seu pai, para que eles ministrem a mim no ofício sacerdotal, pois a sua unção certamente será um sacerdócio perpétuo por suas gerações.</p>
		<p><span class="numero">16)</span> Assim fez Moisés, de acordo com tudo que o Senhor lhe ordenara, assim ele fez.</p>
		<p><span class="numero">17)</span> E aconteceu no primeiro mês do segundo ano, no primeiro dia do mês, que o tabernáculo foi levantado.</p>
		<p><span class="numero">18)</span> E Moisés levantou o tabernáculo, e fixou as suas bases, e colocou as suas tábuas, e colocou as suas barras, e levantou as suas colunas.</p>
		<p><span class="numero">19)</span> E estendeu a tenda sobre o tabernáculo, e colocou a coberta da tenda sobre ela, conforme o Senhor ordenara a Moisés.</p>
		<p><span class="numero">20)</span> E tomou o testemunho e o pôs na arca, e colocou as varas na arca, e pôs o propiciatório sobre a arca;</p>
		<p><span class="numero">21)</span> e ele trouxe a arca para dentro do tabernáculo, e colocou o véu da cobertura, e cobriu a arca do testemunho, como o Senhor ordenara a Moisés.</p>
		<p><span class="numero">22)</span> E colocou a mesa na tenda da congregação, sobre o lado do tabernáculo em direção ao norte, sem o véu.</p>
		<p><span class="numero">23)</span> E ele pôs o pão em ordem sobre ela diante do Senhor, conforme o Senhor ordenara a Moisés.</p>
		<p><span class="numero">24)</span> E colocou o candelabro na tenda da congregação, diante da mesa, no lado do tabernáculo em direção ao sul.</p>
		<p><span class="numero">25)</span> E acendeu as lâmpadas diante do Senhor, conforme o Senhor ordenara a Moisés.</p>
		<p><span class="numero">26)</span> E ele colocou o altar de ouro na tenda da congregação, diante do véu,</p>
		<p><span class="numero">27)</span> e queimou incenso aromático nele, conforme o Senhor ordenara a Moisés.</p>
		<p><span class="numero">28)</span> E colocou a cortina na porta do tabernáculo.</p>
		<p><span class="numero">29)</span> E ele colocou o altar da oferta queimada junto a porta do tabernáculo da tenda da congregação, e ofereceu a oferta queimada sobre ele, e a oferta de carne, conforme o Senhor ordenara a Moisés.</p>
		<p><span class="numero">30)</span> E ele colocou a pia entre a tenda da congregação e o altar, e colocou água nela, para lavar.</p>
		<p><span class="numero">31)</span> E Moisés e Arão e os seus filhos lavaram ali as suas mãos e os seus pés;</p>
		<p><span class="numero">32)</span> quando entravam na tenda da congregação, e quando se aproximavam do altar, eles se lavavam, conforme o Senhor ordenara a Moisés.</p>
		<p><span class="numero">33)</span> E ele levantou o pátio ao redor do tabernáculo e do altar, e colocou a cortina da porta do pátio. Assim Moisés terminou a obra.</p>
		<p><span class="numero">34)</span> Então uma nuvem cobriu a tenda da congregação, e a glória do Senhor encheu o tabernáculo.</p>
		<p><span class="numero">35)</span> E Moisés não podia entrar na tenda da congregação, porque a nuvem permanecia sobre ela, e a glória do Senhor enchia o tabernáculo.</p>
		<p><span class="numero">36)</span> E quando a nuvem se levantava de sobre o tabernáculo, os filhos de Israel prosseguiram em todas as suas jornadas.</p>
		<p><span class="numero">37)</span> Mas quando a nuvem não se levantava, então eles não viajavam até o dia em que ela se levantava.</p>
		<p><span class="numero">38)</span> Porque a nuvem do Senhor estava sobre o tabernáculo de dia, e fogo estava sobre ele à noite, à vista de toda a casa de Israel, em todas as suas jornadas.&nbsp;<a href="../lv/lv-1.php"><span class="proximo">Próximo capítulo</span></a></p>
