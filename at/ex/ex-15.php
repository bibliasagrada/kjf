		<a href="ex.php"><h3>Êxodo 15</h3></a>
		<p><span class="numero">1)</span> Então Moisés e os filhos de Israel cantaram este cântico ao Senhor, e falaram, dizendo: Cantarei ao Senhor, porque ele triunfou gloriosamente; lançou no mar o cavalo e o seu cavaleiro.</p>
		<p><span class="numero">2)</span> O Senhor é a minha força e o meu cântico, e ele se tornou a minha salvação. Ele é o meu Deus, e prepararei para ele uma habitação. Deus de meu pai, e eu o exaltarei.</p>
		<p><span class="numero">3)</span> O Senhor é homem de guerra; Senhor é o seu nome.</p>
		<p><span class="numero">4)</span> Lançou no mar as carruagens de Faraó e o seu exército; seus capitães escolhidos também foram afogados no mar Vermelho.</p>
		<p><span class="numero">5)</span> Os abismos os cobriram; desceram às profundezas como uma pedra.
		<p><span class="numero">6)</span> Tua mão direita, ó Senhor, é gloriosa em poder; tua mão direita, ó Senhor, despedaçou o inimigo.</p>
		<p><span class="numero">7)</span> E na grandeza da tua excelência derrubaste aqueles que se levantaram contra ti; enviaste a tua ira, que os consumiu como restolho.</p>
		<p><span class="numero">8)</span> E com o sopro das tuas narinas as águas amontoaram-se, as correntes pararam em pé como um montão, e as profundezas coalharam- se no coração do mar.</p>
		<p><span class="numero">9)</span> Disse o inimigo: Perseguirei, alcançarei, dividirei o despojo; meu desejo se fartará neles; desembainharei a minha espada, minha mão os destruirá.</p>
		<p><span class="numero">10)</span> Tu sopraste com o teu vento, e o mar os cobriu; eles afundaram como chumbo nas poderosas águas.</p>
		<p><span class="numero">11)</span> Quem é como tu, ó Senhor, entre os deuses? Quem é como tu, glorioso em santidade, temeroso em louvores, fazendo maravilhas?</p>
		<p><span class="numero">12)</span> Tu estendestes a tua mão direita, e a terra os engoliu.</p>
		<p><span class="numero">13)</span> Na tua misericórdia conduziste o povo que resgataste; com a tua força os guiaste à tua santa habitação.</p>
		<p><span class="numero">14)</span> O povo ouvirá e temerá; a dor tomará os habitantes da Filístia.</p>
		<p><span class="numero">15)</span> Então os príncipes de Edom se maravilharam; um tremor apoderou-se dos homens poderosos de Moabe; derreteram-se todos os habitantes de Canaã.</p>
		<p><span class="numero">16)</span> Sobre eles caiu medo e pavor; pela grandeza de teu braço, emudeceram como pedra; até que o teu povo passasse, ó Senhor, até que passasse o povo que tu adquiriste.</p>
		<p><span class="numero">17)</span> Tu os levarás e os plantarás no monte da tua herança, no lugar, ó Senhor, que fizeste para nele habitar; no Santuário, ó Senhor, que tuas mãos estabeleceram.</p>
		<p><span class="numero">18)</span> O Senhor reinará para sempre e sempre.</p>
		<p><span class="numero">19)</span> Porque os cavalos de Faraó, com as suas carruagens e com os seus cavaleiros, entraram no mar, e o Senhor trouxe novamente sobre eles as águas do mar, todavia os filhos de Israel continuaram por terra seca no meio do mar.</p>
		<p><span class="numero">20)</span> E Miriã, a profetisa, a irmã de Arão, tomou um tamborim em sua mão; e todas as mulheres saíram após ela com tamboris e com danças.</p>
		<p><span class="numero">21)</span> E Miriã lhes respondeu: Cantai ao Senhor, porque ele triunfou gloriosamente; o cavalo e seu cavaleiro ele lançou no mar.</p>
		<p><span class="numero">22)</span> Assim Moisés trouxe Israel do mar Vermelho, e eles saíram para o deserto de Sur, e foram três dias pelo deserto, e não encontraram água.</p>
		<p><span class="numero">23)</span> E quando chegaram a Mara, não puderam beber as águas de Mara, porque eram amargas. Por isso o nome foi chamado Mara.</p>
		<p><span class="numero">24)</span> E o povo murmurou contra Moisés, dizendo: O que beberemos?</p>
		<p><span class="numero">25)</span> E ele clamou ao Senhor; e o Senhor lhe mostrou uma árvore, que, quando ele a lançou nas águas, as águas se tornaram doces. Ali fez para eles um estatuto e uma ordenança, e ali ele os testou,</p>
		<p><span class="numero">26)</span> e disse: Se diligentemente ouvires a voz do Senhor teu Deus, e fizeres o que é reto à sua vista, e deres teu ouvido aos seus mandamentos, e guardares todos os seus estatutos, não colocarei sobre ti nenhuma destas doenças que eu trouxe sobre os egípcios, pois eu sou o Senhor que te cura.</p>
		<p><span class="numero">27)</span> E chegaram a Elim, onde havia doze fontes de água e setenta palmeiras. E acamparam ali junto às águas.&nbsp;<a href="ex-16.php"><span class="proximo">Próximo capítulo</span></a></p>
