		<a href="ex.php"><h3>Êxodo 29</h3></a>
		<p><span class="numero">1)</span> E isto é o que lhes farás para santificá- los, para que me ministrem no ofício sacerdotal: Toma um novilho e dois carneiros sem defeito,</p>
		<p><span class="numero">2)</span> e pão sem fermento, e bolos sem fermento, amassados com óleo, e coscorões sem fermento, untados com óleo; com farinha de trigo os farás.</p>
		<p><span class="numero">3)</span> E os colocarás em um cesto, e os trarás no cesto, com o novilho e os dois carneiros.</p>
		<p><span class="numero">4)</span> E tu trarás Arão e seus filhos à porta do tabernáculo da congregação, e tu os lavarás com água.</p>
		<p><span class="numero">5)</span> E tomarás as vestes, e porás em Arão a túnica, e o manto do éfode, e o éfode, e o peitoral, e o cingirás com o cinto trançado do éfode.</p>
		<p><span class="numero">6)</span> E colocarás a mitra sobre sua cabeça, e colocarás a coroa da santidade sobre a mitra.</p>
		<p><span class="numero">7)</span> Então tomarás o óleo da unção e o derramarás sobre a sua cabeça, e o ungirás.</p>
		<p><span class="numero">8)</span> E trarás os seus filhos, e colocarás as túnicas sobre eles.</p>
		<p><span class="numero">9)</span> E os cingirás com cintos, a Arão e aos seus filhos, e colocarás as tiaras neles; e o ofício sacerdotal será deles por estatuto perpétuo; e consagrarás Arão e os seus filhos.</p>
		<p><span class="numero">10)</span> E trarás um novilho diante do tabernáculo da congregação; e Arão e os seus filhos colocarão suas mãos sobre a cabeça do novilho.</p>
		<p><span class="numero">11)</span> E imolarás o novilho diante do Senhor, junto à porta do tabernáculo da congregação.</p>
		<p><span class="numero">12)</span> E tomarás do sangue do novilho, e o colocarás sobre os chifres do altar com o teu dedo, e derramarás todo o sangue ao lado da base do altar.</p>
		<p><span class="numero">13)</span> E tomarás toda a gordura que cobre as entranhas, e o redenho que está sobre o fígado, e os dois rins, e a gordura que está sobre eles, e os queimarás sobre o altar.</p>
		<p><span class="numero">14)</span> Mas a carne do novilho, e a sua pele, e seu esterco, queimarás com fogo fora do acampamento; é oferta pelo pecado.</p>
		<p><span class="numero">15)</span> Também tomarás um carneiro, e Arão e os seus filhos colocarão as suas mãos sobre a cabeça do carneiro.</p>
		<p><span class="numero">16)</span> E imolarás o carneiro, e tomarás do seu sangue, e o aspergirás em redor sobre o altar.</p>
		<p><span class="numero">17)</span> E cortarás o carneiro em partes, e lavarás as suas entranhas e as suas pernas, e as colocarás sobre as suas partes e sobre a sua cabeça.</p>
		<p><span class="numero">18)</span> E queimarás o carneiro por completo sobre o altar; é uma oferta queimada para o Senhor, é um cheiro suave, uma oferta feita pelo fogo ao Senhor.</p>
		<p><span class="numero">19)</span> E tomarás o outro carneiro, e Arão e os seus filhos colocarão as suas mãos sobre a cabeça do carneiro.</p>
		<p><span class="numero">20)</span> Então imolarás o carneiro, e tomarás do seu sangue, e o colocarás sobre a ponta da orelha direita de Arão, e sobre a ponta da orelha direita dos seus filhos, e sobre o polegar da sua mão direita, e sobre o dedão do seu pé direito, e aspergirás o sangue em redor sobre o altar.</p>
		<p><span class="numero">21)</span> E tomarás do sangue que está sobre o altar, e do óleo da unção, e o aspergirás sobre Arão, e sobre suas vestes, e sobre os seus filhos, e sobre as vestes dos seus filhos com ele. E ele será santificado, e as suas vestes, e os seus filhos, e as vestes dos seus filhos com ele.</p>
		<p><span class="numero">22)</span> Também tomarás do carneiro a gordura e a cauda, e a gordura que cobre as entranhas, e o redenho que está sobre o fígado, e os dois rins, e a gordura que está sobre eles, e o ombro direito, pois é um carneiro de consagração,</p>
		<p><span class="numero">23)</span> e um pão, e um bolo de pão azeitado, e um coscorão do cesto de pão sem fermento que está diante do Senhor,</p>
		<p><span class="numero">24)</span> e colocarás tudo nas mãos de Arão, e nas mãos de seus filhos, e com movimento os oferecerás diante do Senhor.</p>
		<p><span class="numero">25)</span> E o receberás das suas mãos, e o queimarás sobre o altar como oferta queimada, para cheiro suave diante do Senhor; é uma oferta queimada ao Senhor.</p>
		<p><span class="numero">26)</span> E tomarás o peito do carneiro da consagração de Arão, e o oferecerás com movimento diante do Senhor, e será a tua porção.</p>
		<p><span class="numero">27)</span> E santificarás o peito da oferta de movimento, e o ombro da oferta alçada, que é de movida, e que é alçada, do carneiro da consagração, daquilo que é para Arão, e daquilo que é para os seus filhos.</p>
		<p><span class="numero">28)</span> E será de Arão e de seus filhos por estatuto para sempre dos filhos de Israel, pois é uma oferta alçada; e será uma oferta alçada dos filhos de Israel, dos seus sacrifícios pacíficos, sua oferta alçada para o Senhor.</p>
		<p><span class="numero">29)</span> E as vestes santas de Arão serão de seus filhos depois dele, para serem ungidos nelas, e para serem santificados nelas.</p>
		<p><span class="numero">30)</span> E por sete dias as colocará no filho que for sacerdote em seu lugar, quando ele entrar no tabernáculo da congregação para ministrar no lugar santo.</p>
		<p><span class="numero">31)</span> E tomarás o carneiro da consagração, e cozinharás sua carne no lugar santo.</p>
		<p><span class="numero">32)</span> E Arão e os seus filhos comerão a carne do carneiro, e o pão que está no cesto, junto à porta do tabernáculo da congregação.</p>
		<p><span class="numero">33)</span> E eles comerão estas coisas com as quais foi feita a expiação, para consagrá-los e santificá- los. Mas um estrangeiro não comerá disso, porque são santas.</p>
		<p><span class="numero">34)</span> E se sobrar alguma coisa da carne das consagrações, ou do pão até de manhã, queimarás com fogo o que restar; não se comerá, pois é santo.</p>
		<p><span class="numero">35)</span> E assim farás a Arão, e a seus filhos, conforme todas as coisas que eu te ordenei; durante sete dias os consagrarás.</p>
		<p><span class="numero">36)</span> E oferecerás todos os dias um novilho por sacrifício pelos pecados por expiação; e purificarás o altar, quando tiveres feito expiação por ele, e o ungirás, para santificá-lo.</p>
		<p><span class="numero">37)</span> Sete dias farás expiação pelo altar, e o santificarás; e o altar será santíssimo, tudo que tocar o altar será santo.</p>
		<p><span class="numero">38)</span> Agora, isto é o que oferecerás no altar: dois cordeiros de um ano cada dia continuamente.</p>
		<p><span class="numero">39)</span> Um cordeiro oferecerás de manhã; e o outro cordeiro oferecerás à tarde.</p>
		<p><span class="numero">40)</span> E com um cordeiro, a décima parte de farinha misturada com a quarta parte de um him de óleo batido, e para a oferta de bebida a quarta parte de um him de vinho.</p>
		<p><span class="numero">41)</span> E o outro cordeiro oferecerás à tarde, e com ele farás conforme a oferta de carne da manhã, e segundo a sua oferta de bebida, para cheiro suave, uma oferta queimada ao Senhor.</p>
		<p><span class="numero">42)</span> Isto será uma oferta queimada contínua por vossas gerações à porta do tabernáculo da congregação diante do Senhor, onde vos encontrarei, para ali falar contigo.</p>
		<p><span class="numero">43)</span> E ali encontrarei os filhos de Israel, e o tabernáculo será santificado pela minha glória.</p>
		<p><span class="numero">44)</span> E santificarei o tabernáculo da congregação e o altar, também santificarei a Arão e aos seus filhos, para que ministrem a mim no ofício sacerdotal.</p>
		<p><span class="numero">45)</span> E habitarei entre os filhos de Israel e serei o seu Deus.</p>
		<p><span class="numero">46)</span> E eles saberão que eu sou o Senhor o seu Deus, que os tirou da terra do Egito, para que eu habite entre eles. Eu sou o Senhor seu Deus.&nbsp;<a href="ex-30.php"><span class="proximo">Próximo capítulo</span></a></p>
