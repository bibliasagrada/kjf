		<a href="ex.php"><h3>Êxodo 25</h3></a>
		<p><span class="numero">1)</span> E o Senhor falou a Moisés, dizendo:</p>
		<p><span class="numero">2)</span> Dize aos filhos de Israel, para que me tragam uma oferta; de todo homem que a der voluntariamente com seu coração tomareis a minha oferta.</p>
		<p><span class="numero">3)</span> E esta é a oferta que recebereis deles: ouro, e prata, e bronze,</p>
		<p><span class="numero">4)</span> e azul, e púrpura, e carmesim, e linho fino, e pelo de cabra,</p>
		<p><span class="numero">5)</span> e peles de carneiro tingidas de vermelho, e peles de texugos, e madeira de acácia,</p>
		<p><span class="numero">6)</span> óleo para a luz, especiarias para o óleo da unção e para o incenso aromático,</p>
		<p><span class="numero">7)</span> pedras de ônix, e pedras de engaste para colocar no éfode e no peitoral.</p>
		<p><span class="numero">8)</span> E me façam um santuário, para que eu habite entre eles.</p>
		<p><span class="numero">9)</span> Conforme tudo o que eu te mostrar, segundo o modelo do tabernáculo, e o modelo de todos os seus instrumentos, assim o fareis.</p>
		<p><span class="numero">10)</span> E farão uma arca de madeira de acácia; dois côvados e meio será o seu comprimento, e um côvado e meio a sua largura, e um côvado e meio a sua altura.</p>
		<p><span class="numero">11)</span> E a revestirás de ouro puro, por dentro e por fora a revestirás, e farás sobre ela uma coroa de ouro ao redor.</p>
		<p><span class="numero">12)</span> E fundirás quatro argolas de ouro para ela, e as colocarás nos seus quatro cantos; em um de seus lados ficarão duas argolas, e no outro lado duas argolas.</p>
		<p><span class="numero">13)</span> E farás varas de madeira de acácia, e as revestirás de ouro.</p>
		<p><span class="numero">14)</span> E colocarás as varas nas argolas, nos lados da arca, para que a arca seja carregada com elas.</p>
		<p><span class="numero">15)</span> As varas estarão nas argolas da arca; não serão tiradas dela.</p>
		<p><span class="numero">16)</span> E colocarás na arca o testemunho que eu te darei.</p>
		<p><span class="numero">17)</span> E farás um propiciatório de ouro puro; dois côvados e meio será o seu comprimento, e um côvado e meio a sua largura.</p>
		<p><span class="numero">18)</span> E farás dois querubins de ouro, de obra batida os farás, nas duas extremidades do propiciatório.</p>
		<p><span class="numero">19)</span> E farás um querubim em uma extremidade, e o outro querubim na outra extremidade; do propiciatório fareis os querubins nas duas extremidades.</p>
		<p><span class="numero">20)</span> E os querubins estenderão suas asas ao alto, cobrindo o propiciatório com suas asas, e suas faces olharão uma para a outra; para o propiciatório estarão voltadas as faces dos querubins.</p>
		<p><span class="numero">21)</span> E colocarás o propiciatório sobre a arca; e dentro da arca colocarás o testemunho que eu te darei.</p>
		<p><span class="numero">22)</span> E ali me encontrarei contigo, e falarei contigo de sobre o propiciatório, de entre os dois querubins que estão sobre a arca do testemunho, todas as coisas eu te darei em mandamento aos filhos de Israel.</p>
		<p><span class="numero">23)</span> Farás também uma mesa de madeira de acácia; dois côvados será o seu comprimento, e um côvado a sua largura, e um côvado e meio a sua altura.</p>
		<p><span class="numero">24)</span> E a revestirás de ouro puro, e lhe farás uma coroa de ouro ao redor.</p>
		<p><span class="numero">25)</span> E lhe farás uma borda ao redor da largura de uma mão, e farás uma coroa de ouro ao redor da borda.</p>
		<p><span class="numero">26)</span> E lhe farás quatro argolas de ouro, e colocarás as argolas nos quatro cantos que estão nos seus quatro pés.</p>
		<p><span class="numero">27)</span> Defronte das bordas estarão as argolas, como lugares para as varas, para carregar a mesa.</p>
		<p><span class="numero">28)</span> E farás as varas de madeira de acácia, e as revestirás com ouro, para que a mesa seja carregada com elas.</p>
		<p><span class="numero">29)</span> E farás os seus pratos, e suas colheres, e as suas cobertas, e as suas tigelas com que serão cobertos; os farás de ouro puro.</p>
		<p><span class="numero">30)</span> E colocarás sobre a mesa o pão da proposição diante de mim sempre.</p>
		<p><span class="numero">31)</span> E farás um candelabro de ouro puro; de trabalho batido será feito o candelabro. O seu pé, e suas hastes, os seus copos, os seus botões e suas flores serão do mesmo.</p>
		<p><span class="numero">32)</span> E seis hastes sairão de seus lados; três hastes do candelabro de um lado, e três hastes do candelabro do outro lado.</p>
		<p><span class="numero">33)</span> Três tigelas feitas como amêndoas, com um botão e uma flor em uma haste; e três tigelas feitas como amêndoas em outra haste, com um botão e uma flor; assim nas seis hastes que saem do candelabro.</p>
		<p><span class="numero">34)</span> E no candelabro haverá quatro tigelas feitas como amêndoas, com seus botões e suas flores.</p>
		<p><span class="numero">35)</span> E haverá um botão debaixo de duas hastes do mesmo, e um botão debaixo de duas hastes do mesmo, e um botão debaixo de duas hastes do mesmo, de acordo com as seis hastes que saem do candelabro.</p>
		<p><span class="numero">36)</span> Seus botões e suas hastes serão do mesmo; tudo será uma obra batida de ouro puro.</p>
		<p><span class="numero">37)</span> E lhe farás sete lâmpadas; e elas acenderão as suas lâmpadas, para que iluminem defronte dele.</p>
		<p><span class="numero">38)</span> E seus espivetadores e os seus apagadores serão de ouro puro.</p>
		<p><span class="numero">39)</span> De um talento de ouro puro ele o fará, com todos estes utensílios.</p>
		<p><span class="numero">40)</span> E observa para que os faças conforme o seu modelo, que te foi mostrado no monte.&nbsp;<a href="ex-26.php"><span class="proximo">Próximo capítulo</span></a></p>
