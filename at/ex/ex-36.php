		<a href="ex.php"><h3>Êxodo 36</h3></a>
		<p><span class="numero">1)</span> Então trabalharam Bezalel e Aoliabe, e todo homem de coração sábio, em quem o Senhor colocou sabedoria e entendimento, para saberem como fazer todo tipo de trabalho para o serviço do santuário, de acordo com tudo o que Senhor havia ordenado.</p>
		<p><span class="numero">2)</span> E Moisés chamou Bezalel e Aoliabe, e todo homem de coração sábio, em cujo coração o Senhor havia colocado sabedoria, todo aquele cujo coração o moveu a vir para o trabalho e fazê-lo,</p>
		<p><span class="numero">3)</span> e eles receberam de Moisés todas as ofertas, que os filhos de Israel haviam trazido para a obra do serviço do santuário, para fazê- la. E trouxeram a ele ainda ofertas voluntárias todas as manhãs.</p>
		<p><span class="numero">4)</span> E todos os homens sábios, que realizavam toda a obra do santuário, veio cada homem da sua obra que havia feito.</p>
		<p><span class="numero">5)</span> E falaram a Moisés, dizendo: O povo está trazendo muito mais do que necessário para o serviço da obra que o Senhor ordenou para fazer.</p>
		<p><span class="numero">6)</span> E Moisés deu ordem, e a fizeram proclamar em todo o acampamento, dizendo: Que nenhum homem nem mulher faça mais alguma obra para a oferta do santuário. Assim, o povo foi proibido de trazer mais,</p>
		<p><span class="numero">7)</span> porque o material que tinham era suficiente para fazer toda a obra, e muito mais.</p>
		<p><span class="numero">8)</span> E todo homem de coração sábio, que entre eles trabalhavam na obra, fizeram o tabernáculo de dez cortinas de linho fino torcido, e azul, e púrpura, e carmesim; com querubins de trabalho esmerado ele os fez.</p>
		<p><span class="numero">9)</span> O comprimento de uma cortina era de vinte e oito côvados, e a largura de uma cortina era de quatro côvados; as cortinas eram todas de uma medida.</p>
		<p><span class="numero">10)</span> E ligou cinco cortinas umas às outras; e as outras cinco cortinas ligou umas às outras.</p>
		<p><span class="numero">11)</span> E fez laçadas de azul na borda de uma cortina, na extremidade e na juntura; da mesma forma fez na extremidade de outra cortina, na juntura da segunda.</p>
		<p><span class="numero">12)</span> Cinquenta laçadas ele fez em uma cortina, e cinquenta laçadas ele fez na borda da cortina que estava na juntura da segunda. As laçadas prendiam uma cortina à outra.</p>
		<p><span class="numero">13)</span> E fez cinquenta colchetes de ouro, e juntou as cortinas umas às outras com os colchetes; assim se tornou um tabernáculo.</p>
		<p><span class="numero">14)</span> E fez cortinas de pelo de cabra para a tenda sobre o tabernáculo; fez onze cortinas.</p>
		<p><span class="numero">15)</span> O comprimento de uma cortina era de trinta côvados, e a largura de uma cortina era de quatro côvados. E as onze cortinas eram de uma medida.</p>
		<p><span class="numero">16)</span> E uniu cinco cortinas por si e seis cortinas por si.</p>
		<p><span class="numero">17)</span> E fez cinquenta laçadas na borda da cortina na extremidade da juntura, e cinquenta laçadas na borda da cortina que ajunta com a segunda.</p>
		<p><span class="numero">18)</span> E fez cinquenta colchetes de bronze, para juntar a tenda, para que fosse uma.</p>
		<p><span class="numero">19)</span> E fez para a tenda uma coberta de pele de carneiro tingida de vermelho, e sobre ela uma coberta de peles de texugo.</p>
		<p><span class="numero">20)</span> E fez as tábuas para o tabernáculo de madeira de acácia que ficavam em pé.</p>
		<p><span class="numero">21)</span> Dez côvados era o comprimento de uma tábua, e um côvado e meio a largura de uma tábua.</p>
		<p><span class="numero">22)</span> Uma tábua tinha dois encaixes, igualmente distantes um do outro. Assim fez para todas as tábuas do tabernáculo.</p>
		<p><span class="numero">23)</span> E fez as tábuas para o tabernáculo, vinte tábuas no lado sul para o sul.</p>
		<p><span class="numero">24)</span> E fez quarenta bases de prata debaixo das vinte tábuas; duas bases debaixo de uma tábua para os seus dois encaixes, e duas bases debaixo de outra tábua para os seus dois encaixes.</p>
		<p><span class="numero">25)</span> E para o outro lado do tabernáculo, que fica voltado para o norte, fez vinte tábuas,</p>
		<p><span class="numero">26)</span> e seus quarenta encaixes de prata; duas bases debaixo de uma tábua, e duas bases debaixo de outra tábua.</p>
		<p><span class="numero">27)</span> E para os lados do tabernáculo para o oeste fez seis tábuas.</p>
		<p><span class="numero">28)</span> E duas tábuas fez para os cantos do tabernáculo nos dois lados.</p>
		<p><span class="numero">29)</span> E foram ajuntados por baixo, e ajuntados acima da sua cabeça em uma argola. Assim fez para ambas, em ambos cantos.</p>
		<p><span class="numero">30)</span> E havia oito tábuas, e suas bases eram dezesseis bases de prata; duas bases debaixo de cada tábua.</p>
		<p><span class="numero">31)</span> E fez barras de madeira de acácia; cinco para as tábuas de um lado do tabernáculo,</p>
		<p><span class="numero">32)</span> e cinco barras para as tábuas do outro lado do tabernáculo, e cinco barras para as tábuas do lado do tabernáculo para os lados do oeste.</p>
		<p><span class="numero">33)</span> E a barra do meio das tábuas fez passar de uma extremidade à outra.</p>
		<p><span class="numero">34)</span> E revestiu as tábuas com ouro, e fez suas argolas de ouro para serem lugares para as barras. E revestiu as barras com ouro.</p>
		<p><span class="numero">35)</span> E fez um véu de azul, e púrpura, e carmesim, e linho fino torcido; com querubins o fez de trabalho esmerado.</p>
		<p><span class="numero">36)</span> E fez-lhe quatro colunas de madeira de acácia e as revestiu com ouro; seus colchetes eram de ouro, e fundiu para elas quatro bases de prata.</p>
		<p><span class="numero">37)</span> E fez uma cortina de azul para a porta do tabernáculo, e púrpura, e carmesim, e linho fino torcido, trabalho de bordador;</p>
		<p><span class="numero">38)</span> e as cinco colunas com seus colchetes; e ele revestiu suas cabeças e suas molduras com ouro. Mas suas cinco bases eram de bronze.&nbsp;<a href="ex-37.php"><span class="proximo">Próximo capítulo</span></a></p>
