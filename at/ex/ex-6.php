		<a href="ex.php"><h3>Êxodo 6</h3></a>
		<p><span class="numero">1)</span> Então disse o Senhor a Moisés: Agora verás o que eu farei a Faraó, porque com forte mão ele os deixará ir, e com forte mão ele os expulsará da sua terra.</p>
		<p><span class="numero">2)</span> E Deus falou a Moisés, e lhe disse: Eu sou o Senhor;</p>
		<p><span class="numero">3)</span> e eu apareci a Abraão, a Isaque e a Jacó, pelo nome do Deus Todo-Poderoso; mas pelo meu nome JEOVÁ, não fui conhecido a eles.</p>
		<p><span class="numero">4)</span> E também estabeleci o meu pacto com eles, para lhes dar a terra de Canaã, a terra de suas peregrinações, em que eram estrangeiros.</p>
		<p><span class="numero">5)</span> E eu também tenho ouvido o gemido dos filhos de Israel, que os egípcios mantêm em servidão; e lembrei-me do meu pacto.</p>
		<p><span class="numero">6)</span> Por isso, dize aos filhos de Israel: Eu sou o Senhor, e vos tirarei de debaixo das cargas dos egípcios, e vos libertarei da sua servidão, e vos redimirei com braço estendido, e com grandes juízos,</p>
		<p><span class="numero">7)</span> e eu vos tomarei por meu povo, e serei vosso Deus; e vós sabereis que eu sou o Senhor, vosso Deus, que vos tira de debaixo das cargas dos egípcios.</p>
		<p><span class="numero">8)</span> E eu vos trarei para a terra, que jurei dar a Abraão, a Isaque e a Jacó. Eu vo-la darei por herança. Eu sou o Senhor.</p>
		<p><span class="numero">9)</span> E assim falou Moisés aos filhos de Israel, mas eles não lhe escutaram, por causa da angústia de espírito, e por causa da cruel servidão.</p>
		<p><span class="numero">10)</span> E o Senhor falou a Moisés, dizendo:</p>
		<p><span class="numero">11)</span> Entra, dize a Faraó, rei do Egito, que ele deixe os filhos de Israel sair da sua terra.</p>
		<p><span class="numero">12)</span> E falou Moisés diante do Senhor, dizendo: Eis que os filhos de Israel não me ouviram. Como, então, Faraó me ouvirá, que sou de lábios incircuncisos?</p>
		<p><span class="numero">13)</span> E o Senhor falou a Moisés e a Arão, e lhes deu ordem para os filhos de Israel, e para Faraó, rei do Egito, para que tirassem os filhos de Israel da terra do Egito.</p>
		<p><span class="numero">14)</span> Estes são os cabeças das casas de seus pais: Os filhos de Rúben, o primogênito de Israel: Enoque e Palu, Hezrom e Carmi; estas são as famílias de Rúben.</p>
		<p><span class="numero">15)</span> E os filhos de Simeão: Jemuel, e Jamim, e Oade, e Jaquim, e Zoar, e Saul, filho de uma mulher cananeia; estas são as famílias de Simeão.</p>
		<p><span class="numero">16)</span> E estes são os nomes dos filhos de Levi, segundo as suas gerações: Gérson, e Coate, e Merari; e os anos da vida de Levi foram cento e trinta e sete anos.</p>
		<p><span class="numero">17)</span> Os filhos de Gérson: Libni, e Simei, segundo as suas famílias.</p>
		<p><span class="numero">18)</span> E os filhos de Coate: Anrão, e Isar, e Hebrom, e Uziel; e os anos da vida de Coate foram cento e trinta e três anos.</p>
		<p><span class="numero">19)</span> E os filhos de Merari: Mali e Musi; estas são as famílias de Levi, segundo as suas gerações.</p>
		<p><span class="numero">20)</span> E Anrão tomou por mulher a Joquebede, sua tia, e ela gerou-lhe Arão e Moisés; e os anos da vida de Anrão foram cento e trinta e sete anos.</p>
		<p><span class="numero">21)</span> E os filhos de Isar: Corá, e Nefegue, e Zicri.</p>
		<p><span class="numero">22)</span> E os filhos de Uziel: Misael, e Elzafã, e Sitri.</p>
		<p><span class="numero">23)</span> E Arão tomou para si Eliseba, filha de Aminadabe, irmã de Naassom; e ela gerou- lhe Nadabe, e Abiú, e Eleazar, e Itamar.</p>
		<p><span class="numero">24)</span> E os filhos de Corá: Assir, e Elcana, e Abiasafe; estas são as famílias dos coraítas.</p>
		<p><span class="numero">25)</span> E Eleazar, filho de Arão, tomou por mulher uma das filhas de Putiel, e ela gerou-lhe a Fineias; estes são os cabeças dos pais dos levitas, segundo as suas famílias.</p>
		<p><span class="numero">26)</span> Estes são Arão e Moisés, aos quais o Senhor disse: Tirai os filhos de Israel da terra do Egito segundo os seus exércitos.</p>
		<p><span class="numero">27)</span> Estes são aqueles que falaram a Faraó, rei do Egito, para que tirasse os filhos de Israel do Egito. Estes são Arão e Moisés.</p>
		<p><span class="numero">28)</span> E aconteceu no dia em que o Senhor falou a Moisés na terra do Egito,</p>
		<p><span class="numero">29)</span> falou o Senhor a Moisés, dizendo: Eu sou o Senhor. Dize a Faraó, rei do Egito, tudo que eu te disser.</p>
