		<a href="ex.php"><h3>Êxodo 5</h3></a>
		<p><span class="numero">1)</span> E depois foram Moisés e Arão e disseram a Faraó: Assim diz o Senhor Deus de Israel: Deixa meu povo ir, para que me celebre uma festa no deserto.</p>
		<p><span class="numero">2)</span> E disse Faraó: Quem é o Senhor, para que eu obedeça à sua voz para deixar Israel ir? Não conheço o Senhor, tampouco deixarei Israel ir.</p>
		<p><span class="numero">3)</span> E eles disseram: O Deus dos hebreus nos encontrou. Deixa-nos ir, rogamos-te, três dias de jornada para o deserto; e oferecer sacrifícios ao Senhor nosso Deus, para que ele não caia sobre nós com peste, ou com a espada.</p>
		<p><span class="numero">4)</span> E o rei do Egito lhes disse: Por que vós, Moisés e Arão, impedis o povo de trabalhar? Ide às vossas cargas.</p>
		<p><span class="numero">5)</span> E Faraó disse: Eis que o povo da terra agora é muito, e vós os fazeis descansar de suas cargas.</p>
		<p><span class="numero">6)</span> E no mesmo dia Faraó ordenou aos capatazes do povo, e aos seus oficiais, dizendo:</p>
		<p><span class="numero">7)</span> Já não dareis ao povo a palha para fazer tijolos, como até agora. Que eles mesmos vão e ajuntem palha para si.</p>
		<p><span class="numero">8)</span> E a conta dos tijolos que faziam até agora, esta poreis sobre eles. Não diminuireis coisa alguma. Pois eles estão ociosos, por isso clamam, dizendo: Deixa-nos ir e sacrificar ao nosso Deus.</p>
		<p><span class="numero">9)</span> Que mais trabalho seja colocado sobre os homens, para que nele se ocupem, e não considerem palavras vãs.</p>
		<p><span class="numero">10)</span> E saíram os capatazes do povo, e os seus oficiais, e falaram ao povo, dizendo: Assim diz Faraó: Não vos darei palha.</p>
		<p><span class="numero">11)</span> Ide vós mesmos, ajuntai a palha onde a achardes; mas nada de vosso trabalho será diminuído.</p>
		<p><span class="numero">12)</span> Assim o povo se espalhou por toda a terra do Egito para ajuntar restolho em vez de palha.</p>
		<p><span class="numero">13)</span> E os capatazes os apressavam, dizendo: Cumpri vossos trabalhos, vossas tarefas diárias, como quando havia palha.</p>
		<p><span class="numero">14)</span> E foram açoitados os oficiais dos filhos de Israel, que os capatazes de Faraó haviam posto sobre eles, e reclamavam: Por que não cumpristes vossa tarefa de fazer tijolos ontem e hoje, como até agora?</p>
		<p><span class="numero">15)</span> Então os oficiais dos filhos de Israel foram e clamaram a Faraó, dizendo: Por que tratas assim os teus servos?</p>
		<p><span class="numero">16)</span> Não se dá palha a teus servos, e eles nos dizem: Fazei tijolos. E eis que os teus servos são açoitados; mas a culpa está em teu próprio povo.</p>
		<p><span class="numero">17)</span> Mas ele disse: Vós estais ociosos; vós estais ociosos, por isso dizeis: Deixa-nos ir e fazer sacrifício para o Senhor.</p>
		<p><span class="numero">18)</span> Portanto, agora ide e trabalhai, porque não se vos dará palha, porém cumprireis a conta dos tijolos.</p>
		<p><span class="numero">19)</span> E os oficiais dos filhos de Israel notaram que eles estavam em uma má situação, depois que foi dito: Não diminuireis coisa alguma dos tijolos da vossa tarefa diária.</p>
		<p><span class="numero">20)</span> E encontraram Moisés e Arão que estavam no caminho, quando vinham de Faraó,</p>
		<p><span class="numero">21)</span> e disseram a eles: O Senhor olhe para vós e julgue, porquanto fizestes que o nosso cheiro fosse abominado aos olhos de Faraó, e aos olhos dos seus servos, colocando-lhes nas mãos uma espada para nos matar.</p>
		<p><span class="numero">22)</span> E Moisés voltou ao Senhor, e disse: Senhor, por que trataste tão mal a este povo? Por que é que me enviaste?</p>
		<p><span class="numero">23)</span> Porque desde que fui a Faraó para falar em teu nome, ele fez mal a este povo. E tu também não livraste este povo.&nbsp;<a href="ex-6.php"><span class="proximo">Próximo capítulo</span></a></p>
