		<a href="gn.php"><h3>Gênesis 49</h3></a>
		<p><span class="numero">1)</span> E Jacó chamou seus filhos e disse: Reuni-vos, para que eu possa vos dizer o que vos acontecerá nos últimos dias.</p>
		<p><span class="numero">2)</span> Reuni-vos, e ouvi, filhos de Jacó, ouvi com atenção a Israel, vosso pai.</p>
		<p><span class="numero">3)</span> Rúben, tu és o meu primogênito, minha força, e o princípio do meu vigor, a excelência da dignidade, e a excelência do poder.</p>
		<p><span class="numero">4)</span> Instável como a água, não serás superior, porquanto subiste à cama de teu pai, e então a contaminaste. Ele subiu à minha cama.</p>
		<p><span class="numero">5)</span> Simeão e Levi são irmãos; instrumentos de crueldade são em suas habitações.</p>
		<p><span class="numero">6)</span> Oh! Minha alma, não entres no conselho secreto deles; minha honra não se una com a sua assembleia. Porque na sua ira mataram um homem, e na sua fúria derrubaram um muro.</p>
		<p><span class="numero">7)</span> Maldito seja seu furor, pois foi violento; e a sua ira, pois foi cruel. Eu os dividirei em Jacó, e os espalharei em Israel.</p>
		<p><span class="numero">8)</span> Judá, tu és aquele que teus irmãos louvarão. Tua mão estará no pescoço dos teus inimigos; os filhos de teu pai se curvarão diante de ti.</p>
		<p><span class="numero">9)</span> Judá é um filhote de um leão; da presa tu subiste, meu filho. Curva-se e deita-se como um leão, e como um leão velho. Quem o despertará?</p>
		<p><span class="numero">10)</span> O cetro não se afastará de Judá, nem o legislador dentre seus pés, até que venha Siló. E a ele se congregarão os povos.</p>
		<p><span class="numero">11)</span> Amarra seu jumentinho à videira, e o filhote de sua jumenta à videira escolhida. Ele lavou suas vestes no vinho, e sua capa no sangue de uvas.</p>
		<p><span class="numero">12)</span> Seus olhos serão vermelhos de vinho, e os seus dentes brancos de leite.</p>
		<p><span class="numero">13)</span> Zebulom habitará no porto do mar, e ele será como um porto para os navios; e sua fronteira será em Sidom.</p>
		<p><span class="numero">14)</span> Issacar é um jumento forte, deitado entre dois fardos.</p>
		<p><span class="numero">15)</span> E ele viu que o descanso era bom, e que a terra era prazerosa. E curvou seu ombro para carregar, e se tornou um servo de tributo.</p>
		<p><span class="numero">16)</span> Dã julgará seu povo, como uma das tribos de Israel.</p>
		<p><span class="numero">17)</span> Dã será uma serpente junto ao caminho, uma víbora junto à vereda, que morde os calcanhares do cavalo, de modo que seu cavaleiro cairá para trás.</p>
		<p><span class="numero">18)</span> Eu tenho esperado pela tua salvação, ó Senhor.</p>
		<p><span class="numero">19)</span> Gade, uma tropa o suplantará, mas no final ele prevalecerá.</p>
		<p><span class="numero">20)</span> De Aser seu pão será gordura, e ele produzirá delícias reais.</p>
		<p><span class="numero">21)</span> Naftali é uma cerva solta; ele dá palavras bondosas.</p>
		<p><span class="numero">22)</span> José é um ramo frutífero, um ramo frutífero junto à fonte, cujos ramos correm sobre o muro.</p>
		<p><span class="numero">23)</span> Os arqueiros o amarguraram, e atiraram nele, e o odiaram,</p>
		<p><span class="numero">24)</span> mas o seu arco habitou na força, e os braços das suas mãos foram fortalecidos pelas mãos do Deus poderoso de Jacó; (de lá é o pastor, a rocha de Israel),</p>
		<p><span class="numero">25)</span> pelo Deus de teu pai, que te ajudará, e pelo Todo-Poderoso, que te abençoará com bênçãos do céu acima, bênçãos da profundeza que está abaixo, bênçãos dos peitos e do útero.</p>
		<p><span class="numero">26)</span> As bênçãos de teu pai prevalecerão sobre as bênçãos dos meus progenitores, até os últimos limites das colinas eternas. Elas estarão sobre a cabeça de José, e na coroa da cabeça daquele que esteve separado de seus irmãos.</p>
		<p><span class="numero">27)</span> Benjamim saqueará como um lobo; pela manhã ele devorará a presa e à noite dividirá o despojo.</p>
		<p><span class="numero">28)</span> Todas estas são as doze tribos de Israel, e isto foi o que seu pai lhes falou, e os abençoou, cada um de acordo com sua bênção ele os abençoou.</p>
		<p><span class="numero">29)</span> E ele lhes ordenou, e lhes disse: Eu serei reunido ao meu povo; sepultai-me com meus pais na caverna que está no campo de Efrom, o heteu,</p>
		<p><span class="numero">30)</span> na caverna que está no campo de Macpela, que está diante de Manre, na terra de Canaã, que Abraão comprou com o campo de Efrom, o heteu, como uma possessão para um lugar de sepultamento.</p>
		<p><span class="numero">31)</span> Ali eles sepultaram Abraão e Sara, sua mulher; ali sepultaram Isaque e Rebeca, sua mulher; e ali eu sepultei Lia.</p>
		<p><span class="numero">32)</span> A compra do campo e da caverna que está nele foi dos filhos de Hete.</p>
		<p><span class="numero">33)</span> E quando Jacó terminou de dar ordens a seus filhos, ele recolheu seus pés dentro da cama, e rendeu-se ao espírito, e foi reunido ao seu povo.&nbsp;<a href="gn-50.php"><span class="proximo">Próximo capítulo</span></a></p>
