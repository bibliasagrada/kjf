		<a href="gn.php"><h3>Gênesis 30</h3></a>
		<p><span class="numero">1)</span> E vendo Raquel que não gerava filhos a Jacó, Raquel teve inveja de sua irmã, e disse a Jacó: Dá-me filhos, ou eu morrerei.</p>
		<p><span class="numero">2)</span> E se acendeu a ira de Jacó contra Raquel, e ele disse: Estou eu no lugar de Deus, que de ti reteve o fruto do útero?</p>
		<p><span class="numero">3)</span> E ela disse: Eis aí minha serva Bila; entra nela, e ela gerará sobre os meus joelhos, para que eu também possa ter filhos por meio dela.</p>
		<p><span class="numero">4)</span> E ela deu sua serva Bila a ele por mulher; e Jacó entrou nela.
		<p><span class="numero">5)</span> E Bila concebeu, e gerou um filho a Jacó.</p>
		<p><span class="numero">6)</span> E Raquel disse: Deus me julgou, e também ouviu a minha voz, e me deu um filho; por isso ela chamou o seu nome Dã.</p>
		<p><span class="numero">7)</span> E Bila, serva de Raquel, concebeu novamente, e gerou um segundo filho a Jacó.</p>
		<p><span class="numero">8)</span> E Raquel disse: Com grandes lutas eu tenho lutado com minha irmã, e eu prevaleci; e ela chamou o seu nome Naftali.</p>
		<p><span class="numero">9)</span> Vendo Lia que ela tinha parado de gerar, ela tomou sua serva Zilpa e a deu a Jacó por mulher.</p>
		<p><span class="numero">10)</span> E Zilpa, serva de Lia, gerou um filho a Jacó.</p>
		<p><span class="numero">11)</span> E Lia disse: Vem uma tropa; e ela chamou o seu nome Gade.</p>
		<p><span class="numero">12)</span> E Zilpa, serva de Lia, gerou a Jacó um segundo filho.</p>
		<p><span class="numero">13)</span> E Lia disse: Eu sou feliz, pois as filhas me chamarão abençoada; e ela chamou seu nome Aser.</p>
		<p><span class="numero">14)</span> E Rúben foi nos dias da colheita de trigo, e encontrou mandrágoras no campo, e as trouxe à sua mãe Lia. Então, Raquel disse a Lia: Dá-me, rogo-te, das mandrágoras de teu filho.</p>
		<p><span class="numero">15)</span> E ela lhe disse: Seria pequena coisa que tomaste o meu marido? Agora tomarias também as mandrágoras de meu filho? E Raquel disse: Ele poderá deitar-se contigo esta noite pelas mandrágoras de teu filho.</p>
		<p><span class="numero">16)</span> E Jacó veio do campo à tarde, e Lia foi ao seu encontro e disse: Tu deves entrar a mim; porque certamente eu te aluguei pelas mandrágoras de meu filho. E ele se deitou com ela naquela noite.</p>
		<p><span class="numero">17)</span> E Deus ouviu a Lia, e ela concebeu e gerou a Jacó o quinto filho.</p>
		<p><span class="numero">18)</span> E Lia disse: Deus me deu meu pagamento, porque eu dei a minha serva a meu marido; e ela chamou o seu nome Issacar.</p>
		<p><span class="numero">19)</span> E Lia concebeu novamente, e gerou a Jacó o sexto filho.</p>
		<p><span class="numero">20)</span> E Lia disse: Deus me dotou com boa dádiva; agora o meu marido habitará comigo, porque lhe gerei seis filhos; e ela chamou o seu nome Zebulom.</p>
		<p><span class="numero">21)</span> E depois ela gerou uma filha, e chamou o seu nome Diná.</p>
		<p><span class="numero">22)</span> E Deus se lembrou de Raquel, e Deus a ouviu, e abriu o seu útero.</p>
		<p><span class="numero">23)</span> E ela concebeu, e gerou um filho, e disse: Deus removeu a minha vergonha.</p>
		<p><span class="numero">24)</span> E ela chamou o seu nome José, e disse: O Senhor me acrescentará outro filho.</p>
		<p><span class="numero">25)</span> E aconteceu que, quando Raquel gerou José, Jacó disse a Labão: Envia-me, para que eu possa ir ao meu próprio lugar e à minha terra.</p>
		<p><span class="numero">26)</span> Dá-me minhas mulheres e meus filhos, pelos quais eu te servi, e deixa-me ir, pois tu sabes o serviço que eu tenho feito a ti.</p>
		<p><span class="numero">27)</span> E Labão lhe disse: Peço-te, se encontrei favor aos teus olhos, fica; pois eu tenho aprendido por experiência que o Senhor me abençoou por tua causa.</p>
		<p><span class="numero">28)</span> E ele disse: Determina o teu salário, e eu o darei.</p>
		<p><span class="numero">29)</span> E ele lhe disse: Tu sabes como eu te servi, e como teu gado estava comigo.</p>
		<p><span class="numero">30)</span> Porque era pouco o que tinhas antes da minha vinda, e agora cresceu para uma multidão; e o Senhor te abençoou desde a minha vinda. E agora, quando deverei prover também para a minha casa?</p>
		<p><span class="numero">31)</span> E ele disse: O que eu te darei? E Jacó disse: Tu não me darás nada. Se fizeres esta coisa por mim, eu alimentarei e guardarei o teu rebanho novamente.</p>
		<p><span class="numero">32)</span> Passarei por todo o teu rebanho hoje, removendo dele todo o gado salpicado e malhado, e todos os marrons entre as ovelhas, e o salpicado e malhado entre as cabras, e de tais será o meu salário.</p>
		<p><span class="numero">33)</span> Assim, a minha justiça responderá por mim no tempo vindouro, quando vieres ver o meu salário diante da tua face; todo o que não for salpicado ou malhado entre as cabras, e marrom entre as ovelhas, isto será contado como furto comigo.</p>
		<p><span class="numero">34)</span> E Labão disse: Eis que possa ser conforme a tua palavra.</p>
		<p><span class="numero">35)</span> E ele separou naquele dia os bodes que eram listrados e malhados, e todas as cabras que eram salpicadas e malhadas, e tudo que tinha algum branco, e tudo o que era marrom entre as ovelhas, e os deu nas mãos de seus filhos.</p>
		<p><span class="numero">36)</span> E ele estabeleceu três dias de jornada entre si e Jacó, e Jacó alimentava o restante dos rebanhos de Labão.</p>
		<p><span class="numero">37)</span> E Jacó tomou para si varas verdes de álamo, e de aveleira, e de castanheiro, e descascou nelas riscas brancas, e fez aparecer o branco que estava nas varas.</p>
		<p><span class="numero">38)</span> E colocou as varas que havia descascado diante dos rebanhos nos cochos e nos bebedouros aonde os rebanhos vinham para beber, para que concebessem quando viessem para beber.</p>
		<p><span class="numero">39)</span> E os rebanhos concebiam diante das varas, e davam crias listradas, salpicadas e malhadas.</p>
		<p><span class="numero">40)</span> E Jacó separou os cordeiros, e colocou as faces do rebanho para os listrados, e todo marrom no rebanho de Labão. E ele separou seus próprios rebanhos, e não os colocou entre o gado de Labão.</p>
		<p><span class="numero">41)</span> E aconteceu que, quando o gado forte concebia, Jacó colocava as varas diante dos olhos do gado nos bebedouros, para que eles pudessem conceber entre as varas.</p>
		<p><span class="numero">42)</span> Mas quando o gado era fraco, ele não os colocava. Assim os fracos eram de Labão, e os fortes de Jacó.</p>
