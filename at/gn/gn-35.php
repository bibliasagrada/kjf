		<p><span class="numero">1)</span> E Deus disse a Jacó: Levanta-te, sobe a Betel, e habita ali. E faze ali um altar para Deus, que te apareceu quando tu fugias da face de Esaú, teu irmão.</p>
		<p><span class="numero">2)</span> Então Jacó disse a sua família, e a todos que estavam com ele: Lançai fora os deuses estranhos que estão entre vós, e sede puros, e mudai as vossas vestes.</p>
		<p><span class="numero">3)</span> Levantemo-nos e vamos a Betel, e lá eu farei um altar para Deus, que me respondeu no dia da minha angústia, e esteve comigo no caminho em que eu andei.</p>
		<p><span class="numero">4)</span> E eles deram a Jacó todos os deuses estranhos que estavam em suas mãos, e todos os seus brincos que estavam em suas orelhas; e Jacó os escondeu debaixo do carvalho que estava junto a Siquém.</p>
		<p><span class="numero">5)</span> E eles viajaram, e o terror de Deus estava sobre as cidades que estavam ao redor deles, e eles não perseguiram os filhos de Jacó.</p>
		<p><span class="numero">6)</span> Então Jacó veio a Luz, que está na terra de Canaã, isto é, Betel, ele e todo o povo que estava com ele.</p>
		<p><span class="numero">7)</span> E ele edificou ali um altar, e chamou o lugar El-Betel, porque ali Deus lhe apareceu, quando ele fugia da face de seu irmão.</p>
		<p><span class="numero">8)</span> Mas morreu Débora, ama de Rebeca, e ela foi sepultada abaixo de Betel debaixo do carvalho; e o nome do lugar foi chamado Alom-Bacute.</p>
		<p><span class="numero">9)</span> E Deus apareceu a Jacó novamente, quando ele veio de Padã-Arã, e o abençoou.</p>
		<p><span class="numero">10)</span> E Deus lhe disse: Teu nome é Jacó; teu nome não será mais chamado Jacó, mas Israel será o teu nome; e ele chamou seu nome Israel.</p>
		<p><span class="numero">11)</span> E Deus lhe disse: Eu sou o Deus Todo- Poderoso; seja frutífero e multiplique; uma nação e uma multidão de nações virão de ti, e reis virão dos teus lombos. </p>
		<p><span class="numero">12)</span> E a terra que eu dei a Abraão e a Isaque, a ti eu a darei, e à tua semente depois de ti eu darei a terra.</p>
		<p><span class="numero">13)</span> E Deus subiu do lugar em que falou com ele.</p>
		<p><span class="numero">14)</span> E Jacó levantou um pilar no lugar em que falou com ele, um pilar de pedra; e ele derramou uma oferta de bebida nele, e derramou óleo nele.</p>
		<p><span class="numero">15)</span> E Jacó chamou Betel o nome do lugar onde Deus falou com ele.</p>
		<p><span class="numero">16)</span> E eles partiram de Betel, e era um curto caminho para chegar a Efrata; e Raquel entrou em trabalho de parto; e ela teve dificuldades no parto.</p>
		<p><span class="numero">17)</span> E aconteceu que, quando ela estava com dificuldades no parto, a parteira lhe disse: Não temas, tu terás este filho também.</p>
		<p><span class="numero">18)</span> E aconteceu que, enquanto sua alma partia (porque ela morreu), ela chamou seu nome Benoni, mas seu pai o chamou Benjamim.</p>
		<p><span class="numero">19)</span> E Raquel morreu, e foi sepultada no caminho de Efrata, que é Belém.</p>
		<p><span class="numero">20)</span> E Jacó colocou um pilar sobre o seu túmulo; este é o pilar do túmulo de Raquel até este dia.</p>
		<p><span class="numero">21)</span> E Israel viajou, e estendeu sua tenda além da torre de Éder.</p>
		<p><span class="numero">22)</span> E aconteceu que, quando Israel habitou naquela terra, Rúben foi e se deitou com Bila, concubina de seu pai, e Israel o escutou. Ora, os filhos de Jacó eram doze:</p>
		<p><span class="numero">23)</span> os filhos de Lia: Rúben, primogênito de Jacó, e Simeão, e Levi, e Judá, e Issacar, e Zebulom;</p>
		<p><span class="numero">24)</span> os filhos de Raquel: José e Benjamim;</p>
		<p><span class="numero">25)</span> e os filhos de Bila, serva de Raquel: Dã e Naftali;</p>
		<p><span class="numero">26)</span> e os filhos de Zilpa, serva de Lia: Gade e Aser. Estes são os filhos de Jacó, que lhe nasceram em Padã-Arã.</p>
		<p><span class="numero">27)</span> E Jacó veio a Isaque, seu pai, em Manre, à cidade de Arba, que é Hebrom, onde Abraão e Isaque peregrinaram.</p>
		<p><span class="numero">28)</span> E os dias de Isaque foram cento e oitenta anos.</p>
		<p><span class="numero">29)</span> E Isaque entregou o espírito, e morreu, e foi reunido ao seu povo, sendo idoso e pleno de dias; e seus filhos, Esaú e Jacó o sepultaram.&nbsp;<a href="gn-36.php"><span class="proximo">Próximo capítulo</span></a></p>
