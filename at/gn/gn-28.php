		<p><span class="numero">1)</span> E Isaque chamou Jacó e o abençoou, e ordenou-lhe, dizendo: Não tomarás mulher das filhas de Canaã.</p>
		<p><span class="numero">2)</span> Levanta-te, vai a Padã-Arã, à casa de Betuel, pai de tua mãe, e toma mulher para ti, dentre as filhas de Labão, irmão de tua mãe.</p>
		<p><span class="numero">3)</span> E o Deus Todo-Poderoso te abençoe, e te faça frutífero, e te multiplique, para que tu possas ser uma multidão de povos,</p>
		<p><span class="numero">4)</span> e te dê a bênção de Abraão, a ti, e a tua semente contigo, para que herdes a terra em que és estrangeiro, que Deus deu a Abraão.</p>
		<p><span class="numero">5)</span> E Isaque enviou Jacó, e ele foi a Padã-Arã até Labão, filho de Betuel, o sírio, o irmão de Rebeca, mãe de Jacó e Esaú.</p>
		<p><span class="numero">6)</span> Quando Esaú viu que Isaque havia abençoado Jacó, e o enviara a Padã-Arã, para tomar uma mulher de lá, e que quando ele o abençoou lhe deu uma ordem, dizendo: Tu não tomarás mulher dentre as filhas de Canaã,</p>
		<p><span class="numero">7)</span> e que Jacó obedeceu ao seu pai e a sua mãe, e foi para Padã-Arã,</p>
		<p><span class="numero">8)</span> Esaú vendo que as filhas de Canaã não agradavam a Isaque, seu pai,</p>
		<p><span class="numero">9)</span> então, foi Esaú a Ismael, e tomou para ser sua mulher, além das mulheres que ele tinha, a Maalate filha de Ismael, filho de Abraão, irmã de Nebaiote.</p>
		<p><span class="numero">10)</span> E Jacó saiu de Berseba, e foi em direção a Harã.</p>
		<p><span class="numero">11)</span> E ele chegou a um certo lugar, e ali ficou a noite toda, porque o sol estava posto, e ele tomou umas pedras daquele lugar e a colocou como seu travesseiro, e se deitou naquele lugar para dormir.</p>
		<p><span class="numero">12)</span> E ele sonhou, e eis que uma escada estava posta sobre a terra, e o seu topo alcançava o céu, e eis que os anjos de Deus subiam e desciam por ela.</p>
		<p><span class="numero">13)</span> E eis que o Senhor estava em pé acima dela, e disse: Eu sou o Senhor Deus de Abraão, teu pai, e o Deus de Isaque. A terra em que estás deitado, darei a ti e à tua semente.</p>
		<p><span class="numero">14)</span> E tua semente será como o pó da terra, e tu serás espalhado para o ocidente, e para o oriente, e para o norte, e para o sul. E em ti e em tua semente todas as famílias da terra serão abençoadas.</p>
		<p><span class="numero">15)</span> E eis que eu estou contigo, e te guardarei em todos os lugares aos quais tu fores, e te trarei novamente a esta terra; pois eu não te deixarei, até que eu tenha feito aquilo que eu tenho falado.</p>
		<p><span class="numero">16)</span> E Jacó despertou de seu sono, e disse: Certamente o Senhor está neste lugar, e eu não o sabia.</p>
		<p><span class="numero">17)</span> E ele estava temeroso, e disse: Quão terrível é este lugar! Este não é outro senão a casa de Deus, e este é o portão do céu.</p>
		<p><span class="numero">18)</span> E Jacó levantou-se cedo de manhã, e tomou a pedra que tinha posto como seu travesseiro, e a colocou como um pilar, e derramou óleo no topo dela.</p>
		<p><span class="numero">19)</span> E ele chamou o nome daquele lugar Betel; mas no começo o nome daquela cidade era chamada de Luz.</p>
		<p><span class="numero">20)</span> E Jacó jurou um juramento, dizendo: Se Deus for comigo, e me guardar neste caminho em que eu vou, e me der pão para comer, e me vestes para vestir, </p>
		<p><span class="numero">21)</span> de modo que eu torne novamente à casa de meu pai em paz, então que o Senhor seja o meu Deus,</p>
		<p><span class="numero">22)</span> e esta pedra, que tenho posto como um pilar, será a casa de Deus, e de tudo que tu me deres eu certamente te darei o dízimo.&nbsp;<a href="gn-29.php"><span class="proximo">Próximo capítulo</span></a></p>
