		<p><span class="numero">1)</span> E o Senhor disse a Noé: Vem tu e toda a tua casa para dentro da arca; pois a ti eu vi como justo diante de mim nesta geração.</p>
		<p><span class="numero">2)</span> De todo animal limpo tomarás para ti de sete em sete, o macho e sua fêmea, e dos animais que não são limpos de dois em dois, o macho e sua fêmea.</p>
		<p><span class="numero">3)</span> Das aves do céu também de sete em sete, o macho e sua fêmea, para manter viva a semente sobre a face de toda a terra.</p>
		<p><span class="numero">4)</span> Pois em mais sete dias, eu farei chover sobre a terra quarenta dias e quarenta noites, e toda substância viva que eu fiz, destruirei da face da terra.</p>
		<p><span class="numero">5)</span> E Noé fez de acordo com tudo o que o Senhor lhe ordenou.</p>
		<p><span class="numero">6)</span> E Noé tinha seiscentos anos de idade quando o dilúvio de águas veio sobre a terra.</p>
		<p><span class="numero">7)</span> E entrou na arca Noé, e seus filhos, e sua mulher, e as mulheres de seus filhos com ele, por causa das águas do dilúvio.</p>
		<p><span class="numero">8)</span> Dos animais limpos, e dos animais que não são limpos, e das aves, e de toda coisa que rasteja sobre a terra,</p>
		<p><span class="numero">9)</span> entraram de dois em dois até Noé na arca, o macho e a fêmea, conforme Deus ordenara a Noé.</p>
		<p><span class="numero">10)</span> E aconteceu que, depois dos sete dias, as águas do dilúvio estavam sobre a terra.</p>
		<p><span class="numero">11)</span> No ano seiscentos da vida de Noé, no segundo mês, no décimo sétimo dia do mês, no mesmo dia todas as fontes do grande abismo foram rompidas, e as janelas do céu foram abertas.</p>
		<p><span class="numero">12)</span> E a chuva esteve sobre a terra quarenta dias e quarenta noites.</p>
		<p><span class="numero">13)</span> Nesse mesmo dia entraram na arca Noé, e Sem, e Cam, e Jafé, os filhos de Noé, e a mulher de Noé, e com eles as três mulheres de seus filhos.</p>
		<p><span class="numero">14)</span> Eles, e todo animal segundo a sua espécie, e todo o gado segundo a sua espécie, e toda coisa que rasteja sobre a terra segundo a sua espécie, e cada pássaro de toda espécie, dois.</p>
		<p><span class="numero">15)</span> E entraram para Noé na arca, de dois em dois de toda a carne em que há o sopro de vida.</p>
		<p><span class="numero">16)</span> E aqueles que entraram, entraram macho e fêmea de toda a carne, conforme Deus lhe ordenara, e o Senhor fechou por fora.</p>
		<p><span class="numero">17)</span> E o dilúvio esteve quarenta dias sobre a terra; e as águas aumentaram, e levantaram a arca, e ela foi elevada sobre a terra.</p>
		<p><span class="numero">18)</span> E as águas prevaleceram, e foram aumentadas grandemente sobre a terra; e a arca andava sobre a face das águas.</p>
		<p><span class="numero">19)</span> E as águas prevaleceram excessivamente sobre a terra; e todos os montes altos, que estavam debaixo de todo o céu, foram cobertos.</p>
		<p><span class="numero">20)</span> Quinze côvados acima as águas prevaleceram; e os montes foram cobertos.</p>
		<p><span class="numero">21)</span> E morreu toda a carne que se movia sobre a terra, tanto as aves, quanto o gado e os animais, e toda coisa rastejante que rasteja sobre a terra, e todo homem.</p>
		<p><span class="numero">22)</span> Todos aqueles em cujas narinas estava o sopro de vida, e tudo que estava na terra seca morreu.</p>
		<p><span class="numero">23)</span> E foi destruída toda substância viva que estava sobre a face da terra, tanto o homem, quanto o gado, e as coisas rastejantes e as aves do céu; e eles foram destruídos da terra; e somente Noé permaneceu vivo, e aqueles que estavam com ele na arca.</p>
