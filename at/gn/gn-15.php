		<p><span class="numero">10)</span> E tomou para ele todos estes, e os dividiu ao meio, e colocou cada parte na frente da outra, mas as aves ele não dividiu.</p>
		<p><span class="numero">11)</span> E quando as aves desciam sobre as carcaças, Abrão as enxotava.</p>
		<p><span class="numero">12)</span> E quando o sol estava se pondo, um profundo sono caiu sobre Abrão, e eis que um horror de grande escuridão caiu sobre ele.</p>
		<p><span class="numero">13)</span> E ele disse a Abrão: Saibas com certeza que tua semente será estrangeira na terra que não é sua, e os servirão, e eles os afligirão por quatrocentos anos.</p>
		<p><span class="numero">14)</span> E também a essa nação, a quem eles servirão, eu julgarei, e depois eles sairão com grandes posses.</p>
		<p><span class="numero">15)</span> E tu irás para os teus pais em paz, tu serás sepultado em boa velhice.</p>
		<p><span class="numero">16)</span> Mas na quarta geração, eles virão para cá novamente, pois a iniquidade dos amorreus ainda não está completa.</p>
		<p><span class="numero">17)</span> E aconteceu que, quando o sol se pôs, e ficou escuro, eis que um forno de fumaça e uma lâmpada acesa passaram entre aqueles pedaços.</p>
		<p><span class="numero">18)</span> No mesmo dia, o Senhor fez um pacto com Abrão, dizendo: À tua semente eu dei esta terra, do rio do Egito até o grande rio, o rio Eufrates:</p>
