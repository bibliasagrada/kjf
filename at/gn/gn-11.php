		espalhados sobre a face de toda a terra.</p>
		<p><span class="numero">5)</span> E o Senhor desceu para ver a cidade e a torre que os filhos dos homens edificavam.</p>
		<p><span class="numero">6)</span> E o Senhor disse: Eis que o povo é um, e todos eles têm uma língua. E isto eles começam a fazer, e agora nada lhes será restrito, do que eles imaginam fazer.</p>
		<p><span class="numero">7)</span> Vamos, desçamos, e ali confundamos a língua deles, para que eles não possam entender a fala uns dos outros.</p>
