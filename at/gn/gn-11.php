		<p><span class="numero">19)</span> E Pelegue viveu, depois que gerou Reú, duzentos e nove anos, e gerou filhos e filhas.</p>
		<p><span class="numero">20)</span> E Reú viveu trinta e dois anos, e gerou Serugue.</p>
		<p><span class="numero">21)</span> E Reú viveu, depois que gerou Serugue, duzentos e sete anos, e gerou filhos e filhas.</p>
		<p><span class="numero">22)</span> E Serugue viveu trinta anos, e gerou Naor.</p>
		<p><span class="numero">23)</span> E Serugue viveu, depois que gerou Naor, duzentos anos, e gerou filhos e filhas.</p>
		<p><span class="numero">24)</span> E Naor viveu vinte e nove anos, e gerou Terá.</p>
		<p><span class="numero">25)</span> E Naor viveu, depois que gerou Terá, cento e dezenove anos, e gerou filhos e filhas.</p>
		<p><span class="numero">26)</span> E Terá viveu setenta anos, e gerou Abrão, Naor e Harã.</p>
		<p><span class="numero">27)</span> Ora, estas são as gerações de Terá: Terá gerou Abrão, Naor e Harã, e Harã gerou Ló.</p>
		<p><span class="numero">28)</span> E Harã morreu antes de seu pai Terá, na terra do seu nascimento, em Ur dos caldeus.</p>
		<p><span class="numero">29)</span> E Abrão e Naor tomaram mulheres para si; o nome da mulher de Abrão Sarai, e o nome da mulher de Naor era Milca, filha de Harã, pai de Milca e pai de Iscá.</p>
		<p><span class="numero">30)</span> Mas Sarai era estéril, e ela não tinha filhos.</p>
