		<a href="gn.php"><h3>Gênesis 38</h3></a>
		<p><span class="numero">1)</span> E aconteceu naquele tempo que separando-se Judá dos seus irmãos, relacionou-se com um adulamita, cujo nome era Hira.</p>
		<p><span class="numero">2)</span> E Judá viu ali uma filha de um certo cananeu, cujo nome era Sua; e ele a tomou, e entrou a ela.</p>
		<p><span class="numero">3)</span> E ela concebeu e teve um filho; e ele chamou o seu nome Er.</p>
		<p><span class="numero">4)</span> E ela concebeu novamente, e teve um filho; e ela chamou o seu nome Onã.</p>
		<p><span class="numero">5)</span> E ela concebeu mais uma vez, e teve um filho; e chamou seu nome Selá. E ele estava em Quezibe quando ela o teve.</p>
		<p><span class="numero">6)</span> E Judá tomou uma mulher para Er, seu primogênito, cujo nome era Tamar.</p>
		<p><span class="numero">7)</span> E Er, o primogênito de Judá, foi mau aos olhos do Senhor; e o Senhor o matou.</p>
		<p><span class="numero">8)</span> E Judá disse a Onã: Entra à mulher de teu irmão, e case-se com ela, e levanta a semente de teu irmão.</p>
		<p><span class="numero">9)</span> E Onã sabia que a semente não seria sua. E aconteceu que, quando ele entrava à mulher de seu irmão, ele o derramava no chão, para que ele não desse semente ao seu irmão.</p>
		<p><span class="numero">10)</span> E a coisa que ele fez desagradou ao Senhor; e por isso ele o matou também.</p>
		<p><span class="numero">11)</span> Então Judá disse a Tamar, sua nora: Permanece viúva na casa de teu pai, até que Selá, meu filho, seja grande, pois ele disse: Para que porventura ele não morra também, como seus irmãos. E Tamar foi e habitou na casa de seu pai.</p>
		<p><span class="numero">12)</span> E no decorrer do tempo, morreu a filha de Sua, mulher de Judá, e Judá foi confortado, e subiu até seus tosquiadores de ovelhas em Timna, ele e seu amigo Hira, o adulamita.</p>
		<p><span class="numero">13)</span> E contaram a Tamar, dizendo: Eis que teu sogro sobe a Timna para tosquiar suas ovelhas.</p>
		<p><span class="numero">14)</span> E ela tirou de si suas vestes da viuvez, e se cobriu com um véu, e enrolou-se, e sentou- se em um lugar aberto, que fica junto ao caminho de Timna, porque ela viu que Selá havia crescido, e ela não havia sido entregue a ele por mulher.</p>
		<p><span class="numero">15)</span> Quando Judá a viu, ele pensou que fosse uma prostituta, porque ela havia coberto a sua face.</p>
		<p><span class="numero">16)</span> E ele se voltou a ela junto ao caminho e disse: Vem, rogo-te, e deixa-me entrar em ti (pois ele não sabia que ela era sua nora). E ela disse: O que me darás para que possas entrar em mim?</p>
		<p><span class="numero">17)</span> E ele disse: Enviar-te-ei um cabrito do rebanho. E ela disse: Dar-me-ás um penhor até que o envies?</p>
		<p><span class="numero">18)</span> E ele disse: Qual penhor eu te darei? E ela disse: Teu selo, e tuas pulseiras, e teu cajado que está em tua mão. E ele o deu a ela, e entrou nela, e ela concebeu dele.</p>
		<p><span class="numero">19)</span> E ela levantando-se, se foi, e colocou de lado seu véu, e pôs as vestes da sua viuvez.</p>
		<p><span class="numero">20)</span> E Judá enviou o cabrito pela mão de seu amigo, o adulamita, para receber seu penhor da mão da mulher; mas ele não a encontrou.</p>
		<p><span class="numero">21)</span> Então ele perguntou aos homens daquele lugar, dizendo: Onde está a prostituta, que estava publicamente junto ao caminho? E eles disseram: Não esteve nenhuma prostituta neste lugar.</p>
		<p><span class="numero">22)</span> E ele voltou a Judá, e disse: Não pude encontrá-la, e também os homens do lugar disseram que não esteve nenhuma prostituta nesse lugar.</p>
		<p><span class="numero">23)</span> E Judá disse: Que ela o tome para si, para que não sejamos envergonhados. Eis que enviei este cabrito, e tu não a encontraste.</p>
		<p><span class="numero">24)</span> E aconteceu que, quase três meses depois, contaram a Judá, dizendo: Tamar, tua nora, prostituiu-se, e também: Eis que está com filho da prostituição. E Judá disse: Trazei-a, e seja ela queimada.</p>
		<p><span class="numero">25)</span> Mas enquanto era trazida, enviou a dizer a seu sogro: Do homem, a quem estas coisas pertencem, estou grávida. E ela disse: Reconheces, suplico-te, de quem são estas coisas: o selo, as pulseiras e o cajado.</p>
		<p><span class="numero">26)</span> E Judá os reconheceu, e disse: Ela foi mais justa do que eu, porque não lhe dei Selá, meu filho. E ele nunca mais a conheceu.</p>
		<p><span class="numero">27)</span> E aconteceu que, no tempo de seu parto, eis que havia gêmeos em seu ventre.</p>
		<p><span class="numero">28)</span> E aconteceu que, quando ela deu à luz, um pôs para fora sua mão; e a parteira tomou e amarrou na sua mão um fio escarlate, dizendo: Este saiu primeiro.</p>
		<p><span class="numero">29)</span> E aconteceu que, quando ele puxou de volta sua mão, eis que seu irmão saiu. E ela disse: Como foi que rompeste? Esta brecha seja sobre ti. Por isso seu nome foi chamado Perez.</p>
		<p><span class="numero">30)</span> E depois saiu seu irmão, o que tinha o fio escarlate sobre sua mão; e seu nome foi chamado Zerá.&nbsp;<a href="gn-39.php"><span class="proximo">Próximo capítulo</span></a></p>
