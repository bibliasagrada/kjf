		<p><span class="numero">1)</span> E aconteceu nos dias Anrafel, rei de Sinar, Arioque, rei de Elasar, Quedorlaomer, rei de Elão, e Tidal, rei de nações,</p>
		<p><span class="numero">2)</span> que estes fizeram guerra a Bera, rei de Sodoma, a Birsa, rei de Gomorra, a Sinabe, rei de Admá, e a Semeber, rei de Zeboim, e ao rei de Bela, que é Zoar.</p>
		<p><span class="numero">3)</span> Todos estes foram reunidos no vale de Sidim, que é o mar de sal.</p>
		<p><span class="numero">4)</span> Eles serviram doze anos a Quedorlaomer, e no décimo terceiro ano se
		rebelaram.</p>
		<p><span class="numero">5)</span> E no décimo quarto ano veio Quedorlaomer, e os reis que estavam com ele, e feriram os refains em Asterote- Carnaim, e os zuzins em Hã, e os emins em Savé-Quiriataim,</p>
		<p><span class="numero">6)</span> e os horeus no seu monte Seir, até El- Parã, que fica junto ao deserto.</p>
		<p><span class="numero">7)</span> E eles retornaram, e vieram a En-Mispate, que é Cades, e feriram toda a terra	dos amalequitas, e também os amorreus, que habitavam em Hazazom-Tamar.</p>
		<p><span class="numero">8)</span> E saiu o rei de Sodoma, e o rei de Gomorra, e o rei de Admá, e o rei de Zeboim, e o rei de Bela (esta é Zoar) e se ajuntaram à batalha contra eles no vale de Sidim,</p>
		<p><span class="numero">9)</span> contra Quedorlaomer, rei de Elão, e Tidal, rei das nações, e Anrafel, rei de Sinar, e Arioque, rei de Elasar; quatro reis contra cinco.</p>
		<p><span class="numero">10)</span> E o vale de Sidim estava cheio de poços de lodo, e os reis de Sodoma e Gomorra fugiram, e caíram ali, e os restantes fugiram para o monte.</p>
		<p><span class="numero">11)</span> E eles tomaram todos os bens de Sodoma e Gomorra, e todos os seus
		mantimentos, e foram no seu caminho.</p>
		<p><span class="numero">12)</span> E eles tomaram Ló, filho do irmão de Abrão, que habitava em Sodoma, e os	seus bens, e partiram.</p>
		<p><span class="numero">13)</span> E veio um que havia escapado, e contou a Abrão, o hebreu, pois ele habitava	na planície de Manre, o amorreu, irmão de Escol e irmão de Aner; e estes eram
		confederados de Abrão.</p>
		<p><span class="numero">14)</span> E quando Abrão ouviu que o seu irmão foi levado cativo, ele armou os seus servos treinados, nascidos na sua própria casa, trezentos e dezoito, e os	perseguiu até Dã.</p>
		<p><span class="numero">15)</span> E ele se dividiu contra eles, ele e seus servos, à noite, e os feriu, os perseguiu até Hobá, que está à esquerda de Damasco.</p>
		<p><span class="numero">16)</span> E ele trouxe de volta todos os bens, e também trouxe novamente o seu irmão Ló, e seus bens, e também as mulheres, e o povo.</p>
		<p><span class="numero">17)</span> E o rei de Sodoma saiu para encontrá- lo depois do seu retorno do massacre a	Quedorlaomer e os reis que estavam com ele, no vale de Savé, que é o vale do rei.</p>
		<p><span class="numero">18)</span> E Melquisedeque, rei de Salém, trouxe pão e vinho, e ele era o sacerdote do Deus Altíssimo.</p>
		<p><span class="numero">19)</span> E ele o abençoou, e disse: Bendito seja Abrão do Deus Altíssimo, criador do céu e da terra.</p>
		<p><span class="numero">20)</span> E bendito seja o Deus Altíssimo, que entregou teus inimigos em tuas mãos. E lhe deu dízimos de tudo.</p>
		<p><span class="numero">21)</span> E o rei de Sodoma disse a Abrão: Dá-me as pessoas, e toma os bens para ti.</p>
		<p><span class="numero">22)</span> E Abrão disse ao rei de Sodoma: Eu levanto a minha mão ao Senhor, o Deus	Altíssimo, o possuidor do céu e da terra,</p>
		<p><span class="numero">23)</span> que não tomarei nem um fio, nem a correia de uma sandália, e que não
		tomarei coisa alguma que é tua, para que não digas: Eu enriqueci a Abrão,</p>
		<p><span class="numero">24)</span> salvo tão somente o que os jovens comeram, e a parte dos homens que foram comigo, Aner, Escol e Manre; que eles tomem a sua parte.&nbsp;<a href="gn-15.php"><span class="proximo">Próximo capítulo</span></a></p>
