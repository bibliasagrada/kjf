		<a href="gn.php"><h3>Gênesis 24</h3></a>
		<p><span class="numero">1)</span> E Abraão era velho e bem avançado em idade; e o Senhor havia abençoado Abraão em todas as coisas.</p>
		<p><span class="numero">2)</span> E Abraão disse ao servo mais velho de sua casa, que governava sobre tudo o que ele tinha: Rogo-te que ponhas tua mão debaixo da minha coxa;</p>
		<p><span class="numero">3)</span> e eu te farei jurar pelo Senhor, o Deus do céu, e o Deus da terra, que tu não tomarás mulher para meu filho dentre as filhas dos cananeus, entre os quais eu habito;</p>
		<p><span class="numero">4)</span> mas irás à minha terra, e à minha parentela, e tomarás uma mulher para meu filho Isaque.</p>
		<p><span class="numero">5)</span> E o servo lhe disse: Se porventura a mulher não quiser seguir-me para esta terra, eu deixarei levar teu filho novamente para a terra de onde tu vieste?</p>
		<p><span class="numero">6)</span> E Abraão lhe disse: Cuida para que não leves meu filho para lá novamente.</p>
		<p><span class="numero">7)</span> O Senhor Deus do céu, que me tirou da casa de meu pai, e da terra de minha parentela, e que falou comigo, e que jurou para mim, dizendo: À tua semente eu darei esta terra, ele enviará seu anjo adiante de ti, e tu tomarás para o meu filho uma mulher de lá.</p>
		<p><span class="numero">8)</span> E se a mulher não quiser te seguir, então tu estarás livre deste meu juramento; somente não leves o meu filho para lá novamente.</p>
		<p><span class="numero">9)</span> E o servo colocou sua mão debaixo da coxa de Abraão, seu senhor, e jurou a ele acerca deste assunto.</p>
		<p><span class="numero">10)</span> E o servo tomou dez camelos dos camelos do seu senhor, e partiu; pois todos os bens de seu senhor estavam em suas mãos. E ele se levantou, e foi à Mesopotâmia, para a cidade de Naor.</p>
		<p><span class="numero">11)</span> E ele fez seus camelos se ajoelharem fora da cidade, junto a um poço de água na hora da tarde, a hora em que as mulheres saem para tirar água.</p>
		<p><span class="numero">12)</span> E ele disse: Ó Senhor Deus de meu senhor Abraão, rogo-te, dá-me bom êxito neste dia, e mostra bondade para com meu senhor Abraão.</p>
		<p><span class="numero">13)</span> Eis que eu estou em pé aqui junto ao poço de água; e as filhas dos homens da cidade saem para tirar água;</p>
		<p><span class="numero">14)</span> E seja, pois, que a donzela a quem eu disser: Inclina o teu cântaro e beberei, e ela responder: Bebe, e também darei de beber aos teus camelos, essa seja a que designaste para o teu servo Isaque, e por ela saberei que tens misericórdia para com meu senhor.</p>
		<p><span class="numero">15)</span> E aconteceu que, antes que ele terminasse de falar, eis que saiu Rebeca, que era nascida a Betuel, filho de Milca, mulher de Naor, irmão de Abraão, com seu cântaro sobre seu ombro.</p>
		<p><span class="numero">16)</span> E a donzela era muito formosa à vista, uma virgem, nenhum homem havia conhecido; e ela desceu ao poço, e encheu seu cântaro, e subiu.</p>
		<p><span class="numero">17)</span> E o servo correu para encontrá-la, e disse: Permite-me, rogo-te, beber um pouco da água de teu cântaro.</p>
		<p><span class="numero">18)</span> E ela disse: Bebe, meu senhor; e ela se apressou e abaixou seu cântaro sobre sua mão, e lhe deu de beber.</p>
		<p><span class="numero">19)</span> E, acabando ela de lhe dar de beber, disse: Eu também tirarei água para os teus camelos, até que tenham bebido.</p>
		<p><span class="numero">20)</span> E ela se apressou e esvaziou o seu cântaro no cocho, e correu novamente para o poço para tirar água, e tirou para todos os seus camelos.</p>
		<p><span class="numero">21)</span> E o homem estava admirado de vê-la, e ficou em paz, para saber se o Senhor havia feito prosperar sua viagem ou não.</p>
		<p><span class="numero">22)</span> E aconteceu que, quando os camelos haviam acabado de beber, que o homem tomou um brinco de ouro de meio siclo de peso, e dois braceletes para as mãos dela, do peso de dez siclos de ouro.</p>
		<p><span class="numero">23)</span> E disse: De quem tu és filha? Dize-me, suplico-te. Há lugar na casa de teu pai para nos alojarmos?</p>
		<p><span class="numero">24)</span> E ela lhe disse: Eu sou a filha de Betuel, filho de Milca, o que deu à luz a Naor.</p>
		<p><span class="numero">25)</span> E ela disse além disso a ele: Nós temos tanto palha quanto forragem suficientes, e lugar para se alojar.</p>
		<p><span class="numero">26)</span> E o homem curvou sua cabeça, e adorou ao Senhor.</p>
		<p><span class="numero">27)</span> E ele disse: Bendito seja o Senhor Deus de meu senhor Abraão, que não deixou desamparado meu senhor sem misericórdia e verdade. Eu estando no caminho, o Senhor me conduziu à casa dos irmãos de meu senhor.</p>
		<p><span class="numero">28)</span> E a donzela correu, e contou aos da casa de sua mãe estas coisas.</p>
		<p><span class="numero">29)</span> E Rebeca tinha um irmão, e seu nome era Labão; e Labão correu até o homem, junto ao poço.</p>
		<p><span class="numero">30)</span> E aconteceu que, quando ele viu o brinco e os braceletes nas mãos de sua irmã, e quando ele ouviu as palavras de Rebeca, sua irmã, dizendo: Assim falou o homem comigo, que ele veio ao homem, e eis que ele estava em pé junto aos camelos diante do poço.</p>
		<p><span class="numero">31)</span> E ele disse: Vem, bendito do Senhor. Por que estás em pé aí fora? Pois eu preparei a casa, e lugar para os camelos.</p>
		<p><span class="numero">32)</span> E o homem entrou na casa, e ele desatou os seus camelos, e deu palha e forragem aos camelos, e água para lavar seus pés, e os pés dos homens que estavam com ele.</p>
		<p><span class="numero">33)</span> E foi posto alimento diante dele para comer, mas ele disse: Não comerei, até que eu tenha dito a minha incumbência. E ele disse: Fala.</p>
		<p><span class="numero">34)</span> E ele disse: Eu sou o servo de Abraão.</p>
		<p><span class="numero">35)</span> E o Senhor abençoou meu senhor grandemente, e ele tornou-se grande; e ele lhe deu rebanhos, e gado, e prata, e ouro, e servos, e servas, e camelos e jumentos.</p>
		<p><span class="numero">36)</span> E Sara, mulher de meu senhor, deu um filho a meu senhor quando ela já estava velha, e a ele deu ele tudo que possui.</p>
		<p><span class="numero">37)</span> E meu senhor me fez jurar, dizendo: Tu não tomarás mulher para meu filho dentre as filhas dos cananeus, em cuja terra eu habito;</p>
		<p><span class="numero">38)</span> mas tu irás à casa de meu pai, e à minha parentela, para tomar uma mulher para meu filho.</p>
		<p><span class="numero">39)</span> E eu disse ao meu senhor: E se porventura a mulher não quiser me seguir.</p>
		<p><span class="numero">40)</span> E ele me disse: O Senhor, diante de quem eu ando, enviará seu anjo contigo, e prosperará o teu caminho; e tu tomarás uma mulher para meu filho da minha parentela, e da casa de meu pai.</p>
		<p><span class="numero">41)</span> Então tu estarás livre deste meu juramento, quando fores à minha parentela; e se eles não te derem uma, estarás livre do meu juramento.</p>
		<p><span class="numero">42)</span> E eu vim neste dia para este poço, e disse: Ó Senhor Deus de meu senhor Abraão, se agora tu fizeres prosperar o caminho em que eu ando,</p>
		<p><span class="numero">43)</span> eis que estou em pé junto ao poço de água, e acontecerá que, quando uma virgem vier para tirar água, e eu disser a ela: Dá-me, rogo-te, um pouco de água de teu cântaro para beber,</p>
		<p><span class="numero">44)</span> e ela me disser: Bebe tu, e eu tirarei também para teus camelos, seja esta a mulher que o Senhor designou para o filho de meu senhor.</p>
		<p><span class="numero">45)</span> E antes que eu tivesse acabado de falar em meu coração, eis que Rebeca veio com seu cântaro no seu ombro, e ela desceu ao poço e tirou água. E eu lhe disse: Permite-me que eu beba, rogo-te.</p>
		<p><span class="numero">46)</span> E ela se apressou, e baixou seu cântaro de seu ombro, e disse: Bebe, e eu darei de beber também aos teus camelos. Assim eu bebi, e ela fez os camelos beberem também.</p>
		<p><span class="numero">47)</span> E eu lhe perguntei, e disse: De quem tu és filha? E ela disse: A filha de Betuel, filho de Naor, que Milca lhe deu, e eu coloquei o brinco sobre a sua face, e os braceletes nas suas mãos.</p>
		<p><span class="numero">48)</span> E eu curvei a minha cabeça, e adorei ao Senhor, e bendisse ao Senhor, Deus do meu senhor Abraão, que havia me conduzido no caminho certo para tomar a filha do irmão de meu senhor para seu filho.</p>
		<p><span class="numero">49)</span> E agora, se quiserdes agir de forma bondosa e verdadeira com meu senhor, dizei- mo; e se não, dizei-mo, para que eu possa tornar para a direita ou para a esquerda.</p>
		<p><span class="numero">50)</span> Então Labão e Betuel responderam e disseram: A coisa procede do Senhor; não podemos falar-te mal ou bem.</p>
		<p><span class="numero">51)</span> Eis que Rebeca está diante de ti, toma-a e vai. E deixa que ela seja a mulher do filho de teu senhor, como o Senhor disse.</p>
		<p><span class="numero">52)</span> E aconteceu que, quando o servo de Abraão ouviu as palavras deles, adorou ao Senhor, curvando-se à terra.</p>
		<p><span class="numero">53)</span> E o servo trouxe joias de prata, e joias de ouro, e vestes e os deu a Rebeca, e ele deu coisas preciosas também ao seu irmão e sua mãe.</p>
		<p><span class="numero">54)</span> E eles comeram e beberam, ele e os homens que estavam com ele, passaram toda a noite. E se levantaram de manhã, e ele disse: Enviai-me a meu senhor.</p>
		<p><span class="numero">55)</span> E o irmão dela e a sua mãe disseram: Deixa que a donzela fique conosco alguns dias, pelo menos dez, e depois disso, ela irá.</p>
		<p><span class="numero">56)</span> E ele lhes disse: Não me detenhais, vendo que o Senhor tem prosperado o meu caminho. Enviai-me para que eu possa ir ao meu senhor.</p>
		<p><span class="numero">57)</span> E eles disseram: Chamaremos a donzela, e perguntaremos de sua boca.</p>
		<p><span class="numero">58)</span> E eles chamaram Rebeca, e lhe disseram: Tu queres ir com este homem? E ela disse: Eu irei.</p>
		<p><span class="numero">59)</span> E eles enviaram Rebeca, sua irmã, e sua ama, e o servo de Abraão, e seus homens.</p>
		<p><span class="numero">60)</span> E eles abençoaram Rebeca, e lhe disseram: Tu és nossa irmã, seja a mãe de milhares de milhões, e que a tua semente possua o portão dos que te odeiam.</p>
		<p><span class="numero">61)</span> E Rebeca se levantou, e suas donzelas, e montaram em seus camelos, e seguiram o homem. E o servo tomou Rebeca, e foi pelo seu caminho.</p>
		<p><span class="numero">62)</span> E Isaque veio do caminho do poço Beer- Laai-Rói, pois ele habitava na terra do sul.</p>
		<p><span class="numero">63)</span> E Isaque saiu para meditar no campo ao anoitecer. E ele levantou seus olhos, e viu, e eis que os camelos estavam vindo.</p>
		<p><span class="numero">64)</span> E Rebeca levantou seus olhos, e quando ela viu Isaque, desceu do camelo,</p>
		<p><span class="numero">65)</span> pois ela havia dito ao servo: Que homem é este que anda no campo ao nosso encontro? E o servo havia dito: É meu senhor. Por isso, ela tomou um véu e se cobriu.</p>
		<p><span class="numero">66)</span> E o servo contou a Isaque todas as coisas que ele havia feito.</p>
		<p><span class="numero">67)</span> E Isaque a levou para a tenda de Sara, sua mãe, e tomou Rebeca, e ela se tornou sua mulher. E ele a amou, e Isaque foi confortado após a morte de sua mãe.&nbsp;<a href="gn-25.php"><span class="proximo">Próximo capítulo</span></a></p>
