		<p><span class="numero">1)</span> Então Abraão tomou novamente uma mulher, e o seu nome era Quetura.</p>
		<p><span class="numero">2)</span> E ela lhe gerou Zinrã, e Jocsã, e Medã, e Midiã, e Jisbaque, e Suá.</p>
		<p><span class="numero">3)</span> E Jocsã gerou Seba e Dedã; e os filhos de Dedã foram Assurim, e Letusim, e Leumim.</p>
		<p><span class="numero">4)</span> E os filhos de Midiã foram Efá, e Efer, e Enoque, e Abida, e Elda. Todos estes foram filhos de Quetura.</p>
		<p><span class="numero">5)</span> E Abraão deu tudo o que ele tinha para Isaque.</p>
		<p><span class="numero">6)</span> Mas aos filhos das concubinas, que Abraão tinha, Abraão deu presentes, e ainda em vida os enviou para longe de seu filho Isaque, ao leste, para a terra oriental.</p>
		<p><span class="numero">7)</span> E estes são os dias dos anos da vida de Abraão, que ele viveu, cento e setenta e cinco anos.</p>
		<p><span class="numero">8)</span> Então Abraão entregou o espírito, e morreu em boa velhice, um homem velho, e cheio de anos. E foi reunido ao seu povo.</p>
		<p><span class="numero">9)</span> E seus filhos, Isaque e Ismael, o sepultaram na caverna de Macpela, no campo de Efrom, filho de Zoar, o heteu, que está diante de Manre;</p>
		<p><span class="numero">10)</span> no campo que Abraão comprou dos filhos de Hete, ali foi Abraão sepultado, e Sara, sua mulher.</p>
		<p><span class="numero">11)</span> E aconteceu que, depois da morte de Abraão, Deus abençoou seu filho Isaque, e Isaque habitou junto ao poço Beer-Laai-Rói.</p>
		<p><span class="numero">12)</span> Agora, estas são as gerações de Ismael, filho de Abraão, que Agar, a egípcia, serva de Sara, gerou para Abraão,</p>
		<p><span class="numero">13)</span> e estes são os nomes dos filhos de Ismael, pelos seus nomes, de acordo com suas gerações: O primogênito de Ismael era Nebaiote, e Quedar, e Adbeel, e Mibsão,</p>
		<p><span class="numero">14)</span> e Misma, e Dumá, e Massá,</p>
		<p><span class="numero">15)</span> e Hadade, e Tema, e Jetur, e Nafis, e Quedemá.</p>
		<p><span class="numero">16)</span> Estes são os filhos de Ismael, e estes são seus nomes, pelas suas aldeias, e pelas suas fortalezas; doze príncipes de acordo com suas nações.</p>
		<p><span class="numero">17)</span> E estes são os anos da vida de Ismael, cento e trinta e sete anos, e ele entregou o espírito e morreu, e foi reunido ao seu povo.</p>
		<p><span class="numero">18)</span> E eles habitaram desde Havilá até Sur, que está em frente ao Egito, quando se vai para a Assíria. E ele morreu na presença de todos os seus irmãos.</p>
		<p><span class="numero">19)</span> E estas são as gerações de Isaque, filho de Abraão; Abraão gerou Isaque.</p>
		<p><span class="numero">20)</span> E Isaque era da idade de quarenta anos quando ele tomou como mulher Rebeca, filha de Betuel, o sírio de Padã-Arã, irmã de Labão, o sírio.</p>
		<p><span class="numero">21)</span> E Isaque intercedeu ao Senhor pela sua mulher, porque ela era estéril. E o Senhor ouviu a intercessão dele, e Rebeca, sua mulher, concebeu.</p>
		<p><span class="numero">22)</span> E os filhos lutavam dentro dela, e ela disse: Se é assim, por que sou eu assim? E ela foi consultar ao Senhor.</p>
		<p><span class="numero">23)</span> E o Senhor lhe disse: Duas nações estão no teu ventre, e dois tipos de povos se dividirão das tuas entranhas; e um povo será mais forte do que o outro povo, e o mais velho servirá ao mais novo.</p>
		<p><span class="numero">24)</span> E cumprindo os seus dias para dar à Luz, eis que havia gêmeos em seu ventre.</p>
		<p><span class="numero">25)</span> E o primeiro saiu ruivo, todo ele peludo como uma veste de pelo, e chamaram o seu nome Esaú.</p>
		<p><span class="numero">26)</span> E depois dele veio seu irmão, e sua mão segurou no calcanhar de Esaú, e seu nome foi chamado Jacó. E Isaque era da idade de sessenta anos quando ela os gerou.</p>
		<p><span class="numero">27)</span> E os meninos cresceram; e Esaú era um caçador habilidoso, um homem do campo. E Jacó era um homem simples, habitando em tendas.</p>
		<p><span class="numero">28)</span> E Isaque amou Esaú, porque ele comia da sua caça; mas Rebeca amou Jacó.</p>
		<p><span class="numero">29)</span> E Jacó cozeu um ensopado; e Esaú veio do campo, e ele estava desfalecendo.</p>
		<p><span class="numero">30)</span> E Esaú disse a Jacó: Dá-me de comer, rogo- te, desse ensopado vermelho, pois eu estou desfalecendo. Por isso, seu nome foi chamado Edom.</p>
		<p><span class="numero">31)</span> E Jacó disse: Vende-me neste dia a tua primogenitura.</p>
		<p><span class="numero">32)</span> E Esaú disse: Eis que eu estou a ponto de morrer, de que me serve essa primogenitura?</p>
		<p><span class="numero">33)</span> E Jacó disse: Jura-me neste dia. E ele lhe jurou, e vendeu sua primogenitura a Jacó.</p>
		<p><span class="numero">34)</span> Então Jacó deu a Esaú pão e ensopado de lentilhas. E ele comeu e bebeu, e se levantou, e foi pelo seu caminho. Assim Esaú desprezou sua primogenitura.&nbsp;<a href="gn-26.php"><span class="proximo">Próximo capítulo</span></a></p>
