		<a href="gn.php"><h3>Gênesis 34</h3></a>
		<p><span class="numero">1)</span> E Diná, filha de Lia, que ela gerou a Jacó, saiu para ver as filhas da terra.</p>
		<p><span class="numero">2)</span> E quando Siquém, filho de Hamor, o heveu, príncipe da terra, a viu, tomou-a e deitou- se com ela, e a desonrou.</p>
		<p><span class="numero">3)</span> E sua alma se apegou a Diná, filha de Jacó, e ele amou a donzela, e falou amorosamente à moça.</p>
		<p><span class="numero">4)</span> E Siquém falou com seu pai, Hamor, dizendo: Toma-me esta donzela por mulher.</p>
		<p><span class="numero">5)</span> E Jacó ouviu que ele havia desonrado Diná, sua filha; ora, seus filhos estavam com seu gado no campo, e Jacó manteve- se quieto até eles chegarem.</p>
		<p><span class="numero">6)</span> E Hamor, pai de Siquém, saiu para ter com Jacó, para conversar com ele.</p>
		<p><span class="numero">7)</span> E os filhos de Jacó vieram do campo quando ouviram sobre isso. E os homens se entristeceram e se iraram muito, pois ele havia feito loucura em Israel ao deitar com a filha de Jacó, coisa que não deveria ter sido feita.</p>
		<p><span class="numero">8)</span> E Hamor conversou com eles, dizendo: A alma de meu filho Siquém anseia por tua filha; suplico-te que lha dês por mulher.</p>
		<p><span class="numero">9)</span> E fazei vós casamentos conosco, e dai vossas filhas a nós, e tomai nossas filhas para vós.</p>
		<p><span class="numero">10)</span> E habitareis conosco, e a terra estará diante de vós; habitai e negociai nela, e adquiri possessão nela.</p>
		<p><span class="numero">11)</span> E Siquém disse ao pai e aos irmãos dela: Ache eu graça aos vossos olhos, e o que me disserdes eu o darei.</p>
		<p><span class="numero">12)</span> Pedi-me o quanto mais quiserdes de dote e dádiva, e eu darei de acordo com o que me disserdes, mas dai-me a donzela por mulher.</p>
		<p><span class="numero">13)</span> E os filhos de Jacó responderam a Siquém e a Hamor, seu pai, enganosamente; e disseram, porque ele havia desonrado Diná, sua irmã,</p>
		<p><span class="numero">14)</span> e disseram-lhes: Não podemos fazer isso, dar a nossa irmã a alguém incircunciso, pois isso seria uma vergonha para nós.</p>
		<p><span class="numero">15)</span> Mas nisto consentiremos convosco: Se fordes como nós somos, que todo homem entre vós seja circuncidado,</p>
		<p><span class="numero">16)</span> então daremos nossas filhas a vós, e nós tomaremos vossas filhas para nós, e habitaremos convosco, e nos tornaremos um povo.</p>
		<p><span class="numero">17)</span> Mas se não nos ouvirdes, para serdes circuncidados, então tomaremos nossa filha, e nós iremos embora.</p>
		<p><span class="numero">18)</span> E as suas palavras agradaram a Hamor, e a Siquém, filho de Hamor.</p>
		<p><span class="numero">19)</span> E o jovem não tardou em fazer isto, pois ele tinha prazer na filha de Jacó, e ele era o mais honrável em toda a casa de seu pai.</p>
		<p><span class="numero">20)</span> E Hamor e Siquém, seu filho, vieram ao portão da sua cidade, e conversaram com os homens da sua cidade, dizendo:</p>
		<p><span class="numero">21)</span> Estes homens são pacíficos conosco; por isso, deixai-os habitar na terra e negociar nela, pois a terra, eis que é grande o suficiente para eles; tomaremos as suas filhas para nós por mulheres, e daremos as nossas filhas a eles.</p>
		<p><span class="numero">22)</span> Somente nisto consentirão os homens conosco para habitar entre nós, para sermos um povo: se todo homem entre nós for circuncidado, assim como eles são circuncidados.</p>
		<p><span class="numero">23)</span> Não serão nossos o seu gado, suas posses e todos os seus animais? Somente consintamos com eles, e habitarão conosco.</p>
		<p><span class="numero">24)</span> E a Harmor e a Siquém, seu filho, ouviram com atenção todos os que saíam do portão da sua cidade. E todo homem foi circuncidado, todos os que saíram do portão da sua cidade.</p>
		<p><span class="numero">25)</span> E aconteceu no terceiro dia que, quando eles estavam doloridos, dois dos filhos de Jacó, Simeão e Levi, irmãos de Diná, tomaram cada homem a sua espada, e vieram contra a cidade corajosamente, e mataram todos os homens.</p>
		<p><span class="numero">26)</span> E eles mataram Hamor e Siquém, seu filho, ao fio da espada, e tomaram Diná da casa de Siquém, e saíram.</p>
		<p><span class="numero">27)</span> Os filhos de Jacó vieram aos mortos e saquearam a cidade, porque eles haviam desonrado a sua irmã.</p>
		<p><span class="numero">28)</span> Tomaram as ovelhas deles, e os bois, e os jumentos, e o que estava na cidade, e o que estava no campo,</p>
		<p><span class="numero">29)</span> e toda a sua riqueza, e todos os seus pequenos, e suas mulheres eles tomaram cativas, e saquearam tudo o que havia na casa.</p>
		<p><span class="numero">30)</span> E Jacó disse a Simeão e a Levi: Tendes me perturbado para me fazer cheirar mal entre os habitantes da terra, entre os cananeus e ferezeus. Sendo eu pequeno em número, eles se unirão contra mim, e me matarão; e serei destruído, eu e a minha casa.</p>
