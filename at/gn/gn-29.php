		<p><span class="numero">1)</span> Então, Jacó seguiu na sua jornada, e veio à terra do povo do oriente.
		<p><span class="numero">2)</span> E ele olhou, e eis um poço no campo, e eis que ali havia três rebanhos de ovelhas deitados junto a ele, porque daquele poço davam de beber aos rebanhos, e uma grande pedra estava sobre a boca do poço.
		<p><span class="numero">3)</span> E ali estavam reunidos todos os rebanhos, e removiam a pedra da boca do poço, e davam de beber às ovelhas, e colocavam a pedra no lugar novamente sobre a boca do poço.
		<p><span class="numero">4)</span> E Jacó lhes disse: Meus irmãos, de onde sois? E eles disseram: Nós somos de Harã.
		<p><span class="numero">5)</span> E ele lhes disse: Conheceis Labão, filho de Naor? E eles disseram: Nós o conhecemos.
		<p><span class="numero">6)</span> E ele lhes disse: Ele está bem? E eles disseram: Ele está bem. E eis que Raquel, sua filha, está vindo com as ovelhas.
		<p><span class="numero">7)</span> E ele disse: Eis que ainda é pleno dia, e nem é tempo de reunir o gado; dai de beber às ovelhas, e ide alimentá-las.
		<p><span class="numero">8)</span> E eles disseram: Não podemos, até que todos os rebanhos sejam reunidos, e até que eles removam a pedra da boca do poço; então damos de beber às ovelhas.
		<p><span class="numero">9)</span> E enquanto ele ainda falava com eles, veio Raquel com as ovelhas de seu pai, porquanto ela as guardava.
		<p><span class="numero">10)</span> E aconteceu que, quando Jacó viu Raquel, a filha de Labão, irmão de sua mãe, e as ovelhas de Labão, irmão de sua mãe, Jacó se aproximou e removeu a pedra da boca do poço, e deu de beber ao rebanho de Labão, irmão de sua mãe.
		<p><span class="numero">11)</span> E Jacó beijou Raquel, e levantando a voz, chorou.
		<p><span class="numero">12)</span> E Jacó contou a Raquel que ele era irmão do pai dela, e que ele era filho de Rebeca; e ela correu e contou a seu pai.
		<p><span class="numero">13)</span> E aconteceu que, quando Labão ouviu as notícias de Jacó, filho de sua irmã, ele correu para encontrá-lo, e o abraçou, e o beijou, e o trouxe para sua casa. E ele contou a Labão todas estas coisas.
		<p><span class="numero">14)</span> E Labão lhe disse: Certamente tu és meu osso e minha carne. E ficou com ele por um período de um mês.
		<p><span class="numero">15)</span> E Labão disse a Jacó: Porque tu és meu irmão, deverias portanto servir-me por nada? Dize-me, qual será o teu salário?
		<p><span class="numero">16)</span> E Labão tinha duas filhas. O nome da mais velha era Lia, e o nome da mais nova era Raquel.
		<p><span class="numero">17)</span> Lia era de olhos ternos, mas Raquel era formosa e bem favorecida.
		<p><span class="numero">18)</span> E Jacó amou Raquel, e disse: Eu te servirei sete anos por Raquel, tua filha mais nova.
		<p><span class="numero">19)</span> E Labão disse: É melhor que eu a dê a ti do que dá-la a outro homem; habita comigo.
		<p><span class="numero">20)</span> E Jacó serviu sete anos por Raquel; e estes lhe pareciam apenas poucos dias, por causa do amor que ele tinha por ela.
		<p><span class="numero">21)</span> E Jacó disse a Labão: Dá-me minha mulher, pois os meus dias se cumpriram para que eu entre a ela.
		<p><span class="numero">22)</span> E Labão reuniu todos os homens do lugar, e fez um banquete.
		<p><span class="numero">23)</span> E aconteceu que, à tarde, ele tomou Lia, sua filha, e a levou a ele, e ele entrou nela.
		<p><span class="numero">24)</span> E Labão deu sua serva Zilpa por serva a Lia, sua filha.
		<p><span class="numero">25)</span> E aconteceu que, de manhã, eis que ela era Lia. E ele disse a Labão: O que é isto que tu me fizeste? Eu não te servi por Raquel? Por que então me enganaste?
		<p><span class="numero">26)</span> E Labão disse: Não se deve fazer assim na nossa terra, dar a mais nova antes da primogênita.
		<p><span class="numero">27)</span> Cumpre a semana dela, e nós te daremos também esta pelo serviço com que tu servirás comigo ainda outros sete anos.
		<p><span class="numero">28)</span> E assim Jacó fez, e cumpriu a semana dela; e ele lhe deu também por mulher Raquel, sua filha.
		<p><span class="numero">29)</span> E Labão deu sua serva Bila por serva a Raquel, sua filha.
		<p><span class="numero">30)</span> E ele entrou também em Raquel, e ele também amou Raquel mais do que Lia, e serviu com ele ainda outros sete anos.
		<p><span class="numero">31)</span> E quando o Senhor viu que Lia era odiada, ele abriu seu ventre, mas Raquel era estéril.
		<p><span class="numero">32)</span> E Lia concebeu, e gerou um filho, e ela chamou o seu nome Rúben, pois ela disse: Certamente o Senhor olhou para a minha aflição, por isso agora o meu marido me amará.
		<p><span class="numero">33)</span> E ela concebeu outra vez, e gerou um filho, e disse: Porque o Senhor ouviu que eu era odiada, por isso ele me deu também este filho, e ela chamou o seu nome Simeão.
		<p><span class="numero">34)</span> E ela concebeu outra vez, e gerou um filho. E disse: Agora, desta vez o meu marido se ajuntará a mim, porque lhe gerei três filhos, por isso seu nome foi chamado Levi.
		<p><span class="numero">35)</span> E ela concebeu outra vez, e gerou um filho. E ela disse: Agora eu louvarei ao Senhor, por isso ela chamou o seu nome Judá, e parou de gerar.&nbsp;<a href="gn-30.php"><span class="proximo">Próximo capítulo</span></a></p>
