		<a href="gn.php"><h3>Gênesis 26</h3></a>
		<p><span class="numero">1)</span> E houve fome na terra, além da primeira fome que houve nos dias de Abraão. E Isaque foi a Abimeleque, rei dos filisteus, até Gerar.</p>
		<p><span class="numero">2)</span> E o Senhor apareceu a ele, e disse: Não desças ao Egito. Habita na terra que eu te direi.</p>
		<p><span class="numero">3)</span> Peregrina nesta terra, e eu serei contigo, e te abençoarei; pois a ti, e à tua semente, eu darei todas estas regiões. E eu farei o juramento que jurei a Abraão, teu pai,</p>
		<p><span class="numero">4)</span> e eu farei tua semente multiplicar como as estrelas do céu, e darei à tua semente todas estas regiões. E em tua semente serão abençoadas todas as nações da terra,</p>
		<p><span class="numero">5)</span> porque Abraão obedeceu à minha voz, e guardou minha ordem, meus mandamentos, meus estatutos e minhas leis.</p>
		<p><span class="numero">6)</span> E Isaque habitou em Gerar.</p>
		<p><span class="numero">7)</span> E os homens do lugar lhe perguntaram acerca de sua mulher, e ele disse: Ela é minha irmã, pois ele temia dizer: Ela é minha mulher; para que, dizia ele, os homens do lugar não me matem por causa de Rebeca, pois ela era formosa à vista.</p>
		<p><span class="numero">8)</span> E aconteceu que, quando ele já estava lá um longo tempo, Abimeleque, rei dos filisteus, olhou pela janela e viu, e eis que Isaque estava brincando com Rebeca, sua mulher.</p>
		<p><span class="numero">9)</span> E Abimeleque chamou Isaque, e disse: Eis que ela certamente é tua mulher. E como tu disseste: Ela é minha irmã? E Isaque lhe disse: Porque eu disse: Para que eu não morra por causa dela.</p>
		<p><span class="numero">10)</span> E Abimeleque disse: O que é isto que tu nos fizeste? Alguém do povo poderia facilmente ter deitado com tua mulher, e tu poderias ter trazido culpa sobre nós.</p>
		<p><span class="numero">11)</span> E Abimeleque ordenou a todo o seu povo, dizendo: Quem tocar neste homem ou em sua mulher certamente morrerá.</p>
		<p><span class="numero">12)</span> Então Isaque semeou naquela terra, e recebeu no mesmo ano cem vezes; e o Senhor o abençoou.</p>
		<p><span class="numero">13)</span> E o homem se engrandeceu e foi adiante e cresceu até ser muito grande.</p>
		<p><span class="numero">14)</span> Porque ele tinha posses de rebanhos, e posses de gado, e uma grande quantidade de servos; e os filisteus o invejaram.</p>
		<p><span class="numero">15)</span> Porquanto, todos os poços que os servos de seu pai haviam cavado nos dias de Abraão, seu pai, os filisteus haviam fechado e enchido com terra.</p>
		<p><span class="numero">16)</span> E Abimeleque disse a Isaque: Sai de nós, pois és muito mais poderoso do que nós.</p>
		<p><span class="numero">17)</span> E Isaque partiu dali, e armou sua tenda no vale de Gerar, e habitou ali.</p>
		<p><span class="numero">18)</span> E Isaque cavou novamente os poços de água que eles haviam cavado nos dias de Abraão, seu pai; pois os filisteus os haviam fechado depois da morte de Abraão. E ele chamou os seus nomes segundo os nomes pelos quais seu pai os havia chamado.</p>
		<p><span class="numero">19)</span> E os servos de Isaque cavaram no vale, e encontraram ali um poço de águas correntes.</p>
		<p><span class="numero">20)</span> E os pastores de Gerar contenderam com os pastores de Isaque, dizendo: A água é nossa. E ele chamou o nome do poço Eseque, porque contenderam com ele.</p>
		<p><span class="numero">21)</span> E eles cavaram um outro poço, e contenderam por aquele também, e chamou o nome dele Sitna.</p>
		<p><span class="numero">22)</span> E ele partiu dali e cavou outro poço, e por aquele eles não contenderam, e chamou o nome dele Reobote. E ele disse: Pois agora o Senhor fez um lugar para nós, e seremos frutíferos na terra.</p>
		<p><span class="numero">23)</span> E ele foi dali para Berseba.</p>
		<p><span class="numero">24)</span> E o Senhor apareceu a ele naquela mesma noite, e disse: Eu sou o Deus de Abraão, teu pai. Não temas, pois eu estou contigo, e te abençoarei, e multiplicarei tua semente por causa do meu servo Abraão.</p>
		<p><span class="numero">25)</span> E ele edificou um altar ali, e invocou o nome do Senhor, e armou sua tenda ali, e ali os servos de Isaque cavaram um poço.</p>
		<p><span class="numero">26)</span> Então, Abimeleque foi até ele de Gerar, com Ausate, um de seus amigos, e Ficol, o capitão-chefe de seu exército.</p>
		<p><span class="numero">27)</span> E Isaque lhes disse: Por que vindes a mim, visto que vós me odiais, e me enviastes de vós?</p>
		<p><span class="numero">28)</span> E eles disseram: Vimos que certamente o Senhor estava contigo, e dissemos: Que haja agora um juramento entre nós, entre nós e ti, e façamos um pacto contigo,</p>
		<p><span class="numero">29)</span> de que tu não nos farás mal, assim como não te tocamos, e assim como fizemos a ti, somente o bem, e te enviamos de nós em paz. Tu és agora o bendito do Senhor.</p>
		<p><span class="numero">30)</span> E ele lhes fez um banquete, e eles comeram e beberam.</p>
		<p><span class="numero">31)</span> E eles se levantaram cedo de manhã, e juraram um ao outro. E Isaque os despediu, e eles partiram dele em paz.</p>
		<p><span class="numero">32)</span> E aconteceu no mesmo dia que os servos de Isaque vieram, e lhe falaram com respeito ao poço que haviam cavado, e lhe disseram: Nós encontramos água.</p>
		<p><span class="numero">33)</span> E ele chamou-o Seba. Por isso o nome da cidade é Berseba até este dia.</p>
		<p><span class="numero">34)</span> E Esaú era da idade de quarenta anos quando ele tomou por mulher Judite, a filha de Beeri, o heteu, e Basemate, filha de Elom, o heteu,</p>
