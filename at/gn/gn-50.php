		<p><span class="numero">1)</span> E José caiu sobre a face de seu pai, e chorou sobre ele, e o beijou.</p>
		<p><span class="numero">2)</span> E José ordenou a seus servos, os médicos, que embalsamassem seu pai. E os médicos embalsamaram Israel.</p>
		<p><span class="numero">3)</span> E quarenta dias foram cumpridos para ele, porque assim são cumpridos os dias daqueles que são embalsamados. E os egípcios choraram por ele setenta dias.</p>
		<p><span class="numero">4)</span> E quando haviam passados os dias do seu luto, José falou à casa de Faraó, dizendo: Se agora eu encontrei graça aos vossos olhos, rogo-vos que faleis aos ouvidos de Faraó, dizendo:</p>
		<p><span class="numero">5)</span> Meu pai me fez jurar: Eis que morrerei. No meu túmulo que eu cavei para mim na terra de Canaã, ali tu me sepultarás. Por isso, permita-me que eu suba, rogo-te, e sepulte meu pai, e retornarei.</p>
		<p><span class="numero">6)</span> E Faraó disse: Sobe, e sepulta teu pai, de acordo com o que ele te fez jurar.</p>
		<p><span class="numero">7)</span> E José subiu para sepultar seu pai, e com ele subiram todos os servos de Faraó, os anciãos de sua casa, e todos os anciãos da terra do Egito,</p>
		<p><span class="numero">8)</span> e toda a casa de José, seus irmãos, e a casa de seu pai. Somente os seus pequenos, seus rebanhos e seu gado eles deixaram na terra de Gósen.</p>
		<p><span class="numero">9)</span> E subiram com ele tanto carruagens quanto cavaleiros; e era muito grande a comitiva.</p>
		<p><span class="numero">10)</span> E eles chegaram à eira de Atade, que está além do Jordão, e ali eles choraram com grande e triste pranto, e ele pranteou por seu pai durante sete dias.</p>
		<p><span class="numero">11)</span> E quando os habitantes da terra, os cananeus, viram o pranto na eira de Atade, eles disseram: Este é um pranto gravíssimo para os egípcios, pelo que o nome foi chamado Abel-Mizraim, que está além do Jordão.</p>
		<p><span class="numero">12)</span> E seus filhos lhe fizeram conforme ele lhes ordenara,</p>
		<p><span class="numero">13)</span> pois seus filhos o levaram à terra de Canaã, e o sepultaram na caverna do campo de Macpela, que Abraão comprou com o campo para possessão de um lugar de sepultamento de Efrom, o heteu, diante de Manre.</p>
		<p><span class="numero">14)</span> E José retornou ao Egito, ele, e seus irmãos, e todos que subiram com ele para sepultar seu pai, após ele ter sepultado seu pai.</p>
		<p><span class="numero">15)</span> E quando os irmãos de José viram que seu pai estava morto, eles disseram: José talvez nos odeie, e certamente exigirá de nós todo mal que lhe fizemos.</p>
		<p><span class="numero">16)</span> E eles enviaram um mensageiro a José dizendo: Teu pai ordenou antes de morrer, dizendo:</p>
		<p><span class="numero">17)</span> Assim direis a José: Perdoa, rogo-te agora, a transgressão de teus irmãos, e o pecado deles, pois eles te fizeram mal, e agora, rogamos- te, perdoa a transgressão dos servos do Deus de teu pai. E José chorou quando falaram com ele.</p>
		<p><span class="numero">18)</span> E seus irmãos também foram e caíram diante da face dele, e disseram: Eis que somos teus servos.</p>
		<p><span class="numero">19)</span> E José lhes disse: Não temais, pois estaria eu no lugar de Deus?</p>
		<p><span class="numero">20)</span> Mas quanto a vós, intentastes o mal contra mim, mas Deus intentou para o bem, para fazer como é neste dia, para salvar muitas pessoas com vida.</p>
		<p><span class="numero">21)</span> Por isso, não temais. Eu vos sustentarei, e a vossos pequenos. E ele os confortou, e falou com eles bondosamente.</p>
		<p><span class="numero">22)</span> E José habitou no Egito, ele, e a casa de seu pai. E José viveu cento e dez anos.</p>
		<p><span class="numero">23)</span> E José viu os filhos de Efraim da terceira geração. Também os filhos de Maquir, filho de Manassés, nasceram sobre os joelhos de José.</p>
		<p><span class="numero">24)</span> E José disse a seus irmãos: Eu morrerei, e Deus certamente vos visitará, e vos tirará desta terra para a terra que ele jurou a Abraão, a Isaque e a Jacó.</p>
		<p><span class="numero">25)</span> E José tomou um juramento dos filhos de Israel, dizendo: Deus certamente vos visitará, e vós levareis os meus ossos daqui.</p>
