		<a href="gn.php"><h3>Gênesis 19</h3></a>
		<p><span class="numero">1)</span> E vieram dois anjos a Sodoma à tarde; e Ló estava sentado no portão de Sodoma. E Ló, vendo-os, levantou-se para encontrá-los, e ele curvou-se com a sua face em direção a terra;</p>
		<p><span class="numero">2)</span> e ele disse: Eis, agora, meus senhores, entrai, rogo-vos, na casa de vosso servo, e ficai a noite toda, e lavai vossos pés, e levantareis cedo e ireis no vosso caminho. E eles disseram: Não! Nós permaneceremos na rua toda noite.</p>
		<p><span class="numero">3)</span> E insistiu com eles grandemente, e foram com ele, e entraram em sua casa; e ele lhes fez um banquete, e assou pão sem fermento, e eles comeram.</p>
		<p><span class="numero">4)</span> Mas antes que eles se deitassem, os homens da cidade, até os homens de Sodoma, rodearam a casa toda, tanto velhos como os jovens, todo o povo de cada quarteirão.</p>
		<p><span class="numero">5)</span> E eles chamaram Ló, e lhe disseram: Onde estão os homens que vieram a ti esta noite? Traze-os fora até nós, para que possamos conhecê-los.</p>
		<p><span class="numero">6)</span> E Ló saiu à porta até eles, e fechou a porta atrás de si;</p>
		<p><span class="numero">7)</span> e disse: Rogo-vos, irmãos, que não venhais a agir tão perversamente.</p>
		<p><span class="numero">8)</span> Eis que eu tenho duas filhas que não conheceram homem. Suplico-vos, deixai que eu as traga a vós, e fazei a elas o que for bom aos vossos olhos; porém a estes homens não façais nada; pois, eles vieram sob a sombra do meu telhado.</p>
		<p><span class="numero">9)</span> E eles disseram: Para trás. E eles disseram novamente: Este indivíduo veio aqui para peregrinar, e ele quer ser juiz; agora agiremos pior contigo do que com eles. E pressionaram severamente sobre o homem, sobre Ló, e chegaram perto de quebrar a porta.</p>
		<p><span class="numero">10)</span> Mas os homens estenderam suas mãos, e puxaram Ló para dentro da casa até eles, e fecharam a porta.</p>
		<p><span class="numero">11)</span> E eles feriram os homens que estavam à porta da casa com cegueira, tanto pequenos quanto grandes, de modo que se cansaram tentando achar a porta.</p>
		<p><span class="numero">12)</span> E os homens disseram a Ló: Tens mais alguém aqui? Genro, e teus filhos e tuas filhas e qualquer um que tiveres na cidade? Traze- os para fora deste lugar.</p>
		<p><span class="numero">13)</span> Porque destruiremos este lugar, porque o clamor deles tem subido diante da face do Senhor, e o Senhor nos enviou para destruí-lo.</p>
		<p><span class="numero">14)</span> E Ló saiu, e falou a seus genros, que haviam casado com suas filhas, e disse: Levantai- vos, saí deste lugar, porque o Senhor destruirá esta cidade. Mas ele parecia com alguém que zombava dos seus genros.</p>
		<p><span class="numero">15)</span> E quando a manhã levantou-se, então os anjos apressaram Ló, dizendo: Levanta-te, toma tua mulher, e tuas duas filhas, que estão aqui, para que não sejas consumido na iniquidade da cidade.</p>
		<p><span class="numero">16)</span> E enquanto ele demorava, os homens seguraram a sua mão e a mão de sua mulher, e a mão de suas duas filhas; o Senhor foi misericordioso com eles, e o tiraram, e o colocaram fora da cidade.</p> 
		<p><span class="numero">17)</span> E aconteceu que, quando os haviam trazido para fora dali, ele disse: Foge pela tua vida, não olhes para trás, nem fiques em toda esta planície; foge para o monte, para que não sejas consumido.</p>
		<p><span class="numero">18)</span> E Ló lhes disse: Oh! Assim não, meu Senhor;</p>
		<p><span class="numero">19)</span> eis que agora teu servo encontrou graça aos teus olhos, e tu magnificaste a tua misericórdia, que mostraste ao salvar a minha vida, e eu não posso fugir para o monte, para que o mal não me alcance, e eu morra.</p>
		<p><span class="numero">20)</span> Eis que esta cidade está próxima para fugir, e é pequena. Oh! Deixa que eu fuja para lá (não é pequena?) e minha alma viverá.</p>
		<p><span class="numero">21)</span> E ele lhe disse: Vê, aceitei-te também com respeito a esta coisa, que eu não derrubarei esta cidade, pela qual tu falaste.</p>
		<p><span class="numero">22)</span> Apressa-te, fuja para lá, pois eu não posso fazer coisa alguma até que tu chegues lá. Por isso se chamou o nome da cidade Zoar.</p>
		<p><span class="numero">23)</span> O sol havia se levantado sobre a terra quando Ló entrou em Zoar.
		<p><span class="numero">24)</span> Então, o Senhor fez chover sobre Sodoma e sobre Gomorra enxofre e fogo do Senhor desde o céu.</p>
		<p><span class="numero">25)</span> E ele derrubou aquelas cidades, e toda a planície, e todos os habitantes das cidades, e o que crescia sobre a terra.</p>
		<p><span class="numero">26)</span> Mas a sua mulher olhou para trás por detrás dele, e ela se tornou um pilar de sal.</p>
		<p><span class="numero">27)</span> E Abraão levantou-se cedo de manhã e foi para o lugar onde havia estado de pé diante do Senhor.</p>
		<p><span class="numero">28)</span> E ele olhou para Sodoma e Gomorra, e para toda a terra da planície, e eis que viu a fumaça da terra que subia como fumaça de uma fornalha.</p>
		<p><span class="numero">29)</span> E aconteceu que, quando Deus destruiu as cidades da planície, Deus lembrouse de Abraão e retirou Ló do meio da destruição, quando ele derrubou as cidades em que Ló habitara.</p>
		<p><span class="numero">30)</span> E Ló subiu de Zoar, e habitou no monte, e suas duas filhas com ele, pois ele temia habitar em Zoar; e ele habitou em uma caverna, ele e suas duas filhas.</p>
		<p><span class="numero">31)</span> E a primogênita disse à mais jovem: Nosso pai está velho, e não há homem na terra para entrar a nós, segundo a maneira de toda a terra;</p>
		<p><span class="numero">32)</span> vem, façamos nosso pai beber vinho, e deitaremos com ele, para que
		possamos preservar semente de nosso pai.</p>
		<p><span class="numero">33)</span> E elas fizeram seu pai beber vinho naquela noite; e a primogênita entrou e deitou com seu pai, e ele não percebeu quando ela deitou, nem quando ela se levantou.</p>
		<p><span class="numero">34)</span> E aconteceu que, no dia seguinte, a primogênita disse à mais jovem: Eis que	eu deitei com meu pai na noite passada; façamo- lo beber vinho esta noite também, e entra tu, e deita com ele, para que possamos preservar a semente de nosso pai.</p>
		<p><span class="numero">35)</span> E elas fizeram seu pai beber vinho naquela noite também; e a mais jovem se levantou, e deitou-se com ele, e ele não percebeu quando ela deitou, nem quando ela se levantou.</p>
		<p><span class="numero">36)</span> Assim, as duas filhas de Ló conceberam de seu pai.</p>
		<p><span class="numero">37)</span> E a primogênita deu à luz um filho, e chamou seu nome Moabe; este mesmo é o pai dos moabitas até este dia.</p>
