		<p><span class="numero">1)</span> E Jacó levantando os seus olhos, olhou, e eis que vinha Esaú, e com ele quatrocentos homens. E ele dividiu seus filhos entre Lia e entre Raquel, e entre as duas servas.</p>
		<p><span class="numero">2)</span> E ele colocou as servas e os filhos delas à frente, e Lia e seus filhos depois, e Raquel e José atrás.</p>
		<p><span class="numero">3)</span> E ele passou adiante deles, e se curvou na terra sete vezes, até chegar perto de seu irmão.</p>
		<p><span class="numero">4)</span> E Esaú correu para encontrá-lo, e o abraçou, e se lançou ao seu pescoço, e o beijou; e eles choraram.</p>
		<p><span class="numero">5)</span> E ele levantando os seus olhos, viu as mulheres e os filhos, e disse: Quem são estes contigo? E ele disse: Os filhos que Deus graciosamente deu a teu servo.</p>
		<p><span class="numero">6)</span> Então as servas se aproximaram, elas e seus filhos, e eles se curvaram.</p>
		<p><span class="numero">7)</span> E Lia também com seus filhos se aproximaram, e se curvaram; e depois se aproximaram José e Raquel, e eles se curvaram.</p>
		<p><span class="numero">8)</span> E ele disse: O que significa todo este rebanho que eu encontrei? E ele disse: Isto é para encontrar graça aos olhos de meu senhor.</p>
		<p><span class="numero">9)</span> E Esaú disse: Eu tenho o suficiente, meu irmão; guarda o que tu tens para ti mesmo.</p>
		<p><span class="numero">10)</span> E Jacó disse: Não! Rogo-te, se agora encontrei graça aos teus olhos, então recebe meu presente da minha mão, porque eu vi a tua face, como se tivesse visto a face de Deus, e tu tiveste contentamento comigo.</p>
		<p><span class="numero">11)</span> Toma, rogo-te, minha bênção que te trago; porque Deus agiu graciosamente comigo, e porque eu tenho o suficiente. E ele insistiu, e ele o tomou.</p>
		<p><span class="numero">12)</span> E ele disse: Partamos, vamos, e eu irei adiante de ti.</p>
		<p><span class="numero">13)</span> E ele lhe disse: Meu senhor sabe que os filhos são tenros, e os rebanhos e o gado com as crias estão comigo. E se os homens os afadigarem por um dia, todo o rebanho morrerá.</p>
		<p><span class="numero">14)</span> Que o meu senhor, rogo-te, passe adiante de seu servo; e eu seguirei calmamente, de acordo com o gado que vai adiante de mim e conforme o passo dos meninos, até que eu alcance o meu senhor em Seir.</p>
		<p><span class="numero">15)</span> E Esaú disse: Permite-me agora deixar contigo alguns do povo que estão comigo. E ele disse: Que necessidade tem? Permite-me encontrar graça aos olhos do meu senhor.</p>
		<p><span class="numero">16)</span> Assim Esaú retornou naquele dia no seu caminho para Seir.</p>
		<p><span class="numero">17)</span> E Jacó viajou para Sucote, e construiu para si uma casa, e fez habitações para seu gado; por isso o nome do lugar é chamado Sucote.</p>
		<p><span class="numero">18)</span> E Jacó veio a Salém, uma cidade de Siquém, que está na terra de Canaã, quando ele veio de Padã-Arã; e armou sua tenda diante da cidade.</p>
		<p><span class="numero">19)</span> E ele comprou uma parte de um campo, onde havia armado sua tenda, da mão dos filhos de Hamor, pai de Siquém, por cem peças de dinheiro.</p>
