		<a href="gn.php"><h3>Gênesis 32</h3></a>
		<p><span class="numero">1)</span> E Jacó foi no seu caminho, e os anjos de Deus o encontraram.</p>
		<p><span class="numero">2)</span> E quando Jacó os viu, ele disse: Este é o exército de Deus. E ele chamou o nome do lugar Maanaim.</p>
		<p><span class="numero">3)</span> E Jacó enviou mensageiros adiante dele a Esaú, seu irmão, à terra de Seir, região de Edom.</p>
		<p><span class="numero">4)</span> E ele lhes ordenou, dizendo: Assim falareis a meu senhor Esaú: Teu servo Jacó diz assim: Eu habitei como peregrino com Labão, e fiquei lá até agora.</p>
		<p><span class="numero">5)</span> E eu tenho bois, e jumentos, rebanhos, e servos, e servas. E eu enviei para dizer a meu senhor para que eu encontre graça aos seus olhos.</p>
		<p><span class="numero">6)</span> E os mensageiros retornaram a Jacó, dizendo: Nós chegamos ao teu irmão Esaú, e ele também vem para te encontrar, e quatrocentos homens com ele.</p>
		<p><span class="numero">7)</span> Então Jacó ficou muito amedrontado e angustiado, e ele dividiu em dois bandos o povo que estava com ele, e os rebanhos, e o gado, e os camelos,</p>
		<p><span class="numero">8)</span> e ele disse: Se Esaú vier a um bando e o ferir, então o outro bando que sobrar escapará.</p>
		<p><span class="numero">9)</span> E Jacó disse: Oh! Deus de meu pai Abraão, e Deus de meu pai Isaque, o Senhor que disse a mim: Torna à tua terra, à tua parentela, e eu te tratarei bem.</p>
		<p><span class="numero">10)</span> Eu não sou digno da menor de todas as misericórdias, e de toda a verdade, que tu tens mostrado ao teu servo, porque com meu cajado passei este Jordão, e agora eu me tornei dois bandos.</p>
		<p><span class="numero">11)</span> Livra-me, rogo-te, da mão de meu irmão, da mão de Esaú, porque eu o temo, para que ele não venha e me fira, e a mãe com os filhos.</p>
		<p><span class="numero">12)</span> E tu disseste: Eu certamente te farei bem, e farei tua semente como a areia do mar, que não pode ser enumerada por ser uma multidão.</p>
		<p><span class="numero">13)</span> E ele pernoitou ali aquela mesma noite, e tomou do que veio à sua mão por presente para Esaú, seu irmão:</p>
		<p><span class="numero">14)</span> duzentas cabras e vinte bodes, duzentas ovelhas e vinte carneiros,</p>
		<p><span class="numero">15)</span> trinta camelas de leite com suas crias, quarenta vacas e dez novilhos, vinte jumentas e dez jumentinhos.</p>
		<p><span class="numero">16)</span> E ele os entregou na mão de seus servos, cada rebanho à parte, e disse a seus servos: Passai adiante de mim, e deixai espaço entre rebanho e rebanho.</p>
		<p><span class="numero">17)</span> E ele ordenou ao primeiro, dizendo: Quando Esaú, meu irmão, te encontrar e te perguntar, dizendo: De quem és tu? E para onde vais? E de quem são estes diante de ti?</p>
		<p><span class="numero">18)</span> Então tu dirás: Eles são de teu servo Jacó. É um presente enviado ao meu senhor Esaú; e eis que ele também está atrás de nós.</p>
		<p><span class="numero">19)</span> E assim ele ordenou ao segundo, e ao terceiro, e a todos os que seguiram os rebanhos, dizendo: Desta maneira falareis a Esaú, quando o encontrardes.</p>
		<p><span class="numero">20)</span> E dizei além disso: Eis que teu servo Jacó está atrás de nós. Porque ele disse: Eu vou apaziguá- lo com o presente que vai adiante de mim, e depois eu verei a sua face, porventura ele me aceitará.</p>
		<p><span class="numero">21)</span> Assim foi o presente antes dele, e ele mesmo pernoitou aquela noite no acampamento.</p>
		<p><span class="numero">22)</span> E ele levantou-se naquela noite, e tomou suas duas mulheres, e suas duas servas, e seus onze filhos, e passou o vau de Jaboque.</p>
		<p><span class="numero">23)</span> E ele os tomou, e os enviou a passar o ribeiro, e enviou o que ele tinha.</p>
		<p><span class="numero">24)</span> E Jacó foi deixado só. E ali lutou com ele um homem até o romper do dia.</p>
		<p><span class="numero">25)</span> E quando este viu que não prevalecia contra ele, tocou a junta de sua coxa. E se desconjuntou a junta de sua coxa, enquanto lutava com ele.</p>
		<p><span class="numero">26)</span> E ele disse: Deixa-me ir, pois o dia já rompe. E ele disse: Eu não te deixarei ir, a não ser que me abençoes.</p>
		<p><span class="numero">27)</span> E ele lhe disse: Qual é o teu nome? E ele disse: Jacó.</p>
		<p><span class="numero">28)</span> E disse-lhe: Teu nome não será mais chamado Jacó, mas Israel, porque como um príncipe tu tens poder com Deus e com homens, e prevaleceste.</p>
		<p><span class="numero">29)</span> E Jacó lhe perguntou, e disse: Dize-me, rogo- te, teu nome. E ele disse: Por que é que tu perguntas o meu nome? E ele o abençoou ali.</p>
		<p><span class="numero">30)</span> E Jacó chamou o nome do lugar Peniel, pois eu vi a Deus face a face, e a minha vida foi preservada.</p>
		<p><span class="numero">31)</span> E quando ele passou por Peniel, o sol se levantou sobre ele, e ele manquejava da sua coxa.</p>
		<p><span class="numero">32)</span> Por isso os filhos de Israel não comem, até o dia de hoje, do nervo que está sobre a juntura da coxa, porque ele tocou a juntura da coxa de Jacó no tendão que se encolheu.&nbsp;<a href="gn-33.php"><span class="proximo">Próximo capítulo</span></a></p>
