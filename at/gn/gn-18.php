		<a href="gn.php"><h3>Gênesis 18</h3></a>
		<p><span class="numero">1)</span> E o Senhor apareceu a ele nas planícies de Manre, e ele sentou-se à porta da tenda no calor do dia.</p>
		<p><span class="numero">2)</span> E ele elevou seus olhos e olhou, e eis que três homens estavam em pé com ele; e quando ele os viu, ele correu da porta da tenda para encontrá-los, e se curvou em direção a terra,</p>
		<p><span class="numero">3)</span> e disse: Meus Senhores, se agora eu encontrei favor aos teus olhos, suplico-te que não passes de teu servo.</p>
		<p><span class="numero">4)</span> Peguem um pouco de água, peço-vos, e lavai os vossos pés e descansai debaixo da árvore.</p>
		<p><span class="numero">5)</span> E trarei um bocado de pão, e confortai os vossos corações; depois disso,passareis adiante, pois para isto vieste a vosso servo. E eles disseram: Faze como disseste.</p>
		<p><span class="numero">6)</span> E Abraão se apressou para dentro da tenda até Sara, e disse: Prepara depressa três medidas de farinha fina, amasse-a e faça bolos sobre a lareira.</p>
		<p><span class="numero">7)</span> E Abraão correu para o rebanho, e trouxe um novilho tenro e bom, e o deu a um jovem, e ele se apressou em prepará-lo.</p>
		<p><span class="numero">8)</span> E ele pegou manteiga, e leite, e o novilho que havia preparado, e o colocou diante deles; e ele ficou em pé junto deles debaixo da árvore, e eles comeram.</p>
		<p><span class="numero">9)</span> E eles lhe disseram: Onde está Sara, tua mulher? E ele disse: Eis que está na tenda.</p>
		<p><span class="numero">10)</span> E ele disse: Eu certamente retornarei a ti de acordo com o tempo da vida; e eis que Sara, tua mulher, terá um filho. E Sara o ouviu na porta da tenda, que estava atrás dele.</p>
		<p><span class="numero">11)</span> Ora, Abraão e Sara eram velhos e bem adiantados em idade, e cessou de estar com Sara a maneira das mulheres.</p>
		<p><span class="numero">12)</span> Por isso, Sara riu dentro de si, dizendo: Depois de tão envelhecida, eu terei prazer, e meu senhor sendo também velho?</p>
		<p><span class="numero">13)</span> E o Senhor disse a Abraão: Por que Sara riu, dizendo: É verdade que eu, que sou velha, gerarei uma criança?</p>
		<p><span class="numero">14)</span> Há alguma coisa difícil demais para o Senhor? No tempo determinado, eu retornarei a ti, de acordo com o tempo de vida, e Sara terá um filho.</p>
		<p><span class="numero">15)</span> Então Sara negou, dizendo: Eu não ri, pois ela estava com medo. E ele disse: Não, mas tu riste.</p>
		<p><span class="numero">16)</span> E os homens se levantaram dali, e olharam para Sodoma; e Abraão foi com eles para levá-los ao caminho.</p>
		<p><span class="numero">17)</span> E o Senhor disse: Eu ocultarei de Abraão as coisas que faço,</p>
		<p><span class="numero">18)</span> vendo que Abraão certamente se tornará uma nação grande e poderosa, e todas as nações da terra serão abençoadas nele?</p>
		<p><span class="numero">19)</span> Porque eu o conheço, que ele ordenará a seus filhos e sua casa depois dele, e eles guardarão o caminho do Senhor, para fazer justiça e juízo, para que o Senhor possa trazer sobre Abraão aquilo que dele tem falado.</p>
		<p><span class="numero">20)</span> E o Senhor disse: Porque o clamor de Sodoma e Gomorra é grande, e porque o seu pecado é muito grave,</p>
		<p><span class="numero">21)</span> eu descerei agora e verei se eles fizeram segundo o clamor que veio a mim, e se não, eu saberei.</p>
		<p><span class="numero">22)</span> E os homens voltaram as suas faces dali, e foram em direção a Sodoma, mas Abraão ainda estava em pé diante do Senhor.</p>
		<p><span class="numero">23)</span> E Abraão se aproximou e disse: Tu des- truirás também os justos com os ímpios?</p>
		<p><span class="numero">24)</span> Se porventura houver cinquenta justos na cidade, tu destruirás também e não pouparás o lugar por causa dos cinquenta justos que estão nela?</p>
		<p><span class="numero">25)</span> Esteja longe de ti fazer segundo essa maneira, matar os justos com os ímpios; e que os justos sejam como os ímpios, isso esteja longe de ti. Não fará justiça o Juiz de toda a terra?</p>
		<p><span class="numero">26)</span> E o Senhor disse: Se eu achar em Sodoma cinquenta justos na cidade, então eu pouparei todo o lugar por causa deles.</p>
		<p><span class="numero">27)</span> E Abraão respondeu e disse: Eis que agora resolvi falar ao Senhor, que sou somente pó e cinzas.</p>
		<p><span class="numero">28)</span> Se porventura faltarem cinco dos cinquenta justos, tu destruirás toda a cidade pela falta de cinco? E ele disse: Se eu achar ali quarenta e cinco, eu não a destruirei.</p>
		<p><span class="numero">29)</span> E falou-lhe mais uma vez e disse: Se porventura se acharem quarenta ali. E ele disse: Não o farei por causa dos quarenta.</p>
		<p><span class="numero">30)</span> E ele lhe disse: Oh! Não se ire o Senhor, e eu falarei: Se porventura se acharem trinta ali. E ele disse: Não o farei, se eu encontrar trinta ali.</p>
		<p><span class="numero">31)</span> E ele disse: Eis que agora ousei falar ao Senhor: Se porventura houver vinte ali. E ele disse: Não a destruirei por causa dos vinte.</p>
		<p><span class="numero">32)</span> E ele disse: Oh! Não se ire o Senhor, e ainda eu falarei somente esta vez. Se porventura se encontrarem dez ali. E ele disse: Não a destruirei por causa dos dez.</p>
