		<p><span class="numero">1)</span> E aconteceu, depois destas coisas, que alguém contou a José: Eis que teu pai está enfermo. E ele tomou seus dois filhos, Manassés e Efraim.</p>
		<p><span class="numero">2)</span> E alguém contou a Jacó: Eis que teu filho José vem a ti. E Israel se fortaleceu, e sentou- se sobre a sua cama.</p>
		<p><span class="numero">3)</span> E Jacó disse a José: O Deus Todo- Poderoso me apareceu em Luz, na terra de Canaã, e me abençoou,</p>
		<p><span class="numero">4)</span> e me disse: Eis que eu te farei frutificar e te multiplicarei, e farei de ti uma multidão de povos, e darei esta terra à tua semente depois de ti por possessão eterna.</p>
		<p><span class="numero">5)</span> E agora teus dois filhos, Efraim e Manassés, que te nasceram na terra do Egito, antes de eu vir a ti no Egito, são meus. Assim como Rúben e Simeão, eles serão meus.</p>
		<p><span class="numero">6)</span> Mas a tua descendência, que gerares depois deles, serão teus, e serão chamados segundo o nome de teus irmãos na sua herança.</p>
		<p><span class="numero">7)</span> Quanto a mim, quando eu vim de Padã, Raquel morreu junto a mim na terra de Canaã, no caminho, quando ainda havia somente um pequeno caminho para chegar a Efrata. E eu a sepultei ali no caminho de Efrata; esta é Belém.</p>
		<p><span class="numero">8)</span> E Israel viu os filhos de José, e disse: Quem são estes?
		<p><span class="numero">9)</span> E José disse a seu pai: Estes são meus filhos, que Deus me deu neste lugar. E ele disse: Traze-os a mim, rogo-te, e eu os abençoarei.</p>
		<p><span class="numero">10)</span> Ora, os olhos de Israel estavam escurecidos pela idade, de modo que ele não podia ver. E ele os levou para perto dele; e ele os beijou, e os abraçou.</p>
		<p><span class="numero">11)</span> E Israel disse a José: Eu não havia pensado em ver a tua face, e eis que Deus me mostrou também tua semente.</p>
		<p><span class="numero">12)</span> E José os tirou dentre os seus joelhos, e ele se curvou com sua face em terra.</p>
		<p><span class="numero">13)</span> E José tomou os dois, Efraim em sua mão direita, em direção à esquerda de Israel, e Manassés na sua mão esquerda, em direção à direita de Israel, e os levou para perto dele.</p>
		<p><span class="numero">14)</span> E Israel estendeu sua mão direita, e a colocou sobre a cabeça de Efraim, que era o mais jovem, e sua mão esquerda sobre a cabeça de Manassés, guiando suas mãos conscientemente, pois Manassés era o primogênito.</p>
		<p><span class="numero">15)</span> E ele abençoou José, e disse: Deus, diante do qual andaram meus pais Abraão e Isaque, o Deus que me sustentou toda a minha longa vida até este dia, </p>
		<p><span class="numero">16)</span> o anjo que me redimiu de todo o mal, abençoe os rapazes; e permita que meu nome seja colocado neles, e o nome de meus pais Abraão e Isaque; e que os faça crescer em uma multidão no meio da terra.</p>
		<p><span class="numero">17)</span> E quando José viu que o seu pai colocou sua mão direita sobre a cabeça de Efraim, isso o desagradou. E ele levantou a mão de seu pai, para removê-la de sobre a cabeça de Efraim para a cabeça de Manassés.</p>
		<p><span class="numero">18)</span> E José disse a seu pai: Assim não, meu pai, pois este é o primogênito; põe tua mão direita sobre a cabeça dele.</p>
		<p><span class="numero">19)</span> E seu pai se recusou, e disse: Eu sei, meu filho, eu sei. Ele também se tornará um povo, e ele também será grande. Mas em verdade seu irmão mais jovem será maior do que ele, e sua semente se tornará uma multidão de nações.</p>
		<p><span class="numero">20)</span> E ele os abençoou naquele dia, dizendo: Em ti abençoará Israel, dizendo: Deus te faça como Efraim e Manassés, e ele colocou Efraim diante de Manassés.</p>
		<p><span class="numero">21)</span> E Israel disse a José: Eis que eu morro, mas Deus estará convosco, e vos levará novamente à terra de vossos pais.</p>
		<p><span class="numero">22)</span> Além disso, eu tenho dado a ti uma porção a mais que a teus irmãos, que eu tirei da mão dos amorreus com a minha espada e com o meu arco.&nbsp;<a href="gn-49.php"><span class="proximo">Próximo capítulo</span></a></p>
