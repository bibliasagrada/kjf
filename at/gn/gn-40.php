		<p><span class="numero">1)</span> E aconteceu, depois destas coisas, que o mordomo do rei do Egito e seu padeiro haviam ofendido seu senhor e rei do Egito.</p>
		<p><span class="numero">2)</span> E Faraó estava irado contra dois de seus oficiais, contra o chefe dos mordomos, e contra o chefe dos padeiros.</p>
		<p><span class="numero">3)</span> E ele colocou-os em custódia, na casa do capitão da guarda, na prisão, o lugar em que José estava preso.</p>
		<p><span class="numero">4)</span> E o capitão da guarda encarregou José deles, e ele os serviu, e eles estiveram um período na prisão.</p>
		<p><span class="numero">5)</span> E ambos sonharam um sonho, cada homem seu sonho em uma noite, cada homem de acordo com a interpretação do seu sonho, o mordomo e o padeiro do rei do Egito, que estavam presos na prisão.</p>
		<p><span class="numero">6)</span> E José veio a eles de manhã, e olhou para eles, e eis que eles estavam tristes.</p>
		<p><span class="numero">7)</span> E ele perguntou aos oficiais de Faraó, que estavam com ele na prisão, da casa de seu senhor, dizendo: Por que aparentais tão tristes hoje?</p>
		<p><span class="numero">8)</span> E lhes disseram: Sonhamos um sonho, e não há quem o interprete. E José lhes disse: Não pertencem as interpretações a Deus? Diga-me eles, rogo-vos.</p>
		<p><span class="numero">9)</span> E o chefe dos mordomos contou seu sonho a José, e lhe disse: No meu sonho, eis que uma videira estava diante de mim,</p>
		<p><span class="numero">10)</span> e na videira estavam três ramos; e era como se estivesse brotando, e sua flor saía, e os seus cachos produziram uvas maduras.</p>
		<p><span class="numero">11)</span> E o copo de Faraó estava na minha mão, e eu apanhei as uvas, e as espremi dentro do copo de Faraó, e dei o copo na mão de Faraó.</p>
		<p><span class="numero">12)</span> E José lhes disse: Esta é a sua interpretação: Os três ramos são três dias.</p>
		<p><span class="numero">13)</span> Mas em três dias Faraó levantará a tua cabeça, e te restabelecerá ao teu lugar, e tu servirás o copo de Faraó nas mãos dele, conforme a maneira antiga quando tu eras seu mordomo.</p></p>
		<p><span class="numero">14)</span> Mas lembra-te de mim quando estiver bem contigo, e mostra bondade, rogote, para comigo, e faze menção de mim a Faraó, e tira-me desta casa,</p>
		<p><span class="numero">15)</span> pois na verdade eu fui roubado da terra dos hebreus, e aqui também não fiz nada para que eles me pusessem na masmorra.</p>
		<p><span class="numero">16)</span> Quando o chefe dos padeiros viu que a interpretação era boa, ele disse a José: Eu também estava no meu sonho, e eis que eu tinha três cestos brancos sobre minha cabeça,</p>
		<p><span class="numero">17)</span> e no cesto mais alto havia todo tipo de pão para Faraó; e as aves os comiam do cesto sobre a minha cabeça.</p>
		<p><span class="numero">18)</span> E José respondeu e disse: Esta é a sua interpretação: Os três cestos são três dias.</p>
		<p><span class="numero">19)</span> Mas em três dias Faraó levantará tua cabeça de sobre ti, e te pendurará em uma árvore, e as aves comerão a tua carne de sobre ti.</p>
		<p><span class="numero">20)</span> E aconteceu que, no terceiro dia, era o aniversário de Faraó, e ele fez uma festa para todos os seus servos; e ele levantou a cabeça do chefe dos mordomos e do chefe dos padeiros entre seus servos.</p>
		<p><span class="numero">21)</span> E ele restabeleceu o chefe dos mordomos a seu ofício de mordomo novamente; e ele serviu o copo na mão de Faraó.</p>
		<p><span class="numero">22)</span> Mas ele enforcou o chefe dos padeiros, como José havia lhes interpretado.</p>
