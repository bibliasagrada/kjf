		<a href="gn.php"><h3>Gênesis 44</h3></a>
		<p><span class="numero">1)</span> E ele ordenou ao administrador de sua casa, dizendo: Enche os sacos dos homens com alimento, tanto quanto eles puderem carregar, e põe o dinheiro de cada homem na boca de seu saco.</p>
		<p><span class="numero">2)</span> E põe o meu copo, o copo de prata, na boca do saco do mais jovem, e o dinheiro do seu trigo. E ele fez conforme a palavra que José havia falado.</p>
		<p><span class="numero">3)</span> Assim que raiou a luz da manhã, os homens foram enviados, eles e seus jumentos.</p>
		<p><span class="numero">4)</span> E quando haviam saído da cidade, e ainda não estavam distantes, José disse a seu administrador: Levanta-te, segue os homens, e quando os alcançares, dizelhes: Por que pagastes o bem com o mal? </p>
		<p><span class="numero">5)</span> Isso não é o que o meu senhor usa para beber, e pelo qual ele adivinha? Fizestes mal em fazer isso.</p>
		<p><span class="numero">6)</span> E ele os alcançou e lhes falou as mesmas palavras.</p>
		<p><span class="numero">7)</span> E eles lhe disseram: Por que meu senhor diz estas palavras? Deus proíbe que teus servos façam conforme esta coisa.</p>
		<p><span class="numero">8)</span> Eis que o dinheiro que encontramos na boca dos nossos sacos trouxemos novamente a ti da terra de Canaã. Como então nós roubaríamos da casa de teu senhor prata ou ouro?</p>
		<p><span class="numero">9)</span> Aquele dos teus servos com quem for achado, que morra, e nós também seremos escravos de meu senhor.</p>
		<p><span class="numero">10)</span> E ele disse: Então seja agora de acordo com vossas palavras. Aquele com quem for encontrado será meu escravo, e vós estareis sem culpa.</p>
		<p><span class="numero">11)</span> Então eles se apressaram em descer, cada homem pôs em terra o seu saco, e cada homem abriu o seu saco.</p>
		<p><span class="numero">12)</span> E ele procurou, e começou pelo mais velho, e terminou com o mais jovem, e o copo foi encontrado no saco de Benjamim.</p>
		<p><span class="numero">13)</span> Então eles rasgaram suas vestes, e carregaram cada homem os seus jumentos, e voltaram à cidade.</p>
		<p><span class="numero">14)</span> E Judá e seus irmãos vieram à casa de José, pois ele ainda estava ali; e eles prostraram- se em terra diante dele.</p>
		<p><span class="numero">15)</span> E José lhes disse: Que ato é este que fizestes? Não sabeis que um homem como eu pode certamente adivinhar?</p>
		<p><span class="numero">16)</span> E Judá disse: O que diremos a meu senhor? O que falaremos? Ou como nos inocentaremos? Deus descobriu a iniquidade de teus servos. Eis que somos servos de meu senhor, tanto nós quanto aquele com quem também foi achado o copo.</p>
		<p><span class="numero">17)</span> E ele disse: Deus proíbe que eu faça isso, mas o homem em cuja mão foi achado o copo, este será meu servo. E quanto a vós, levantai- vos e voltai em paz ao vosso pai.</p>
		<p><span class="numero">18)</span> Então Judá se aproximou dele, e disse: Ó meu senhor, deixai que teu servo, suplico-te, fale uma palavra aos ouvidos de meu senhor, e não deixai que a tua ira se acenda contra teu servo, porque tu és como o próprio Faraó.</p>
		<p><span class="numero">19)</span> Meu senhor perguntou a seus servos, dizendo: Tendes ainda um pai, ou um irmão?</p>
		<p><span class="numero">20)</span> E nós dissemos a meu senhor: Temos um pai, um homem velho, e um filho da sua velhice, um pequeno. E seu irmão está morto, e só ele foi deixado de sua mãe, e seu pai o ama.</p>
		<p><span class="numero">21)</span> E tu disseste a teus servos: Trazei-o a mim, para que eu possa colocar meus olhos nele.</p>
		<p><span class="numero">22)</span> E dissemos ao meu senhor: O rapaz não pode deixar seu pai, pois se ele deixar seu pai, seu pai morrerá.</p>
		<p><span class="numero">23)</span> E tu disseste a teus servos: Se vosso irmão mais jovem não vier, não vereis mais a minha face.</p>
		<p><span class="numero">24)</span> E aconteceu que, quando nós subimos a teu servo, meu pai, dissemos-lhe as palavras de meu senhor.</p>
		<p><span class="numero">25)</span> E nosso pai disse: Ide novamente, e comprai- nos um pouco de alimento.</p>
		<p><span class="numero">26)</span> E dissemos: Não podemos descer. Se o nosso irmão mais jovem for conosco, aí desceremos, porque não podemos ver a face do homem, se nosso irmão mais jovem não estiver conosco.</p>
		<p><span class="numero">27)</span> E teu servo, meu pai, disse-nos: Sabeis que minha mulher me deu dois filhos;</p>
		<p><span class="numero">28)</span> e um se foi de mim, e eu disse: Ele certamente foi despedaçado, e eu não o vi desde então.</p>
		<p><span class="numero">29)</span> E se tirardes também este de mim, e um mal acontecer a ele, levareis meus cabelos grisalhos com tristeza à sepultura.</p>
		<p><span class="numero">30)</span> Agora, pois, quando eu for a teu servo, meu pai, e o rapaz não estiver conosco, visto que a sua vida está atada com a vida dele,</p>
		<p><span class="numero">31)</span> acontecerá, quando ele vir que o rapaz não está conosco, ele morrerá, e teus servos levarão, com tristeza à sepultura, os cabelos grisalhos de teu servo, nosso pai.</p>
		<p><span class="numero">32)</span> Porquanto, teu servo se tornou fiador do rapaz diante do meu pai, dizendo: Se eu não o trouxer a ti, então eu levarei a culpa diante do meu pai para sempre.</p>
		<p><span class="numero">33)</span> Agora, pois, suplico-te: Deixa teu servo ficar no lugar do rapaz como servo para o meu senhor, e deixa o rapaz subir com seus irmãos.</p>
		<p><span class="numero">34)</span> Como, pois, eu subirei a meu pai, e o rapaz não estando comigo? Para que porventura eu não veja o mal que virá sobre o meu pai.&nbsp;<a href="gn-45.php"><span class="proximo">Próximo capítulo</span></a></p>
