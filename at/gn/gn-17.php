		<a href="gn.php"><h3>Gênesis 17</h3></a>
		<p><span class="numero">1)</span> E quando Abrão era da idade de noventa e nove anos, o Senhor apareceu a Abrão, e lhe disse: Eu sou o Deus Todo- Poderoso; anda diante de mim, e sê perfeito.</p>
		<p><span class="numero">2)</span> E eu farei o meu pacto entre mim e ti, e multiplicar- te-ei excessivamente.</p>
		<p><span class="numero">3)</span> E Abrão caiu sobre a sua face, e Deus falou com ele, dizendo:</p>
		<p><span class="numero">4)</span> Quanto a mim, eis que o meu pacto é contigo, e tu serás um pai de muitas nações.</p>
		<p><span class="numero">5)</span> O teu nome não se chamará mais Abrão, mas teu nome será Abraão, pois pai de muitas nações eu te fiz.</p>
		<p><span class="numero">6)</span> E eu te farei extremamente fértil, e farei nações de ti, e reis sairão de ti.</p>
		<p><span class="numero">7)</span> E eu estabelecerei o meu pacto entre mim e ti, e tua semente depois de ti nas suas gerações, para um pacto eterno, para ser um Deus para ti, e para tua semente depois de ti.</p>
		<p><span class="numero">8)</span> E eu darei a ti, e para a tua semente depois de ti, a terra em que és estrangeiro, toda a terra de Canaã, para possessão eterna, e eu serei seu Deus.</p>
		<p><span class="numero">9)</span> E Deus disse a Abraão: Portanto, tu guardarás o meu pacto, tu, e tua semente depois de ti nas suas gerações.</p>
		<p><span class="numero">10)</span> Este é o meu pacto, que guardareis, entre mim e vós e tua semente depois de ti: Todo filho homem entre vós será circuncidado.</p>
		<p><span class="numero">11)</span> E vós circuncidareis a carne do vosso prepúcio, e será um sinal do pacto entre mim e vós.</p>
		<p><span class="numero">12)</span> E aquele que tem oito dias será circuncidado entre vós, todo filho homem nas vossas gerações, aquele que é nascido em casa, ou comprado com dinheiro de algum estrangeiro, que não é da tua semente.</p>
		<p><span class="numero">13)</span> Aquele que é nascido em tua casa, e aquele que é comprado com teu dinheiro deverá ser circuncidado; e meu pacto estará na vossa carne como um pacto eterno.</p>
		<p><span class="numero">14)</span> E o homem incircunciso cuja carne do seu prepúcio não for circuncidada, esta alma será cortada de seu povo; ele quebrou o meu pacto.</p>
		<p><span class="numero">15)</span> E Deus disse a Abraão: Quanto a Sarai, tua mulher, não chamarás seu nome Sarai, mas Sara será seu nome.</p>
		<p><span class="numero">16)</span> E eu a abençoarei, e te darei também um filho dela; e a abençoarei, e ela será uma mãe de nações; reis de povos virão dela.</p>
		<p><span class="numero">17)</span> Então Abraão caiu sobre sua face e riu, e disse no seu coração: Nascerá um filho àquele que tem cem anos de idade? E gerará Sara, com noventa anos de idade?</p>
		<p><span class="numero">18)</span> E Abraão disse a Deus: Que Ismael possa viver diante de ti!</p>
		<p><span class="numero">19)</span> E Deus disse: Sara, tua mulher, de fato te gerará um filho, e tu chamarás seu nome Isaque. E eu estabelecerei o meu pacto com ele como pacto eterno, e com sua semente depois dele.</p>
		<p><span class="numero">20)</span> E quanto a Ismael, eu te ouvi: Eis que o tenho abençoado, e o farei frutífero, e o multiplicarei excessivamente; doze príncipes ele gerará, e eu farei dele uma grande nação.</p>
		<p><span class="numero">21)</span> Mas, o meu pacto eu estabelecerei com Isaque, que Sara te gerará neste tempo	determinado, no próximo ano.</p>
		<p><span class="numero">22)</span> E deixou de falar com ele, e Deus retirou- se de Abraão.</p>
		<p><span class="numero">23)</span> E Abraão tomou Ismael, seu filho, e todos os que haviam nascido em sua casa, e todos os que haviam sido comprados com seu dinheiro, todo homem entre os homens da casa de Abraão, e circuncidou a carne de seu prepúcio no mesmo dia, como Deus lhe havia dito.</p>
		<p><span class="numero">24)</span> E Abraão era da idade de noventa e nove anos, quando ele foi circuncidado na	carne de seu prepúcio.</p>
		<p><span class="numero">25)</span> E Ismael, seu filho, era da idade de treze anos quando ele foi circuncidado na carne de seu prepúcio.</p>
		<p><span class="numero">26)</span> No mesmo dia foi circuncidado Abraão e seu filho Ismael.</p>
