		<a href="gn.php"><h3>Gênesis 46</h3></a>
		<p><span class="numero">1)</span> E Israel iniciou sua jornada com tudo que ele tinha, e veio a Berseba, e ofereceu sacrifícios ao Deus de seu pai Isaque.</p>
		<p><span class="numero">2)</span> E Deus falou a Israel em visões da noite, e disse: Jacó, Jacó. E ele disse: Aqui estou.</p>
		<p><span class="numero">3)</span> E ele disse: Eu sou Deus, o Deus de teu pai; não temas em descer ao Egito, porque lá eu farei de ti uma grande nação.</p>
		<p><span class="numero">4)</span> Eu descerei contigo ao Egito, e eu também certamente te trarei de lá novamente. E José colocará a sua mão sobre os teus olhos.</p>
		<p><span class="numero">5)</span> E Jacó levantou-se de Berseba, e os filhos de Israel carregaram Jacó, seu pai, e seus pequenos, e suas mulheres, nas carroças que Faraó havia enviado para carregá-lo.</p>
		<p><span class="numero">6)</span> E eles tomaram seu gado, e seus bens, que haviam obtido na terra de Canaã, e vieram ao Egito, Jacó e toda a sua semente com ele;</p>
		<p><span class="numero">7)</span> seus filhos, e os filhos de seus filhos com ele, suas filhas, e as filhas de seus filhos, e toda a sua semente ele trouxe consigo para o Egito.</p>
		<p><span class="numero">8)</span> E estes são os nomes dos filhos de Israel, que vieram ao Egito, Jacó e seus filhos: Rúben, o primogênito de Jacó,</p>
		<p><span class="numero">9)</span> e os filhos de Rúben: Enoque, e Palu, e Hezrom, e Carmi.</p>
		<p><span class="numero">10)</span> E os filhos de Simeão: Jemuel, e Jamim, e Oade, e Jaquim, e Zoar, e Saul, filho de uma mulher cananeia.</p>
		<p><span class="numero">11)</span> E os filhos de Levi: Gérson, Coate e Merari.</p>
		<p><span class="numero">12)</span> E os filhos de Judá: Er, e Onã, e Selá, e Perez, e Zerá; mas Er e Onã morreram na terra de Canaã; e os filhos de Perez foram Hezrom e Hamul.</p>
		<p><span class="numero">13)</span> E os filhos de Issacar: Tola, e Puva, e Jó, e Sinrom.</p>
		<p><span class="numero">14)</span> E os filhos de Zebulom: Serede, e Elom, e Jaleel.</p>
		<p><span class="numero">15)</span> Estes são os filhos de Lia, que ela deu a Jacó em Padã-Arã, com Diná, sua filha; todas as almas de seus filhos e de suas filhas foram trinta e três.</p>
		<p><span class="numero">16)</span> E os filhos de Gade: Zifiom, e Hagi, e Suni, e Esbom, e Eri, e Arodi, e Areli.</p>
		<p><span class="numero">17)</span> E os filhos de Aser: Imna, e Isvá, e Isvi, e Berias, e Sera, irmã deles; e os filhos de Berias: Héber e Malquiel.</p>
		<p><span class="numero">18)</span> Estes são os filhos de Zilpa, a qual Labão deu à sua filha Lia; e ela deu estes a Jacó, dezesseis almas.</p>
		<p><span class="numero">19)</span> Os filhos de Raquel, mulher de Jacó: José e Benjamim.</p>
		<p><span class="numero">20)</span> E nasceram a José, na terra do Egito, Manassés e Efraim, que lhe deu Azenate, filha de Potífera, sacerdote de Om.</p>
		<p><span class="numero">21)</span> E os filhos de Benjamim foram Belá, e Bequer, e Asbel, e Gera, e Naamã, e Eí, e Rôs, e Mupim, e Hupim, e Arde.</p>
		<p><span class="numero">22)</span> Estes são os filhos de Raquel, que nasceram a Jacó, ao todo foram catorze almas.</p>
		<p><span class="numero">23)</span> E o filho de Dã: Husim.</p>
		<p><span class="numero">24)</span> E os filhos de Naftali: Jazeel, e Guni, e Jezer, e Silém.</p>
		<p><span class="numero">25)</span> Estes são os filhos de Bila, a qual Labão deu à sua filha Raquel; e ela deu estes a Jacó; todas as almas foram sete.</p>
		<p><span class="numero">26)</span> Todas as almas que vieram com Jacó ao Egito, que saíram de seus lombos, fora as mulheres dos filhos de Jacó, todas as almas foram sessenta e seis.</p>
		<p><span class="numero">27)</span> E os filhos de José, que lhe nasceram no Egito, foram duas almas. Todas as almas da casa de Jacó, que vieram ao Egito, foram setenta.</p>
		<p><span class="numero">28)</span> E ele enviou Judá adiante até José, para direcionar sua face para Gósen, e eles vieram à terra de Gósen.</p>
		<p><span class="numero">29)</span> E José preparou sua carruagem, e subiu para encontrar Israel, seu pai, em Gósen, e se apresentou a ele, e se lançou ao seu pescoço, e chorou ao seu pescoço por um bom tempo.</p>
		<p><span class="numero">30)</span> E Israel disse a José: Agora, deixa-me morrer, pois eu vi a tua face, porque tu estás vivo ainda.</p>
		<p><span class="numero">31)</span> E José disse a seus irmãos, e à casa de seu pai: Eu subirei, e anunciarei a Faraó, para dizer- lhe: Meus irmãos e a casa de meu pai, que estavam na terra de Canaã, vieram a mim;</p>
		<p><span class="numero">32)</span> e os homens são pastores, pois seu trabalho tem sido apascentar gado, e eles trouxeram seus rebanhos, e seu gado, e tudo que eles tem.</p>
		<p><span class="numero">33)</span> E acontecerá que, quando Faraó vos chamar, e lhes disser: Qual é a vossa ocupação?</p>
		<p><span class="numero">34)</span> Vós direis: O negócio de teus servos tem sido com gado desde a nossa juventude até agora, tanto a nossa, como também de nossos pais, para que possais habitar a terra de Gósen, pois todo pastor é uma abominação para os egípcios.&nbsp;<a href="gn-47.php"><span class="proximo">Próximo capítulo</span></a></p>
