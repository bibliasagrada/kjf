		<p><span class="numero">1)</span> E Sara tinha cento e vinte e sete anos de idade; estes foram os anos da vida de Sara.</p>
		<p><span class="numero">2)</span> E Sara morreu em Quiriate-Arba, que é Hebrom, na terra de Canaã; e Abraão veio lamentar por Sara, e chorar por ela.</p>
		<p><span class="numero">3)</span> E Abraão levantou-se de diante de seu corpo, e falou aos filhos de Hete, dizendo:</p>
		<p><span class="numero">4)</span> Eu sou um estrangeiro e peregrino convosco; dai-me a posse de um lugar de sepultamento convosco, para que eu possa sepultar a minha falecida de diante da minha vista.</p>
		<p><span class="numero">5)</span> E os filhos de Hete responderam a Abraão, dizendo-lhe:</p>
		<p><span class="numero">6)</span> Ouve-nos, meu senhor; tu és um príncipe poderoso entre nós; na escolha dos nossos sepulcros, sepulta a tua falecida; ninguém de nós reterá de ti seu sepulcro, para que possas sepultar a tua falecida.</p>
		<p><span class="numero">7)</span> E Abraão levantou-se e curvou-se diante do povo da terra, aos filhos de Hete.</p>
		<p><span class="numero">8)</span> E falou com eles, dizendo: Se é de vossa vontade que eu sepulte a minha falecida distante da minha vista, ouvi-me e intercedei por mim a Efrom, filho de Zoar;</p>
		<p><span class="numero">9)</span> para que ele possa me dar a caverna de Macpela, que ele tem, que está na extremidade do seu campo; pois não importa o preço que custe, ele ma dará como posse para lugar de sepultamento entre vós.</p>
		<p><span class="numero">10)</span> E Efrom habitava entre os filhos de Hete, e Efrom, o heteu, respondeu a Abraão aos ouvidos dos filhos de Hete, de todos os que entravam pela porta da cidade, dizendo:</p>
		<p><span class="numero">11)</span> Não, meu senhor, ouve-me: O campo eu te dou, e a caverna que está nele, eu a dou para ti; na presença dos filhos do meu povo eu te dou; sepulta a tua falecida.</p>
		<p><span class="numero">12)</span> E Abraão curvou-se diante do povo da terra.</p>
		<p><span class="numero">13)</span> E ele falou a Efrom aos ouvidos do povo da terra, dizendo: Mas se tu o deres, te suplico, ouve-me: Dar-te-ei dinheiro pelo campo; toma- o de mim, e eu sepultarei ali a minha falecida.
		<p><span class="numero">14)</span> E Efrom respondeu a Abraão, dizendo lhe:</p>
		<p><span class="numero">15)</span> Meu senhor, ouve-me, a terra vale quatrocentos siclos de prata; o que é isso entre mim e ti? Por isso, sepulta tua falecida.</p>
		<p><span class="numero">16)</span> E Abraão ouviu a Efrom; e Abraão pesou a prata para Efrom, da qual ele tinha falado aos ouvidos dos filhos de Hete, quatrocentos siclos de prata, dinheiro corrente entre os mercadores.</p>
		<p><span class="numero">17)</span> E o campo de Efrom, que estava em Macpela, que estava diante de Manre, o campo e a caverna que estava nele, e todas as árvores que estavam no campo, que estavam em todas as extremidades ao redor, foram confirmados,</p>
		<p><span class="numero">18)</span> para Abraão como possessão na presença dos filhos de Hete, diante de todos os que entravam no portão da cidade.</p>
		<p><span class="numero">19)</span> E depois disso, Abraão sepultou Sara, sua mulher, na caverna do campo de Macpela, diante de Manre, que é Hebrom na terra de Canaã.</p>
