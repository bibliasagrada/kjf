		<a href="gn.php"><h3>Gênesis 22</h3></a>
		<p><span class="numero">1)</span> E aconteceu depois destas coisas, que Deus tentou Abraão, e lhe disse: Abraão; e ele disse: Eis-me aqui.</p>
		<p><span class="numero">2)</span> E ele disse: Toma agora o teu filho, teu único filho Isaque, a quem tu amas, e vai para a terra de Moriá, e oferece-o ali como oferta queimada sobre um dos montes que eu te direi.</p>
		<p><span class="numero">3)</span> E Abraão levantou-se cedo de manhã e selou seu jumento, e tomou consigo dois de seus servos, e Isaque, seu filho, e cortou a lenha para a oferta queimada, e se levantou e foi para o lugar que Deus lhe dissera.</p>
		<p><span class="numero">4)</span> Então, no terceiro dia, Abraão levantou seus olhos, e viu o lugar de longe.</p>
		<p><span class="numero">5)</span> E Abraão disse aos seus servos: Ficai aqui com o jumento, e eu e o menino vamos adiante para adorar, e voltaremos a vós.</p>
		<p><span class="numero">6)</span> E Abraão pegou a lenha da oferta queimada, e a colocou sobre Isaque, seu filho; e ele tomou o fogo em sua mão, e uma faca; e foram os dois juntos.</p>
		<p><span class="numero">7)</span> E Isaque falou a Abraão, seu pai, e disse: Meu pai, e ele disse: Aqui estou, meu filho. E ele disse: Eis o fogo e a lenha, mas onde está o cordeiro para a oferta queimada?
		<p><span class="numero">8)</span> E Abraão disse: Meu filho, Deus proverá para si um cordeiro para a oferta queimada; então foram os dois juntos.</p>
		<p><span class="numero">9)</span> E eles chegaram ao lugar de que Deus lhe dissera; e Abraão construiu ali um altar, e pôs em ordem a lenha, e amarrou Isaque, seu filho, e o pôs no altar sobre a lenha.</p>
		<p><span class="numero">10)</span> E Abraão estendeu sua mão, e tomou a faca para imolar seu filho.</p>
		<p><span class="numero">11)</span> E o anjo do Senhor o chamou do céu e disse: Abraão, Abraão; e ele disse: Aqui estou.</p>
		<p><span class="numero">12)</span> E ele disse: Não ponhas a tua mão sobre o menino, nem faças alguma coisa com ele. Porque agora eu sei que temes a Deus, vendo que não negaste a mim teu filho, teu único filho.</p>
		<p><span class="numero">13)</span> E Abraão levantou seus olhos, e olhou, e eis detrás dele um carneiro, preso pelos chifres em um arbusto; e Abraão foi e tomou o carneiro, e o ofereceu como oferta queimada no lugar de seu filho.</p>
		<p><span class="numero">14)</span> E Abraão chamou o nome daquele lugar: Javé-Jiré, como se diz até este dia: No monte do Senhor ele será visto.</p>
		<p><span class="numero">15)</span> E o anjo do Senhor chamou a Abraão do céu uma segunda vez,</p>
		<p><span class="numero">16)</span> e disse: Por mim mesmo jurei, diz o Senhor, pois porque tu fizeste tal coisa, e não negaste teu filho, teu único filho;</p>
		<p><span class="numero">17)</span> em bênção eu te abençoarei, e em multiplicação eu multiplicarei tua semente como as estrelas do céu, e como a areia que está sobre a beira do mar; e a tua semente possuirá o portão dos seus inimigos;</p>
		<p><span class="numero">18)</span> e em tua semente todas as nações da terra serão abençoadas, porque tu obedeceste à minha voz.</p>
		<p><span class="numero">19)</span> Então Abraão voltou aos seus servos, e eles se levantaram e foram juntos a Berseba; e Abraão habitou em Berseba.</p>
		<p><span class="numero">20)</span> E depois destas coisas, comunicaram a Abraão, dizendo: Eis que Milca, ela também gerou filhos a teu irmão Naor.</p>
		<p><span class="numero">21)</span> Uz, seu primogênito, e Buz, seu irmão, e Quemuel, o pai de Arã,</p>
		<p><span class="numero">22)</span> e Quésede, e Hazo, e Pildas, e Jidlafe, e Betuel.</p>
		<p><span class="numero">23)</span> E Betuel gerou Rebeca; estes oito Milca deu a Naor, irmão de Abraão.</p>
		<p><span class="numero">24)</span> E a sua concubina, cujo nome era Reumá, lhe gerou também Tebá, e Gaã, e Taás, e Maaca.&nbsp;<a href="gn-23.php"><span class="proximo">Próximo capítulo</span></a></p>
