		<p><span class="numero">3)</span> Mas Deus veio a Abimeleque em um sonho à noite, e lhe disse: Eis que és nada mais que um homem morto, pela mulher que tomaste, pois ela é a mulher de um homem.</p>
		<p><span class="numero">4)</span> Mas Abimeleque não havia se aproximado dela. E ele disse: Senhor, tu matarás também uma nação justa?</p>
		<p><span class="numero">5)</span> Não me disse ele: Ela é minha irmã? E ela também disse: Ele é meu irmão. Na integridade de meu coração e na inocência das minhas mãos eu fiz isso.</p>
		<p><span class="numero">6)</span> E Deus lhe disse em um sonho: Sim, eu sei que o fizeste na integridade de teu coração; pois também eu te impedi de pecar contra mim; por isso não te permiti tocá-la.</p>
		<p><span class="numero">7)</span> Agora, portanto, restitui ao homem sua mulher, pois ele é um profeta, e ele orará por ti, e tu viverás; e se tu não a restituíres, sabe que certamente morrerás, tu e todos os que são teus.</p>
		<p><span class="numero">8)</span> Por isso, Abimeleque levantou-se cedo de manhã, e chamou todos os seus servos, e contou todas estas coisas em seus ouvidos. E os homens temeram muito.</p>
		<p><span class="numero">9)</span> Então Abimeleque chamou Abraão, e lhe disse: O que nos fizeste? E em que eu te ofendi, para que trouxesses sobre mim e sobre meu reino um grande pecado? Tu fizeste- me coisas que não deviam ser feitas.</p>
		<p><span class="numero">10)</span> E Abimeleque disse a Abraão: O que tu viste para fazeres tal coisa?</p>
		<p><span class="numero">11)</span> E Abraão disse: Porque pensei: Certamente o temor de Deus não está neste lugar, e eles me matarão por causa de minha mulher.</p>
		<p><span class="numero">12)</span> E, na verdade, ela é minha irmã; ela é a filha de meu pai, mas não a filha de minha mãe; e ela se tornou minha mulher.</p>
		<p><span class="numero">13)</span> E aconteceu que, quando Deus me fez peregrinar desde a casa de meu pai, eu disse a ela: Esta é a bondade que tu me mostrarás: em todo lugar aonde chegarmos, dirás de mim: Ele é meu irmão.</p>
		<p><span class="numero">14)</span> E Abimeleque tomou ovelhas, e bois, e servos, e servas, e os deu a Abraão, e lhe restituiu Sara, sua mulher.</p>
		<p><span class="numero">15)</span> E Abimeleque disse: Eis que a minha terra está diante de ti; habita onde te agradar.</p>
		<p><span class="numero">16)</span> E a Sara ele disse: Eis que eu dei a teu irmão mil peças de prata. Servirão de honra para ti, para todos os que estão contigo e com todos os outros; assim ela foi repreendida.</p>
		<p><span class="numero">17)</span> Então Abraão orou a Deus; e Deus curou Abimeleque, e sua mulher, e suas servas; e elas geraram filhos.</p>
