		<a href="gn.php"><h3>Gênesis 47</h3></a>
		<p><span class="numero">1)</span> Então, José veio e contou a Faraó, e disse: Meu pai e meus irmãos, e seus rebanhos, e seu gado, e tudo que eles possuem vieram da terra de Canaã; e eis que eles estão na terra de Gósen.</p>
		<p><span class="numero">2)</span> E ele tomou alguns de seus irmãos, cinco homens, e os apresentou a Faraó.</p>
		<p><span class="numero">3)</span> E Faraó disse a seus irmãos: Qual é a vossa ocupação? E eles disseram a Faraó: Teus servos são pastores, tanto nós como também nossos pais.</p>
		<p><span class="numero">4)</span> Disseram mais a Faraó: Viemos para peregrinar nesta terra, porque teus servos não têm pastagem para seus rebanhos. Porque a fome é dolorida na terra de Canaã, por isso, suplicamos-te agora, deixa que teus servos habitem na terra de Gósen.</p>
		<p><span class="numero">5)</span> E Faraó falou a José, dizendo: Teu pai e teus irmãos vieram a ti.</p>
		<p><span class="numero">6)</span> A terra do Egito está diante de ti; no melhor da terra faze teu pai e teus irmãos habitar; na terra de Gósen, deixa-os habitar. E se tu conheces algum homem de atividade entre eles, então faze-os responsáveis por meu gado.</p>
		<p><span class="numero">7)</span> E José trouxe Jacó, seu pai, e o colocou diante de Faraó, e Jacó abençoou Faraó.</p>
		<p><span class="numero">8)</span> E Faraó disse a Jacó: Quantos anos tu tens?</p>
		<p><span class="numero">9)</span> E Jacó disse a Faraó: Os dias da minha peregrinação são cento e trinta anos. Poucos e maus têm sido os dias dos anos da minha vida, e não alcançaram aos dias dos anos da vida de meus pais nos dias de suas peregrinações.</p>
		<p><span class="numero">10)</span> E Jacó abençoou Faraó, e saiu da presença de Faraó.</p>
		<p><span class="numero">11)</span> E José estabeleceu seu pai e seus irmãos, e lhes deu posses na terra do Egito, no melhor da terra, na terra de Ramessés, como Faraó ordenara.</p>
		<p><span class="numero">12)</span> E José sustentou seu pai, e seus irmãos, e toda a casa de seu pai com pão, de acordo com suas famílias.</p>
		<p><span class="numero">13)</span> E não havia pão em toda a terra, porque a fome era muito dolorida, de modo que a terra do Egito e toda a terra de Canaã desfaleciam por causa da fome.</p>
		<p><span class="numero">14)</span> E José ajuntou todo o dinheiro que foi encontrado na terra do Egito, e na terra de Canaã, pelo trigo que eles compravam, e José levou o dinheiro para a casa de Faraó.</p>
		<p><span class="numero">15)</span> E quando o dinheiro acabou na terra do Egito, e na terra de Canaã, todos os egípcios vieram a José, e disseram: Dá-nos pão, por que morreremos na tua presença? Pois o dinheiro acabou.</p>
		<p><span class="numero">16)</span> E José disse: Dai o vosso gado, e eu vos darei pelo vosso gado, se não há dinheiro.</p>
		<p><span class="numero">17)</span> E eles levaram seu gado a José, e José lhes deu pão em troca pelos cavalos, e pelos rebanhos, e pelo gado dos rebanhos, e pelos jumentos. E durante aquele ano ele os alimentou em troca de todo o seu gado.</p>
		<p><span class="numero">18)</span> Quando aquele ano terminou, vieram a ele no segundo ano, e lhe disseram: Não ocultaremos a meu senhor que o nosso dinheiro acabou. Meu senhor também tem nossos rebanhos de gado. Nada nos sobrou diante da vista do meu senhor, exceto nossos corpos e nossas terras.</p>
		<p><span class="numero">19)</span> Por que morreremos diante dos teus olhos, tanto nós, como a nossa terra? Compra a nós e a nossa terra por pão, e nós e nossa terra seremos servos de Faraó, e dá- nos semente, para que possamos viver, e não morrer, para que a terra não seja desolada.</p>
		<p><span class="numero">20)</span> E José comprou toda a terra do Egito para Faraó, porque os egípcios venderam, cada homem, o seu campo, porque a fome prevalecia sobre eles. Assim, a terra passou a ser de Faraó.</p>
		<p><span class="numero">21)</span> E quanto ao povo, ele os removeu para as cidades, desde uma extremidade das fronteiras do Egito até a outra extremidade.</p>
		<p><span class="numero">22)</span> Somente a terra dos sacerdotes ele não comprou, pois os sacerdotes possuíam uma porção atribuída por Faraó, e comiam a sua porção que Faraó lhes dava; por isso, eles não venderam suas terras.</p>
		<p><span class="numero">23)</span> Então José disse a seu povo: Eis que neste dia comprei a vós e a vossa terra para Faraó. Vede, aqui há semente para vós, e semeareis a terra.</p>
		<p><span class="numero">24)</span> E acontecerá que, das colheitas, dareis uma quinta parte a Faraó, e quatro partes serão vossas, para semear o campo, e para vosso alimento, e para os da vossa família, e para alimento para os seus pequenos.</p>
		<p><span class="numero">25)</span> E eles disseram: Tu salvastes as nossas vidas. Que encontremos graça à vista de meu senhor, e nós seremos servos de Faraó.</p>
		<p><span class="numero">26)</span> E José fez esta lei sobre a terra do Egito até este dia, que Faraó deveria ter a quinta parte, exceto a terra dos sacerdotes, que não se tornou de Faraó.</p>
		<p><span class="numero">27)</span> E Israel habitou na terra do Egito, na região de Gósen; e eles tinham posses ali, e cresceram, e se multiplicaram grandemente.</p>
		<p><span class="numero">28)</span> E Jacó viveu na terra do Egito por dezessete anos. Assim, toda a idade de Jacó foi cento e quarenta e sete anos.</p>
		<p><span class="numero">29)</span> E chegando-se o tempo que Israel devia morrer. Ele chamou seu filho José, e lhe disse: Se agora eu encontrei graça à tua vista, rogo-te que ponhas tua mão debaixo da minha coxa, e age com bondade e verdade para comigo: Não me enterres, rogo-te, no Egito,</p>
		<p><span class="numero">30)</span> mas quando eu descansar com meus pais, tu me levarás para fora do Egito, e me enterrarás no lugar de sepultamento deles. E ele disse: Eu farei como tu disseste.</p>
