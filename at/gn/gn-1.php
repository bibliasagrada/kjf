		<a href="gn.php"><h3>Gênesis 1</h3></a>
		<p><span class="numero">1) </span> No princípio Deus criou o céu e a terra.</p>
		<p><span class="numero">2) </span> E a terra era sem forma e vazia; e trevas estava sobre a face do abismo. E o Espirito de Deus se movia sobre a face das aguas.</p>
		<p><span class="numero">3) </span> E disse Deus: Haja luz; e houve luz.</p>
		<p><span class="numero">4) </span> E viu Deus a luz, que isto era bom; e Deus separou a luz das trevas.</p>
		<p><span class="numero">5) </span> E chamou Deus a luz Dia, e as trevas ele chamou Noite. E houve a tarde e a manha, o primeiro dia.</p>
		<p><span class="numero">6) </span> E disse Deus: Haja um firmamento no meio das aguas, e deixe que separe as aguas das aguas.</p>
		<p><span class="numero">7) </span> E fez Deus o firmamento, e separou as aguas que estavam debaixo do firmamento das aguas que estavam acima do firmamento. E assim foi.</p>
		<p><span class="numero">8) </span> E Deus chamou ao firmamento Céu. E houve a tarde e a manha, o segundo dia.</p>
		<p><span class="numero">9) </span> E disse Deus: Ajuntem-se as aguas sob o céu em um lugar, e apareça a terra seca. E assim foi.</p>
		<p><span class="numero">10) </span> E chamou Deus a terra seca Terra; e ao ajuntamento das aguas ele chamou Mares. E Deus viu que isto era bom.</p>
		<p><span class="numero">11) </span> E disse Deus: Produza a terra, a relva, a erva que de semente, e a arvore frutífera que de fruto segundo a sua espécie, cuja semente esteja nela, sobre a terra. E assim foi.</p>
		<p><span class="numero">12) </span> E a terra produziu a relva, e a erva que dava semente segundo a sua espécie, e a arvore frutífera, cuja semente estava nela, segundo a sua espécie. E Deus viu que isto era bom.</p>
		<p><span class="numero">13) </span> E houve a tarde e a manhã, o terceiro dia.</p>
		<p><span class="numero">14) </span> E disse Deus: Haja luzes no firmamento do céu para separar o dia da noite; e que sejam por sinais, e para estações, e para dias, e anos;</p>
		<p><span class="numero">15) </span> e que eles sejam por luzes no firmamento do céu para dar luz sobre a terra. E assim foi.</p>
		<p><span class="numero">16) </span> E fez Deus duas grandes luzes; a luz maior para governar o dia, e a luz menor para governar a noite; ele também fez as estrelas.</p>
		<p><span class="numero">17) </span> E Deus os colocou no firmamento do céu para dar luz sobre a terra;</p>
		<p><span class="numero">18) </span> e para governar sobre o dia e sobre a noite, e para separar a luz das trevas, e Deus viu que isto era bom.</p>
		<p><span class="numero">19) </span> E houve a tarde e a manha, o quarto dia.</p>
		<p><span class="numero">20) </span> E disse Deus: Produzam as aguas abundantemente criaturas viventes que se movem, e aves que possam voar acima da terra, no vasto firmamento do céu.</p>
		<p><span class="numero">21) </span> E Deus criou grandes baleias, e toda criatura vivente que se move, que as aguas produziram abundantemente, segundo a sua espécie, e toda ave alada segundo a sua espécie; e Deus viu que isto era bom.</p>
		<p><span class="numero">22) </span> E Deus os abençoou, dizendo: Sede frutíferos e multiplicai-vos, e enchei as aguas nos mares, e multipliquem-se as aves sobre a terra.</p>
		<p><span class="numero">23) </span> E houve a tarde e a manha, o quinto dia.</p>
		<p><span class="numero">24) </span> E disse Deus: Produza a terra criaturas viventes segundo as suas espécies, gado, e seres rastejantes, e animais da terra segundo a sua espécie. E assim foi.</p>
		<p><span class="numero">25) </span> E fez Deus os animais da terra segundo a sua espécie, e o gado segundo a sua espécie e tudo que rasteja sobre a terra segundo a sua espécie; e Deus viu que isto era bom.</p>
		<p><span class="numero">26) </span> E disse Deus: Façamos o homem a nossa imagem, conforme a nossa semelhança; e que eles tenham domínio sobre os peixes do mar, e sobre as aves do céu, e sobre o gado, e sobre toda a terra, e sobre toda a coisa rastejante que rasteja sobre a terra.</p>
		<p><span class="numero">27) </span> Assim Deus criou o homem em sua própria imagem, a imagem de Deus o criou; macho e fêmea ele os criou.</p>
		<p><span class="numero">28) </span> E Deus os abençoou, e Deus lhes disse: Sede frutíferos e multiplicai-vos, e enchei a terra e subjugai-a; e tende domínio sobre os peixes do mar, e sobre as aves do céu, e sobre toda a coisa vivente que se move sobre a terra.</p>
		<p><span class="numero">29) </span> E disse Deus: Eis que vos tenho dado toda erva que da semente, que esta sobre a face de toda a terra, e toda arvore na qual esta o fruto de uma arvore que produz semente; para vós será para alimento.</p>
		<p><span class="numero">30) </span> E a todo animal da terra, e a toda ave do céu, e a cada coisa que rasteja sobre a terra, em que há vida, eu tenho dado toda erva verde para alimento. E assim foi.</p>
		<p><span class="numero">31) </span> E Deus viu todas as coisas que ele havia feito; e eis que era muito bom. E houve a tarde e a manha, o sexto dia.&nbsp;<a href="gn-2.php"><span class="proximo">Próximo capítulo</span></a></p>
