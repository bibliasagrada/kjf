		<a href="gn.php"><h3>Gênesis 6</h3></a>
		<p><span class="numero">1)</span> E aconteceu que, quando os homens começaram a se multiplicar sobre a face da terra, e filhas lhes nasceram,</p>
		<p><span class="numero">2)</span> os filhos de Deus viram que as filhas dos homens eram belas; e tomaram para si esposas de todas que escolheram.</p>
		<p><span class="numero">3)</span> E o Senhor disse: Meu espírito não contenderá sempre com o homem, pois ele também é carne. Porém, seus dias serão cento e vinte anos.</p>
		<p><span class="numero">4)</span> Havia gigantes na terra naqueles dias, e também depois disso, quando os filhos de Deus entraram às filhas dos homens, e elas lhes geraram filhos, estes se tornaram homens poderosos que eram na antiguidade, homens de renome.</p>
		<p><span class="numero">5)</span> E Deus viu que a maldade do homem era grande na terra, e que toda a imaginação dos pensamentos do seu coração era apenas vil continuamente.</p>
		<p><span class="numero">6)</span> E arrependeu-se o Senhor de haver feito o homem na terra, e isso o afligia em seu coração.</p>
		<p><span class="numero">7)</span> E o Senhor disse: Eu destruirei o homem a quem criei da face da terra; tanto o homem quanto o animal, e a coisa rastejante, e as aves do céu; pois me arrependi de havê- los feito.</p>
		<p><span class="numero">8)</span> Mas Noé encontrou graça aos olhos do Senhor.</p>
		<p><span class="numero">9)</span> Estas são as gerações de Noé: Noé foi um homem justo e perfeito nas suas gerações, e Noé andava com Deus.</p>
		<p><span class="numero">10)</span> E Noé gerou três filhos: Sem, Cam e Jafé.</p>
		<p><span class="numero">11)</span> A terra também estava corrompida diante de Deus, e a terra estava cheia de violência.</p>
		<p><span class="numero">12)</span> E Deus olhou para a terra, e eis que ela estava corrompida, pois toda a carne havia corrompido seu caminho sobre a terra.</p>
		<p><span class="numero">13)</span> E Deus disse a Noé: O fim de toda a carne chegou diante de mim; pois a terra está cheia de violência por meio deles; e eis que eu os destruirei com a terra.</p>
		<p><span class="numero">14)</span> Faze para ti uma arca de madeira de gofer; farás compartimentos na arca e a betumarás por dentro e por fora com betume.</p>
		<p><span class="numero">15)</span> E esta é a forma em que tu a farás: O comprimento da arca será trezentos côvados; a sua largura de cinquenta côvados; e a sua altura de trinta côvados.</p>
		<p><span class="numero">16)</span> Uma janela farás para a arca, e em um côvado a acabarás em cima; e a porta da arca colocarás em sua lateral, faze-as com andares, inferior, um segundo e um terceiro.</p>
		<p><span class="numero">17)</span> E eis que eu mesmo trago um dilúvio de águas sobre a terra, para destruir toda a carne em que há sopro de vida de debaixo do céu. E toda coisa que está na terra morrerá.</p>
		<p><span class="numero">18)</span> Mas contigo eu estabelecerei o meu pacto; e tu entrarás na arca, tu e teus filhos, e tua mulher, e as mulheres de teus filhos contigo.</p>
		<p><span class="numero">19)</span> E de toda coisa vivente de toda a carne, dois de cada espécie, tu trarás para dentro da arca, para guardá-los vivos contigo; eles serão macho e fêmea.</p>
		<p><span class="numero">20)</span> De aves segundo a sua espécie, e de gado segundo a sua espécie, de toda coisa rastejante da terra segundo a sua espécie, dois de cada espécie virão a ti, para guardá- los vivos.</p>
		<p><span class="numero">21)</span> E tomarás para ti de todo alimento que é comido, e o ajuntarás a ti, e será por alimento para ti, e para eles.</p>
