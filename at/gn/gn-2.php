		<p><span class="numero">6) </span> Mas ali subia uma neblina da terra, e regava toda a face da terra.</p>
		<p><span class="numero">7) </span> E o Senhor Deus formou o homem do pó da terra, e soprou nas suas narinas o sopro da vida; e o homem se tornou uma alma vivente.</p>
		<p><span class="numero">8) </span> E o Senhor Deus plantou um jardim na direção leste no Éden; e ali ele colocou o homem a quem havia formado.</p>
		<p><span class="numero">9) </span> E da terra o Senhor Deus fez crescer toda árvore que é agradável à vista, e boa para alimento; e também a árvore da vida no meio do jardim, e a árvore do conhecimento do bem e do mal.</p>
		<p><span class="numero">10) </span> E um rio saía do Éden para regar o jardim; e dali partia-se, e tornava-se quatro cabeças.</p>
		<p><span class="numero">11) </span> O nome do primeiro é Pisom; este é o que circunda toda a terra de Ávila, onde há ouro;</p>
		<p><span class="numero">12) </span> e o ouro dessa terra é bom; ali há bdélio e a pedra ônix.</p>
		<p><span class="numero">13) </span> E o nome do segundo rio é Giom; esse é o mesmo que circunda toda a terra da Etiópia.</p>
		<p><span class="numero">14) </span> E o nome do terceiro rio é Tigre; este é o que vai para o leste da Assíria. E o quarto rio é o Eufrates.</p>
		<p><span class="numero">15) </span> E o Senhor Deus tomou o homem, e o colocou no jardim do Éden para cultivá-lo e guardá-lo.</p>
		<p><span class="numero">16) </span> E o Senhor Deus ordenou ao homem, dizendo: De toda árvore do jardim tu poderás comer livremente;</p>
		<p><span class="numero">17) </span> mas da árvore do conhecimento do bem e do mal, dela tu não comerás. Pois no dia em que dela comeres, tu certamente morrerás.</p>
		<p><span class="numero">18) </span> E o Senhor Deus disse: Não é bom que o homem esteja sozinho; eu farei uma ajudadora adequada para ele.</p>
		<p><span class="numero">19) </span> E da terra o Senhor Deus formou todo animal do campo, e toda ave do ar; e os levou até Adão para ver como ele lhes chamaria. E como quer que Adão chamasse cada criatura vivente, este era o seu nome.</p>
		<p><span class="numero">20) </span> E Adão deu nomes a todo o gado, e a toda ave do céu, e a todo animal do campo; mas para Adão não foi encontrada uma ajudadora adequada.</p>
		<p><span class="numero">21) </span> E o Senhor Deus fez um profundo sono cair sobre Adão, e ele dormiu; e ele tomou uma de suas costelas, e fechou a carne em seu lugar;</p>
		<p><span class="numero">22) </span> e da costela que o Senhor Deus havia tirado do homem, ele fez uma mulher, e a levou ao homem.</p>
		<p><span class="numero">23) </span> E Adão disse: Esta agora é osso dos meus ossos, e carne da minha carne; ela será chamada Mulher, porque ela foi tomada de dentro do Homem.</p>
		<p><span class="numero">24) </span> Por isso, o homem deixará seu pai e sua mãe e se apegará a sua mulher, e eles serão uma carne.</p>
