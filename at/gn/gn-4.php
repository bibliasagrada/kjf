		<p><span class="numero">1)</span> E Adão conheceu Eva, sua mulher; e ela concebeu e teve Caim, e disse:	Concebi um homem do Senhor.</p>
		<p><span class="numero">2)</span> E ela também teve Abel, irmão dele. E Abel era um guardador de ovelhas, mas Caim era cultivador da terra.</p>
		<p><span class="numero">3)</span> E no passar do tempo, aconteceu que Caim trouxe do fruto da terra uma oferta ao Senhor.</p>
		<p><span class="numero">4)</span> E Abel, ele também trouxe das primícias e da gordura do seu rebanho. E o Senhor teve consideração por Abel e por sua oferta;</p>
		<p><span class="numero">5)</span> mas por Caim e por sua oferta ele não teve consideração. E Caim ficou muito irado, e o seu semblante caiu.</p>
		<p><span class="numero">6)</span> E o Senhor disse a Caim: Por que estás irado? E por que o teu semblante está caído?</p>
		<p><span class="numero">7)</span> Se tu fazes bem, não serás aceito? E se não fazes bem, o pecado jaz à porta. E para ti será o desejo dele, e tu governará sobre ele.</p>
		<p><span class="numero">8)</span> E Caim falou com Abel, seu irmão; e aconteceu que, quando eles estavam no	campo, Caim se levantou contra Abel, seu irmão, e o matou.</p>
		<p><span class="numero">9)</span> E o Senhor disse a Caim: Onde está Abel, teu irmão? E ele disse: Eu não sei; sou eu guardador do meu irmão?</p>
		<p><span class="numero">10)</span>  E ele disse: O que tu fizeste? A voz do sangue de teu irmão está clamando a mim desde a terra.</p>
		<p><span class="numero">11)</span>  E agora tu és amaldiçoado desde a terra, que abriu a sua boca para receber o sangue do teu irmão da tua mão;</p>
		<p><span class="numero">12)</span>  quando tu cultivares a terra, ela não te dará mais a sua força; fugitivo e errante serás na terra.</p>
		<p><span class="numero">13)</span>  E Caim disse ao Senhor: Meu castigo é maior do que eu posso suportar.</p>
		<p><span class="numero">14)</span>  Eis que tu me expulsaste neste dia da face da terra; e de tua face eu estarei escondido; e serei fugitivo e errante na terra. E acontecerá que todo aquele que me encontrar me matará.</p>
		<p><span class="numero">15)</span>  E o Senhor lhe disse: Portanto, todo aquele que matar Caim, a vingança será tomada sobre ele sete vezes. E o Senhor fixou uma marca sobre Caim, para que qualquer que o achasse não o matasse.</p>
		<p><span class="numero">16)</span>  E Caim saiu da presença do Senhor, e habitou na terra de Node, no leste do Éden.</p>
		<p><span class="numero">17)</span>  E Caim conheceu sua mulher, e ela concebeu, e teve Enoque; e ele edificou uma cidade, e chamou o nome da cidade de Enoque, conforme o nome do seu filho.</p>
		<p><span class="numero">18)</span>  E a Enoque nasceu Irade, e Irade gerou Meujael, e Meujael gerou Metusael, e Metusael gerou Lameque.</p>
		<p><span class="numero">19)</span>  E Lameque tomou para si duas esposas. O nome de uma era Ada, e o nome da outra Zilá.</p>
		<p><span class="numero">20)</span>  E Ada teve Jabal; ele foi o pai dos que habitam em tendas, e dos que têm gado.</p>
		<p><span class="numero">21)</span>  E o nome de seu irmão era Jubal. Ele foi o pai de todos os que manuseiam a harpa e o órgão.</p>
		<p><span class="numero">22)</span>  E Zilá, ela também teve Tubalcaim, um instrutor de todo artífice de bronze e ferro; e a irmã de Tubalcaim era Naamá.</p>
		<p><span class="numero">23)</span>  E Lameque disse a suas mulheres, Ada e Zilá: Ouvi a minha voz, mulheres de Lameque, escutai as minhas palavras, pois eu matei um homem pela minha ferida e um jovem pelo meu sofrimento.</p>
		<p><span class="numero">24)</span>  Se Caim for vingado sete vezes, Lameque certamente setenta vezes sete.</p>
		<p><span class="numero">25)</span>  E Adão conheceu novamente sua mulher; e ela teve um filho, e chamou seu	nome Sete, porque Deus, ela disse, me designou outra semente no lugar de Abel,</p>
		a quem Caim assassinou.</p>
