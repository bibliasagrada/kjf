		<p><span class="numero">1)</span> E Deus lembrou de Noé, e de toda coisa vivente, e de todo o gado que estava com ele na arca; e Deus fez um vento passar sobre a terra, e as águas se diminuíram.</p>
		<p><span class="numero">2)</span> Também as fontes do abismo e as janelas do céu foram fechadas, e a chuva do céu foi contida;</p>
		<p><span class="numero">3)</span> e as águas retornaram de sobre a terra continuamente; e após o fim dos cento e cinquenta dias as águas foram diminuídas.</p>
		<p><span class="numero">4)</span> E a arca descansou no sétimo mês, no décimo sétimo dia do mês, sobre os
		montes de Ararate.</p>
		<p><span class="numero">5)</span> E as águas diminuíram continuamente até o décimo mês; no décimo mês, no
		primeiro dia do mês, foram vistos os topos dos montes.</p>
		<p><span class="numero">6)</span> E aconteceu que, ao fim de quarenta dias, Noé abriu a janela da arca que ele fizera.</p>
		<p><span class="numero">7)</span> E ele enviou um corvo, que saindo, ia e voltava, até secar as águas de sobre a terra.</p>
		<p><span class="numero">8)</span> Ele também enviou uma pomba, para ver se as águas haviam diminuído da
		face da terra;</p>
		<p><span class="numero">9)</span> mas a pomba não encontrou descanso para a sola de seu pé, e ela retornou para ele na arca, pois as águas estavam sobre a face de toda a terra. Então, ele estendeu sua mão e a tomou, e a puxou para si para dentro da arca.</p>
		<p><span class="numero">10)</span> E ele ficou mais outros sete dias, e novamente enviou a pomba para fora da arca,</p>
		<p><span class="numero">11)</span> e a pomba veio a ele à tarde, e eis que no seu bico estava uma folha de
		oliveira arrancada. Assim Noé soube que as águas haviam diminuído de sobre a terra.</p>
		<p><span class="numero">12)</span> E ele ficou mais outros sete dias, e enviou a pomba, que não mais retornou a ele.</p>
		<p><span class="numero">13)</span> E aconteceu, no seiscentésimo primeiro ano, no primeiro mês, no primeiro dia do mês, que as águas foram secas de sobre a terra. E Noé removeu a cobertura da arca, e olhou, e eis que a face da terra estava seca.</p>
		<p><span class="numero">14)</span> E no segundo mês, no vigésimo sétimo dia do mês, a terra estava seca.</p>
		<p><span class="numero">15)</span> E Deus falou a Noé, dizendo:</p>
		<p><span class="numero">16)</span> Vai adiante da arca, tu e tua mulher e teus filhos, e as mulheres de teus filhos contigo.</p>
		<p><span class="numero">17)</span> Traze toda coisa vivente que está contigo, de toda carne, tanto das aves, quanto do gado e de toda coisa rastejante que rasteja sobre a terra; que eles possam procriar abundantemente na terra, e sejam frutíferos, e se multipliquem sobre a terra.</p>
		<p><span class="numero">18)</span> E Noé foi adiante, e seus filhos e sua mulher, e as mulheres de seus filhos com ele.</p>
		<p><span class="numero">19)</span> E saíram da arca todo animal, toda coisa rastejante, e toda ave, e tudo que rasteja sobre a terra, segundo as suas espécies.</p>
		<p><span class="numero">20)</span> E Noé construiu um altar ao Senhor; e tomou de todo animal limpo, e de toda ave limpa, e ofereceu ofertas queimadas sobre o altar.</p>
		<p><span class="numero">21)</span> E o Senhor cheirou um aroma doce, e o Senhor disse em seu coração: Eu não amaldiçoarei novamente a terra por causa do homem; pois a imaginação do coração do homem é má desde a sua juventude. Tampouco eu ferirei novamente toda coisa vivente, como o fiz.</p>
