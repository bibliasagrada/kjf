		<a href="gn.php"><h3>Gênesis 21</h3></a>
		<p><span class="numero">1)</span> E o Senhor visitou a Sara como ele dissera, e fez o Senhor a Sara como ele tinha falado.</p>
		<p><span class="numero">2)</span> Pois Sara concebeu e gerou um filho a Abraão em sua velhice, no tempo estabelecido de que Deus lhe falara.</p>
		<p><span class="numero">3)</span> E Abraão chamou o nome de seu filho que lhe nasceu, que Sara lhe concebeu, Isaque.</p>
		<p><span class="numero">4)</span> E Abraão circuncidou seu filho Isaque, sendo ele de oito dias de idade, como Deus lhe ordenara.</p>
		<p><span class="numero">5)</span> E Abraão era da idade de cem anos quando seu filho Isaque lhe nasceu.</p>
		<p><span class="numero">6)</span> E Sara disse: Deus me fez rir, de modo que todos os que ouvirem rirão comigo.</p>
		<p><span class="numero">7)</span> E ela disse: Quem teria dito a Abraão que Sara daria de mamar a filhos? Pois eu lhe dei um filho em sua velhice.</p>
		<p><span class="numero">8)</span> E o menino cresceu, e foi desmamado. E Abraão fez um grande banquete no mesmo dia em que Isaque foi desmamado.</p>
		<p><span class="numero">9)</span> E Sara viu o filho de Agar, a egípcia, que ela dera a Abraão, zombando.</p>
		<p><span class="numero">10)</span> Por isso ela disse a Abraão: Lança fora esta serva e seu filho, porque o filho dessa serva não será herdeiro com meu filho, com Isaque.</p>
		<p><span class="numero">11)</span> E a coisa pareceu muito grave aos olhos de Abraão por causa de seu filho.</p>
		<p><span class="numero">12)</span> E Deus disse a Abraão: Não seja isso grave a tua vista por causa do rapaz, e por causa da tua serva. Em tudo que Sara disser, dá ouvidos à sua voz, porque em Isaque será chamada a tua semente.</p>
		<p><span class="numero">13)</span> E também do filho da serva eu farei uma nação, porque ele é tua semente.</p>
		<p><span class="numero">14)</span> E Abraão se levantou cedo de manhã, e tomou pão, e um odre de água, e os deu a Agar, pondo-os sobre o seu ombro, e ao filho, e a despediu. E ela partiu, e peregrinou pelo deserto de Berseba.</p>
		<p><span class="numero">15)</span> E a água do odre foi consumida, e ela colocou o filho debaixo de um dos arbustos.</p>
		<p><span class="numero">16)</span> E ela foi sentar-se em frente dele a boa distância, como a de um tiro de arco. Pois ela disse: Que eu não veja a morte da criança. E sentada em frente dele, levantou sua voz e chorou.</p>
		<p><span class="numero">17)</span> E Deus ouviu a voz do menino. E o anjo de Deus chamou Agar desde o céu, e lhe disse: Que te aflige, Agar? Não temas, pois Deus ouviu a voz do menino de onde ele está.</p>
		<p><span class="numero">18)</span> Ergue-te, levanta o menino nos teus braços, pois farei dele uma grande nação.</p>
		<p><span class="numero">19)</span> E Deus abriu-lhe os olhos, e ela viu um poço de água. E foi, e encheu o odre com água, e deu de beber ao menino.</p>
		<p><span class="numero">20)</span> E Deus estava com o menino, e ele cresceu e habitou no deserto, e se tornou um arqueiro.</p>
		<p><span class="numero">21)</span> E ele habitou no deserto de Parã; e sua mãe lhe tomou uma mulher da terra do Egito.</p>
		<p><span class="numero">22)</span> E aconteceu naquele tempo que Abimeleque e Ficol, capitão-chefe de seu exército, falou a Abraão, dizendo: Deus está contigo em tudo que tu fazes;</p>
		<p><span class="numero">23)</span> por isso, agora, jura a mim por Deus que não agirás falsamente comigo, nem com meu filho, nem com o filho de meu filho, mas, de acordo com a bondade que eu te fiz, tu farás comigo, e para com a terra na qual peregrinaste.</p>
		<p><span class="numero">24)</span> E Abraão disse: Eu jurarei.</p>
		<p><span class="numero">25)</span> E Abraão repreendeu Abimeleque por causa de um poço de água, que os servos de Abimeleque tinham tomado violentamente.</p>
		<p><span class="numero">26)</span> E Abimeleque disse: Eu não sei quem fez isso, nem tampouco me contaste, nem ouvi a respeito disso, a não ser hoje.</p>
		<p><span class="numero">27)</span> E Abraão tomou ovelhas e bois, e os deu a Abimeleque, e os dois fizeram um pacto.</p>
		<p><span class="numero">28)</span> E Abraão pôs à parte sete cordeiras do rebanho.</p>
		<p><span class="numero">29)</span> E Abimeleque disse a Abraão: O que significam essas sete cordeiras que puseste à parte?</p>
		<p><span class="numero">30)</span> E ele disse: Estas sete cordeiras tomarás da minha mão, para que sejam uma testemunha para mim de que eu cavei este poço.</p>
		<p><span class="numero">31)</span> Por isso, chamou aquele lugar Berseba, porque ambos juraram ali.</p>
		<p><span class="numero">32)</span> Assim, eles fizeram um pacto em Berseba; então levantaram-se Abimeleque e Ficol, o capitão-chefe do seu exército, e eles retornaram à terra dos filisteus.</p>
		<p><span class="numero">33)</span> E Abraão plantou um bosque em Berseba, e invocou ali o nome do Senhor, o Deus eterno.</p>
