		<p><span class="numero">1)</span> E Deus abençoou Noé e seus filhos, e lhes disse: Sede frutíferos e multiplicaivos, e enchei a terra.</p>
		<p><span class="numero">2)</span> E o temor de vós e o pavor de vós estará sobre todo animal da terra, e sobre toda ave do céu, sobre tudo que se move sobre a terra, e sobre todos os peixes do
		mar; em vossas mãos eles foram entregues.</p>
		<p><span class="numero">3)</span> Toda coisa viva que se move será por alimento para vós; assim como a erva verde, eu vos dei todas as coisas.</p>
		<p><span class="numero">4)</span> Mas a carne com a sua vida, que é o sangue dela, não comereis.</p>
		<p><span class="numero">5)</span> E, certamente, vosso sangue das vossas vidas eu requererei; da mão de todo animal requererei, e da mão do homem, e da mão de todo irmão do homem requererei a vida do homem.</p>
		<p><span class="numero">6)</span> Quem assim derramar o sangue do homem, pelo homem seu sangue será
		derramado, pois à imagem de Deus ele fez o homem.</p>
		<p><span class="numero">7)</span> E vós, sede fecundos e multiplicai- vos, povoai abundantemente a terra, e multiplicai- vos nela.</p>
		<p><span class="numero">8)</span> E Deus falou a Noé, e a seus filhos com ele, dizendo:</p>
		<p><span class="numero">9)</span> E eu, eis que eu estabeleço meu pacto convosco, e com vossa semente depois de vós,</p>
		<p><span class="numero">10)</span> e com toda criatura vivente que está convosco, das aves, do gado e de todo animal da terra convosco; de todos os que saem da arca, a todo animal da terra.</p>
		<p><span class="numero">11)</span> E eu estabelecerei o meu pacto convosco; não será mais cortada toda carne pelas águas de um dilúvio, nem haverá mais dilúvio para destruir a terra.</p>
		<p><span class="numero">12)</span> E Deus disse: Este é o sinal do pacto que fiz entre mim e vós, e toda criatura vivente que está convosco, para as gerações perpétuas.</p>
		<p><span class="numero">13)</span> Eu ponho o meu arco na nuvem, e isto será por sinal do pacto entre mim e a terra.</p>
		<p><span class="numero">14)</span> E acontecerá, quando eu trouxer uma nuvem sobre a terra, que o arco será visto na nuvem.</p>
		<p><span class="numero">15)</span> E eu lembrarei do meu pacto, que está entre mim e vós e toda criatura vivente de toda a carne; e as águas não mais se tornarão um dilúvio para destruir toda a
		carne.</p>
		<p><span class="numero">16)</span> E o arco estará na nuvem; e eu olharei para ele, para que eu me lembre do pacto eterno entre Deus e toda criatura vivente de toda carne que está sobre a terra.</p>
		<p><span class="numero">17)</span> E Deus disse a Noé: Este é o sinal do pacto, que eu estabeleci entre mim e toda carne que está sobre a terra.</p>
		<p><span class="numero">18)</span> E os filhos de Noé, que saíram da arca, foram: Sem, Cam e Jafé; e Cam é o pai de Canaã.</p>
		<p><span class="numero">19)</span> Estes são os três filhos de Noé; e por eles toda a terra foi coberta.</p>
		<p><span class="numero">20)</span> E Noé começou a ser lavrador, e ele plantou uma vinha.</p>
		<p><span class="numero">21)</span> E ele bebeu do vinho, e ficou embriagado, e ele ficou desnudo dentro da sua tenda.</p>
		<p><span class="numero">22)</span> E Cam, o pai de Canaã, viu a nudez de seu pai, e contou a seus dois irmãos que estavam fora.</p>
		<p><span class="numero">23)</span> E Sem e Jafé tomaram uma capa, e a puseram sobre os seus ombros, e
		viraram para trás, e cobriram a nudez de seu pai; e suas faces estavam virados para trás, e eles não viram a nudez de seu pai.</p>
		<p><span class="numero">24)</span> E Noé despertou de seu vinho, e soube o que seu filho mais novo havia feito a ele.</p>
		<p><span class="numero">25)</span> E ele disse: Amaldiçoado seja Canaã; servo de servos ele será para seus irmãos.</p>
		<p><span class="numero">26)</span> E ele disse: Abençoado seja o Senhor Deus de Sem; e Canaã será o seu servo.</p>
		<p><span class="numero">27)</span> E Deus alargará Jafé, e ele habitará nas tendas de Sem; e Canaã será o seu servo.</p>
		<p><span class="numero">28)</span> E Noé viveu, depois do dilúvio, trezentos e cinquenta anos.</p>
