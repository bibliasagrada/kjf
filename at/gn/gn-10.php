		<p><span class="numero">16)</span> e o jebuseu, e o amorreu, e o girgaseu,</p>
		<p><span class="numero">17)</span> e o heveu, e o arqueu, e o sineu,</p>
		<p><span class="numero">18)</span> e o arvadeu, e o zemareu, e o hamateu, e depois as famílias dos cananeus foram espalhadas.</p>
		<p><span class="numero">19)</span> E o termo dos cananeus era desde Sidom, quando se vai para Gerar, até Gaza; quando se vai para Sodoma, e Gomorra, e Admá, e Zeboim até Lasa.</p>
		<p><span class="numero">20)</span> Estes são os filhos de Cam, segundo as suas famílias, segundo as suas línguas, em suas terras, e em suas nações.</p>
		<p><span class="numero">21)</span> A Sem também nasceram filhos, o pai de todos os filhos de Éber, e o irmão mais velho de Jafé.</p>
		<p><span class="numero">22)</span> Os filhos de Sem: Elão, e Assur, e Arfaxade, e Lude, e Arã.</p>
		<p><span class="numero">23)</span> E os filhos de Arã: Uz, e Hul, e Geter, e Más.</p>
		<p><span class="numero">24)</span> E Arfaxade gerou Selá; e Selá gerou Éber.</p>
		<p><span class="numero">25)</span> E a Éber nasceram dois filhos: o nome de um foi Pelegue, pois em seus dias foi dividida a terra; e o nome do seu irmão foi Joctã.</p>
		<p><span class="numero">26)</span> E Joctã gerou Almodá, e Selefe, e Hazarmavé, e Jerá,</p>
		<p><span class="numero">27)</span> e Hadorão, e Usal, e Dicla,</p>
		<p><span class="numero">28)</span> e Obal, e Abimael, e Seb,</p>
		<p><span class="numero">29)</span> e Ofir, e Havilá, e Jobabe; todos estes eram filhos de Joctã.</p>
		<p><span class="numero">30)</span> E sua habitação foi desde Messa, quando se vai para Sefar, um monte do leste.</p>
