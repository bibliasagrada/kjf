		<a href="gn.php"><h3>Gênesis 43</h3></a>
		<p><span class="numero">1)</span> E a fome era grave na terra.</p>
		<p><span class="numero">2)</span> E aconteceu que, quando eles terminaram de comer o trigo que haviam trazido do Egito, seu pai lhes disse: Ide novamente, comprai um pouco de alimento.</p>
		<p><span class="numero">3)</span> E Judá falou com ele, dizendo: O homem nos afirmou solenemente, dizendo: Não vereis a minha face, exceto se vosso irmão estiver convosco.</p>
		<p><span class="numero">4)</span> Se tu enviares nosso irmão conosco, desceremos para comprar-te alimento,</p>
		<p><span class="numero">5)</span> mas se tu não o enviares, não desceremos, porque o homem nos disse: Não vereis a minha face, exceto se vosso irmão estiver convosco.</p>
		<p><span class="numero">6)</span> E Israel disse: Por que agistes tão maldosamente comigo, ao contar ao homem que tínheis ainda um irmão?</p>
		<p><span class="numero">7)</span> E eles disseram: O homem nos perguntou particularmente por nossa condição, e por nossa parentela, dizendo: Vosso pai ainda está vivo? Tendes outro irmão? E lhe contamos de acordo com o teor destas palavras. Como poderíamos saber que ele diria: Trazei vosso irmão?</p>
		<p><span class="numero">8)</span> E Judá disse a Israel, seu pai: Envia o rapaz comigo, e nos levantaremos e partiremos, para que vivamos, e não morramos, tanto nós, como tu e também nossos pequenos.</p>
		<p><span class="numero">9)</span> Eu serei fiador por ele; da minha mão o exigirás. Se eu não o trouxer a ti, e o colocar diante de ti, então deixa-me carregar a culpa para sempre,</p>
		<p><span class="numero">10)</span> porque se não tivéssemos demorado, certamente agora já teríamos retornado uma segunda vez.</p>
		<p><span class="numero">11)</span> E o seu pai, Israel, disse-lhes: Se precisa ser assim agora, fazei-o: tomai os melhores frutos da terra em vossos vasos, e levai um presente ao homem, um pouco de bálsamo, um pouco de mel, especiarias e mirra, nozes e amêndoas.</p>
		<p><span class="numero">12)</span> E levai dinheiro em dobro em vossas mãos. E o dinheiro que foi trazido novamente na boca dos vossos sacos, levai-o novamente em vossas mãos. Talvez tenha sido um erro.</p>
		<p><span class="numero">13)</span> Levai também vosso irmão, e levantai- vos, ide novamente ao homem;</p>
		<p><span class="numero">14)</span> e o Deus Todo-Poderoso vos dê misericórdia diante do homem, para que ele possa enviar vosso outro irmão, e Benjamim. Se eu for privado de meus filhos, privado serei.</p>
		<p><span class="numero">15)</span> E os homens tomaram o presente, e levaram dinheiro em dobro nas suas mãos, e a Benjamim, e se levantaram, e desceram ao Egito, e se colocaram diante de José.</p>
		<p><span class="numero">16)</span> E quando José viu Benjamim com eles, disse ao administrador de sua casa: Levai estes homens para casa, e mata, e prepara, porque estes homens comerão comigo ao meio-dia.</p>
		<p><span class="numero">17)</span> E o homem fez como José ordenara, e o homem levou os homens para a casa de José.</p>
		<p><span class="numero">18)</span> E os homens ficaram temerosos, porque eles foram levados à casa de José, e disseram: Por causa do dinheiro que foi devolvido aos nossos sacos na primeira vez fomos trazidos aqui, para procurarem motivo contra nós, e se arremessar sobre nós, e nos tomar por escravos, e a nossos jumentos.</p>
		<p><span class="numero">19)</span> E se aproximaram do administrador da casa de José, e conversaram com ele à porta da casa,</p>
		<p><span class="numero">20)</span> e disseram: Oh! Senhor, viemos, na verdade, a primeira vez para comprar alimento,</p>
		<p><span class="numero">21)</span> e aconteceu que, quando chegamos à hospedaria, abrimos nossos sacos e eis que o dinheiro de cada homem estava na boca de seu saco, nosso dinheiro em todo o seu peso, e o trouxemos nas nossas mãos novamente.</p>
		<p><span class="numero">22)</span> E outro dinheiro trouxemos nas nossas mãos para comprar alimento; não sabemos quem colocou o nosso dinheiro em nossos sacos.</p>
		<p><span class="numero">23)</span> E ele disse: Paz esteja convosco, não temais. Vosso Deus, e o Deus de vosso pai, deu-vos um tesouro em vossos sacos; eu recebi o vosso dinheiro. E ele lhes trouxe Simeão.</p>
		<p><span class="numero">24)</span> E o homem conduziu os homens à casa de José, e lhes deu água, e eles lavaram seus pés, e ele deu forragem aos seus jumentos.</p>
		<p><span class="numero">25)</span> E eles preparam o presente para José, que viria ao meio-dia, porque ouviram que eles deveriam comer pão ali.</p>
		<p><span class="numero">26)</span> E quando José veio para casa, trouxeram- lhe o presente que estava nas mãos deles para dentro da casa, e se curvaram diante dele com a face em terra.</p>
		<p><span class="numero">27)</span> E ele lhes perguntou sobre seu bem- estar, e disse: Está bem o vosso pai, o velho de quem falastes? Ele ainda está vivo?</p>
		<p><span class="numero">28)</span> E eles responderam: Teu servo, nosso pai, está com boa saúde, ele ainda está vivo. E eles curvaram sua cabeça, e fizeram reverência.</p>
		<p><span class="numero">29)</span> E ele levantou seus olhos, e viu seu irmão Benjamim, filho de sua mãe, e disse: Este é vosso irmão mais jovem, do qual me falastes? E ele disse: Deus seja gracioso contigo, meu filho.</p>
		<p><span class="numero">30)</span> E José se apressou, pois as suas entranhas se moveram para com seu irmão. E ele procurou onde chorar, e entrou na sua câmara, e chorou ali.</p>
		<p><span class="numero">31)</span> E ele lavou sua face, e saiu, e se conteve, e disse: Ponde o pão.</p>
		<p><span class="numero">32)</span> E colocaram para ele à parte, e para eles à parte, e à parte para os egípcios que comiam com ele, porque os egípcios não podiam comer pão com os hebreus, porque isso é abominação para os egípcios.</p>
		<p><span class="numero">33)</span> E assentaram-se diante dele, o primogênito de acordo com seu direito de nascimento, e o mais jovem de acordo com sua juventude, e os homens se maravilharam entre si.</p>
		<p><span class="numero">34)</span> E de si mesmo ele tomou porções para eles, mas a porção de Benjamim era cinco vezes maior do que as dos outros. E eles beberam, e se alegraram com ele.&nbsp;<a href="gn-44.php"><span class="proximo">Próximo capítulo</span></a></p>
