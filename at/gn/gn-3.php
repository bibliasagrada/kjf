		<p><span class="numero">3)</span> mas do fruto da árvore que está no meio do jardim, Deus disse: Não comereis dele, nem o tocareis, para que não morrais.</p>
		<p><span class="numero">4)</span> E a serpente disse à mulher: Certamente não morrereis.</p>
		<p><span class="numero">5)</span> Porque Deus sabe que no dia em que dele comerdes, então vossos olhos serão abertos, e vós sereis como deuses, conhecendo o bem e o mal.</p>
		<p><span class="numero">6)</span> E quando a mulher viu que a árvore era boa para alimento, e que era agradável aos olhos, e uma árvore a ser desejada para fazer alguém sábio, ela tomou do seu fruto, e o comeu, e deu também a seu marido com ela; e ele o comeu.</p>
		<p><span class="numero">7)</span> E os olhos de ambos foram abertos, e eles souberam que estavam nus; e coseram folhas de figos, e fizeram para si aventais.</p>
		<p><span class="numero">8)</span> E eles ouviram a voz do Senhor Deus andando pelo jardim no frescor do dia. E Adão e sua mulher se esconderam da presença do Senhor Deus entre as árvores do jardim.</p>
		<p><span class="numero">9)</span> E o Senhor Deus chamou Adão, e lhe disse: Onde tu estás? </p>
		<p><span class="numero">10)</span> E ele disse: Eu ouvi a tua voz no jardim e tive medo, porque eu estava nu, e me escondi.</p>
		<p><span class="numero">11)</span> E ele disse: Quem te contou que estavas nu? Tens tu comido da árvore da qual eu te ordenei que não comesses?</p>
		<p><span class="numero">12)</span> E o homem disse: A mulher que tu me deste para estar comigo, ela me deu da árvore e eu comi.</p>
		<p><span class="numero">13)</span> E o Senhor Deus disse à mulher: O que é isto que tu fizeste? E a mulher disse: A serpente me enganou, e eu comi.</p>
		<p><span class="numero">14)</span> E o Senhor Deus disse à serpente: Porque tu fizeste isso, tu és amaldiçoada acima de todo gado, e acima de todo animal do campo; sobre o teu ventre tu andarás, e pó comerás todos os dias da tua vida.</p>
		<p><span class="numero">15)</span> E eu colocarei inimizade entre ti e a mulher, e entre a tua semente e a sua semente; ela ferirá a tua cabeça, e tu lhe ferirás o calcanhar.</p>
		<p><span class="numero">16)</span> À mulher ele disse: Eu multiplicarei grandemente o teu sofrimento e a tua concepção. Com sofrimento terás filhos; e o teu desejo será para o teu marido, e ele governará sobre ti.</p>
		<p><span class="numero">17)</span> E a Adão ele disse: Porque tu escutaste a voz de tua mulher, e comeste da árvore, da qual eu te ordenei dizendo: Tu não comerás dela, amaldiçoada é a terra por tua causa; com sofrimento tu comerás dela todos os dias da tua vida.</p>
		<p><span class="numero">18)</span> Espinhos e cardos também produzirá para ti; e comerás a erva do campo;</p>
		<p><span class="numero">19)</span> no suor da tua face comerás o pão, até que retornes a terra, pois dele tu foste tirado; porque pó tu és, e ao pó tu retornarás.</p>
		<p><span class="numero">20)</span> E Adão chamou o nome de sua mulher Eva, porque ela foi a mãe de todos os viventes.</p>
		<p><span class="numero">21)</span> Para Adão e também para sua mulher o Senhor Deus fez vestes de pele, e os vestiu.</p>
		<p><span class="numero">22)</span> E o Senhor Deus disse: Eis que o homem se tornou como um de nós, para conhecer o bem e o mal; e agora, para que ele não estenda sua mão, e tome também da árvore da vida, e coma, e viva para sempre;</p>
		<p><span class="numero">23)</span> o Senhor Deus, portanto, o lançou fora do jardim do Éden, para cultivar a terra da qual fora tomado.</p>
		<p><span class="numero">24)</span> Assim ele expulsou o homem, e colocou no leste do jardim do Éden querubins, e uma espada flamejante, que se voltava a todos os lados para guardar o caminho para a árvore da vida.&nbsp;<a href="gn-4.php"><span class="proximo">Próximo capítulo</span></a></p>
