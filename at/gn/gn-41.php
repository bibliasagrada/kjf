		<a href="gn.php"><h3>Gênesis 41</h3></a>
		<p><span class="numero">1)</span> E aconteceu que, ao final de dois anos completos, Faraó sonhou. E eis que ele estava em pé junto ao rio.</p>
		<p><span class="numero">2)</span> E eis que saíram do rio sete vacas gordas e de formoso aspecto, e pastavam na campina.</p>
		<p><span class="numero">3)</span> E eis que sete outras vacas saíram depois delas do rio, feias de aparência e magras, e estavam em pé junto às outras vacas sobre a margem do rio.</p>
		<p><span class="numero">4)</span> E as vacas feias de aparência e magras, comeram as sete vacas gordas e de formoso aspecto. Então Faraó acordou.</p>
		<p><span class="numero">5)</span> E ele dormiu e sonhou uma segunda vez. E eis que sete espigas de trigo brotaram de um mesmo talo, cheias e boas.</p>
		<p><span class="numero">6)</span> E eis que sete espigas miúdas e queimadas do vento oriental brotavam depois delas.</p>
		<p><span class="numero">7)</span> E as sete espigas miúdas devoraram as sete espigas cheias e boas. E Faraó acordou, e eis que era um sonho.</p>
		<p><span class="numero">8)</span> E aconteceu que, de manhã, seu espírito estava perturbado, e ele enviou e chamou todos os magos do Egito, e todos os homens sábios de lá. E Faraó lhes contou o seu sonho, mas não houve um que pudesse interpretá- lo para Faraó.</p>
		<p><span class="numero">9)</span> Então, falou o chefe dos mordomos a Faraó, dizendo: Lembro-me hoje das minhas falhas.</p>
		<p><span class="numero">10)</span> Faraó estava irado com seus servos, e me colocou na prisão da casa do capitão da guarda, a mim e o chefe dos padeiros;</p>
		<p><span class="numero">11)</span> e nós sonhamos um sonho certa noite, eu e ele. Sonhamos cada homem de acordo com a interpretação do seu sonho.</p>
		<p><span class="numero">12)</span> E havia lá conosco um jovem, um hebreu, servo do capitão da guarda. E nós lhe contamos, e ele nos interpretou nossos sonhos, para cada homem de acordo com o seu sonho ele interpretou.</p>
		<p><span class="numero">13)</span> E aconteceu que, assim como ele interpretou para nós, assim foi. A mim ele restabeleceu para o meu ofício, e a ele enforcou.</p>
		<p><span class="numero">14)</span> Então, Faraó enviou e chamou José, e eles o trouxeram apressadamente da masmorra. E ele se barbeou, e mudou as suas vestes, e veio a Faraó.</p>
		<p><span class="numero">15)</span> E Faraó disse a José: Sonhei um sonho, e não há ninguém que o possa interpretar. E eu ouvi dizer de ti, que tu podes entender um sonho e interpretá-lo.</p>
		<p><span class="numero">16)</span> E José respondeu a Faraó, dizendo: Não está em mim; Deus dará a Faraó uma resposta de paz.</p>
		<p><span class="numero">17)</span> E Faraó disse a José: No meu sonho, eis que eu estava em pé na margem do rio,</p>
		<p><span class="numero">18)</span> e eis que saíram do rio sete vacas gordas e de formoso aspecto, e pastavam na campina.</p>
		<p><span class="numero">19)</span> E eis que sete outras vacas saíram depois delas, feias de aparência e magras, tais como eu nunca vi em toda a terra do Egito, quanto à fealdade.</p>
		<p><span class="numero">20)</span> E as vacas magras e feias à vista comeram as primeiras sete vacas gordas.</p>
		<p><span class="numero">21)</span> E quando as haviam comido, não se podia saber que as haviam comido, mas ainda eram feias à vista, como no início. Então eu acordei.</p>
		<p><span class="numero">22)</span> E eu vi no meu sonho, e eis que sete espigas brotaram de um talo, cheias e boas.</p>
		<p><span class="numero">23)</span> E eis que sete espigas secas, miúdas e queimadas do vento oriental brotaram depois delas.</p>
		<p><span class="numero">24)</span> E as espigas miúdas devoraram as sete espigas boas. E eu contei isso aos magos, mas não houve ninguém que pudesse interpretá- lo para mim.</p>
		<p><span class="numero">25)</span> E José disse a Faraó: O sonho de Faraó é um: Deus mostrou a Faraó o que ele está para fazer.</p>
		<p><span class="numero">26)</span> As sete vacas boas são sete anos, e as sete espigas boas são sete anos; o sonho é um.</p>
		<p><span class="numero">27)</span> E as sete vacas magras e feias à vista que saíram depois delas são sete anos, e as sete espigas vazias queimadas do vento oriental serão sete anos de fome.</p>
		<p><span class="numero">28)</span> Isto é o que eu tenho para falar a Faraó: O que Deus está prestes a fazer, ele mostrou a Faraó.</p>
		<p><span class="numero">29)</span> Eis que vêm sete anos de grande fartura em toda a terra do Egito;</p>
		<p><span class="numero">30)</span> e depois deles surgirão sete anos de fome, e toda a fartura será esquecida na terra do Egito; e a fome consumirá a terra,</p>
		<p><span class="numero">31)</span> e a fartura não será conhecida na terra por causa da fome que se seguirá, pois esta será muito grave.</p>
		<p><span class="numero">32)</span> E por isso, o sonho foi repetido a Faraó duas vezes; é porque a coisa está estabelecida por Deus, e Deus em breve a fará acontecer.</p>
		<p><span class="numero">33)</span> Agora faça Faraó encontrar um homem prudente e sábio, e o coloque sobre a terra do Egito.</p>
		<p><span class="numero">34)</span> Que Faraó o faça, e que ele nomeei oficiais sobre a terra, e que recolham uma quinta parte da terra do Egito nos sete anos de fartura.</p>
		<p><span class="numero">35)</span> E ajuntem eles todo o alimento desses bons anos que vêm, e amontoem trigo sob a mão de Faraó, e que guardem alimento nas cidades.</p>
		<p><span class="numero">36)</span> E esse alimento será para o provimento da terra durante os sete anos de fome que haverá na terra do Egito, para que a terra não pereça de fome.</p>
		<p><span class="numero">37)</span> E a coisa foi boa aos olhos de Faraó e aos olhos de todos os seus servos.</p>
		<p><span class="numero">38)</span> E Faraó disse a seus servos: Acharemos alguém como este, um homem em quem está o Espírito de Deus?</p>
		<p><span class="numero">39)</span> E Faraó disse a José: Visto que Deus te mostrou tudo isto, não há ninguém tão prudente e sábio como tu és.</p>
		<p><span class="numero">40)</span> Tu estarás sobre a minha casa, de acordo com tua palavra todo o meu povo será governado; somente no trono eu serei maior do que tu.</p>
		<p><span class="numero">41)</span> E Faraó disse a José: Vê! Coloquei-te sobre toda a terra do Egito.</p>
		<p><span class="numero">42)</span> E Faraó tomou seu anel da sua mão e o colocou sobre a mão de José, e o vestiu com vestes de linho fino, e colocou um colar de ouro em volta do seu pescoço,</p>
		<p><span class="numero">43)</span> e o fez subir na segunda carruagem que ele tinha, e clamavam adiante dele: Ajoelhai; e ele o fez governador sobre toda a terra do Egito.</p>
		<p><span class="numero">44)</span> E Faraó disse a José: Eu sou Faraó, e sem ti nenhum homem levantará sua mão ou pé em toda a terra do Egito.</p>
		<p><span class="numero">45)</span> E Faraó chamou o nome de José Zafenate-Paneia, e lhe deu por mulher Azenate, a filha de Potífera, sacerdote de Om. E José saiu por toda a terra do Egito.</p>
		<p><span class="numero">46)</span> E José estava com trinta anos de idade quando estava diante de Faraó, rei do Egito. E José saiu da presença de Faraó, e foi por toda a terra do Egito.</p>
		<p><span class="numero">47)</span> E nos sete anos de fartura a terra produziu aos montões.</p>
		<p><span class="numero">48)</span> E ele ajuntou todo o alimento dos sete anos, que havia na terra do Egito, e armazenou o alimento nas cidades. O alimento do campo, que estava ao redor de cada cidade, ele armazenou da mesma forma.</p>
		<p><span class="numero">49)</span> E José ajuntou trigo como a areia do mar, muitíssimo, até ele perder a conta, pois era sem número.</p>
		<p><span class="numero">50)</span> E a José nasceram dois filhos, antes de virem os anos da fome, que Azenate, a filha de Potífera, sacerdote de Om, lhe deu.</p>
		<p><span class="numero">51)</span> E José chamou o nome do primeiro Manassés, pois Deus, disse ele, me fez esquecer todo o meu labor, e toda a casa de meu pai.</p>
		<p><span class="numero">52)</span> E o nome do segundo chamou Efraim, pois Deus me fez ser frutífero na terra da minha aflição.</p>
		<p><span class="numero">53)</span> E os sete anos de fartura, que houve na terra do Egito, terminaram.</p>
		<p><span class="numero">54)</span> E os sete anos de escassez começaram, de acordo com o que José havia dito; e a escassez estava em todas as terras, mas em toda a terra do Egito havia pão.</p>
		<p><span class="numero">55)</span> E quando toda a terra do Egito teve fome, o povo clamou a Faraó por pão; e Faraó disse a todos os egípcios: Ide a José; o que ele lhes disser, fazei.</p>
		<p><span class="numero">56)</span> E a fome estava sobre toda a face da terra, e José abriu todos os depósitos, e vendeu aos egípcios, e a fome aumentou muito na terra do Egito.</p>
