		<p><span class="numero">6)</span> E Abrão passou pela terra até o lugar de Siquém, até a planície de Moré. E os cananeus estavam nesse tempo na terra.</p>
		<p><span class="numero">7)</span> E o Senhor apareceu a Abrão e disse: À tua semente eu darei esta terra; e ali ele edificou um altar ao Senhor, que lhe apareceu.</p>
		<p><span class="numero">8)</span> E ele moveu-se dali para o monte ao leste de Betel, e armou sua tenda, tendo Betel ao oeste e Ai ao leste. E ali ele edificou um altar ao Senhor, e invocou o nome do Senhor.</p>
		<p><span class="numero">9)</span> E Abrão viajou, indo adiante para o sul.</p>
		<p><span class="numero">10)</span> E houve fome na terra, e Abrão desceu para o Egito para peregrinar ali, pois a fome era severa na terra.</p>
		<p><span class="numero">11)</span> E aconteceu que, quando ele estava prestes a entrar no Egito, ele disse a Sarai, sua mulher: Eis que eu sei que tu és uma mulher formosa à vista.</p>
		<p><span class="numero">12)</span> Por isso, acontecerá que, quando os egípcios te virem, eles dirão: Esta é a mulher dele. E me matarão, mas te manterão viva.</p>
		<p><span class="numero">13)</span> Dize, suplico-te, que tu és minha irmã, para que eu possa ficar bem por tua causa, e a minha alma viverá por causa de ti.</p>
		<p><span class="numero">14)</span> E aconteceu que, quando Abrão havia chegado ao Egito, os egípcios viram a mulher, e que ela era muito formosa.</p>
		<p><span class="numero">15)</span> Também os príncipes do faraó a viram, e a elogiaram diante do faraó, e a mulher foi levada à casa do faraó.</p>
		<p><span class="numero">16)</span> E ele tratou bem a Abrão por causa dela. E ele teve ovelhas, e bois, e jumentos, e servos, e servas, e jumentas e camelos.</p>
		<p><span class="numero">17)</span> E o Senhor atormentou faraó e a sua casa com grandes pragas por causa de Sarai, mulher de Abrão.</p>
		<p><span class="numero">18)</span> E faraó chamou Abrão, e disse: O que é isto que tu me fizeste? Por que não me disseste que ela era tua mulher?</p>
		<p><span class="numero">19)</span> Por que disseste: Ela é minha irmã? Portanto eu a tomei para ser minha mulher. Agora, pois, eis a tua mulher. Toma-a e vai no teu caminho.</p>
